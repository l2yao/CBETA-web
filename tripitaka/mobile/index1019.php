name="0918c06" id="0918c06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。教府自陳等者。承上自<span class="corr" title='[巳&gt;己]'>己</span>心言不盡。指如教藏。自<a 
