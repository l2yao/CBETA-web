name="0918a09" id="0918a09"></a>外問云。且如華嚴涅槃等經。研覈性相。廣談因果。此<a 
name="0918a10" id="0918a10"></a>經之中。皆無所有。如何以喻斯偏讚耶。疏兩句為奪<a 
name="0918a11" id="0918a11"></a>雖者。文含得失。即縱奪也。探者取也。線經華理。其涅<a 
name="0918a12" id="0918a12"></a>槃等。別經名異。大義意云。探幽深之性相。曉秘密之<a 
name="0918a13" id="0918a13"></a>果因。實在其餘涅槃等經。若據拔此末世沒溺之有<a 
name="0918a14" id="0918a14"></a>情順生天之機器者。唯在於此經爾。是以偏讚而以<a 
name="0918a15" id="0918a15"></a>哉。筌者目教。取魚之器。而為喻故。葉即貝葉。上書其<a 
name="0918a16" id="0918a16"></a>經故。與前線華。隨<span class="gaiji">[牙-(必-心)+?]<!--gaiji,,1[牙-(必-心)+?],2&#x3E26;,3--></span>舉示。</p><a 
name="0918a17" id="0918a17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。所以金光夕燭等者。此下釋成。何以得知。先釋拯<a 
name="0918a18" id="0918a18"></a>溺。一行餘也。下經自敘放光現華。放光除闇。表斷黑<a 
name="0918a19" id="0918a19"></a>因。華示六行。表當有果。顯依此經易而成之。其果可<a 
name="0918a20" id="0918a20"></a>准。故偏讚之。慈氏威力。不退轉故。外即初夜。燭者照<a 
