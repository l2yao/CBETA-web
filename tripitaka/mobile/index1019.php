name="0918c10" id="0918c10"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。六事齊修等者。二句舉體。二句歸名。六事如經。二<a 
name="0918c11" id="0918c11"></a>因之言。有為行願。有云中下。此等多說。以觀為正。行<a 
name="0918c12" id="0918c12"></a>為助因。今恐招感正要六事。所言二者。是願及觀。以<a 
name="0918c13" id="0918c13"></a>此二種。為六事行之前導也。攝引不令六事之行歸<a 
name="0918c14" id="0918c14"></a>於餘處。如是願觀。名之為助。其六事行。為正因感。以<a 
name="0918c15" id="0918c15"></a>正從助。而得名者。如七作意。修定之者。須加行定。是<a 
name="0918c16" id="0918c16"></a>其正要。作意為助。然從作意以為名者。修習之時。彼<a 
name="0918c17" id="0918c17"></a>用勝故。又如懺悔。雖體是悔。然云慚愧。又彼損力。益<a 
name="0918c18" id="0918c18"></a>能轉體。正唯是慧。亦云由習勝解慚愧。損本識中。二<a 
name="0918c19" id="0918c19"></a>障云云。並皆不曾言其正體。但從&#x96A3;近勝者為言。正<a 
name="0918c20" id="0918c20"></a>同此中言其觀也。其觀之言。攝六事。&#x96A3;近得名。然行<a 
name="0918c21" id="0918c21"></a>為正。觀為助引。於修行時。不敢離故。離觀修行。便名<a 
name="0918c22" id="0918c22"></a>為邪。故其正行。從助得名。問。中下修者。如何不說。答。<a 
name="0918c23" id="0918c23"></a>此且從勝。實皆名觀。又其中下。所列諸行。亦有六事。<a 
name="0918c24" id="0918c24"></a>此中皆攝。並名觀也。問。如是三品。有何差別。又直與<a 
name="0919a01" id="0919a01"></a>下疏文相違。九品之中。不皆具故。答。下疏約正為頭。<a 
name="0919a02" id="0919a02"></a>修習如是六行三業十事。以分三品。若要得生。須是<a 
name="0919a03" id="0919a03"></a>諸行互相助成。又其三三。細分九品。內一二等。如何<a 
name="0919a04" id="0919a04"></a>無助。孤示得生。其彼果報。相非輕故。無因果生。非道<a 
name="0919a05" id="0919a05"></a>理故。且示一相。有無戒者得生彼乎。故乃分品。約頭<a 
name="0919a06" id="0919a06"></a>約正。若其相皆有六事。招果內外。無非思業。得生於<a 
name="0919a07" id="0919a07"></a>彼。闕行不可。於此義中。應極思擇。至下更辨。</p><a 
name="0919a08" id="0919a08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。母志悲纏蠢類等者。三因得名。如餘具辨。纏者繞<a 
name="0919a09" id="0919a09"></a>也。不離之義。蠢類即是下劣之虫。以例於餘。尚以悲<a 
name="0919a10" id="0919a10"></a>心。不離於此。何況於餘。故是至慈。懷生即是有情異<a 
name="0919a11" id="0919a11"></a>名。懷含情識。或煩惱故。</p><a 
name="0919a12" id="0919a12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。道圓上果等者。下解菩薩。二句辨位。二句器名。實<a 
name="0919a13" id="0919a13"></a><span class="corr" title='[巳&gt;已]'>已</span>成佛。示跡為因。故為菩薩。言祈覺者。得菩薩名。言<a 
name="0919a14" id="0919a14"></a>運生者。得薩埵名。此即且依弘誓語也。假即非實。結<a 
name="0919a15" id="0919a15"></a>歸示現。</p><a 
name="0919a16" id="0919a16"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。光潔自在神用名天等者。三義名天。如文易見。此<a 
name="0919a17" id="0919a17"></a>多目情。有辨器界。菩薩生彼。約到彼處。約到眾中。並<a 
name="0919a18" id="0919a18"></a>名生故。</p><a 
name="0919a19" id="0919a19"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。此由補處天宮等者。此下總釋立題之意。先舉所<a 
name="0919a20" id="0919a20"></a>觀。後舉能觀。大意令人觀之修行。求生瞻奉。必獲願<a 
name="0919a21" id="0919a21"></a>也。言補處者。此是菩薩。當於題中彌勒菩薩。言天宮<a 
name="0919a22" id="0919a22"></a>者。當於題中兜率天也。合下端居利化之語。即見上<a 
name="0919a23" id="0919a23"></a>生二字之義。此總意云。此由一生補處菩薩。於兜率<a 
name="0919a24" id="0919a24"></a>天宮之中端居。利化諸有情也。此為所觀。令人修習<a 
name="0919b01" id="0919b01"></a>祈觀得生。無失&#x609E;也。</p><a 
name="0919b02" id="0919b02"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。經者攝也等者。列其四義。次第解<span class="corr" title='[巳&gt;已]'>已</span>。結歸二義。契<a 
name="0919b03" id="0919b03"></a>理契根。名之為經。攝常及法。即利物也。乃是契根。貫<a 
name="0919b04" id="0919b04"></a>即詮真。是其契理。有處說云。攝則集斯妙義。貫又御<a 
name="0919b05" id="0919b05"></a>彼庸。與此相翻。隨釋無違。二皆可對情及理故。言樊<a 
name="0919b06" id="0919b06"></a>籠者。云樊訓籠也。云樊是深密罥礙之義。煩惱若此<a 
name="0919b07" id="0919b07"></a>有作煩字。言百靈。神聖也。無改曰常。言千葉者。金枝<a 
name="0919b08" id="0919b08"></a>玉葉。是王者也。上皆舉勝。以例於餘。</p><a 
name="0919b09" id="0919b09"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。第一佛成權實者。佛之身說並名為佛。無不攝盡。<a 
name="0919b10" id="0919b10"></a>問。何故成字安之在下。常及聖教言成佛故。答。是兩<a 
name="0919b11" id="0919b11"></a>門故。故安於下。下文先辨佛之權實。後方辨於成權<a 
name="0919b12" id="0919b12"></a>實故。佛即直辨三身之體。成即辨其從因至果。其佛<a 
name="0919b13" id="0919b13"></a>地經是先辨其五法三身。次後例其十地分證。故附<a 
name="0919b14" id="0919b14"></a>於彼。如是為文。若不爾者。倒其成字。有何義意。不為<a 
name="0919b15" id="0919b15"></a>文章。非語順故。故就義門。恐同常言。唯為一義。置之<a 
name="0919b16" id="0919b16"></a>於下。遂令覽者起異論端。至下當知。</p><a 
name="0919b17" id="0919b17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。權謂隨機接物等者。此下釋權實總義。事理未盡。<a 
name="0919b18" id="0919b18"></a>名之為權。稱於理事。名之為實。抄分兩對。不依次第。<a 
name="0919b19" id="0919b19"></a>隨機名權。&#x8FEC;真名實。是勝鬘經。接物名權。究理名實。<a 
name="0919b20" id="0919b20"></a>是法華經。今或解云。只依次第。配其二經。隨機究理。<a 
name="0919b21" id="0919b21"></a>是法華經。接物&#x8FEC;真。是勝鬘經。其法華經。二權一實。<a 
name="0919b22" id="0919b22"></a>隨機說有三乘究竟。二乘實未得究竟故。究不究竟。<a 
name="0919b23" id="0919b23"></a>皆為究竟。事理未盡。故是其權。窮理唯有一乘究竟。<a 
name="0919b24" id="0919b24"></a>故是其實。此法華也。其勝鬘說四實一權。為接二乘<a 
name="0919c01" id="0919c01"></a>歸於大乘。說唯有一。無有二乘。既實有三。事理未盡。<a 
name="0919c02" id="0919c02"></a>故是權也。稱實有四。故四是實。此言一乘。是其權者。<a 
name="0919c03" id="0919c03"></a>乃是勝鬘。指於法華所說一乘。名之為權。非自勝鬘<a 
name="0919c04" id="0919c04"></a>所說一乘亦名為權。勝鬘自說真如名一。此之一乘。<a 
name="0919c05" id="0919c05"></a>出生四乘。及攝四乘。皆盡理故。不同法華說一無二。<a 
name="0919c06" id="0919c06"></a>故彼是權。其勝鬘經說一乘亦是實也。此上約說辨<a 
name="0919c07" id="0919c07"></a>其權實。約身辨者。法華經意。隨機變現。非實修成。名<a 
name="0919c08" id="0919c08"></a>之為權。究理修得。名之為實。即自受用。名之為實。彼<a 
name="0919c09" id="0919c09"></a>未說有自性身故。勝鬘之意。指於法華接物別說自<a 
name="0919c10" id="0919c10"></a>受用身。亦名為權。不歸法身。別說之故。為接物故。尚<a 
name="0919c11" id="0919c11"></a>擬二佛。是不盡理。名之為權。依自所說。攝相歸性。只<a 
name="0919c12" id="0919c12"></a>立法身。名之為實。一人只成一&#x4E2A;佛故。是盡理也。善<a 
name="0919c13" id="0919c13"></a>師解云。隨機本為接物。究理便是&#x8FEC;真。各唯一義。似<a 
name="0919c14" id="0919c14"></a>於標釋。非兩對也。今作兩對。配於二經。次第亦好。名<a 
name="0919c15" id="0919c15"></a>義雖有。可互屬之。隨義就意。亦無失也。</p><a 
name="0919c16" id="0919c16"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。佛之身說者。說通所說。故不言教。所說二乘。亦是<a 
name="0919c17" id="0919c17"></a>權故。問。法華亦說。定姓二乘。既實有爾。不歸大乘。如<a 
name="0919c18" id="0919c18"></a>何說三。不亦名實。而名權耶。答。皆說究竟。實不究竟。<a 
name="0919c19" id="0919c19"></a>故亦是權。勝不曾說為究竟。但說有爾。故是其實。</p><a 
name="0919c20" id="0919c20"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。即涅槃經半滿教也者。此約未會二乘歸一。大乘<a 
name="0919c21" id="0919c21"></a>為滿。說二空故。二乘名半。唯生空故。若會之時。不分<a 
name="0919c22" id="0919c22"></a>半滿。皆為一故。</p><a 
name="0919c23" id="0919c23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。若如來隨彼意欲等者。指法華經說為實者。為方<a 
name="0919c24" id="0919c24"></a>便說。方便即是權巧說之。未盡理也。</p><a 
name="0920a01" id="0920a01"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。即說唯有等者。此是疏之釋勝意。</p><a 
name="0920a02" id="0920a02"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。謂大乘獨覺等者。下引勝鬘所說四乘是實者也。<a 
name="0920a03" id="0920a03"></a>言無聞者。無聞慧也。無三乘故非法器。言成熟者。令<a 
name="0920a04" id="0920a04"></a>彼成熟。是化度也。以人善而化度之。</p><a 
name="0920a05" id="0920a05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。若以色見我等者。此初一頌。遣如色等見於佛也。<a 
name="0920a06" id="0920a06"></a>我即是佛自性法身。仍須攝相歸性之門。并自受用。<a 
name="0920a07" id="0920a07"></a>兩句示情。一句遣之。生即眾生。斷道彼如色等求見<a 
name="0920a08" id="0920a08"></a>佛者。如是眾生。是眾生是遊履於邪斷道也。故不見。<a 
name="0920a09" id="0920a09"></a>末句為結。彼論釋云。如所不應見不可見。頌上三句。<a 
name="0920a10" id="0920a10"></a>如所不應見。其不可見是末結句。色等名所。若如於<a 
name="0920a11" id="0920a11"></a>彼不應見也。</p><a 
name="0920a12" id="0920a12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。彼如來妙體等者。此第二頌。上之三句。法身體妙<a 
name="0920a13" id="0920a13"></a>不應見也。亦須相通自受用。末之一句。麤識不知。故<a 
name="0920a14" id="0920a14"></a>不可見。識顯麤故。導師之言。是變化身。此是道師之法<a 
name="0920a15" id="0920a15"></a>身也。顯不應見。彼論釋云。如彼不應見及不見因緣。<a 
name="0920a16" id="0920a16"></a>因緣即是所以之義。即當末句以識麤故。上之一句。<a 
name="0920a17" id="0920a17"></a>釋前三句。彼目妙體。既是法身。故彼體性不應見也。</p><a 
name="0920a18" id="0920a18"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。彼論釋云等者。此頌非是釋次前經。是承前經斷<a 
name="0920a19" id="0920a19"></a>說得疑。既非見等。如何名佛。如何有說。經自斷云。無<a 
name="0920a20" id="0920a20"></a>有定法名阿耨等。此是釋彼。於此頌中。亦自受用。攝<a 
name="0920a21" id="0920a21"></a>相歸性。總名法身。故但應化。非真佛也。應機現佛。名<a 
name="0920a22" id="0920a22"></a>之為應。現趣類身。名之為化。有云合言并自受用非<a 
name="0920a23" id="0920a23"></a>真佛者。非文意也。應云但說法身實處。攝自受用。說<a 
name="0920a24" id="0920a24"></a>自受用。名為權處。是約離本別說之也。上句遣身。次<a 
name="0920b01" id="0920b01"></a>句遣說。亦是示現。非實說故。又恐疑云。既云應化非<a 
name="0920b02" id="0920b02"></a>說法者。莫其應化全不說法。故次經云。如來所說法。<a 
name="0920b03" id="0920b03"></a>不取不可說。非法非非法。今此頌云。說法不二取。二<a 
name="0920b04" id="0920b04"></a>即是其法也。下句又釋。能說無實。所說離言。故上說<a 
name="0920b05" id="0920b05"></a>云。非說法者。</p><a 
name="0920b06" id="0920b06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。以法身為實等者。金剛經中。攝相歸性。其自受用。<a 
name="0920b07" id="0920b07"></a>亦名法身。金光明經。其義正同。下言二身但是應化。<a 
name="0920b08" id="0920b08"></a>無自受用。</p><a 
name="0920b09" id="0920b09"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。此以受用為實等者。法華未說自性法身。說自受<a 
name="0920b10" id="0920b10"></a>用。名之為實。實修成故。所言化者。即通他報。非實修<a 
name="0920b11" id="0920b11"></a>成。變現起故。</p><a 
name="0920b12" id="0920b12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。有二四句者。其初四句。不論隨類。約是佛相。名之<a 
name="0920b13" id="0920b13"></a>為佛。方乃於中辨其四句。小化無受法樂之義。不得<a 
name="0920b14" id="0920b14"></a>名報。第二四句。既辨隨類。名之為佛。是佛相者。便名<a 
name="0920b15" id="0920b15"></a>為報。故小化身亦入俱句。或初四句。約受法樂。似自<a 
name="0920b16" id="0920b16"></a>受用。方名為報。不通小化。於唯化中。影略隨類。第二<a 
name="0920b17" id="0920b17"></a>四句。但是佛相。便名為報。即通小化。其自受用。及隨<a 
name="0920b18" id="0920b18"></a>類化他受用身自性法身。如次四句。兩下皆同。唯小<a 
name="0920b19" id="0920b19"></a>化身二有別也。又法苑章。應化相對。而為四句。其他<a 
name="0920b20" id="0920b20"></a>受用。不名為化。為此相違。各隨一義。亦無過失。彼約<a 
name="0920b21" id="0920b21"></a>應現佛身之者。名之為應。現五趣身。名之為化。故丈<a 
name="0920b22" id="0920b22"></a>六身。六應亦化。先現為人。後成佛故。今此但約變現<a 
