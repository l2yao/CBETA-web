name="0918a23" id="0918a23"></a>義。實即果實。</p><a 
name="0918a24" id="0918a24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。妙祥之儔波委等者。下釋有機。菩薩聲聞二句文<a 
name="0918b01" id="0918b01"></a>也。委者赴也。善唯識云累也。累累而來。今或委託。如<a 
name="0918b02" id="0918b02"></a>波迅廣倚仗之也。舉下眾集。如波如雲。故是符順至<a 
name="0918b03" id="0918b03"></a>如此也。</p><a 
name="0918b04" id="0918b04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。雖則稟訓一時等者。下是第二轉通但益一時妨。<a 
name="0918b05" id="0918b05"></a>由上但云妙祥之儔鶖子之輩。外人問云。如是所益。<a 
name="0918b06" id="0918b06"></a>近而非多。何假如是讚之者乎。又或問云。所益若此。<a 
