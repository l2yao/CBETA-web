name="0931a05" id="0931a05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>經。如是我聞者。切見今時講說之宗。皆以先讀文後<a 
name="0931a06" id="0931a06"></a>科判。然後&#x5374;來唱經解之。蓋自燕臺為其首也。彼科<a 
