name="0918b23" id="0918b23"></a>內。正所居故。</p><a 
name="0918b24" id="0918b24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。匪心樞而攬觀等者。上略讚勝。下彰難盡。初之一<a 
name="0918c01" id="0918c01"></a>句。心緣莫窮。次之一句。語讚無盡。語隨心故。樞其心<a 
name="0918c02" id="0918c02"></a>如此。便名為樞。斯大意云。如樞之心。既不能觀之得<a 
name="0918c03" id="0918c03"></a>盡。故語至極亦不能讚揚得盡者也。光亦是讚。此即<a 
name="0918c04" id="0918c04"></a>約勝。我此緣說不能盡爾。作如此語。非關自相離言<a 
