name="0933c24" id="0933c24"></a>如次輪之。其六時者。東西南北。初後相對。日中夜半。<a 
name="0934a01" id="0934a01"></a>相對亦爾。後日初夜。兩&#x96A3;未移。右為日中。左為半夜。<a 
name="0934a02" id="0934a02"></a>後夜初日。不移亦爾。右&#x5374;夜半。左&#x5374;日中。如是四洲。<a 
name="0934a03" id="0934a03"></a>輪次皆爾。是即合於瑜伽論文。恒於二洲。而作光明。<a 
name="0934a04" id="0934a04"></a>於二洲而作於暗。於四洲中。隨一日中。於一日出。於<a 
name="0934a05" id="0934a05"></a>一半夜。於一日沒。無無中者。若無日中。但有出沒。故<a 
name="0934a06" id="0934a06"></a>為非也。如是八等。隨指於一。餘洲不定。故不言之。但<a 
name="0934a07" id="0934a07"></a>言一時。</p><a 
name="0934a08" id="0934a08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span><img src="../cb_tripitaka_web/figures/X/X21p0934_01.gif" alt="X21p0934_01.gif"/></p><a 
name="0934a09" id="0934a09"></a><a 
name="0934a10" id="0934a10"></a><a 
name="0934a11" id="0934a11"></a><a 
name="0934a12" id="0934a12"></a><a 
name="0934a13" id="0934a13"></a><a 
name="0934a14" id="0934a14"></a><a 
name="0934a15" id="0934a15"></a><a 
name="0934a16" id="0934a16"></a><a 
name="0934a17" id="0934a17"></a><a 
name="0934a18" id="0934a18"></a><a 
name="0934a19" id="0934a19"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>問。何故放光即言初夜。答。光時一方。故時言定。集經<a 
name="0934a20" id="0934a20"></a>初普。故不可也。問。放光何不言十二者。答。如下有表</p><a 
name="0934a21" id="0934a21"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。答雖隨化相等者。前時約機。其時不定。此即約佛。<a 
name="0934a22" id="0934a22"></a>報化不同。今指化佛成道時故。其前第一。雙約機佛。<a 
name="0934a23" id="0934a23"></a>二皆不定。</p><a 
name="0934a24" id="0934a24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。答說處標淨穢等者。淨穢雖殊。所指處定。如云陵<a 
name="0934b01" id="0934b01"></a>鷲。報化雙彰。其成道時。自報定先。他報不定。不能同<a 
name="0934b02" id="0934b02"></a>指。</p><a 
name="0934b03" id="0934b03"></a><p><span class="juanname">上生經瑞應鈔卷上</span></p>
