<a 
name="0915a01" id="0915a01"></a><a 
name="0915a02" id="0915a02"></a><p style="margin-left: 2em"><span class="juannum">No. 394</span></p><a 
name="0915a03" id="0915a03"></a><p><span class="juanname">上生經瑞應鈔卷上</span></p><a 
name="0915a04" id="0915a04"></a><a 
name="0915a05" id="0915a05"></a><p style="margin-left: 4em"><span class="byline">真定府龍興寺比丘僧　守千　集</span></p><a 
name="0915a06" id="0915a06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>夫其科者。本要別其義類。須是識其階降。譬如排其<a 
name="0915a07" id="0915a07"></a>皈從切要知其生所。而其名者。元在簡於他濫。當可<a 
name="0915a08" id="0915a08"></a>了其增添。猶若續其枝葉且與見其連隔。如今此中。<a 
name="0915a09" id="0915a09"></a>只於疏題。除其卷目。有其四階。一者能釋所釋對。初<a 
name="0915a10" id="0915a10"></a>十一字是其所釋。未有疏時。<span class="corr" title='[巳&gt;已]'>已</span>有此名。故為其一。疏<a 
name="0915a11" id="0915a11"></a>之一字。是其能釋。疏是疏條解釋義故。造疏之後。加<a 
name="0915a12" id="0915a12"></a>成其名。如是不可先以經字合疏字了。然後合於觀<a 
name="0915a13" id="0915a13"></a>等五字。故作釋時。通連上字。依主釋也。於所釋中。復<a 
name="0915a14" id="0915a14"></a>有第二能詮所詮對。初之十字。是其所詮。經之一字。<a 
name="0915a15" id="0915a15"></a>是其能詮。雖其經體一文二義。而於要者正在聽聞。<a 
name="0915a16" id="0915a16"></a>又其相對解釋之家。多云能詮。略其所詮。亦不相違。<a 
name="0915a17" id="0915a17"></a>此字正是所說所聞。於其名中。濫於眾經。又要宗趣。<a 
name="0915a18" id="0915a18"></a>故添所詮共為一名。亦依主釋。於所詮中。復有第三<a 
name="0915a19" id="0915a19"></a>能觀所觀對。觀之一字。汎雖通所。今此題中。只要能<a 
name="0915a20" id="0915a20"></a>觀。其次九字。是所觀也。此經雖亦說其彌勒上生之<a 
name="0915a21" id="0915a21"></a>事及天宮等。然本只為勸其九品行人忻生。故合釋<a 
name="0915a22" id="0915a22"></a>時。以能所觀。倒墮依主。復於所觀九字之中。有其第<a 
name="0915a23" id="0915a23"></a>四能生所生對。初之六字。是其能生。次之三字。是其<a 
name="0915a24" id="0915a24"></a>所生。或以器界。名為所生。如到家中家名所到。然其<a 
name="0915b01" id="0915b01"></a>天者。光潔自在神用名天。知足者天正在有情。此以<a 
name="0915b02" id="0915b02"></a>器界而得名者。全取他名。有財釋也。或舊天人名知<a 
name="0915b03" id="0915b03"></a>足大。一天總名。今此天主。生彼眾中。別到總中。名為<a 
name="0915b04" id="0915b04"></a>生彼。亦如處所名所生義。此對立能所之稱。今此題<a 
name="0915b05" id="0915b05"></a>中正辨能生。令人忻戀菩薩故也。由是亦作倒墮依<a 
name="0915b06" id="0915b06"></a>主。上解是約從外向內。若約從前向後解者。即可先<a 
name="0915b07" id="0915b07"></a>作能所觀對。次能所生。方能所詮。後能所釋。又或於<a 
name="0915b08" id="0915b08"></a>彼能生之中。立為一對。舉人彰用對。彌勒菩薩舉其<a 
name="0915b09" id="0915b09"></a>人也。上生之言。是彰其用。以其彌勒即是菩薩。彌勒<a 
name="0915b10" id="0915b10"></a>菩薩即是上生。持業釋也。望下兜率天之三字者。倒<a 
name="0915b11" id="0915b11"></a>墮依主。兜率天之彌勒菩薩上生故也。餘問如是離<a 
name="0915b12" id="0915b12"></a>合兩重倒墮。餘皆隔之。只有其觀。云何可應經之所<a 
name="0915b13" id="0915b13"></a>詮。答。經詮具備。說正并兼。離合揀濫。隔兼存正。觀倒<a 
name="0915b14" id="0915b14"></a>如此。不相違也。問。經中具有九品之行。今此題中何<a 
name="0915b15" id="0915b15"></a>處見有。答。在觀字中。問。繫念思惟一義可爾。修功德<a 
name="0915b16" id="0915b16"></a>等。云何名觀。答。修餘行時。亦恒觀故。所有諸行。並名<a 
name="0915b17" id="0915b17"></a>觀。如唯識言。故下疏云。以觀為首。並名為觀。又如信<a 
name="0915b18" id="0915b18"></a>戒現觀之名。作&#x96A3;近釋。&#x96A3;近亦有全取他名。然此正<a 
name="0915b19" id="0915b19"></a>要九品之行。其行名正。正觀察為助。不同現觀信戒<a 
name="0915b20" id="0915b20"></a>為助。助得正名名為現觀。何以故。彼說現觀。有其六<a 
name="0915b21" id="0915b21"></a>種。故以現觀而為其正。此說造因招感得生。須九品<a 
name="0915b22" id="0915b22"></a>行正為招感。不可只以觀察之行。而能招彼殊勝身<a 
name="0915b23" id="0915b23"></a>器。問。既於觀字。見其生因。云何不說行人能所。但說<a 
name="0915b24" id="0915b24"></a>菩薩能所生耶。答。經意是令觀於菩薩。忻同生彼。而<a 
name="0915c01" id="0915c01"></a>造其因。菩薩所生。便是行人之所生故。更不別說。其<a 
name="0915c02" id="0915c02"></a>造因者。即是行者能生之身。既非別類。於所觀中。無<a 
name="0915c03" id="0915c03"></a>由別說。如是題義。於經所詮無不具足。不相違也。上<a 
name="0915c04" id="0915c04"></a>來且略隨其自情。釋疏題<span class="corr" title='[巳&gt;已]'>已</span>。所有離合。有異舊解。於<a 
name="0915c05" id="0915c05"></a>來鑒者。宜詳&#x97F5;焉。其餘參差。至文當辨。</p><a 
name="0915c06" id="0915c06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又支述作之者。臨時為文。各有廣略。展縮不定。量其<a 
name="0915c07" id="0915c07"></a>機器及自&#x80F7;懷。然須達其宗趣。事在合宜。所患太過<a 
name="0915c08" id="0915c08"></a>而以哉。太展即忘本。太縮即難解。如此經中所說之<a 
name="0915c09" id="0915c09"></a>義。無非性相及以因果。展轉推尋。義門無量。假使盡<a 
name="0915c10" id="0915c10"></a>其壽命。亦不能窮極如是。寧及本為生天者哉。且如<a 
name="0915c11" id="0915c11"></a>三境。何須論其所觀。何者。諸如是等。但知行行管取<a 
name="0915c12" id="0915c12"></a>得生諸爾。而以若隨解釋。展轉無窮。恐失修習生天<a 
name="0915c13" id="0915c13"></a>之業。故但隨疏略釋大綱。作九品之由漸定得生之<a 
name="0915c14" id="0915c14"></a>要者矣。諸來勿責。問。經置外題。不假開卷。令人速辨。<a 
name="0915c15" id="0915c15"></a>理在不疑。諸經題上。多有此狀。又四筆相叉。不知何<a 
name="0915c16" id="0915c16"></a>者。答。此是當於三箇梵字陀羅喃也。此云持轉。師子<a 
name="0915c17" id="0915c17"></a>覺云。即是持義。依經修習。任持善根。不墮惡道。天親<a 
name="0915c18" id="0915c18"></a>解云。即是轉義。依經修習。轉凡成聖。轉小成大。乃至<a 
name="0915c19" id="0915c19"></a>成佛。共為揀異俗書故也。出律文。問。經論疏鈔。四等<a 
name="0915c20" id="0915c20"></a>之名。所為之文。有規格乎。答有。經即隨根就義。直以<a 
name="0915c21" id="0915c21"></a>長行偈頌。更無引他解他文義。論乃便以論量性相。<a 
name="0915c22" id="0915c22"></a>或敘經教。而以釋之。多分不引他文解義。但有自解<a 
name="0915c23" id="0915c23"></a>文義了<span class="corr" title='[巳&gt;已]'>已</span>。引他為證。</p><a 
name="0915c24" id="0915c24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏是疏條為義。多分引文解義。然具次為門。抄乃漏<a 
name="0916a01" id="0916a01"></a>略不備之義。多分臨時隨釋。而不及具備。故云漏略<a 
name="0916a02" id="0916a02"></a>不備之稱。漏即脫也。心力不及。故有所漏。此為勞也。<a 
name="0916a03" id="0916a03"></a>即謙也。或勞謙別。力不及故勞也。有力故謙也。略者<a 
name="0916a04" id="0916a04"></a>少略。勞謙亦爾。共為不備。或隨自力。或先<span class="corr" title='[巳&gt;已]'>已</span>釋。或其<a 
name="0916a05" id="0916a05"></a>易解。並可略之。又隨但義。但只消解。不必頭尾標結<a 
name="0916a06" id="0916a06"></a>具足。於此疏抄所立之名。有是疏而自謙為抄。有是<a 
name="0916a07" id="0916a07"></a>抄而他獎為疏。如略纂等。自呼為抄。他獎為疏。對法<a 
name="0916a08" id="0916a08"></a>疏等。本分是疏。自謙為抄。問。講說解釋名義何殊。答。<a 
name="0916a09" id="0916a09"></a>講即論量。說乃宣吐。字從於言。發之於口。解者是其<a 
name="0916a10" id="0916a10"></a>免離之義。又分也開也。釋者是其除脫之義。又去也<a 
name="0916a11" id="0916a11"></a>遠也。此之二名。字不從言。發於三業。如離怨等亦名<a 
name="0916a12" id="0916a12"></a>解釋。然於此中。非關怨等只惡。解釋先所不了諸有<a 
name="0916a13" id="0916a13"></a>疑滯及以詰難文義者也。所不了等。猶如怨等。得此<a 
name="0916a14" id="0916a14"></a>言教而解釋之。上法題<span class="corr" title='[巳&gt;已]'>已</span>。</p><a 
name="0916a15" id="0916a15"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>人名之中。先舉所依。京者大也。此有四。今取長安舊<a 
name="0916a16" id="0916a16"></a>西京也。亦是數名。彼中居家人及京數。故以為名。有<a 
name="0916a17" id="0916a17"></a>財釋也。風俗通云。京非人力所成。天地性也者。歎勝<a 
name="0916a18" id="0916a18"></a>之詞。非關字義。大慈恩寺者。即是長安十大寺中之<a 
name="0916a19" id="0916a19"></a>一寺也。高宗大帝。為報慈母養育之恩之所建也。寺<a 
name="0916a20" id="0916a20"></a>者司也至也。從九寺中之鴻臚寺而為名焉。沙門息<a 
name="0916a21" id="0916a21"></a>惡。有其四種。具如餘辨。撰亦集也。問。說作述撰四名<a 
name="0916a22" id="0916a22"></a>何異。答。說即口談。造即起本。並非先有。名說造也。先<a 
name="0916a23" id="0916a23"></a>有名述。通本無本。撰即集。集舊成本。名之為集。今世<a 
name="0916a24" id="0916a24"></a>所傳。撰勝於集。但在人情。非關字義。問。有題言述。人<a 
name="0916b01" id="0916b01"></a>下言撰。人法相對。何見不同。答。形先為述。示今為撰。<a 
name="0916b02" id="0916b02"></a>若論新舊皆有也。問。如今疏文。云何舊有。答。皆出他<a 
name="0916b03" id="0916b03"></a>典。或聞他說。名為舊有。非關此本。問。如唯識疏。經論<a 
name="0916b04" id="0916b04"></a>相對。本釋相對。名為述造。及其論疏。何舊何新。答。經<a 
name="0916b05" id="0916b05"></a>談初證。歎勝唯新。論約從經。自早唯舊。以論對經。彰<a 
name="0916b06" id="0916b06"></a>敘經故。名之為述。雖不全文。義必爾也。然不如疏引<a 
name="0916b07" id="0916b07"></a>文解義。或敘他義。為自釋文。&#x5374;名為造。本釋相對。如<a 
name="0916b08" id="0916b08"></a>對疏也。又其釋論。必牒本論。是敘本論之意故也。問。<a 
name="0916b09" id="0916b09"></a>撰之一字。為能為所。答初作三段。唯目能撰。人法分<a 
name="0916b10" id="0916b10"></a>故。兩段科者。可作所撰。顯法是誰人所。故歸撰<span class="corr" title='[巳&gt;已]'>已</span>。可<a 
name="0916b11" id="0916b11"></a>為法題。而作持業。若取能撰。只相違釋。本抄之科。只<a 
name="0916b12" id="0916b12"></a>為三段。准善師等。可作兩段。今別刪補。合為兩段。恐<a 
name="0916b13" id="0916b13"></a>未知者令知有爾。非只為令異舊故也。其餘文義。諸<a 
name="0916b14" id="0916b14"></a>皆解<span class="corr" title='[巳&gt;已]'>已</span>。雖有二褒貶。恐繁皆止。</p><a 
name="0916b15" id="0916b15"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。原夫者。發語之端。入文之勢。若依字義。原者始也<a 
name="0916b16" id="0916b16"></a>夫是語詞。窮其經旨。始自真如。展轉流之。由此名為<a 
name="0916b17" id="0916b17"></a>等流正法。自受用身。證於法身。法身淨<span class="corr" title='[巳&gt;已]'>已</span>。智於如上<a 
name="0916b18" id="0916b18"></a>現起化身。說此經故。故便示如包羅通遍。</p><a 
name="0916b19" id="0916b19"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。性質杳冥等者。今或性者。諸法體性。質者體也。真<a 
name="0916b20" id="0916b20"></a>如是諸法之體故。名為性質。此體深遠。幽微難及。名<a 
name="0916b21" id="0916b21"></a>為杳冥。所以超於蹄跡言不及也。教如跡故。越於物<a 
name="0916b22" id="0916b22"></a>象。非世間物喻所喻故。雖然如此。然乃包含諸法。諸<a 
name="0916b23" id="0916b23"></a>法之總主也。上約本來法性。下就當為覺性。此非唯<a 
name="0916b24" id="0916b24"></a>是出纏。前亦非唯在纏。但以超言非境而為對也。上<a 
name="0916c01" id="0916c01"></a>即超於言象。豈出纏者不如此也。下是耳目非境。豈<a 
name="0916c02" id="0916c02"></a>在纏者視聽之也。上即攝末歸本。下即以本末。如是<a 
name="0916c03" id="0916c03"></a>寧由在纏出。若以覺字為出纏者。豈非本來覺性者<a 
name="0916c04" id="0916c04"></a>也。直成佛時。為覺性乎。但云出纏號大法身。不約出<a 
name="0916c05" id="0916c05"></a>纏方為覺性。諸後思之。</p><a 
name="0916c06" id="0916c06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。方智雲於縠月等者。此下四句。餘莫能知。初之二<a 
name="0916c07" id="0916c07"></a>句。菩薩猶迷真。第十地亦未明證。先喻後法。方者比<a 
name="0916c08" id="0916c08"></a>也。其第十地。名法雲地。智如雲故。此雲之智。名方智<a 
name="0916c09" id="0916c09"></a>雲。或可解云。方者大也。大法之智。如似雲故。名智雲。<a 
name="0916c10" id="0916c10"></a>以此菩薩觀於真如。似隔羅縠而觀於月未明故也。<a 
name="0916c11" id="0916c11"></a>有佛地障。故是猶迷。問。何故此地智如其雲。有三義。<a 
name="0916c12" id="0916c12"></a>一者含其眾水。二者能蔽&#x8657;空。三者充滿&#x8657;空。智亦<a 
name="0916c13" id="0916c13"></a>三義。一者智能含藏總持等水。二者智能隱覆如空。<a 
name="0916c14" id="0916c14"></a>麤重二障廣大如&#x8657;空也。三者智能充滿如空。法身<a 
name="0916c15" id="0916c15"></a>智上功德。如水遍故。</p><a 
name="0916c16" id="0916c16"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。假慧刃於藂筠等者。此之二句。聲聞不測。亦有法<a 
name="0916c17" id="0916c17"></a>喻。舉其利根亦未知證。假者藉也。或假設也。言慧刃<a 
name="0916c18" id="0916c18"></a>者。慧如刃故。是利根人。有束或超。劈竹斷者。一竹可<a 
name="0916c19" id="0916c19"></a>爾。於其多竹。不能頓爾。藂即多也。筠即是竹。今取多<a 
name="0916c20" id="0916c20"></a>竹不能一刃而劈之爾。聲聞之人。但證生空。不能證<a 
name="0916c21" id="0916c21"></a>於法空理故。此是不測。不頓斷也。對上縠月見而不<a 
name="0916c22" id="0916c22"></a>明。今此藂筠劈而非頓。揆者度也。與察大同。神之與<a 
name="0916c23" id="0916c23"></a>靈。皆訓妙也。理即真理。機者要也。並目真如。大義可<a 
name="0916c24" id="0916c24"></a>知。</p><a 
name="0917a01" id="0917a01"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。至道圓而粹容顯等者。此下二句。二身齊證。初之<a 
name="0917a02" id="0917a02"></a>一句。得自受用身。後之一句。證得法身。皆由解脫至<a 
name="0917a03" id="0917a03"></a>極道圓而獲得故。粹者純也。容即身儀。故此自其自<a 
name="0917a04" id="0917a04"></a>受用身。實際真識。並是真如。淨即出纏。故是法身。彰<a 
name="0917a05" id="0917a05"></a>即明也。顯智<span class="corr" title='[巳&gt;已]'>已</span>證。淨即離障。由智證明。故得離障稱<a 
name="0917a06" id="0917a06"></a>法身也。</p><a 
name="0917a07" id="0917a07"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。然後者。證實身<span class="corr" title='[巳&gt;已]'>已</span>。然後隨機。現其權身。</p><a 
name="0917a08" id="0917a08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。俯提十地等者。初彰隨機變現。後明變現儀範。初<a 
name="0917a09" id="0917a09"></a>有四句。二句他報。二句化身。他報應於十地。俯者下<a 
name="0917a10" id="0917a10"></a>也。佛居其上。俯而接之。現其佛身。&#x758E;謂分&#x758E;可了之<a 
name="0917a11" id="0917a11"></a>義。自受法身。人所不了。故云&#x758E;。海目。此且舉目如四<a 
name="0917a12" id="0917a12"></a>大海。更有眉如五須彌等。為文難具。言寶方者。顯是<a 
name="0917a13" id="0917a13"></a>淨土唯實所成。他報唯現居淨土故。下辨化身。控亦<a 
name="0917a14" id="0917a14"></a>提也。挺者出也。為接三乘。現出化身。化身通被三乘<a 
name="0917a15" id="0917a15"></a>眾故。化身如山。魔不動故。又如須彌。最尊高故。多居<a 
name="0917a16" id="0917a16"></a>穢土。名為垢域。現丘陵等。名之為穢。非關有漏。</p><a 
name="0917a17" id="0917a17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。若鴻鍾等者。上法釋<span class="corr" title='[巳&gt;已]'>已</span>。下四句喻。雖有兩事。並通<a 
name="0917a18" id="0917a18"></a>他化。能現之身。如鍾如月。所現之身。如聲如影。其如<a 
name="0917a19" id="0917a19"></a>鍾者。能現之身。內離所執。名之為空。如鍾內不&#x8657;不<a 
name="0917a20" id="0917a20"></a>能發聲。又其實身慈悲之體。本欲利他。即如其鍾。外<a 
name="0917a21" id="0917a21"></a>受扣擊。名之為受。本未受扣。亦未發聲。扣擊即是機<a 
name="0917a22" id="0917a22"></a>欲緣也。其如月者。能現之身。內離二障。名之為證。如<a 
name="0917a23" id="0917a23"></a>月不澄。即無其光。又其實身。發利他用。即如其月。外<a 
name="0917a24" id="0917a24"></a>發光輝。即現其形。不發外輝。影亦不現。今復略云&#x8657;<a 
name="0917b01" id="0917b01"></a>非執。證即離障。受即待機。輝即發用。上辨能現。所被<a 
name="0917b02" id="0917b02"></a>別者。扣擊器清。皆是機故。扣擊即是根熟。啟請待度<a 
name="0917b03" id="0917b03"></a>等緣器清。即是眾生。無有不信等障。根熟無障。方乃<a 
name="0917b04" id="0917b04"></a>現也。鴻者大也。皎魄月也。循者順也。肆者恣也。縱放<a 
name="0917b05" id="0917b05"></a>之義。今取實身縱恣現於權身者也。或可二喻他化<a 
name="0917b06" id="0917b06"></a>有別。他報無有陰生之相。如直鍾處出於聲也。化身<a 
name="0917b07" id="0917b07"></a>降生母等如器。雖器喻機。機有母等。故可別也。</p><a 
name="0917b08" id="0917b08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。故有者。乘前標後。由前應機。其二身他化不同。故<a 
name="0917b09" id="0917b09"></a>有儀軌。二身亦異。若依前解。遠承法說。若依後解。亦<a 
name="0917b10" id="0917b10"></a>近承喻。</p><a 
name="0917b11" id="0917b11"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。紹隆報佛等。下示儀範。先示。後結。示中有二。由彼<a 
name="0917b12" id="0917b12"></a>二身各有初後之二相故。文有兩節。於他報中。初即<a 
name="0917b13" id="0917b13"></a>受職成佛。後即趍座說法。無有處天及下生故。於化<a 
name="0917b14" id="0917b14"></a>身中。初即處天。後即降生。初之四句。辨其初相二身<a 
name="0917b15" id="0917b15"></a>之文。各有二句。紹即繼也。隆者盛也。前佛成處。名之<a 
name="0917b16" id="0917b16"></a>為隆。繼紹彼隆。名為紹隆。言甄臺者。甄叔迦寶為臺<a 
name="0917b17" id="0917b17"></a>座故。其受職時。在此座上。名之為於。纂亦繼也。業即<a 
name="0917b18" id="0917b18"></a>基業。大同紹隆。貽者遺也。離本而現。&#x5374;留麤身。令機<a 
name="0917b19" id="0917b19"></a>得遇。名之為遺。遺身在紫紺摩尼殿兜率天也。貽字<a 
name="0917b20" id="0917b20"></a>有本作移。詳之。</p><a 
name="0917b21" id="0917b21"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。撫根熟而趍座等者。此下二句。辨其後相一身一<a 
name="0917b22" id="0917b22"></a>句。撫者恤也。趍者赴也。他受用身。不從天下。直趍其<a 
name="0917b23" id="0917b23"></a>座。成佛說法。化身翻此。降即下生。跡即縱跡。處胎等<a 
name="0917b24" id="0917b24"></a>相。皆蹤跡故。</p><a 
name="0917c01" id="0917c01"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。斯乃者。斯猶此也。指上儀狀。乃是語詞。指<span class="corr" title='[巳&gt;已]'>已</span>方結。<a 
name="0917c02" id="0917c02"></a>結文亦別。然須影顯。三際之言。亦通化身。一生補處。<a 
name="0917c03" id="0917c03"></a>亦通他報。宏者大也。靈者妙也。無文說於化身灌頂。<a 
name="0917c04" id="0917c04"></a>故受職言目於他報。直順此經一生補處。故後但結<a 
name="0917c05" id="0917c05"></a>化身者也。此但結至。故有下文云儀範故。不結<span class="corr" title='[巳&gt;已]'>已</span>前<a 
name="0917c06" id="0917c06"></a>隨機變現。詳之可知。或可言之在近。勢連遠起。問。一<a 
name="0917c07" id="0917c07"></a>生補處他報何有。答。准法苑章。亦義准有。如彼具明。<a 
name="0917c08" id="0917c08"></a>不煩示之。問。爾化身何處之身。名為一生。又所補處。<a 
name="0917c09" id="0917c09"></a>為天為人。答。准法苑章。補闕佛處。即在人處。非關兜<a 
name="0917c10" id="0917c10"></a>率。其一生者。非是指其成佛之身。更有一生。方補處<a 
name="0917c11" id="0917c11"></a>故。於此義中。或約大生。即指菩薩。欲往兜率。在佛前<a 
name="0917c12" id="0917c12"></a>身。為名一生。更有人天一往來故。兩本兩中。共為一<a 
name="0917c13" id="0917c13"></a>生。或約小生。即指天上。菩薩之身。唯有下生。一中本<a 
name="0917c14" id="0917c14"></a>故。中本別言。或四或二。今約一期。合之為一。名為一<a 
name="0917c15" id="0917c15"></a>生。問。如是皆是菩薩之身。如何順此佛身耶。答。此對<a 
name="0917c16" id="0917c16"></a>他報。並名化身。化身亦有因之與果。相從隨舉。不須<a 
name="0917c17" id="0917c17"></a>具言。體例皆爾。不相違也。</p><a 
name="0917c18" id="0917c18"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。今此經者等者。經體雖二。下歎便亦兼其行果。從<a 
name="0917c19" id="0917c19"></a>經出故。故有四喻。如次應知。或於義中。亦攝行果。抄<a 
name="0917c20" id="0917c20"></a>斷非勝。今唯取此。教如一雨。能普洒故。理若其河。三<a 
name="0917c21" id="0917c21"></a>獸度故。六行似舟。運越河故。其果如車。引出宅故。果<a 
name="0917c22" id="0917c22"></a>中有二。一者天果。二者涅槃。車雖通餘。今且喻果。但<a 
name="0917c23" id="0917c23"></a>云引故。其河對獸。要其濟義。度時有濟。直喻此經。<a 
name="0917c24" id="0917c24"></a>其河對舟。要其溺義。以舟越之。喻經所拔。軫是車後<a 
name="0918a01" id="0918a01"></a>之橫木也。今舉要車。問。六行如舟。如何合數。答。隨數<a 
name="0918a02" id="0918a02"></a>言之。六為六<span class="gaiji">&#x4E87;<!--gaiji,個,1[乞-乙+〡],2&#x4E87;,3--></span>。非必一舟。要其六事。不爾唯有上上<a 
name="0918a03" id="0918a03"></a>生故。或船底船相船舍&#x69F9;棹墜石稍公為六。六度六<a 
name="0918a04" id="0918a04"></a>事且具舉之。亦無妨礙。</p><a 
name="0918a05" id="0918a05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏。雖復探幽曉秘等者。此上四喻。歎經功能。此下之<a 
name="0918a06" id="0918a06"></a>文。潛通妨難。於中有二。初通前唯讚此經妨。後轉通<a 
name="0918a07" id="0918a07"></a>但益一時妨。初近三行。於中有二。初正通。後釋成。外<a 
