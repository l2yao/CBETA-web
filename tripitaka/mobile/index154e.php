<div class="root" id="0303" sutra_name="楞嚴經貫攝" sutra_juan="[卷9]">　
<a 
name="0517b15" id="0517b15"></a><a 
name="0517b16" id="0517b16"></a><a 
name="0517b17" id="0517b17"></a><a 
name="0517b18" id="0517b18"></a><p><span class="juanname">楞嚴說通卷之九</span></p><a 
name="0517b19" id="0517b19"></a><p style="margin-left: 4em"><span class="byline">巴縣了庵居士劉道開纂述</span></p><a 
name="0517b20" id="0517b20"></a><p style="margin-left: 4em"><span class="byline">成都昭覺沙門方　示鑒定</span></p><a 
name="0517b21" id="0517b21"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>阿難。世間一切所修心人。不假禪那。無有智慧。但能<a 
name="0517b22" id="0517b22"></a>執身。不行婬欲。若行若坐。想念俱無。愛染不生。無留<a 
name="0517b23" id="0517b23"></a>欲界。是人應念。身為梵侶。如是一類。名梵眾天。</p><a 
name="0517b24" id="0517b24"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此下明色界四禪十八天而此先明初禪第一天<a 
name="0517c01" id="0517c01"></a>也。阿難。世間一切所修心人。凡迷冥修。各各自謂<a 
name="0517c02" id="0517c02"></a>真正修心。然而不假禪那。無首楞嚴定。無有智慧。<a 
name="0517c03" id="0517c03"></a>無金剛慧。但以有漏靜慮為因心。制彼婬習。而執<a 
name="0517c04" id="0517c04"></a>身不婬。或行或坐。於一切時。想念俱無。愛染不生。<a 
name="0517c05" id="0517c05"></a>夫六欲縱強忘情。不免身犯。此身全遠。梵行方成。<a 
name="0517c06" id="0517c06"></a>由彼身心俱潔。下無卜居。故臨終時。應念而生梵<a 
name="0517c07" id="0517c07"></a>世。為梵民。如是一類。名梵眾天。而較之欲界穢濁。<a 
name="0517c08" id="0517c08"></a>又高出一層矣。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○通後十八天。雖離欲染。尚有色<a 
name="0517c09" id="0517c09"></a>質。故通名色界。又通名梵世。謂<span class="corr" title='[巳&gt;已]'>已</span>離欲染也。通號<a 
name="0517c10" id="0517c10"></a>四禪。為<span class="corr" title='[巳&gt;已]'>已</span>離散動也。欲天但十善感生。此天兼禪<a 
name="0517c11" id="0517c11"></a>定感生。然特有漏禪觀。六事行耳。六行者。厭欲界<a 
name="0517c12" id="0517c12"></a>是苦是粗是障。欣色界是淨是玅是離。此則凡夫<a 
name="0517c13" id="0517c13"></a>伏惑。超欲界道也。問。前天亦間有禪定。而此界方<a 
name="0517c14" id="0517c14"></a>名禪者何故。答。以前結云形雖出動。心跡尚交。自<a 
name="0517c15" id="0517c15"></a>此以上。絕無女人。心&#x8FF9;俱離。無所交接。兼無食眠。<a 
name="0517c16" id="0517c16"></a>三欲俱忘。稍覺飢倦。即入禪定。出定則飽滿精明。<a 
name="0517c17" id="0517c17"></a>是但以禪悅為食為息。而稍離粗重身心也。又此<a 
name="0517c18" id="0517c18"></a>天獨顯戒德。而未彰定慧。後二天略顯定慧。至文<a 
name="0517c19" id="0517c19"></a>自見也。</p><a 
name="0517c20" id="0517c20"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>欲習既除。離欲心現。於諸律儀。愛樂隨順。是人應時。<a 
name="0517c21" id="0517c21"></a>能行梵德。如是一類。名梵輔天。</p><a 
name="0517c22" id="0517c22"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此明初禪第二天此天戒與定共也。夫前天欲習<a 
name="0517c23" id="0517c23"></a>雖除。心相未至顯現。此則離欲清淨。心相現發著<a 
name="0517c24" id="0517c24"></a>明。於諸梵行戒品。愛樂而悅豫。隨順而輕安。此即<a 
name="0518a01" id="0518a01"></a>與定共之相也。是人臨終。宿習不妄。應念而行梵<a 
name="0518a02" id="0518a02"></a>德。不但清淨身心。亦且弘揚德化。如是一類。名梵<a 
name="0518a03" id="0518a03"></a>輔天。而能匡弼梵王矣。</p><a 
name="0518a04" id="0518a04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>身心玅圓。威儀不缺。清淨禁戒。加以明悟。是人應時。<a 
name="0518a05" id="0518a05"></a>能統梵眾。為大梵王。如是一類。名大梵天。</p><a 
name="0518a06" id="0518a06"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此明初禪第三天此天戒與慧俱也。夫初天由執<a 
name="0518a07" id="0518a07"></a>身而攝心。次天由心現而行德。此則雙攝身心得<a 
name="0518a08" id="0518a08"></a>一如無二之玅。滿足分量之圓。於是行住坐臥皆<a 
name="0518a09" id="0518a09"></a>玅圓。而威儀不缺。以此清淨禁戒。而又加以明悟。<a 
name="0518a10" id="0518a10"></a>從定戒中。發生智慧。此即與慧俱之相也。由其慧<a 
name="0518a11" id="0518a11"></a>勝前天。堪為梵主。是人臨終。應念而生梵天。統攝<a 
name="0518a12" id="0518a12"></a>梵眾為大梵王。如是一類。為大梵天。其德皆足以<a 
name="0518a13" id="0518a13"></a>長人矣。</p><a 
name="0518a14" id="0518a14"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>阿難。此三勝流。一切苦惱。所不能逼。雖非正修真三<a 
name="0518a15" id="0518a15"></a>摩地。清淨心中。諸漏不動。名為初禪。</p><a 
name="0518a16" id="0518a16"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此統結三天名初禪也。阿難。此三天者。皆勝欲界<a 
name="0518a17" id="0518a17"></a>諸趣。同名勝流。<span class="corr" title='[巳&gt;已]'>已</span>離欲界八苦。故苦惱不逼。雖非<a 
name="0518a18" id="0518a18"></a>正修真三摩地。去首楞嚴大定甚遠。然彼戒心清<a 
name="0518a19" id="0518a19"></a>淨。漏雖未伏。亦自遏抑不動。初得禪定。無有剎那<a 
name="0518a20" id="0518a20"></a>雜念間入。故統而名之曰初禪焉。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○禪有四類。一<a 
name="0518a21" id="0518a21"></a>有漏禪。即今四禪也。二無漏禪。謂九想八背等也。<a 
name="0518a22" id="0518a22"></a>三亦有漏亦無漏禪。謂六玅通明等也。四非有漏<a 
name="0518a23" id="0518a23"></a>非無漏禪。即今經首楞嚴大定也。以上三天。疑非<a 
name="0518a24" id="0518a24"></a>&#x7AEA;分。夫天民天臣天王。各居一層。何成一統。且臣<a 
name="0518b01" id="0518b01"></a>民若許眾多。而天王滿天。獨居一層。彼此皆王。何<a 
name="0518b02" id="0518b02"></a>所使令。或是以德之優劣相較。堪為民。為輔。為王<a 
name="0518b03" id="0518b03"></a>耳。故列為三者。非一天盡臣民而無王。一天盡王<a 
name="0518b04" id="0518b04"></a>而無臣民也。又初之二天。尋伺皆有。第三天無尋<a 
name="0518b05" id="0518b05"></a>有伺。由內有尋伺。故外感火災。而火災壞初禪也。</p><a 
name="0518b06" id="0518b06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>阿難。其次梵天。統攝梵人。圓滿梵行。澄心不動。寂湛<a 
name="0518b07" id="0518b07"></a>生光。如是一類。名少光天。</p><a 
name="0518b08" id="0518b08"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此二禪初天也。阿難。其次二禪。即此大梵天統攝<a 
name="0518b09" id="0518b09"></a>梵人者。而能圓滿梵行。至於澄心不動。則寂湛之<a 
name="0518b10" id="0518b10"></a>極。而生光明。但光明尚劣。未極其量。如是一類。又<a 
name="0518b11" id="0518b11"></a>昇梵天一層。名少光天。所謂泰宇定而天光發。但<a 
name="0518b12" id="0518b12"></a>非周&#x5FA7;湛寂之比耳。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○此天疑從彼天中修來。&#x8462;<a 
name="0518b13" id="0518b13"></a>躡前行而但加圓滿。則升此天矣。問。此與菩薩寂<a 
name="0518b14" id="0518b14"></a>照何殊。答。人法二執。毫髮未動。但以離欲得定。定<a 
name="0518b15" id="0518b15"></a>深生照而<span class="corr" title='[巳&gt;已]'>已</span>。此其別也。又前有覺觀粗動。發生三<a 
name="0518b16" id="0518b16"></a>識。今內淨一心。<span class="corr" title='[巳&gt;已]'>已</span>無覺觀。惟緣內塵。不同初禪有<a 
name="0518b17" id="0518b17"></a>三識也。</p><a 
name="0518b18" id="0518b18"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>光光相然。照耀無盡。映十方界。&#x5FA7;成琉璃。如是一類。<a 
name="0518b19" id="0518b19"></a>名無量光天。</p><a 
name="0518b20" id="0518b20"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此二禪第二天也。從前少光。更發多光。光光相然。<a 
name="0518b21" id="0518b21"></a>照耀無盡。映十方界。成琉璃色。此雖<span class="corr" title='[巳&gt;已]'>已</span>極其量。猶<a 
name="0518b22" id="0518b22"></a>未成音。如是一類。名無量光天。然非如淨琉璃內<a 
name="0518b23" id="0518b23"></a>含寶月之比也。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○按此天雖不能映諸大千。而於<a 
name="0518b24" id="0518b24"></a>本界。亦覆小千。當滿一千箇四天下十方界也。</p><a 
name="0518c01" id="0518c01"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>吸持圓光。成就教體。發化清淨。應用無盡。如是一類。<a 
name="0518c02" id="0518c02"></a>名光音天。</p><a 
name="0518c03" id="0518c03"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此二禪第三天也。夫光光相然。則成圓光矣。由是<a 
name="0518c04" id="0518c04"></a>吸攬保持此圓光。即以光明代其言音。而成就教<a 
name="0518c05" id="0518c05"></a>體。於以闡揚梵教。發清淨化。隨機開示。應用無盡。<a 
name="0518c06" id="0518c06"></a>如是一類。名光音天。謂其不假言詮而以光為音<a 
name="0518c07" id="0518c07"></a>也。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○按此天<span class="corr" title='[巳&gt;已]'>已</span>無尋伺。故無語言。而但用光明作<a 
name="0518c08" id="0518c08"></a>佛事。然亦非盡無語言也。如世間紙墨文字。雖代<a 
name="0518c09" id="0518c09"></a>言教。豈盡廢其言語哉。</p><a 
name="0518c10" id="0518c10"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>阿難。此三勝流。一切憂懸。<span class="note">(懸不安也。或作愁字之誤)</span>所不能逼。雖<a 
name="0518c11" id="0518c11"></a>非正修真三摩地。清淨心中。粗漏<span class="corr" title='[巳&gt;已]'>已</span>伏。名為二禪。</p><a 
name="0518c12" id="0518c12"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此總結三天名二禪也。阿難此三勝流。當定生時。<a 
name="0518c13" id="0518c13"></a>與喜俱發。以有定喜。滅世間憂愁。故憂懸不能逼。<a 
name="0518c14" id="0518c14"></a>雖非正修三摩。去楞嚴大定尚遠。然而清淨心中。<a 
name="0518c15" id="0518c15"></a><span class="corr" title='[巳&gt;已]'>已</span>伏粗漏。較初禪之漏心不動而未能伏者。<span class="corr" title='[巳&gt;已]'>已</span>進<a 
name="0518c16" id="0518c16"></a>一籌。故統而名之曰二禪焉。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○地持論。目第二禪<a 
name="0518c17" id="0518c17"></a>名喜俱禪。此三天總名定生喜樂地。憂懸與苦惱<a 
name="0518c18" id="0518c18"></a>不同。苦惱乃粗重。切於身心。憂懸輕細。但涉於念<a 
name="0518c19" id="0518c19"></a>慮而<span class="corr" title='[巳&gt;已]'>已</span>。粗漏<span class="corr" title='[巳&gt;已]'>已</span>伏者。謂以定力伏前五識。由伏前<a 
name="0518c20" id="0518c20"></a>五。故無尋伺。未伏第六。故有喜樂。內有喜水擾動。<a 
name="0518c21" id="0518c21"></a>故外感水災。而水災壞二禪也。前三天以戒勝。此<a 
name="0518c22" id="0518c22"></a>三天以定勝也。</p><a 
name="0518c23" id="0518c23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>阿難。如是天人。圓光成音。披音露玅。發成精行。通寂<a 
name="0518c24" id="0518c24"></a>滅樂。如是一類。名少淨天。</p><a 
name="0519a01" id="0519a01"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此三禪第一天也。阿難。由前天人。既以圓光而為<a 
name="0519a02" id="0519a02"></a>教體矣。披光即披音也。披音。則玅理自露。然有喜<a 
name="0519a03" id="0519a03"></a>心。分別亂動。能障定樂。此天能依此玅理。發成精<a 
name="0519a04" id="0519a04"></a>行。而通寂滅樂。離前喜動。而生淨樂。恬泊寂靜。故<a 
name="0519a05" id="0519a05"></a>名寂滅。由伏第六識。故相似二乘之寂滅。非生滅<a 
name="0519a06" id="0519a06"></a>既滅寂滅現前之比也。如是一類。名少淨天。定樂<a 
name="0519a07" id="0519a07"></a>纔通。淨空初顯。故名少淨也。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○能通而<span class="corr" title='[巳&gt;已]'>已</span>。尚未能<a 
name="0519a08" id="0519a08"></a>成。以猶劣故。名少淨焉。</p><a 
name="0519a09" id="0519a09"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>淨空現前。引發無際。身心輕安。成寂滅樂。如是一類。<a 
name="0519a10" id="0519a10"></a>名無量淨天。</p><a 
name="0519a11" id="0519a11"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此三禪第二天也。由伏六識。而似得人空。故淨空<a 
name="0519a12" id="0519a12"></a>現前。前但得淨。此更發空。漸以&#x8657;豁。至無邊際。且<a 
name="0519a13" id="0519a13"></a>前通寂滅。但樂內心。此則身心內外。皆得輕安。積<a 
name="0519a14" id="0519a14"></a>中發外。廓然廣大。則寂滅<span class="corr" title='[巳&gt;已]'>已</span>成。淨空無量。如是一<a 
name="0519a15" id="0519a15"></a>類。名無量淨天。而升少淨一層矣。</p><a 
name="0519a16" id="0519a16"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>世界身心。一切圓淨。淨德成就。勝託現前。歸寂滅樂。<a 
name="0519a17" id="0519a17"></a>如是一類。名&#x5FA7;淨天。</p><a 
name="0519a18" id="0519a18"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此三禪第三天也。前天淨樂。止在身心。未名為&#x5FA7;。<a 
name="0519a19" id="0519a19"></a>此則合世界身心。觸處無非玅樂。是謂一切圓淨。<a 
name="0519a20" id="0519a20"></a>既世界身心圓淨。豈非淨德成就乎。然雖淨德成<a 
name="0519a21" id="0519a21"></a>就。原非可託可歸之地。此天人便認此為終身託<a 
name="0519a22" id="0519a22"></a>宿之地。而勝託現前。認此為清淨極樂之鄉。而歸<a 
name="0519a23" id="0519a23"></a>寂滅樂。將謂安身立命。唯此<span class="corr" title='[巳&gt;已]'>已</span>而。由前未融世界。<a 
name="0519a24" id="0519a24"></a>雖淨而不&#x5FA7;。今並融之。故如是一類。名&#x5FA7;淨天。又<a 
name="0519b01" id="0519b01"></a>升無量淨一層矣。</p><a 
name="0519b02" id="0519b02"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>阿難。此三勝流。具大隨順。身心安隱。得無量樂。雖非<a 
name="0519b03" id="0519b03"></a>正得真三摩地。安隱心中。歡喜畢具。名為三禪。</p><a 
name="0519b04" id="0519b04"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此總結三天名三禪也。阿難。此三勝流。隨順淨樂。<a 
name="0519b05" id="0519b05"></a>至無量周&#x5FA7;。其為隨順也大矣。且初二禪心有喜<a 
name="0519b06" id="0519b06"></a>受。故多散動。不得身親受樂。今則喜受既除。身心<a 
name="0519b07" id="0519b07"></a>安穩。自在受用。無量玅樂。此雖非正得真三摩地<a 
name="0519b08" id="0519b08"></a>乎。而安穩心中。歡喜畢具。夫喜者動心所發也。樂<a 
name="0519b09" id="0519b09"></a>者靜心所融也。若在飛動心。縱樂而仍是喜支也。<a 
name="0519b10" id="0519b10"></a>若在安穩心。縱喜而仍是樂支也。今歡喜而出於<a 
name="0519b11" id="0519b11"></a>安穩。是謂離喜之喜。玅樂之樂。離喜玅樂。故統而<a 
name="0519b12" id="0519b12"></a>名之曰三禪焉。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○地持論目第三禪為樂俱禪。此<a 
name="0519b13" id="0519b13"></a>三天總名離喜玅樂地。&#x8462;前二禪雖有樂支。為喜<a 
name="0519b14" id="0519b14"></a>支所障。今滅喜純樂。故得其名也。又此三天<span class="corr" title='[巳&gt;已]'>已</span>伏<a 
name="0519b15" id="0519b15"></a>意識。雖無喜受。尚有出入息擾亂。故外感風災。而<a 
name="0519b16" id="0519b16"></a>風災壞三禪也。</p><a 
name="0519b17" id="0519b17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>阿難。復次天人不逼身心。苦因<span class="corr" title='[巳&gt;已]'>已</span>盡。樂非常住。久必<a 
name="0519b18" id="0519b18"></a>壞生。苦樂二心。俱時頓捨。粗重相滅。淨福性生。如是<a 
name="0519b19" id="0519b19"></a>一類。名福生天。</p><a 
name="0519b20" id="0519b20"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此下明四禪九天而此明第一天也。然天雖有九。<a 
name="0519b21" id="0519b21"></a>而四禪報境。但有三天。第四無想。乃廣果別開。是<a 
name="0519b22" id="0519b22"></a>外道報境。此四之上五不還天。乃聖賢別修靜慮。<a 
name="0519b23" id="0519b23"></a>與凡夫不同也。阿難。復次三禪天人。初苦禪惱離。<a 
name="0519b24" id="0519b24"></a>二禪憂懸盡。皆能不逼身心。苦因<span class="corr" title='[巳&gt;已]'>已</span>盡。第三禪則<a 
name="0519c01" id="0519c01"></a>無苦有樂。然而樂亦不當受之也。&#x8462;樂非常住。久<a 
name="0519c02" id="0519c02"></a>必壞生。壞即成苦矣。此天既<span class="corr" title='[巳&gt;已]'>已</span>離苦。亦不受樂。將<a 
name="0519c03" id="0519c03"></a>苦樂二心。一時頓捨。樂既不受。壞亦不生。苦壞雙<a 
name="0519c04" id="0519c04"></a>超。故粗重相滅。一切下地災患<span class="corr" title='[巳&gt;已]'>已</span>斷。而清淨福性。<a 
name="0519c05" id="0519c05"></a>油然發生。由捨生福。名福生天焉。可見不能捨樂。<a 
name="0519c06" id="0519c06"></a>則壞生矣。苟能捨樂。則福生矣。捨不捨之間。此天<a 
name="0519c07" id="0519c07"></a>之別於前天也。</p><a 
name="0519c08" id="0519c08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>捨心圓融。勝解清淨。福無遮中。得玅隨順。窮未來際。<a 
name="0519c09" id="0519c09"></a>如是一類。名福愛天。</p><a 
name="0519c10" id="0519c10"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此四禪第二天也。前天初專於捨。未免偏空。此既<a 
name="0519c11" id="0519c11"></a>純熟。漸不礙有。是謂捨心圓融。前以捨樂為清淨。<a 
name="0519c12" id="0519c12"></a>此則樂受既空。復不礙有。是謂勝解清淨。以勝淨<a 
name="0519c13" id="0519c13"></a>生勝福。故福無遮限。有所願求。無不如意。得玅隨<a 
name="0519c14" id="0519c14"></a>順。直窮於未來際焉。然雖受報無窮。亦非真得常<a 
name="0519c15" id="0519c15"></a>住。如是一類。名福愛天。謂諸天之福。惟此天為最<a 
name="0519c16" id="0519c16"></a>可愛也。</p><a 
name="0519c17" id="0519c17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>阿難。從是天中。有二岐路。</p><a 
name="0519c18" id="0519c18"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此明四禪第三天而先別其有二路也。阿難。從是<a 
name="0519c19" id="0519c19"></a>福愛天而升焉。有兩條岐路。一直往而趣廣果。一<a 
name="0519c20" id="0519c20"></a>迂僻而趣無想。心念行業。各自不同。學者可弗辨<a 
name="0519c21" id="0519c21"></a>耶。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○賀云。二岐路者。一精進。一斷滅也。不取苦樂。<a 
name="0519c22" id="0519c22"></a>亦不厭苦樂。但以四無量心。薰修福德。證廣大果。<a 
name="0519c23" id="0519c23"></a>名廣果天。若以苦樂為可厭。而執著捨心。精研不<a 
name="0519c24" id="0519c24"></a>斷。所謂苦樂心滅。厭捨心生。全是生滅之因矣。從<a 
name="0520a01" id="0520a01"></a>此窮極捨道。獲受<span class="gaiji">&#x7070;<!--gaiji,灰,1[厂@火],2&#x7070;,3--></span>心之果。彼自以為不生不滅。<a 
name="0520a02" id="0520a02"></a>豈知生滅之因。必不能發明無生滅之性乎。初生<a 
name="0520a03" id="0520a03"></a>此天。未全無想。經半劫始無。後半劫復生矣。</p><a 
name="0520a04" id="0520a04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若於先心無量淨光。福德圓明。修證而住。如是一類。<a 
name="0520a05" id="0520a05"></a>名廣果天。</p><a 
name="0520a06" id="0520a06"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>何謂二岐。一者。若於福愛天中。圓融捨心。所發無<a 
