name="0520c04" id="0520c04"></a><span class="corr" title='[巳&gt;已]'>已</span>隔天淵。況如來真三摩地不動之體。便欲竊而<a 
name="0520c05" id="0520c05"></a>擬之哉。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○地持論。目四禪為捨俱禪。</p><a 
name="0520c06" id="0520c06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>阿難。此中復有五不還天。於下界中。九品習氣。俱時<a 
name="0520c07" id="0520c07"></a>滅盡。苦樂雙忘。下無卜居。故於捨心眾同分中。安立<a 
name="0520c08" id="0520c08"></a>居處。</p><a 
name="0520c09" id="0520c09"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此明五不還天寄居廣果之由也。阿難。此廣果天<a 
