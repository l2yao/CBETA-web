name="0520c13" id="0520c13"></a>忘而色界三地無卜居。惟四禪捨念清淨。是其同<a 
name="0520c14" id="0520c14"></a>分。故獨於此中。安立居處。住此<span class="corr" title='[巳&gt;已]'>已</span>斷七十二品最<a 
name="0520c15" id="0520c15"></a>細貪嗔痴慢。希成阿羅漢矣。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○梵語阿那含。此云<a 
name="0520c16" id="0520c16"></a>不還。謂不復欲界受生也。亦名五淨居。習氣。即思<a 
name="0520c17" id="0520c17"></a>惑種子。思惑。謂貪嗔痴慢。任運而起微細之惑。非<a 
