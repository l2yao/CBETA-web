name="0520a19" id="0520a19"></a>道。而必求究竟。不惟樂受捨。而捨樂之心亦捨。以<a 
name="0520a20" id="0520a20"></a>捨定滅除捨心。至於身心<span class="gaiji">&#x7070;<!--gaiji,灰,1[厂@火],2&#x7070;,3--></span>滅。渾成晦昧。冥然一<a 
