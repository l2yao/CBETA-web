name="0520a10" id="0520a10"></a>至此為極。故名廣果也。要知四禪報境。惟此三天。<a 
name="0520a11" id="0520a11"></a>第四無想。是福愛之差。及五不還天。乃三果聖人<a 
name="0520a12" id="0520a12"></a>之別寓耳。</p><a 
name="0520a13" id="0520a13"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若於捨心。雙厭苦樂。精研捨心。相續不斷。圓窮捨道。<a 
name="0520a14" id="0520a14"></a>身心俱滅。心慮<span class="gaiji">&#x7070;<!--gaiji,灰,1[厂@火],2&#x7070;,3--></span>凝。經五百劫。是人既以生滅為因。<a 
