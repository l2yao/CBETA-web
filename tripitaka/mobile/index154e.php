name="0538c15" id="0538c15"></a>聖。<span class="note">(此亦異前。謂得聖師而登聖果也)</span>大妄語成。墮無間獄。汝等。<span class="note">(前專責阿<a 
name="0538c16" id="0538c16"></a>難。此等字兼囑大眾)</span>必須將如來語。於我滅後。傳示末法。&#x5FA7;令<a 
name="0538c17" id="0538c17"></a>眾生開悟斯義。無令天魔得其方便。保持覆護。成無<a 
name="0538c18" id="0538c18"></a>上道。</p><a 
name="0538c19" id="0538c19"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此總結想陰十境而勉令覆護也。講與前同。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○尊<a 
