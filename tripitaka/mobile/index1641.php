name="0808b20" id="0808b20"></a><a 
name="0808b21" id="0808b21"></a><p style="margin-left: 4em"><span class="byline">後秦龜茲國三藏鳩摩羅什譯</span></p><a 
name="0808b22" id="0808b22"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若種少善於勝福田，人天受樂，後得涅槃。是<a 
name="0808b23" id="0808b23"></a>以智者應當勤心修集善業。言福田者，即<a 
name="0808b24" id="0808b24"></a>是佛也。佛身光明如融金聚，功德智慧以<a 
name="0808b25" id="0808b25"></a>自莊嚴。得圓足眼，善能觀察眾生諸根。世<a 
name="0808b26" id="0808b26"></a>間黑闇為作燈明，眾生愚癡為作親善，眾<a 
name="0808b27" id="0808b27"></a>善悉備、名稱普聞。牟尼世尊眾所歸依，是<a 
name="0808b28" id="0808b28"></a>故人天至心修福無不獲報。</p><a 
name="0808b29" id="0808b29"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>昔王舍城，五山圍繞，於五摩伽陀最處其<a 
name="0808c01" id="0808c01"></a>裏。此王城內，里巷相當、<span class="gaiji">[這-言+(序-予+手)]<!--gaiji,,1[這-言+(序-予+手)],2&#x284CD;,3--></span>園廣博、臺觀<a 
name="0808c02" id="0808c02"></a>嚴麗、堂室綺妙、高軒敞朗，周匝欄楯。有<a 
name="0808c03" id="0808c03"></a>好林池，甚可愛樂。其水清淨，溫涼調適，通<a 
name="0808c04" id="0808c04"></a>渠迴流轉相交注。林樹蕭森、枝條蓊&#x6B1D;，華<a 
name="0808c05" id="0808c05"></a>實繁茂，映蔽日月。風吹花林出微妙香，其<a 
name="0808c06" id="0808c06"></a>香苾&#x999A;，芳馨四塞，遍王舍城。諸勝智人修<a 
name="0808c07" id="0808c07"></a>梵行者，咸以此地莊嚴殊特，心生喜樂，自<a 
name="0808c08" id="0808c08"></a>遠而至雲集其中。</p><a 
name="0808c09" id="0808c09"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>時此城主阿闍世王，道化光被遐邇所歸，正<a 
name="0808c10" id="0808c10"></a>法治國，修善者眾，國實民殷安隱快樂。爾<a 
name="0808c11" id="0808c11"></a>時城中有一長者，其家巨富，庫藏盈溢，如<a 
name="0808c12" id="0808c12"></a>毘沙門，然無子胤。禱祀神祇求乞有子，其<a 
name="0808c13" id="0808c13"></a>婦不久便覺有身，滿足十月生一男兒。是<a 
name="0808c14" id="0808c14"></a>兒先世宿殖福因，初生之日，其手一指出<a 
name="0808c15" id="0808c15"></a>大光明，明照十里。父母歡喜，即集親族及<a 
name="0808c16" id="0808c16"></a>諸相師，施設大會為兒立字。因其指光，字<a 
name="0808c17" id="0808c17"></a>曰燈指。諸集會者&#x89A9;其異相，歎未曾有。時<a 
name="0808c18" id="0808c18"></a>此會中有婆羅門，名曰婆修，誦四圍陀典，<a 
name="0808c19" id="0808c19"></a>博聞多知事無不曉。見兒姿貌奇相非常，<a 
name="0808c20" id="0808c20"></a>含笑而言：「今此兒者，或是那羅延天、釋提<a 
name="0808c21" id="0808c21"></a>桓因、日之天子、諸大德天來現生也。」時兒<a 
name="0808c22" id="0808c22"></a>父母聞是語已倍增歡喜，設大檀會，七日<a 
name="0808c23" id="0808c23"></a>七夜布施作福。如是展轉，舉國聞知，皆云<a 
name="0808c24" id="0808c24"></a>長者產一福子。稱美之音上徹於王。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>時王<a 
name="0808c25" id="0808c25"></a>聞已，即&#x52C5;將來。長者受教，尋即抱兒詣王<a 
name="0808c26" id="0808c26"></a>宮門。值王宴會作眾伎樂，無人通啟，不得<a 
name="0808c27" id="0808c27"></a>輒前。其兒指光徹照宮<span class="gaiji">[這-言+(序-予+手)]<!--gaiji,,1[這-言+(序-予+手)],2&#x284CD;,3--></span>赫然大明，照于<a 
name="0808c28" id="0808c28"></a>王身及以宮觀，一切雜物斯皆金色。其光<a 
name="0808c29" id="0808c29"></a>遍照於王宮內，譬如大水湛然盈滿。王即怪<a 
name="0809a01" id="0809a01"></a>問：「此光何來，忽照吾宮。將非世尊欲化眾<a 
name="0809a02" id="0809a02"></a>生至我門耶？又非大德諸天、釋提桓因、日天<a 
name="0809a03" id="0809a03"></a>子等下降來耶？」王尋遣人往門外看。使人<a 
name="0809a04" id="0809a04"></a>見已，還入白王：「向者大王所喚小兒，今在<a 
name="0809a05" id="0809a05"></a>門外。此兒手在乳母肩上，其指出光明來徹<a 
name="0809a06" id="0809a06"></a>照，故有此光。」王&#x52C5;使言：「速將兒來。」王既見<a 
name="0809a07" id="0809a07"></a>之，深異此兒，自捉兒手、觀其兒相。諦瞻&#x89A9;<a 
name="0809a08" id="0809a08"></a>已而作是言：「外道六師稱無因果，真偽誑<a 
name="0809a09" id="0809a09"></a>惑。若無因果，云何此兒從生已來容貌超<a 
name="0809a10" id="0809a10"></a>絕、指光炳著？以此觀之，諸外道輩陷諸眾<a 
name="0809a11" id="0809a11"></a>生顛墜惡趣。定知此兒非自在天之所化生，<a 
name="0809a12" id="0809a12"></a>亦非神祇、自然而有，必因宿福獲斯善報，始<a 
name="0809a13" id="0809a13"></a>知佛語誠諦不虛。佛說種種業緣莊嚴世<a 
name="0809a14" id="0809a14"></a>間，一切眾生眼見報應而不修福。一何怪<a 
name="0809a15" id="0809a15"></a>哉！」王復言曰：「今猶未審，此指光曜或因於<a 
name="0809a16" id="0809a16"></a>日而有此明。必欲驗者，須待夜半。」既至日<a 
name="0809a17" id="0809a17"></a>暮，即以小兒置于象上在前而行，王將群<a 
name="0809a18" id="0809a18"></a>臣共入園中。而此小兒指光所照，幽闇大<a 
name="0809a19" id="0809a19"></a>明，觀視園中鳥獸華果與晝無異。王觀此<a 
name="0809a20" id="0809a20"></a>已喟然歎曰：「佛之所說何其真妙。我於今<a 
name="0809a21" id="0809a21"></a>日於因於果生大堅信，深鄙六師愚迷之<a 
name="0809a22" id="0809a22"></a>甚，是故於佛倍生宗仰。」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>於時耆域即白王<a 
name="0809a23" id="0809a23"></a>言：「佛於修多羅中說，若不見業，故有慳貪；<a 
name="0809a24" id="0809a24"></a>以見業故，慳貪永息。今見燈指有此福報，<a 
name="0809a25" id="0809a25"></a>假令窮困尚應罄竭而修善業，況復富饒<a 
name="0809a26" id="0809a26"></a>而不作福。」如是語頃天已平曉，還將燈指<a 
name="0809a27" id="0809a27"></a>入于王宮。王甚歡喜，大賜珍寶放令還家。</p><a 
name="0809a28" id="0809a28"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>燈指漸漸遂便長大。其父長者為求婚所，<a 
name="0809a29" id="0809a29"></a>選擇高門與己等者娉以為婦。長者既富，禮<a 
name="0809b01" id="0809b01"></a>教光備，閨門雍穆，資產轉盛。夫盛有衰、<a 
name="0809b02" id="0809b02"></a>合會有離，長者夫妻俱時喪亡，譬如日到<a 
name="0809b03" id="0809b03"></a>沒處暉光潛翳、如日既出月光不現、如火為<a 
name="0809b04" id="0809b04"></a>灰熾炎永滅、強健好色為病所壞、少壯之<a 
name="0809b05" id="0809b05"></a>年為老所侵、所愛之命為死所奪。父母既<a 
name="0809b06" id="0809b06"></a>終，生計漸損。而此燈指少長富逸，不閑家<a 
name="0809b07" id="0809b07"></a>業、惡伴交遊，恣心放意耽惑酒色，用錢無<a 
name="0809b08" id="0809b08"></a>度，倉庫儲積無人料理，如月盈則闕轉就<a 
name="0809b09" id="0809b09"></a>損減。時彼國法，歲一大會，集般周山。于時<a 
name="0809b10" id="0809b10"></a>燈指服飾奢靡，將從伎樂皆悉嚴麗擬於<a 
name="0809b11" id="0809b11"></a>王者，詣彼會所。彼會大眾見其如是，無不<a 
name="0809b12" id="0809b12"></a>敬美。爾時眾人共相酣飲、歡娛適意，鐘鼓<a 
name="0809b13" id="0809b13"></a>競陳、絃歌普作，歡舞平場、嬉戲原野，娛樂<a 
name="0809b14" id="0809b14"></a>之音動山蓋谷。時後群賊知燈指詣會，未<a 
name="0809b15" id="0809b15"></a>還之間，伺其空便往到其家劫掠錢財，一<a 
name="0809b16" id="0809b16"></a>切盡取。燈指暮歸，見己舍內為賊劫掠，唯<a 
name="0809b17" id="0809b17"></a>有木石<span class="corr" title='[[土*尃]&gt;塼]'>塼</span>瓦等在。見此事已悶絕躄地，傍<a 
name="0809b18" id="0809b18"></a>人水灑方得醒寤，憂愁啼哭而作是念：「我<a 
name="0809b19" id="0809b19"></a>父昔來廣作方宜、修治家業，劬勞積聚倉<a 
name="0809b20" id="0809b20"></a>庫財寶。是父所為，生育我身，覬有委付。如<a 
name="0809b21" id="0809b21"></a>何至我不紹父業，浮遊懶惰為人欺陵，父<a 
name="0809b22" id="0809b22"></a>之餘財一旦喪失，倉庫空虛、畜產迸散。顧<a 
name="0809b23" id="0809b23"></a>瞻舍宅，唯我子然，著身瓔珞及以服乘，當<a 
name="0809b24" id="0809b24"></a>用貿食以濟交急。用之既盡，當如之何？」當<a 
name="0809b25" id="0809b25"></a>于爾時指光亦滅，其妻厭賤捨棄而走，僮<a 
name="0809b26" id="0809b26"></a>僕逃失、親里斷絕，素與情昵極親厚者，反<a 
name="0809b27" id="0809b27"></a>如怨&#x8B90;，見其貧窮，恐從乞索，逆生瞋怒。婦<a 
name="0809b28" id="0809b28"></a>尚捨棄，況於餘人。當知貧窮比於地獄，貧<a 
name="0809b29" id="0809b29"></a>窮苟生與死無別。先慣富樂，卒罹窮困，失<a 
name="0809c01" id="0809c01"></a>所依憑，栖寄無處。憂心火熾，愁毒燋然，華<a 
name="0809c02" id="0809c02"></a>色既衰、悴容轉彰，身體&#x5C2B;羸、飢渴消削，眼<a 
name="0809c03" id="0809c03"></a>目押陷、諸節骨立、薄皮纏&#x7DB6;、筋脈露現、頭<a 
name="0809c04" id="0809c04"></a>髮蓬亂、手足銳細其色艾白，舉體皴裂又<a 
name="0809c05" id="0809c05"></a>無衣裳，至糞穢中拾掇&#x9E81;弊連綴相著，纔<a 
name="0809c06" id="0809c06"></a>遮人根、赤露四體。倚臥糞&#x5860;，復無席薦。諸<a 
name="0809c07" id="0809c07"></a>親舊等見而不識，歷巷乞食猶如餓烏。至<a 
name="0809c08" id="0809c08"></a>知友邊欲從乞食，守門之人遮而不聽。伺<a 
name="0809c09" id="0809c09"></a>便輒入，復為排辱。舍主既出，欲加鞭打，俯<a 
name="0809c10" id="0809c10"></a>僂曲躬再拜謝罪，舍主輕蔑都不迴顧。設<a 
name="0809c11" id="0809c11"></a>得入舍，輕賤之故，既不與語又不敷座，與<a 
name="0809c12" id="0809c12"></a>少飲食撩擲盂器，不使充飽。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>時彼國內，取<a 
name="0809c13" id="0809c13"></a>婦、生子、剃髮，法皆設會。往到會中望乞殘食，<a 
name="0809c14" id="0809c14"></a>以輕賤故，不喚令坐、驅其走使。益索所須，<a 
name="0809c15" id="0809c15"></a>得少餘殘，與奴共器。便自思惟：「怪哉怪哉！<a 
name="0809c16" id="0809c16"></a>我今云何貧賤伶<span class="corr" title='[俜&gt;俜]'>俜</span>忽至如此？」私自念言：<a 
