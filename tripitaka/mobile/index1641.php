name="0809c18" id="0809c18"></a>為是本形？更受身耶？辛苦荼毒世所無偶，<a 
name="0809c19" id="0809c19"></a>譬如林樹無花眾蜂遠離，被霜之草葉自燋<a 
name="0809c20" id="0809c20"></a>捲，枯涸之池鴻鴈不遊，被燒之林<span class="corr" title='[糜&gt;麋]'>麋</span>鹿不趣，<a 
name="0809c21" id="0809c21"></a>田苗刈盡無人捃拾。今日貧困，說往富樂但<a 
name="0809c22" id="0809c22"></a>謂虛談，誰肯信之？世人甚眾，無知我者。由<a 
name="0809c23" id="0809c23"></a>我貧窮，所向無路。譬如曠野為火所焚，人<a 
name="0809c24" id="0809c24"></a>不喜樂；如枯樹無蔭，無依投者；如苗被雹<a 
name="0809c25" id="0809c25"></a>霜，捐棄不收；如毒蛇室，人皆遠離；如雜毒<a 
name="0809c26" id="0809c26"></a>食，無有&#x751E;者；如空塚間，無人趣向；如惡廁<a 
name="0809c27" id="0809c27"></a>溷，臭穢盈集；如魁膾者，人所惡賤；如常偷<a 
name="0809c28" id="0809c28"></a>賊，人所猜疑。我亦如是，所向之處動作譏<a 
name="0809c29" id="0809c29"></a>嫌，所可談說發言生過。雖說好語，他以為<a 
name="0810a01" id="0810a01"></a>非；若造善業，他以為鄙。所為機捷，復嫌輕<a 
name="0810a02" id="0810a02"></a>躁；若復舒緩，又言重直。設復讚歎，人謂諂<a 
name="0810a03" id="0810a03"></a>譽；若不加譽，復生誹謗，言此貧人常無好<a 
name="0810a04" id="0810a04"></a>語。若復教授，復言詐偽耆舊，強有所知。若<a 
name="0810a05" id="0810a05"></a>廣言說，人謂多舌；若默無言，人謂藏情。若<a 
name="0810a06" id="0810a06"></a>正直說，復云&#x9E81;獷；若求人意，復言諂曲。若<a 
name="0810a07" id="0810a07"></a>數親附，復言幻惑；若不親附，復言驕誕。若<a 
name="0810a08" id="0810a08"></a>順他所說，復言詐取他意；若不隨順，復言<a 
name="0810a09" id="0810a09"></a>自專。若屈意承望，罵言寒賤；若不屈意，言<a 
name="0810a10" id="0810a10"></a>是貧人猶故自我。若小自寬放，言其愚癡無<a 
name="0810a11" id="0810a11"></a>有拘忌；若自攝撿，言其空&#x9E81;詐自端礭。若<a 
name="0810a12" id="0810a12"></a>復歡逸，言其譸張狀似狂人；若復憂慘，言<a 
name="0810a13" id="0810a13"></a>其舍毒初無歡心。若聞他語有所不盡，為<a 
name="0810a14" id="0810a14"></a>其判釋，言其命趣以愚代智耐著之甚；若<a 
name="0810a15" id="0810a15"></a>復默然，復言頑嚚不識道理；若小戲論，言<a 
name="0810a16" id="0810a16"></a>不信罪福。若有所索，言其苟得不知廉恥；若<a 
name="0810a17" id="0810a17"></a>無所索，言今雖不求後望大得。若言引經<a 
name="0810a18" id="0810a18"></a>書，復云詐作聰明；若言語樸素，復嫌&#x8E08;鈍。<a 
name="0810a19" id="0810a19"></a>若公論事實，復言強說；若私屏正語，復言<a 
name="0810a20" id="0810a20"></a>讒佞。若著新衣，復言假借嚴飾；若著弊衣，<a 
name="0810a21" id="0810a21"></a>復言儜劣寒悴。若多飲食，復言飢餓饕<span class="corr" title='[餐&gt;餮] cf.【麗】'>餮</span>；<a 
name="0810a22" id="0810a22"></a>若少飲食，言腹中實飢詐作清廉。若說經論，<a 
name="0810a23" id="0810a23"></a>言顯己所知、彰我闇短；若不說經論，言愚<a 
name="0810a24" id="0810a24"></a>癡無識，可使放牛。若自道昔事業，言誇業<a 
name="0810a25" id="0810a25"></a>自譽；若自杜默，言門資淺薄。諸貧窮者行<a 
name="0810a26" id="0810a26"></a>來進止言說俯仰，盡是<span class="gaiji">愆<!--gaiji,愆,1[億-音+(夫*夫)],2&#x2039D;,3--></span>過；富貴之人作<a 
name="0810a27" id="0810a27"></a>諸非法都無過患，舉措云為斯皆得所。貧<a 
name="0810a28" id="0810a28"></a>窮之人如起尸鬼，一切怖畏，如遇死病難<a 
name="0810a29" id="0810a29"></a>可療治，如曠野嶮處絕無水草，如墮大海沒<a 
name="0810b01" id="0810b01"></a>溺洪流，如人捺咽不得出氣，如眼上翳不<a 
name="0810b02" id="0810b02"></a>知所至，如厚垢穢難可洗去，亦如怨家雖<a 
name="0810b03" id="0810b03"></a>同衣食不捨惡心，如夏暴井入中斷氣，如<a 
name="0810b04" id="0810b04"></a>入深泥滯不可出，如山暴水駃流吹漂樹木<a 
name="0810b05" id="0810b05"></a>摧折。貧亦如是，多諸艱難。貧窮又能毀壞<a 
name="0810b06" id="0810b06"></a>壯年好色、氣力名聞、種族門戶，智慧持戒、<a 
name="0810b07" id="0810b07"></a>布施慚愧、仁義信行、勇武意志悉能壞之。又<a 
name="0810b08" id="0810b08"></a>復能生飢寒怨憎、輕躁褊狹、憂愁慘毒、嫌<a 
name="0810b09" id="0810b09"></a>責罪負。如是眾苦，從貧窮生。譬如伏藏多<a 
name="0810b10" id="0810b10"></a>有雜物，貧伏藏中多有種種身心苦惱。夫<a 
name="0810b11" id="0810b11"></a>富貴者，有好威德、姿貌從容、意度寬廣、禮<a 
name="0810b12" id="0810b12"></a>義競興，能生智勇、增長家業、眷屬和讓、善<a 
name="0810b13" id="0810b13"></a>名遠聞。」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>燈指思惟：「我今貧厄，世間少比。正<a 
name="0810b14" id="0810b14"></a>欲捨身，不能自殞；當作何方以自存濟？」復<a 
name="0810b15" id="0810b15"></a>作是念：「世人所鄙，不過擔尸。此事雖惡，交<a 
name="0810b16" id="0810b16"></a>無後世受苦之業。若當餘作，或值殺生作<a 
name="0810b17" id="0810b17"></a>諸不善。以此而言，我請為之。」爾時有人聞<a 
name="0810b18" id="0810b18"></a>其此語，即雇擔尸。燈指取直，尋從其言，擔<a 
name="0810b19" id="0810b19"></a>負死人到於塚間，意欲擲棄。于時死人急<a 
name="0810b20" id="0810b20"></a>抱燈指，譬如小兒抱其父母，急捉不放，盡<a 
name="0810b21" id="0810b21"></a>力<span class="gaiji">挽<!--gaiji,挽,1[打-丁+勉],2&#x22C4D;,3--></span>&#x5374;不能得去。死人著脊，猶如胡膠，不<a 
name="0810b22" id="0810b22"></a>可得脫。排推不離，甚大怖畏，作是念言：「我<a 
name="0810b23" id="0810b23"></a>於今日擔此死人，欲何處活？」即詣旃陀羅<a 
name="0810b24" id="0810b24"></a>村語言：「誰能&#x5374;我背上死尸，當重相雇。」諸<a 
name="0810b25" id="0810b25"></a>旃陀羅詳共盡力共挽&#x5374;之，亦不肯去。餘<a 
name="0810b26" id="0810b26"></a>見之者罵燈指言：「狂人何為擔負死尸入<a 
name="0810b27" id="0810b27"></a>人村落？」競以杖石而打擲之，身體傷破，痛<a 
name="0810b28" id="0810b28"></a>懼並至。有人憐愍將其詣城，遂到城門。既<a 
name="0810b29" id="0810b29"></a>到門下，守門之人逆遮打之，不得近門。「此<a 
name="0810c01" id="0810c01"></a>何癡人，擔負死尸欲來入城。」自見己身被<a 
name="0810c02" id="0810c02"></a>諸杖木，身體皆破，甚懷懊惱發聲大哭，而<a 
name="0810c03" id="0810c03"></a>作是言：「我正為食作此鄙事。今日忽然遭<a 
name="0810c04" id="0810c04"></a>此大苦，由我貧困不擇作處為斯賤業，冀<a 
name="0810c05" id="0810c05"></a>得價直以自存活。如何一旦復值苦毒？寧<a 
name="0810c06" id="0810c06"></a>作餘死，不負尸生。」且哭且言。時守門者深<a 
name="0810c07" id="0810c07"></a>生憐愍，放令還家。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>到自空室，先同乞索諸<a 
name="0810c08" id="0810c08"></a>貧人等共住之者，遙見死尸在其背上，悉<a 
name="0810c09" id="0810c09"></a>皆捨去。既到舍已，尸自墮地。燈指于時踰<a 
name="0810c10" id="0810c10"></a>增惶怖，悶絕躄地，久乃得穌。尋見死尸手<a 
name="0810c11" id="0810c11"></a>指純是黃金，雖復怖畏，見是好金即前視<a 
name="0810c12" id="0810c12"></a>之，以刀試割實是真金。既得金已，心生歡<a 
name="0810c13" id="0810c13"></a>喜，復剪頭項手足。如是剪已，尋復還生。須<a 
name="0810c14" id="0810c14"></a>臾之頃，金頭手足其積過人。譬如王者失<a 
name="0810c15" id="0810c15"></a>國還復本位，如盲得眼視瞻明了，如久思<a 
name="0810c16" id="0810c16"></a>他女得與交歡，如學禪者忽得道證，燈指歡<a 
name="0810c17" id="0810c17"></a>喜亦復如是。庫藏珍寶倍勝於前，威德名<a 
name="0810c18" id="0810c18"></a>譽有過先日，親里朋友、妻子僮僕一切還<a 
name="0810c19" id="0810c19"></a>來。燈指歎曰：「嗚呼怪哉！富有大力，能使世<a 
name="0810c20" id="0810c20"></a>人來歸極疾。嗚呼怪哉！貧有大力，能使所<a 
name="0810c21" id="0810c21"></a>親捨我極速。我先貧時，素所親昵交遊道<a 
name="0810c22" id="0810c22"></a>絕，總無一人與我語者。今日一切顒顒承<a 
name="0810c23" id="0810c23"></a>事，合掌恭敬。假使生處如帝釋、勇力如羅<a 
name="0810c24" id="0810c24"></a>摩、知見如天師，若無錢財都無所直。富者不<a 
name="0810c25" id="0810c25"></a>問愚智皆稱好人，實無所知人以為智，亦<a 
name="0810c26" id="0810c26"></a>得勇健諸善名聞。雖復醜陋老弊，少壯婦<a 
name="0810c27" id="0810c27"></a>女樂至其邊。」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>阿闍世王聞其還富，尋即遣<a 
name="0810c28" id="0810c28"></a>人來取其寶。其所取者，盡是死人；還擲屋<a 
name="0810c29" id="0810c29"></a>中，見是真金。燈指知王欲得此寶，即以金<a 
name="0811a01" id="0811a01"></a>頭手足以用上王，王既得已齎之還宮。於後<a 
name="0811a02" id="0811a02"></a>燈指作是思惟，而說偈言：</p><a 
name="0811a03" id="0811a03"></a><p><span class="lg">「五欲極輕動，　　如電毒蛇虫，<a 
name="0811a04" id="0811a04"></a><br>　榮樂不久停，　　即生厭患心。」</span></p><a 
name="0811a05" id="0811a05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>尋以珍寶施與眾人，於佛法中出家求道，<a 
name="0811a06" id="0811a06"></a>精勤修習得阿羅漢。雖獲道果，而此尸寶<a 
name="0811a07" id="0811a07"></a>常隨逐之。</p><a 
name="0811a08" id="0811a08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>比丘問佛：「燈指比丘，以何因緣從生以來<a 
name="0811a09" id="0811a09"></a>有是指光？以何因緣受此貧困？復何因緣<a 
name="0811a10" id="0811a10"></a>有此尸寶常隨逐之？」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛告比丘：「至心諦聽，吾<a 
name="0811a11" id="0811a11"></a>當為汝說其宿緣。燈指比丘，乃往古世生<a 
name="0811a12" id="0811a12"></a>波羅<span class="gaiji">[木*奈]<!--gaiji,,1[木*奈],2&#x3B88;,3--></span>國大長者家，為小兒時乘車在外，<a 
name="0811a13" id="0811a13"></a>遊戲晚來門戶已閉。大喚開門，無人來應。<a 
name="0811a14" id="0811a14"></a>良久母來與兒開門，瞋罵母言：『舉家擔死<a 
name="0811a15" id="0811a15"></a>人去耶？賊來劫耶？何以無人與我開門。』以<a 
name="0811a16" id="0811a16"></a>是業緣死墮地獄，地獄餘報還生人中受<a 
name="0811a17" id="0811a17"></a>斯貧困。光指因緣、尸寶因緣，為汝更說。過<a 
name="0811a18" id="0811a18"></a>去九十一劫有佛，名毘婆尸。彼佛入涅槃後，<a 
name="0811a19" id="0811a19"></a>佛法住世。燈指爾時為大長者，其家大富。<a 
name="0811a20" id="0811a20"></a>往至塔寺，恭敬禮拜。見有泥像一指破落，<a 
name="0811a21" id="0811a21"></a>尋治此指，以金薄補之。修治已訖，尋發願<a 
name="0811a22" id="0811a22"></a>言：『我以香華伎樂供養、治像功德因緣，持<a 
name="0811a23" id="0811a23"></a>此功德願生天上人間，常得尊豪富貴，假<a 
name="0811a24" id="0811a24"></a>令漏失尋還得之，使我於佛法中出家得道。』<a 
name="0811a25" id="0811a25"></a>以治佛指故，得是指光及死尸寶聚。以惡口<a 
name="0811a26" id="0811a26"></a>故，從地獄出，得貧窮果報。」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛說是燈指因<a 
name="0811a27" id="0811a27"></a>緣經時，諸天人民散眾天華、作天伎樂<a 
name="0811a28" id="0811a28"></a>供養已訖，便還天宮。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>以是因緣，少種福業<a 
name="0811a29" id="0811a29"></a>於形像所得是福報，乃至涅槃形像尚爾，況<a 
name="0811b01" id="0811b01"></a>復如來法身者乎。能於佛法如說修行，如此<a 
name="0811b02" id="0811b02"></a>功德不可限量，若欲生天人中受諸快樂，應<a 
name="0811b03" id="0811b03"></a>當至心聽法。以惡口因緣受大苦報，應畏<a 
name="0811b04" id="0811b04"></a>眾苦，遠離惡口諸不善業。以此觀之，一切<a 
name="0811b05" id="0811b05"></a>世人富貴榮華不足貪著，於諸人天尊貴<a 
name="0811b06" id="0811b06"></a>不應喜樂。當知貧窮是大苦聚，欲斷貧窮不<a 
name="0811b07" id="0811b07"></a>應慳貪。是以經中言貧窮者甚為大苦。</p><a 
name="0811b08" id="0811b08"></a><p><span class="juanname">燈指因緣經</span></p>
