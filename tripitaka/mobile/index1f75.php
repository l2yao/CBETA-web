name="0799b12" id="0799b12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修齋功德。奉為仰贊道族男女。常相勸發。舉行大齋。<a 
name="0799b13" id="0799b13"></a>普度一切。盡未來際。無令間歇。</p><a 
name="0799b14" id="0799b14"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修齋功德。奉為仰贊歷劫宗親。增福開慧。隨所居處。<a 
name="0799b15" id="0799b15"></a>常聞大乘。進學菩提。入佛知見。</p><a 
name="0799b16" id="0799b16"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修齋功德。奉為仰贊高曾以來。祖宗上世。生產術業。<a 
name="0799b17" id="0799b17"></a>成立我身。憑此追嚴。增長福德。</p><a 
name="0799b18" id="0799b18"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修齋功德。奉為仰贊生身父母。增福開慧。見在世者。<a 
name="0799b19" id="0799b19"></a>富壽康寧。或以遷神。即歸安養。</p><a 
name="0799b20" id="0799b20"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修齋功德。奉為仰贊兄友弟恭。妻和子孝。奉承家訓。<a 
name="0799b21" id="0799b21"></a>行義有聞。憑此良緣。願增勝福。</p><a 
name="0799b22" id="0799b22"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修齋功德。奉為仰贊師席友朋。教誨我者。世及出世。<a 
name="0799b23" id="0799b23"></a>嘉言至行。憑此酬恩。願增勝福。</p><a 
name="0799b24" id="0799b24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修齋功德。奉為仰贊此生恩舊。提挈我者。官祿財物。<a 
name="0799c01" id="0799c01"></a>俾至富貴。憑此酬恩。願增勝福。</p><a 
name="0799c02" id="0799c02"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修齋功德。奉為仰贊外親眷屬。情好密者。患難相救。<a 
name="0799c03" id="0799c03"></a>饋遺往來。憑此酬恩。願增勝福。</p><a 
name="0799c04" id="0799c04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修齋功德。奉為仰贊梵釋四王。諸天聖眾。護持佛法。<a 
name="0799c05" id="0799c05"></a>光顯伽藍。保護出家。永無外侮。</p><a 
name="0799c06" id="0799c06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修齋功德。奉為仰贊天神地祇。遊空真宰。三災殄息。<a 
name="0799c07" id="0799c07"></a>風雨調和。穀果豐成。草木蕃廡。</p><a 
name="0799c08" id="0799c08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修齋功德。奉為仰贊護國天王。係祀神靈。保持土境。<a 
name="0799c09" id="0799c09"></a>常降吉祥。主聖臣賢。四民樂業。</p><a 
name="0799c10" id="0799c10"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修齋功德。奉為仰贊鎮宅神王。住居香火。保持門戶。<a 
name="0799c11" id="0799c11"></a>訶禁不祥。火盜災殃。永無干犯。</p><a 
name="0799c12" id="0799c12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修齋功德。奉為仰贊盡法界內六道四生。一切男女。<a 
name="0799c13" id="0799c13"></a>皆我父母。憑此莊嚴。增福開慧。</p><a 
name="0799c14" id="0799c14"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修齋功德。奉為仰贊盡法界內地水火風。一切依報。<a 
name="0799c15" id="0799c15"></a>皆我故身。憑此莊嚴。轉成妙境。</p><a 
name="0799c16" id="0799c16"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修齋功德。奉為仰贊僕隸婢使。奉承我者。願承勝福。<a 
name="0799c17" id="0799c17"></a>俾脫賤流。實冀他生。免相責報。</p><a 
name="0799c18" id="0799c18"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>上來施主。虔對三尊。恪意開陳。三十二願。報恩心滿。<a 
name="0799c19" id="0799c19"></a>普度功圓。法界&#x7FA3;生。齊成佛道。</p><a 
name="0799c20" id="0799c20"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span><span class="note">(法師施主同作禮。上香。表白述偈讚三寶。宣疏。　此偈十二句。出涅槃經)</span>。</p><a 
name="0799c21" id="0799c21"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>獨一無侶。無師自悟。人中象王。大法之師。所可說法。<a 
name="0799c22" id="0799c22"></a>最上最妙。諸佛所遊。常不變易。受正真法。隨順修行。<a 
name="0799c23" id="0799c23"></a>一切眾生。良祐福田。讚歎三寶。恭請證明。茲日開建<a 
name="0799c24" id="0799c24"></a>法界聖凡水陸普度大齋法事周圓。所有疏文。謹當<a 
name="0800a01" id="0800a01"></a>宣讀<span class="note">(宣疏畢。再為施主祈福保安)</span>。</p><a 
name="0800a02" id="0800a02"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>上來文疏。披讀告圓。仰冀慈悲。特垂監念。弟子某人<a 
name="0800a03" id="0800a03"></a>伏願所修齋事。本自誠心。上奉三尊。下度六道。惟冀<a 
name="0800a04" id="0800a04"></a>舉家眷序。終日歡和。飲食起居。吉祥安樂。增輝祖業。<a 
name="0800a05" id="0800a05"></a>垂裕孫謀。既壽而康。曰富且貴。火災絕警。寇黨迷蹤。<a 
name="0800a06" id="0800a06"></a>隨心有求。無事不遂。眾等無任歸依懇禱之至。</p><a 
name="0800a07" id="0800a07"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span><span class="note">(此祈叩語隨人換易為之。未必專用此也法師施主俱作禮。眾動鈸。少時俱退)</span>。</p><a 
name="0800a08" id="0800a08"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span><span class="note">(齋退。設香燈凡席。大眾就一處誦<span class="corr" title='[輕&gt;經]'>經</span>。準獻狀迴向上堂三位。并下堂十位。及隨筵諸位。正誦經時。須各安<a 
