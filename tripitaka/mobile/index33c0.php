name="0015c16" id="0015c16"></a>祗對和尚。南泉云。他&#x5374;是異類中行。<span class="note">(檐得一檐懵懂<a 
name="0015c17" id="0015c17"></a>換得一檐骨懂)</span>　巖云。如何是異類中行。泉云。不見<a 
name="0015c18" id="0015c18"></a>道。智不到處。切忌道著。道著即頭角生。直須向異類<a 
name="0015c19" id="0015c19"></a>中行。<span class="note">(第二杓惡水)</span>　巖亦不會。道吾知巖不薦。乃云。<a 
name="0015c20" id="0015c20"></a>此人因緣不任此。便與回藥山。<span class="note">(一盲引眾盲。相牽入<a 
name="0015c21" id="0015c21"></a>火坑)</span>　藥山&#x89A9;二人回。乃問雲巖。汝到甚處去來。巖<a 
name="0015c22" id="0015c22"></a>云。到南泉來。藥山云。南泉有何言句。雲巖遂舉前話。<a 
name="0015c23" id="0015c23"></a><span class="note">(拾得封皮作信讀)</span>　藥山云。子作麼生會他這箇時<a 
name="0015c24" id="0015c24"></a>節便回來。雲巖無對。藥山乃大笑。<span class="note">(盡道藥山笑裏有<a 
name="0016a01" id="0016a01"></a>刀。殊不知怜兒不覺醜)</span>　雲巖便問如。何是異類中<a 
name="0016a02" id="0016a02"></a>行。藥山云。吾今日困。別時來。<span class="note">(舌頭拖地)</span>　雲巖云。某<a 
name="0016a03" id="0016a03"></a>甲特為此事歸來。藥山云。且去。雲巖便出。<span class="note">(教休不肯<a 
name="0016a04" id="0016a04"></a>休。直待雨霖頭)</span>　道吾在方丈外。聞雲巖不薦。不覺<a 
name="0016a05" id="0016a05"></a>咬得指頭血出。<span class="note">(為他閑事長無明)</span>　道吾&#x5374;下來。問<a 
name="0016a06" id="0016a06"></a>雲巖云。師兄去問和尚。那因緣作麼。雲巖云。和尚不<a 
name="0016a07" id="0016a07"></a>與某甲說。道吾便低頭。<span class="note">(臨袁<span class="gaiji">虔<!--gaiji,虔,1[虔-文+ㄆ],2&#x458D;,3--></span>吉頭上插&#x7B14;)</span>　後雲<a 
name="0016a08" id="0016a08"></a>巖遷化。遣遺書至。道吾覽了曰。雲巖不知有。悔當時<a 
name="0016a09" id="0016a09"></a>不向伊道。然雖如是。要且不違藥山之子。<span class="note">(同死同生<a 
name="0016a10" id="0016a10"></a>為君決)</span>　師拈云。道吾雲巖。可謂蚌鷸相持。俱落漁<a 
name="0016a11" id="0016a11"></a>人之手。南泉藥山。正令不行。拗曲作直。當時若使德<a 
name="0016a12" id="0016a12"></a>山令行。曹洞一宗。未至掃土。復成一頌。往復落人綣<a 
name="0016a13" id="0016a13"></a>繢間。那知同步不同行。當初乍可無容質。別作心腸<a 
name="0016a14" id="0016a14"></a>過一生。</p><a 
name="0016a15" id="0016a15"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>靈隱前堂首座寮結夏秉拂。</p><a 
name="0016a16" id="0016a16"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>語漸也。返常合道。方木逗圓孔。論頓也。不留朕跡。泥<a 
name="0016a17" id="0016a17"></a>裏洗土塊。所以。一機一境。總是接響承&#x8657;。舉古舉今。<a 
name="0016a18" id="0016a18"></a>特地無風起浪。豈不見道。杜耳目於胎&#x6BBB;。掩玄象於<a 
name="0016a19" id="0016a19"></a>霄外。貴宮商之異。辯玄素之殊。雖則盡力<span class="gaiji">[跳-兆+孛]<!--gaiji,,1[跳-兆+孛],2&#x2805D;,3--></span>跳。要且<a 
name="0016a20" id="0016a20"></a>前不迭村。後不迭店。縱使據菩薩乘。修寂滅行。高步<a 
name="0016a21" id="0016a21"></a>毗盧頂。不稟釋迦文。亦未出靈山綣繢在。是則是。只<a 
name="0016a22" id="0016a22"></a>如五祖和尚道。羅邏招。羅邏搖。羅邏送。莫&#x6060;空&#x758E;。伏<a 
name="0016a23" id="0016a23"></a>惟珍重。又作麼生。良久云。不逢別者。終不開拳。</p><a 
name="0016a24" id="0016a24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>舉保福和尚。因長生卓庵時。去相訪。茶話次。長生云。<a 
name="0016b01" id="0016b01"></a>有僧問某甲。如何是祖師西來意。某甲&#x7AEA;起拂子。不<a 
name="0016b02" id="0016b02"></a>知得不得。保福云。某甲爭敢道得不得。有箇問。有人<a 
name="0016b03" id="0016b03"></a>讚嘆此事。如虎帶角。有人輕毀此事。分文不直。一等<a 
name="0016b04" id="0016b04"></a>是任麼事。為甚讚毀不同。長生云。適來出自偶爾。有<a 
name="0016b05" id="0016b05"></a>老宿云。毀又爭得。有老宿云。惜取眉毛。後來雪竇都<a 
name="0016b06" id="0016b06"></a>別云。若非和尚證明。拂子一生無用。玄覺云。一等是<a 
name="0016b07" id="0016b07"></a>與麼事。為什麼有得有不得。孚上座云。若無智眼。難<a 
name="0016b08" id="0016b08"></a>辯得失。師拈云。雪竇玄覺。等是口甜心苦。半似輕毀。<a 
name="0016b09" id="0016b09"></a>半似讚嘆。引得孚上座。無端插觜。拂子忍俊不禁。今<a 
name="0016b10" id="0016b10"></a>夜對眾決破。讚之與毀。得與不得。以拂子擊禪床一<a 
name="0016b11" id="0016b11"></a>下云。甚處得來。然雖如是。惜取眉毛。</p><a 
name="0016b12" id="0016b12"></a><a 
name="0016b13" id="0016b13"></a><p><span class="juanname">兀菴和尚語錄卷中</span></p>
