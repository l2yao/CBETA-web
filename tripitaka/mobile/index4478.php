name="0255a01" id="0255a01"></a><p style="margin-left: 4em"><span class="headname">四、〔十二緣起之雜論〕</span></p><a 
name="0255a02" id="0255a02"></a><p style="margin-left: 2em"><span class="headname">〔一、十二緣起之特質〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次於〔十二緣起〕中，愁等於最後說故，如說：<a 
name="0255a03" id="0255a03"></a>「由無明之緣而有行。」於最初說此有輪，其間：</p><a 
name="0255a04" id="0255a04"></a><p><span class="lg">　　　　無明是愁等而成就，<a 
name="0255a05" id="0255a05"></a><br>　　　　初不知此有輪，<a 
name="0255a06" id="0255a06"></a><br>　　　　無作者亦無有受者，<a 
name="0255a07" id="0255a07"></a><br>　　　　十二種之空性故空</span></p><a 
name="0255a08" id="0255a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>當知常相續而轉起。然，此於〔一〕「由愁等而成就無明」者云何？〔二〕「初不知此<a 
name="0255a09" id="0255a09"></a>有輪」者云何？〔三〕「無作者亦無受者」者云何？〔四〕「十二種之空性故空」者云何？</p><a 
name="0255a10" id="0255a10"></a><p style="margin-left: 3em"><span class="headname">〔一〕〔由愁等而成就無明〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>曰：於此愁、苦、憂、惱非離無明。又悲者是<a 
name="0255a11" id="0255a11"></a>癡者故，只要彼成就〔愁悲等〕，無明亦成就。又說：「漏之集故有無明之集。」從<a 
name="0255a12" id="0255a12"></a>漏之集故有此等之愁等。何以故？</p><a 
name="0255a13" id="0255a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔一〕於事欲不相應之時，愁以欲漏為集（因）。所謂：</p><a 
name="0255a14" id="0255a14"></a><p><span class="lg">　　　　若有為彼欲　　　　及欲生起者<a 
name="0256a01" id="0256a01"></a><br>　　　　失掉彼諸欲　　　　〔彼〕惱如箭刺</span></p><a 
name="0256a02" id="0256a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.577"> [P.577] </span>又所謂：「由欲而愁生。」〔二〕又此等一切〔之愁等〕以見漏為集。所謂：「彼<a 
name="0256a03" id="0256a03"></a>『我是色、色是我所』之住纏結者，依色之變易，變異而愁、悲、苦、憂、惱生起。」<a 
name="0256a04" id="0256a04"></a>〔三〕〔愁等〕如見漏為集，又有漏亦是集。所謂：「長壽、美麗、多樂而雖長時<a 
name="0256a05" id="0256a05"></a>止住於高大宮殿所有彼等諸天，但聞如之說法陷於佈畏、戰慄、悚懼。」如諸天見五<a 
name="0256a06" id="0256a06"></a>前兆（五衰相）為死之怖畏而戰慄。〔四〕〔愁等〕如有漏為集，亦以無明漏為集。<a 
name="0256a07" id="0256a07"></a>所謂：「諸比丘！彼愚者於現世受三種之苦憂。」</p><a 
name="0256a08" id="0256a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>由斯漏之集（因）而有此等〔愁等〕諸法故，成就此等〔愁等〕，無明之因令成<a 
name="0256a09" id="0256a09"></a>諸漏。而成就諸漏之時，於緣之有時亦有〔果〕故，而必成就〔果之〕無明。當知<a 
name="0256a10" id="0256a10"></a>如斯於此：「依愁等而成就無明。」</p><a 
name="0256a11" id="0256a11"></a><p style="margin-left: 3em"><span class="headname">〔二〕〔此有輪不知其始〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>如斯緣之有時〔果亦〕有故，於無明之成就時，更<a 
name="0256a12" id="0256a12"></a>「由無明之緣而有行，由行之緣而有識」，如斯於因果之相續而無終期。故其因果之<a 
name="0256a13" id="0256a13"></a>連結亦不知成就轉起十二支有輪之始。如斯之時，而有〔反問：〕「由無明之緣而有<a 
name="0256a14" id="0256a14"></a>行。」與說此〔無明〕是最初為矛盾。此非〔以無明〕為最初之說。此但為最要之法而<a 
name="0257a01" id="0257a01"></a>〔最初〕說。即於〔過去、現在、未來之〕三輪轉是無明最為重要。由執著無明而<a 
name="0257a02" id="0257a02"></a>餘之煩惱輪轉或業以縛礙愚者。恰如捕蛇頭而餘他之蛇身〔卷著〕於腕。而進行無<a 
name="0257a03" id="0257a03"></a>明之斷絕時，有彼等〔餘之煩惱及業等〕之解脫。恰如若斷蛇頭而脫離卷著於腕之<a 
name="0257a04" id="0257a04"></a>〔蛇身〕。所謂：「由無無明之離貪、滅而有行之滅。」等。如斯〔置無明於最初〕，<a 
name="0257a05" id="0257a05"></a>執之即有縛，放之即脫其〔無明〕，此說〔緣起支中〕最要之法而非為最初之說。如<a 
name="0257a06" id="0257a06"></a>斯謂：「有此輪不知其始。」應當知。</p><a 
name="0257a07" id="0257a07"></a><p style="margin-left: 3em"><span class="headname"><span class="linehead" title="PTS.Vism.578"> [P.578] </span>〔三〕〔無作者無受者〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>依無明等之原因而有行等之轉起故，其由〔無明〕以<a 
name="0257a08" id="0257a08"></a>外如云梵天、大梵天、最勝者、創造者，即〔無〕有輪迴之作者及所&#x5FA7;計之梵天等，<a 
name="0257a09" id="0257a09"></a>又其〔輪迴〕如云：「此我是我、為語者、受者。」之所&#x5FA7;計，即我無有苦樂之受者。<a 
name="0257a10" id="0257a10"></a>當知如斯「無作者無受者」。</p><a 
name="0257a11" id="0257a11"></a><p style="margin-left: 3em"><span class="headname">〔四〕〔十二種之空性故空〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>因為無明有生滅之故，〔無明〕恒常之性是〔空<a 
name="0257a12" id="0257a12"></a>無〕。因為雜染所染故，淨之性是〔空無〕。為生死所惱故，樂性是〔空無〕。因依屬<a 
name="0257a13" id="0257a13"></a>於緣之故，自在之我是空無。行等之諸支亦同樣。或無明非我、亦非我所、非在我<a 
name="0257a14" id="0257a14"></a>中、我非於有。又行等之諸支亦同樣。故當知：「十二種之空性故此有輪是空。」</p><a 
name="0258a01" id="0258a01"></a><p style="margin-left: 2em"><span class="headname">〔二、三世兩重之因果〕</span></p><p style="margin-left: 3em"><span class="headname">〔一〕〔二種有輪、三時〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>如斯已知，更：</p><a 
name="0258a02" id="0258a02"></a><p><span class="lg">　　　　其〔有輪〕以無明、渴愛為根本，有過等之三時<a 
name="0258a03" id="0258a03"></a><br>　　　　其中，二、八、十二之諸支從自性而〔過去等〕。</span></p><a 
name="0258a04" id="0258a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，於彼有輪，當知無明、渴愛之二法為根本。其〔有輪〕由前際而持來〔現<a 
name="0258a05" id="0258a05"></a>世果〕將來故，無明為根本而受為最後。後際之相續故渴愛為根本，以老死為最後<a 
name="0258a06" id="0258a06"></a>等二種。其中，前〔之有輪〕是為見行者而說，後〔之有輪〕是為渴愛行者而〔說〕。<a 
name="0258a07" id="0258a07"></a>然，在見行者而無明〔是輪迴之導引者〕，在渴愛行者而渴愛是輪迴之導引者。或為<a 
name="0258a08" id="0258a08"></a>斷見根絕而說第一〔之有輪，此〕說明果生起之諸因不斷絕故。為常見之根絕，而<a 
name="0258a09" id="0258a09"></a>說第二〔之有輪，此〕說明令生起老死故。或為胎生者而說前〔之有輪，此〕說明<a 
name="0258a10" id="0258a10"></a>順序之轉故。為化生者而說後〔之有輪，此〕說明一時之生起故。其次，其〔有輪〕<a 
name="0258a11" id="0258a11"></a>有過去、現在、未來之三時。其中，聖典中從自性而說，無明、行之二支是過去時，<a 
name="0258a12" id="0258a12"></a>有為最後，識等之八是現在。當知生與老死之二是未來時。</p><a 
name="0258a13" id="0258a13"></a><p style="margin-left: 3em"><span class="headname"><span class="linehead" title="PTS.Vism.579"> [P.579] </span>〔二〕〔三連結、四攝〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>更於：</p><a 
name="0258a14" id="0258a14"></a><p><span class="lg">　　　　此〔有輪〕是因與果因〔為先〕次第之<a 
name="0259a01" id="0259a01"></a><br>　　　　三連結，又四區分所攝<a 
name="0259a02" id="0259a02"></a><br>　　　　為二十行相之輻，而有三輪轉，<a 
name="0259a03" id="0259a03"></a><br>　　　　巡環輪轉無停止。</span></p><a 
name="0259a04" id="0259a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>亦當知之。此中，行與結生識之間為因與果之一連結。受與渴愛之間果與因亦為一<a 
name="0259a05" id="0259a05"></a>連結。有與生之間因與果亦為一連結。當知如斯此〔有輪〕是「因與果及因與〔果〕<a 
name="0259a06" id="0259a06"></a>之次第為三連結。」</p><a 
name="0259a07" id="0259a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，其〔有輪〕差別〔三〕連結之初後而有四攝。即無明是一攝。識、名、<a 
name="0259a08" id="0259a08"></a>色、六處、觸、受是第二〔攝〕。渴愛、取、有是第三〔攝〕。生、老死是第四〔攝〕。<a 
name="0259a09" id="0259a09"></a>當知如斯此〔有輪〕是「四區分所攝」。</p><a 
name="0259a10" id="0259a10"></a><p style="margin-left: 3em"><span class="headname">〔三〕〔二十行相之輻〕</span></p><a 
name="0259a11" id="0259a11"></a><p><span class="lg">　　　　過去有五因，今〔世〕有五果，<a 
name="0259a12" id="0259a12"></a><br>　　　　今〔世〕有五因，未來有五果。</span></p><a 
name="0259a13" id="0259a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>而稱此等二十行相，當知依輻而有「二十行相之輻」。其中，「過去有五因」不過是言<a 
name="0259a14" id="0259a14"></a>無明與行此等之二。而無知者渴愛、渴愛者是取，由其取之緣有有。故〔無明、行<a 
name="0260a01" id="0260a01"></a>之中〕亦攝渴愛、取、有。故曰：「於前之業有，癡是無明、營作是行、欲求是渴<a 
name="0260a02" id="0260a02"></a>愛、接近是取、思是有。如斯於前之業有之此等五法對於今世之結生為緣。」</p><a 
name="0260a03" id="0260a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其中，「於前之業有」者，是於前之業有也，過去生之業有謂行時之義。「癡」<a 
name="0260a04" id="0260a04"></a>是無明，對於時苦等之癡，〔及〕由此癡迷而行業之癡是此無明。「營作是行」，行其<a 
name="0260a05" id="0260a05"></a>業者所有前思（以前之意志），〔即〕起「如何我行布施」之心，無論是一箇月、一箇<a 
name="0260a06" id="0260a06"></a><span class="linehead" title="PTS.Vism.580"> [P.580] </span>年準備布施者之前思，或置施物於諸受者手中者之思言為〔業〕有。或於一〔意門〕<a 
name="0260a07" id="0260a07"></a>轉與六速行思之營作是行。第七〔速行思〕是有。或所有之思是〔業〕有，與〔思〕<a 
name="0260a08" id="0260a08"></a>相應之營作是行。「欲求為渴愛」者，對行業者之果之起有，所有欲求、冀求此為渴<a 
name="0260a09" id="0260a09"></a>愛。「接近是取」，為所有業有之緣。〔即〕思惟：「行此於斯之處所，我受諸欲或斷<a 
name="0260a10" id="0260a10"></a>滅。」等而轉起、接近、執、執取，此云為取。「思是有」，有營作後說〔為有〕之思是<a 
name="0260a11" id="0260a11"></a>〔業〕有。當知如斯之義。</p><a 
name="0260a12" id="0260a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「今〔世〕有五果」，聖典所述識至受等〔五〕是〔今世之五果〕。所謂：「此<a 
name="0260a13" id="0260a13"></a>世之結生是識，入胎是名色，淨〔色〕是處，所觸是觸，所受是受，於斯此世之起<a 
name="0260a14" id="0260a14"></a>有之此等五法，乃由宿作業緣之〔果〕。」</p><a 
name="0261a01" id="0261a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其中，「結生是識」，連結他（過）去有而生起〔現在有〕故所言結生者，是<a 
name="0261a02" id="0261a02"></a>識也。「入胎是名」，所有色、非色之諸法入來〔母〕胎中——如來入——此為名色。<a 
name="0261a03" id="0261a03"></a>「淨色是處」，言此眼等五處。「所觸是觸」，所緣之所觸者，所觸而生起者，此為觸<a 
name="0261a04" id="0261a04"></a>也。「所受是受」，是結生之識、六處之緣，又觸俱生起異熟之所受者，此為受。應<a 
name="0261a05" id="0261a05"></a>知如斯之義。</p><a 
name="0261a06" id="0261a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「今〔世〕有五因」是渴愛等。於聖典說渴愛、取、有。而於有之概念是其前分<a 
name="0261a07" id="0261a07"></a>又含括與其相應之行。渴愛、取之概念相應於〔無明〕，又由此癡者含括所行業之<a 
name="0261a08" id="0261a08"></a>彼無明。如斯有〔渴愛、取、有、行、無明之〕五〔因〕。故曰：「於此世成熟〔六<a 
name="0261a09" id="0261a09"></a>內〕處所〔生之〕癡是無明、營作是行、欲求是渴愛、接近是取、思是有。於斯此<a 
name="0261a10" id="0261a10"></a>世之業有之此等五法，是對於未來之結生為緣。」</p><a 
name="0261a11" id="0261a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其中，「於此世成熟之處」，成熟之〔內六〕處於作業時示為癡蒙。餘他其義即<a 
name="0261a12" id="0261a12"></a>明瞭。</p><a 
name="0261a13" id="0261a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.581"> [P.581] </span>「於未來有五果」，識等之五也。其等由〔緣起支〕生之語而說。又老死是彼等<a 
name="0261a14" id="0261a14"></a>〔識等〕之老死。故曰：「未來之結生是識、入胎是名色、淨〔色〕是處、所觸是<a 
name="0262a01" id="0262a01"></a>觸、所受是受。於斯未來之起有此等五法，是由此世作業之緣〔而生果〕也。」如斯<a 
name="0262a02" id="0262a02"></a>〔之有輪〕為二十行相之輻。</p><a 
name="0262a03" id="0262a03"></a><p style="margin-left: 3em"><span class="headname">〔四〕〔三輪轉〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，「為三輪轉循環而無停止」者，此〔緣起支之〕中，行、<a 
name="0262a04" id="0262a04"></a>有是「業輪轉」。無明、渴愛、取是「煩惱輪轉」。識、名色、六處、觸、受是「異熟輪<a 
name="0262a05" id="0262a05"></a>轉」。此有輪是依此等三之輪轉「為三輪轉」。類惱輪轉不斷絕之間，是緣不斷絕故無<a 
name="0262a06" id="0262a06"></a>有停止，當知返復再返復而迴轉故為循環。</p><a 
name="0262a07" id="0262a07"></a><p style="margin-left: 2em"><span class="headname">〔三、緣起之決定說〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>如斯循環而有此〔有輪〕。</p><a 
name="0262a08" id="0262a08"></a><p><span class="lg">　　　　〔一〕由諦之發生、〔二〕由作用、〔三〕由遮、又〔四〕由譬喻、<a 
name="0262a09" id="0262a09"></a><br>　　　　又〔五〕由甚深、〔六〕由理法之別，應知於適宜。</span></p><a 
name="0262a10" id="0262a10"></a><p style="margin-left: 3em"><span class="headname">其中，〔一〕〔由諦之發生〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>善、不善之業，總體是依集諦與諦分別而說故。<a 
name="0262a11" id="0262a11"></a>〔於〕「無明之緣而有行」〔等句〕，由無明〔生〕行，是由第二諦發生第二諦。由行<a 
name="0262a12" id="0262a12"></a>〔生〕識，是由第二諦發生第一諦。由識〔生〕名色及至異熟之受止是由第一諦發<a 
name="0262a13" id="0262a13"></a>生第一諦。由受〔生〕渴愛，是由第一諦發生第二諦。由渴愛〔生〕取，是由第二<a 
name="0262a14" id="0262a14"></a>諦發生第二諦。由取〔生〕有，是由第二諦發生第二諦。由有〔生〕生，是由第二<a 
name="0263a01" id="0263a01"></a>諦發生第一諦。由生〔生〕老死，是由第一諦發生第一諦。當知如斯此〔有輪〕是<a 
name="0263a02" id="0263a02"></a>適當由諦之發生。</p><a 
name="0263a03" id="0263a03"></a><p style="margin-left: 3em"><span class="headname"><span class="linehead" title="PTS.Vism.582"> [P.582] </span>〔二〕〔由作用〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，此〔緣起支〕中，無明是使有情對於事物之癡迷，且<a 
name="0263a04" id="0263a04"></a>行之現前為緣。又行以行作有為，且識〔現前〕為緣。識以知別事物，且名色之〔現<a 
name="0263a05" id="0263a05"></a>前〕為緣。名色亦互相支持，且六處之〔現前〕為緣。六處於轉起自境，且觸之〔現<a 
name="0263a06" id="0263a06"></a>前〕為緣。觸亦觸於所緣且受之〔現前〕為緣。受亦味著所緣之味，且以渴愛之〔現<a 
name="0263a07" id="0263a07"></a>前〕為緣。渴愛亦染著可染著之諸法，且以取之〔現前〕為緣。取亦取可取之諸法，<a 
name="0263a08" id="0263a08"></a>且以有之〔現前〕為緣。有亦散布於種種之趣，且以生之〔現前〕為緣，生亦使生<a 
name="0263a09" id="0263a09"></a>諸蘊，且以彼等〔諸蘊〕生而轉起故，以老死之〔現前〕為緣。老死亦司諸蘊之老<a 
name="0263a10" id="0263a10"></a>熟與破壞，且為愁等之持處（原因）故，其有現前為緣。是故一切之句有二種之<a 
name="0263a11" id="0263a11"></a>轉起（活動）。當知此〔有輪〕亦適宜依於作用。</p><a 
name="0263a12" id="0263a12"></a><p style="margin-left: 3em"><span class="headname">〔三〕〔由遮止〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>此〔有輪〕中，「由無明而有行」，是遮止有作者之見。「由<a 
name="0263a13" id="0263a13"></a>行之緣而有識」，是應止自我轉生之見。「由識之緣而有名色」，是見自我所&#x5FA7;計事<a 
name="0263a14" id="0263a14"></a>之區別故，以遮止厚想。「由名色之緣而有六處」等是自我見……乃至……遮止如識、<a 
name="0264a01" id="0264a01"></a>觸、覺受、渴愛、取、有、生、老、死等之見。當知此有輪適宜於邪見之遮止。</p><a 
name="0264a02" id="0264a02"></a><p style="margin-left: 3em"><span class="headname">〔四〕〔由譬喻〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，此〔有輪之〕中，無明乃不見自相、共相之諸法故如<a 
name="0264a03" id="0264a03"></a>盲人。由無明而〔生〕行如盲人之躓。由行之緣而〔生〕識如躓者之倒。由識之緣<a 
name="0264a04" id="0264a04"></a>而〔生〕名色，如倒者〔打身〕成腫物。由名色之緣而〔生〕六處，如腫物破裂成<a 
name="0264a05" id="0264a05"></a>膿。由六處而〔生〕觸，如擊觸腫物之膿包。由觸之緣而〔生〕受，如擊觸之痛苦。<a 
name="0264a06" id="0264a06"></a>由受之緣而〔生〕渴愛，如熱望痛苦之治癒。由渴愛之緣而〔生〕取，如為治癒之<a 
name="0264a07" id="0264a07"></a><span class="linehead" title="PTS.Vism.583"> [P.583] </span>熱望而取不適當之〔藥〕。由取之緣而〔生〕有，如取塗不適當之〔藥〕。由有之緣<a 
name="0264a08" id="0264a08"></a>而〔生〕生，如塗不適當之〔藥〕而腫物現出變異。由生之緣而〔生〕老死，如腫<a 
name="0264a09" id="0264a09"></a>物之破裂。</p><a 
name="0264a10" id="0264a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>或於此〔有輪〕中，無明依不行道、邪行道之狀態以打破諸有情，恰如白內障<a 
name="0264a11" id="0264a11"></a>之（打破）兩眼。被其〔無明〕所打破之愚者持再有，由諸行而包著自己，恰如蠶<a 
name="0264a12" id="0264a12"></a>以繭〔纏自己〕。於行所&#x5FA7;取之識，於諸趣得住立，恰如&#x5FA7;取於指導者之王子，〔得<a 
name="0264a13" id="0264a13"></a>住立〕於王位。識&#x5FA7;計生起相故，於結生令生起多種種類之名色，恰如幻師（魔術<a 
name="0264a14" id="0264a14"></a>師）〔顯現〕幻。存立於名色之六處而至增大、生長、廣大，恰如存立肥沃土地之林<a 
name="0265a01" id="0265a01"></a>叢〔而至增大、生長、廣大〕。由處之擊觸而生觸，恰如由燧木之摩擦而火〔生〕。<a 
name="0265a02" id="0265a02"></a>觸於所觸者而受現，恰如觸火者而〔現〕火傷。受〔苦樂受〕者渴愛增大，恰如喝<a 
name="0265a03" id="0265a03"></a>鹽水而渴〔增大〕。渴愛者對諸有成為熱望，恰如乾渴者對於水之〔熱望〕。其取由<a 
name="0265a04" id="0265a04"></a>〔四〕取而取有，恰如魚由味貪〔吞〕釣針。有有時而有生，恰如有種子時而有芽。<a 
name="0265a05" id="0265a05"></a>生者必有老死，恰如生樹必倒。故當知由如斯譬喻適宜此有輪。</p><a 
name="0265a06" id="0265a06"></a><p style="margin-left: 3em"><span class="headname">〔五〕〔由甚深之差別〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，關於〔緣起〕義、法、說示、通達、甚深而世<a 
name="0265a07" id="0265a07"></a>尊說：「阿難！此緣起顯示甚深、極甚深。」故，當知甚深之差別適宜此有輪。</p><a 
name="0265a08" id="0265a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（一）〔義甚深〕　此中，若無生即無老死，又生之以外無別有老死，如斯〔老<a 
name="0265a09" id="0265a09"></a>死是〕由生而來。如斯由生之緣而起來之義甚難覺知故，由老死之緣而生成、起來<a 
name="0265a10" id="0265a10"></a>之義是甚深也。同樣由生有之緣……乃至……由行無明之緣而有生成、起來之義是<a 
name="0265a11" id="0265a11"></a><span class="linehead" title="PTS.Vism.584"> [P.584] </span>甚深也。故有輪「義甚深」。先述此〔有輪之〕義甚深。然，因之果言為義。所謂：<a 
name="0265a12" id="0265a12"></a>「關於因之果智是義無礙解。」</p><a 
name="0265a13" id="0265a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（二）〔法甚深〕　其次，由何行相、任何位置之無明，皆為各各諸行之緣耶是<a 
name="0265a14" id="0265a14"></a>難覺知故，對無明緣行之義是甚深。同樣於行……乃至……對生緣老死之義是甚深。<a 
name="0266a01" id="0266a01"></a>故此有輪是「法甚深」。此是〔有輪之〕甚深。然，於因謂有法之名。所謂：「對因<a 
name="0266a02" id="0266a02"></a>之智是法無礙解。」</p><a 
name="0266a03" id="0266a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（三）〔說示甚深〕　又〔緣起〕由各各之原因當令其各各轉起，故其〔緣起之〕<a 
name="0266a04" id="0266a04"></a>說示亦甚深。一切知智以外之智，不得住立其處〔說示緣起〕。然，此〔緣起〕或於<a 
name="0266a05" id="0266a05"></a>經順〔說〕、或於〔經〕逆〔說〕、或於〔經〕順逆〔說〕、或於〔經〕由中間始而順<a 
name="0266a06" id="0266a06"></a>或逆〔說〕、或於〔經〕三連結、四略〔說〕、或於〔經〕二連結、三略〔說〕、或於<a 
name="0266a07" id="0266a07"></a>〔經〕一連結、二略說也。故此有輪是「說示甚深」。此〔有輪之〕說示甚深也。</p><a 
name="0266a08" id="0266a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（四）〔通達甚深〕　其次，此〔緣起〕之中，為無明之自性者，由其自相正通<a 
name="0266a09" id="0266a09"></a>達所通達〔智〕，而沉潛（洞察）無明等是極難故甚深。然，無明之無智見、諦不通<a 
name="0266a10" id="0266a10"></a>達之義是甚深。行之行作、營作、有貪、離貪之義是〔甚深〕。識之空性、不作為、<a 
name="0266a11" id="0266a11"></a>不轉生、結生現前之義是〔甚深〕、名色之同時生起、簡別、不簡別、向傾（名）、<a 
name="0266a12" id="0266a12"></a>惱壞（色）之義是〔甚深〕。六處之增上、世間、門、田、具境之義是〔甚深〕。觸<a 
name="0266a13" id="0266a13"></a>之接觸、擊觸、合會、集合之義是〔甚深〕。受所緣味之嘗味、苦、樂、中庸（捨）、<a 
name="0266a14" id="0266a14"></a>無命者、所受之義是〔甚深〕。渴愛之歡喜、縛著、如流水、如蔓草、如河、愛海、<a 
name="0267a01" id="0267a01"></a>難充滿之義是〔甚深〕。取之取持、把持、住著、執取、難度越之義是〔甚深〕。有<a 
name="0267a02" id="0267a02"></a>之營作、行作、胎、趣、〔識〕住、投置於〔有情〕居之義是〔甚深〕。生之生、出<a 
name="0267a03" id="0267a03"></a>生、入胎、生起、現前之義是〔甚深〕。老死之盡滅、壞滅、破壞、變易之義是甚深。<a 
name="0267a04" id="0267a04"></a>此是〔緣起之〕「通達甚深」。</p><a 
name="0267a05" id="0267a05"></a><p style="margin-left: 3em"><span class="headname"><span class="linehead" title="PTS.Vism.585"> [P.585] </span>〔六〕〔由理法之差別〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，此〔緣起〕，同一之理、別異之理、不作為之<a 
name="0267a06" id="0267a06"></a>理、如是法性之理有此四義理。故當知由理法之別為適宜此有輪。</p><a 
name="0267a07" id="0267a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（一）〔同一之理〕　此中，如謂：「由無明之緣而有行、行之緣而有識。」恰如<a 
name="0267a08" id="0267a08"></a>種子過了芽等之狀態而至達樹木之狀態，相續之不斷謂「同一之理」。正見此者，依<a 
name="0267a09" id="0267a09"></a>因果之連結而覺知相續不斷絕故而捨斷斷見。〔此〕之誤見者，由因果之連結而轉起<a 
name="0267a10" id="0267a10"></a>相續不斷絕，解為同一故以取常見。</p><a 
name="0267a11" id="0267a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（二）〔別異之理〕　其次，無明等有各自差別之相，謂「差別之理」，正見此者，<a 
name="0267a12" id="0267a12"></a>見〔事物〕常新生起而捨斷常見。誤見〔此〕者，如於一相續解為多相續之差別故，<a 
name="0267a13" id="0267a13"></a>以取斷見。</p><a 
name="0267a14" id="0267a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（三）〔不作為之理〕　於無明：「諸行應由我而令生起。」〔如此言無作為〕，於<a 
name="0268a01" id="0268a01"></a>諸行：「識是由我等而〔令生起〕。」如言是無作為，謂：「不作為之理。」此之正見者，<a 
name="0268a02" id="0268a02"></a>覺知無作者故而捨斷我見。誤見〔此〕者，於無明等雖無作為，不理解自性而有決<a 
name="0268a03" id="0268a03"></a>定成就之因性故，以取無作見。</p><a 
name="0268a04" id="0268a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（四）〔如是法性之理〕　其次，恰如唯由牛乳等，而有酪等之〔生成〕，唯由<a 
name="0268a05" id="0268a05"></a>無明等之原因，而有行之生成，其他則無〔生成〕，此謂「如是法性之理」。此之正見<a 
name="0268a06" id="0268a06"></a>者，覺知隨順於緣而有果故而捨斷因見、無作見。誤見〔此〕者，不理解隨順於緣<a 
name="0268a07" id="0268a07"></a>而果之起，解為由何物而不生成何物故，以取無因見與決定論。如斯之有輪：</p><a 
name="0268a08" id="0268a08"></a><p><span class="lg">　　　　由諦之發生、作用、遮止、譬喻，<a 
name="0268a09" id="0268a09"></a><br>　　　　應知適宜甚深、理法之差別。</span></p><a 
name="0268a10" id="0268a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>然，此〔有輪〕為甚深故難究其底，有種種之理法，為密林故是甚難通過。<a 
name="0268a11" id="0268a11"></a>雖依殊勝之定石，研磨為智劍，亦不能擊破此有輪，常如雷電碎破〔人〕輪迴之怖<a 
name="0268a12" id="0268a12"></a>畏，則於夢中任何人亦不超越。</p><a 
name="0268a13" id="0268a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.586"> [P.586] </span>又依世尊如次說：「阿難！此緣起顯示甚深、極甚深。而阿難！不知、不覺此<a 
name="0268a14" id="0268a14"></a>法故，如斯如人為絲所縺，如為腫物之覆，如文邪草、波羅波草不能越度苦界、惡<a 
name="0269a01" id="0269a01"></a>趣、墮處、輪迴。」故為自己之利益、安樂而行道以捨斷餘之諸作用，</p><a 
name="0269a02" id="0269a02"></a><p><span class="lg">　　　　於此賢者於甚深種種之緣相<a 
name="0269a03" id="0269a03"></a><br>　　　　以得其深底，當有常念而勤勵。</span></p><a 
name="0269a04" id="0269a04"></a><p style="text-indent: 0em; margin-left: 4em"><span class="linehead"></span>此為喜悅善人，造此清淨道〔論〕，解釋慧修習論中之慧地，名為第十<br><a 
name="0269a05" id="0269a05"></a>七品。</p><a 
