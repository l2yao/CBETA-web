name="0196a13" id="0196a13"></a><p style="margin-left: 2em"><span class="headname"><span class="linehead" title="PTS.Vism.545"> [P.545] </span>〔二、依行之緣而有識〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>於「由行之緣而有識」之句，「識」是眼識等之六種。其<a 
name="0196a14" id="0196a14"></a>中，眼識有善異熟與不善異熟二種，耳、鼻、舌、身識亦同此。意識有善、不善<a 
name="0197a01" id="0197a01"></a>異熟之二意界與（三九、五五）無因〔異熟〕之三意識界（四&#x3007;、四一、五六）、有<a 
name="0197a02" id="0197a02"></a>因異熟之八欲界心、五色界〔異熟心〕、四無色界〔異熟心〕等二十二種。如斯由此<a 
name="0197a03" id="0197a03"></a>等六識，包攝一切三十三世間（有漏）異熟識。其次出世間〔心〕，不適當於輪迴論<a 
name="0197a04" id="0197a04"></a>故不攝。</p><a 
name="0197a05" id="0197a05"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>於此有〔問者〕，然，如上所述知識是由行之緣而生耶？〔曰：〕不積業即無有<a 
name="0197a06" id="0197a06"></a>異熟〔之報〕。然，〔緣此行而生識〕是異熟，異熟不積業時即不生起。若〔業〕之<a 
name="0197a07" id="0197a07"></a>生起，所有者應所有之異熟生起。然，不生故當知無有行之緣而有此識。</p><a 
name="0197a08" id="0197a08"></a><p style="margin-left: 3em"><span class="headname">〔一〕〔行與識之關係〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>由何行之緣而有何識耶？先（一）由欲界福行之緣於<a 
name="0197a09" id="0197a09"></a>善異熟之眼等五識及意識中之一意界、二〔無因〕意識界、八欲界異熟之十六心〔生〕。<a 
name="0197a10" id="0197a10"></a>所謂「欲界之善業所作積故〔善〕異熟之眼識生起。耳、鼻、舌、身識……生起<a 
name="0197a11" id="0197a11"></a>異熟意界……生起喜俱意識界……生起捨俱意識界……〔乃至生起〕喜俱智<a 
name="0197a12" id="0197a12"></a>相應〔無行〕……喜俱智相應有行……喜俱智不相應〔無行〕……喜俱智不相應<a 
name="0197a13" id="0197a13"></a>有行……捨俱智相應〔無行〕……捨俱智相應有行……捨俱智不相應〔無行〕……<a 
name="0197a14" id="0197a14"></a>捨俱智不相應有行〔之意識界〕。」</p><a 
name="0198a01" id="0198a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.546"> [P.546] </span>其次（二）由色界之福行之緣有五色界異熟。所謂：「作積彼色界善業故，離<a 
name="0198a02" id="0198a02"></a>諸欲……以初禪異熟……乃至……具足第五禪而住。」由如斯福行之緣而有二十一種<a 
name="0198a03" id="0198a03"></a>識。</p><a 
name="0198a04" id="0198a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次（三）由非福行之緣有不善異熟眼等之五、一意界、一意識界之七種識。<a 
name="0198a05" id="0198a05"></a>所謂：「作積不善業故，〔不善〕異熟之眼識生起……耳、鼻、舌、身識……〔不<a 
name="0198a06" id="0198a06"></a>善〕異熟之意界……〔不善〕異熟之意識界生起。」</p><a 
name="0198a07" id="0198a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次（四）由不動行之緣而有四無色異熟之四種識。所謂「作積彼無色界之善<a 
name="0198a08" id="0198a08"></a>業故，&#x5FA7;超色想……俱空無邊處想之異熟〔識〕……俱識無邊處……無所有處……<a 
name="0198a09" id="0198a09"></a>非想非非想處〔想〕之〔異熟識〕捨斷樂、苦故……具足第四禪而住」。</p><a 
name="0198a10" id="0198a10"></a><p style="margin-left: 3em"><span class="headname">〔二〕〔異熟識轉起、結生之活動〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>依何行之緣而有何識耶？如以上已知，<a 
name="0198a11" id="0198a11"></a>其次應知如次其〔識之〕活動。即所有〔之識〕轉起、結生為二種之活動。其<a 
name="0198a12" id="0198a12"></a>中，二〔前〕五識、二意界、喜俱無因意識界之此十三，於五蘊有（欲、色界）唯<a 
name="0198a13" id="0198a13"></a>轉起活動。其餘之十九於三有不適宜轉起、結生活動。其為云何？</p><a 
name="0198a14" id="0198a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>首先，善異熟之眼識等之五由善異熟或不善異熟而生——隨業而至其〔眼等之〕<a 
name="0199a01" id="0199a01"></a>根成熟——者，現好或好中之色等之所緣為緣於眼等之識閾，依止於眼淨（眼根）<a 
name="0199a02" id="0199a02"></a>等實行見、聞、嗅、嘗、觸之作用活動。不善異熟〔前〕五〔識〕亦同樣。唯於此<a 
name="0199a03" id="0199a03"></a>等有不好或不好中之所緣為其相違。於此等十〔識認識之〕門、所緣、基（所依）、<a 
name="0199a04" id="0199a04"></a>處所一定，又其作用亦一定。由此於善異熟眼識等之後，於善異熟之意界是與彼<a 
name="0199a05" id="0199a05"></a><span class="linehead" title="PTS.Vism.547"> [P.547] </span>等〔眼識等〕同所緣為緣，依止心基而實行領受作用之活動。不善異熟〔前五識〕<a 
name="0199a06" id="0199a06"></a>直後之不善異熟〔之意界〕亦同樣。而且此二〔意界〕門及所緣是不一定，基與處<a 
name="0199a07" id="0199a07"></a>所是一定，又作用亦一定。其次喜俱無因〔異熟〕意識界，於善異熟意界直後，與<a 
name="0199a08" id="0199a08"></a>其〔意界〕同所緣為緣，依止於心基而實行推度作用之〔活動，又此喜俱無因意識<a 
name="0199a09" id="0199a09"></a>界〕於六門依強力之所緣，欲界諸有情多〔起〕貪相應速行之後，斷有分路，由速<a 
name="0199a10" id="0199a10"></a>行〔心〕對所執取之所緣，為彼所緣〔作用〕一次或二次轉起，是中部義疏之所說。<a 
name="0199a11" id="0199a11"></a>然，於阿毘達磨之義疏，彼所緣〔作用唯〕述有二心分（二次）。而此心有「彼所緣」<a 
name="0199a12" id="0199a12"></a>及「有分頂」之二名。〔於此心〕門與所緣不一定，基為一定，處所與作用為不一定。<a 
name="0199a13" id="0199a13"></a>如斯先述十三〔心〕唯於五蘊有轉起活動。</p><a 
name="0199a14" id="0199a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其餘，十九任何之一，無不適合自己結生之活動。然於轉起，先述善、不善異<a 
name="0200a01" id="0200a01"></a>熟二之無因意識界，於（一）五門善、不善異熟意界之直後行推度作用，（二）於六<a 
name="0200a02" id="0200a02"></a>門如前說之方法而行彼所緣作用，（三）自與結生以來，令斷絕有分心不生起時，行<a 
name="0200a03" id="0200a03"></a>有分作用，（四）於〔人生之〕最後行死之作用，實行斯四作用，基有一定，門、所<a 
name="0200a04" id="0200a04"></a>緣、處所、作用乃不一定而活動。八欲界有因〔異熟〕心，（一）如前說之方法，於<a 
name="0200a05" id="0200a05"></a>六門行彼所緣作用，（二）與自結生以來，令斷絕有分之無心生起時，行有分作用，<a 
name="0200a06" id="0200a06"></a>（三）〔於一生〕最後死之作用，實行斯三作用，基為一定，門、所緣、處所、作用<a 
name="0200a07" id="0200a07"></a>乃不一定而活動。五色界〔異熟心〕及四無界〔異熟心〕者，（一）與自結生以來，<a 
name="0200a08" id="0200a08"></a>令斷絕有分，無心生起時，行有分作用，（二）〔一生之〕最後行死之作用，實行斯<a 
name="0200a09" id="0200a09"></a>二作用之活動。其等中，於色界〔心〕，基、門、所緣是一定，處所、作用是不一<a 
name="0200a10" id="0200a10"></a>定。餘〔之無色界心〕，基一定〔不存在〕，所緣是一定，處所與作用是不一定而活<a 
name="0200a11" id="0200a11"></a>動。如斯先述三十二種之識於轉起，由行之緣而活動。此時，彼等諸行對其〔識〕<a 
name="0200a12" id="0200a12"></a><span class="linehead" title="PTS.Vism.548"> [P.548] </span>而業緣及親依緣為緣。</p><a 
name="0200a13" id="0200a13"></a><p style="margin-left: 3em"><span class="headname">〔三〕〔三界諸趣之業與結生〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次言：「其餘之十九任何之一，無不適合於<a 
name="0200a14" id="0200a14"></a>自己結生之活動。」其餘簡略故難知之。故為其詳細之說示曰：（一）有幾何之結生<a 
name="0201a01" id="0201a01"></a>耶？（二）有幾何之結生心耶？（三）何者於何處結生耶？（四）結心〔心〕之所<a 
name="0201a02" id="0201a02"></a>緣云何？</p><a 
name="0201a03" id="0201a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（一）無想之結生共二十結生。（二）如既述有十九種之結生心。（三）其〔十<a 
name="0201a04" id="0201a04"></a>九心之〕中，不善異熟之無因意識界（五六）為惡趣中之結生〔心〕。善異熟〔之無<a 
name="0201a05" id="0201a05"></a>因意識界〕（四一）於人界生而盲者、生而聾者、生而精神錯亂者、生而聾啞者、<a 
name="0201a06" id="0201a06"></a>非男女（中性）等之〔結生心〕。八之有因欲界異熟〔心〕，是欲界諸天及人中具<a 
name="0201a07" id="0201a07"></a>福者之結生〔心〕。五色界異熟〔心〕，是有色梵界之〔結生心〕。四無色界異熟〔心〕，<a 
name="0201a08" id="0201a08"></a>是無色界之〔結生心〕。何者結生於何處，但其〔結生〕是適合其〔心〕之結生。<a 
name="0201a09" id="0201a09"></a>（四）其次略言〔結生心之所緣〕者，於結生〔心〕，有過去、現在、不可說之三所<a 
name="0201a10" id="0201a10"></a>緣，於無想〔有情之〕結生沒有所緣。其中，識無邊處、非想非非想處之結生〔心〕，<a 
name="0201a11" id="0201a11"></a>唯有過去所緣。於十欲界〔結生心〕，有過去或現在〔之所緣〕。其餘者唯有不可說<a 
name="0201a12" id="0201a12"></a>〔所緣〕。如斯結生對三所緣而轉起，過去所緣或不可說所緣於某死心之直後轉起。<a 
name="0201a13" id="0201a13"></a>而現在所緣為某死心者不存在。故當知以〔過去、不可說〕二所緣之何者為所緣於<a 
name="0201a14" id="0201a14"></a>〔心〕之直後，以〔過去、現在、不可說〕三所緣之何者為所緣之結生〔心〕而轉<a 
name="0202a01" id="0202a01"></a>起善趣、惡趣之行相。即：</p><a 
name="0202a02" id="0202a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（一）〔於欲界善趣有惡業者之結生〕　先述在欲界善趣而有惡業者，「其等<a 
name="0202a03" id="0202a03"></a>〔惡業〕，其時懸於彼前」等語，臥於臨終之&#x7240;者，〔於生前〕所積之惡業或〔惡〕業<a 
name="0202a04" id="0202a04"></a>之相即現來彼意門。緣其〔惡業或業之相〕而生起——為終彼所緣——於速行心之<a 
name="0202a05" id="0202a05"></a>直後，所緣有分境而死心生起。其滅歿時，現其〔意門〕之業、業相為緣，依未斷<a 
name="0202a06" id="0202a06"></a><span class="linehead" title="PTS.Vism.549"> [P.549] </span>絕之煩惱力，使向惡趣所攝之結生心生起。此是以過去為所緣，於死〔心〕之直後，<a 
name="0202a07" id="0202a07"></a>〔轉起〕過去為所緣之結生〔心〕。</p><a 
name="0202a08" id="0202a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其他者於臨終之時，依上述之業類，而地獄等火焰色等之惡趣相現來意門。由<a 
name="0202a09" id="0202a09"></a>此有分二次生起而滅時，以其〔惡趣相之〕所緣為緣，一〔剎那之〕轉向〔心〕、由<a 
name="0202a10" id="0202a10"></a>死之近而速力鈍故五〔剎那之〕速行〔心〕、二〔剎那之〕彼所緣〔心〕等之三路心<a 
name="0202a11" id="0202a11"></a>生起。由此以有分之境為所緣而一死心〔生起〕。至此經過十一心剎那。如斯於彼五<a 
name="0202a12" id="0202a12"></a>心剎那之壽命殘存時，同其所緣而結生心生起。此是過去為所緣之死〔心〕之直後，<a 
name="0202a13" id="0202a13"></a>〔轉起〕現在為所緣之結生〔心〕。</p><a 
name="0202a14" id="0202a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>又其他者於臨終之時，於五門任何之一，為貪等因之劣所緣現來。彼於順次生<a 
name="0203a01" id="0203a01"></a>起確定〔作用〕之終。由死之近而速力鈍故五〔剎那之〕速行〔心〕及二〔剎那之〕<a 
name="0203a02" id="0203a02"></a>彼所緣〔心〕生起。由此有分之境為所緣而一〔剎那之〕死心〔生起〕。而至經過二<a 
name="0203a03" id="0203a03"></a>有分、一轉向、一見〔作用〕、一領受、一推度、一確定、五速行、二彼所緣、一死<a 
name="0203a04" id="0203a04"></a>心等之十五心剎那。如斯存留一剎那壽命時，同以此為所緣而結生心生起。此亦過<a 
name="0203a05" id="0203a05"></a>去為所緣，於死〔心〕之直後，〔轉起〕現在為所緣之結生〔心〕。以上先述過去為<a 
name="0203a06" id="0203a06"></a>所緣於善趣死〔心〕之直後，〔轉起〕過去、現在為所緣而惡趣之結生〔心〕之轉起<a 
name="0203a07" id="0203a07"></a>行相。</p><a 
name="0203a08" id="0203a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（二）〔於惡趣善業者之結生〕　其次，於惡趣積無罪之業者，依如前述之方法，<a 
name="0203a09" id="0203a09"></a>其無罪之業或〔無罪之〕業相現來於意門故，〔前述之〕黑分、〔此時之〕白分以外，<a 
name="0203a10" id="0203a10"></a>當知皆同前說。此是過去為所緣於惡趣死〔心〕之直後，〔轉起〕過去、現在為所緣<a 
name="0203a11" id="0203a11"></a>而善趣之結生〔心〕之轉起行相。</p><a 
name="0203a12" id="0203a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（三）〔於欲界善趣善業者之結生〕　其次，在善趣積無罪之業者，「其等〔善<a 
name="0203a13" id="0203a13"></a>業〕於其時懸彼」等之語故，〔生前〕所積無罪業或〔無罪業〕之相現來臥於臨終之<a 
name="0203a14" id="0203a14"></a>&#x7240;者之意門。而其〔無罪業或無罪業之相〕，唯於欲界積無罪業者而〔現〕。</p><a 
name="0204a01" id="0204a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.550"> [P.550] </span>其次，〔於生前〕積大業（上二界之禪定）者，唯業相之現來。緣此而生起彼所<a 
name="0204a02" id="0204a02"></a>緣為最後，又〔無彼所緣作用〕，唯於速行路之直後，有分境為所緣而死心生起。此<a 
name="0204a03" id="0204a03"></a>為滅歿時，〔於臨終〕現來緣其同樣之業或業相，依未斷絕之煩惱力，令同善趣所攝<a 
name="0204a04" id="0204a04"></a>結生心生起。此以過去為所緣於死〔心〕之直後，〔轉起〕過去為所緣或不可說為所<a 
name="0204a05" id="0204a05"></a>緣而結生。</p><a 
name="0204a06" id="0204a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>又其他者，於臨終之時，依欲界之無罪業，於人界稱母胎樣相之〔善趣相〕，又<a 
name="0204a07" id="0204a07"></a>於天界稱遊苑、宮殿、劫波（晝度）樹等樣相之善趣相現來意門。彼死心之直後，<a 
name="0204a08" id="0204a08"></a>示惡趣相亦同順序而生起結生心。此以過去為所緣於死〔心〕之直後，〔轉起〕現在<a 
name="0204a09" id="0204a09"></a>為所緣而結生。</p><a 
name="0204a10" id="0204a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>又其他者，於臨終之時，諸近親者言：「汝！為汝向佛行此供養，令心欣樂吧！」<a 
name="0204a11" id="0204a11"></a>依〔所見〕花環、幢幡等色之所緣，又依法之聽聞、音樂之供養等聲之所緣，又依<a 
name="0204a12" id="0204a12"></a>香煙、薰香等香之所緣，又言：「汝！以嘗為汝布施〔於佛〕之供物！」依〔令嘗〕<a 
name="0204a13" id="0204a13"></a>蜜、砂糖等味之所緣，又言：「汝！應觸為汝布施〔於佛〕之施物！」依〔令觸〕支<a 
name="0204a14" id="0204a14"></a>那〔絹〕布或蘇摩那布等觸之所緣，持去於五門。於彼對於現其識閾之色等之所<a 
name="0205a01" id="0205a01"></a>緣，於順次生起確定〔作用〕之後，由死之近而速力鈍故，生起五〔剎那之〕速行<a 
name="0205a02" id="0205a02"></a>及二〔剎那之〕彼所緣。由此有分境為所緣而一〔剎那之〕死心〔生起〕，其後，一<a 
name="0205a03" id="0205a03"></a>心剎那住立對其同所緣而結生心生起。此亦過去為所緣於死〔心〕之直後，〔轉起〕<a 
name="0205a04" id="0205a04"></a>現在為所緣而結生。</p><a 
name="0205a05" id="0205a05"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（四）〔由色界之結生〕　其次，依地&#x5FA7;禪等獲得大（色界定），而在〔色界〕<a 
name="0205a06" id="0205a06"></a>善趣之其他者，於臨終之時，欲界善業、〔善〕業相〔善〕趣相之任何一種，或地&#x5FA7;<a 
name="0205a07" id="0205a07"></a><span class="linehead" title="PTS.Vism.551"> [P.551] </span>等之相，或大心（上二界心）現來意門。又於眼、耳等之任何一種，善生起因之殊<a 
name="0205a08" id="0205a08"></a>勝所緣現來。彼於順次生起確定〔作用〕之後，依死之近而速行鈍故，而五〔剎那<a 
name="0205a09" id="0205a09"></a>之〕速行生起。而大趣者（上二界有情）無有彼所緣。故速行之直後以有分境為所<a 
name="0205a10" id="0205a10"></a>緣而一〔剎那之〕死心生起。其後欲界、大〔界〕善趣任何之一種屬於善趣——現<a 
name="0205a11" id="0205a11"></a>起諸所緣中任何一所緣——結生心生起。此以不可說為所緣於〔色界〕善趣死〔心〕<a 
name="0205a12" id="0205a12"></a>之直後，〔轉起〕過去、現在、不可說所緣之任何一種為所緣而結生。</p><a 
name="0205a13" id="0205a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（五）〔由無色界之結生〕　無色死〔心〕直後之結生亦當以此類推可知。此以<a 
name="0205a14" id="0205a14"></a>過去、不可說為所緣於善趣死〔心〕之直後，〔轉起〕過去、不可說、現在為所緣之<a 
name="0206a01" id="0206a01"></a>結生轉起之行相。</p><a 
name="0206a02" id="0206a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（六）〔於惡趣惡業者之結生〕　其次，在惡趣而有惡業者，由既說之方法，彼<a 
name="0206a03" id="0206a03"></a>〔惡〕業又〔惡〕業相或〔惡〕趣相〔現於〕意門，或於五門不善生起因之所緣現<a 
name="0206a04" id="0206a04"></a>來。如斯於彼，於順次〔轉起〕死心之後，屬於惡趣——彼等所緣任何一所緣——結<a 
name="0206a05" id="0206a05"></a>生心生起。此以過去為所緣於惡趣死〔心〕之直後，〔轉起〕過去、現在為所緣轉起<a 
name="0206a06" id="0206a06"></a>結生之行相。</p><a 
name="0206a07" id="0206a07"></a><p style="margin-left: 3em"><span class="headname">〔四〕〔結生識與諸色法之關係〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>依以上十九種識之結生說明其活動。一切悉<a 
name="0206a08" id="0206a08"></a>如斯：</p><a 
name="0206a09" id="0206a09"></a><p><span class="lg">　　　　正在活動，依於結〔生〕業之二種活動，<a 
name="0206a10" id="0206a10"></a><br>　　　　又依混合等之別而有二種以上之別。</span></p><a 
name="0206a11" id="0206a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>即此十九種之異熟識進行結生，依業之二種活動。然，令生此〔結生〕之業，為多<a 
name="0206a12" id="0206a12"></a>剎那之業緣，又為親依緣之緣。即如次說：「善、不善業對於異熟為親依緣之緣。」<a 
name="0206a13" id="0206a13"></a>其次，進行如斯活動彼〔結生〕，由混合等之別，當知有二種以上之差別。即此〔結<a 
name="0206a14" id="0206a14"></a><span class="linehead" title="PTS.Vism.552"> [P.552] </span>生〕雖依結生進行一種活動，但與色混合、不混合之差別故為二種，欲、色、無色<a 
name="0207a01" id="0207a01"></a>有之差別故為三種，依卵生、胎生、濕生、化生而為四種，依〔五〕趣而為五種，<a 
name="0207a02" id="0207a02"></a>依〔七〕識住而為七種，依〔八〕有情居而為八種，其中：</p><a 
name="0207a03" id="0207a03"></a><p><span class="lg">　　　　性之差別故混合為二種<a 
name="0207a04" id="0207a04"></a><br>　　　　又其中之有性亦為二種。<a 
name="0207a05" id="0207a05"></a><br>　　　　與最初者相共，<a 
name="0207a06" id="0207a06"></a><br>　　　　至少共二或三之十法。</span></p><a 
name="0207a07" id="0207a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「性之差別故混合為二種。」此〔十九結生〕中，除不與色混合無色有之〔結生〕，<a 
name="0207a08" id="0207a08"></a>所生起之結生識，於此色有、無有稱為女根、男根之性而生起，又於欲有，除生來<a 
name="0207a09" id="0207a09"></a>半擇迦（閹人即無性者）之結生，有性而生起故為有性與無性之二種。</p><a 
name="0207a10" id="0207a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「又其中亦有二種」，又其中為有性者，具女〔性〕、男性任何之一而生起故為二<a 
name="0207a11" id="0207a11"></a>種。</p><a 
name="0207a12" id="0207a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「與最初者之相共至少共二或三之十法」，於此與〔色〕混合及不混合之二者中，<a 
name="0207a13" id="0207a13"></a>最初者與色混合之結生識，其共〔結生識〕之基、身十法為二之十法，又至少<a 
name="0207a14" id="0207a14"></a>共生起基、身、性之十法為三之十法，由此色無有減少。而此〔色〕如斯唯最少<a 
name="0208a01" id="0208a01"></a>量之生起，於卵生、胎生之二胎，依一支極細之毛所沾起醍醐量程〔大〕名為羯羅<a 
name="0208a02" id="0208a02"></a>藍而生起。其〔結生〕中，應知胎發生之別即是趣〔之別〕。即此等之中：</p><a 
name="0208a03" id="0208a03"></a><p><span class="lg">　　　　除去地獄與地〔居天〕之諸天等無有前之三胎，<a 
name="0208a04" id="0208a04"></a><br>　　　　〔人、畜、餓鬼之〕三趣具有四胎。</span></p><a 
name="0208a05" id="0208a05"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其中，「諸天等。」依「等」之語而除去地獄與地〔居天〕，如於諸天，如斯於燒渴<a 
name="0208a06" id="0208a06"></a>餓鬼，亦應知無有前三胎（卵生、胎生、濕生）。然，彼等唯是化生。其餘除去畜生、<a 
name="0208a07" id="0208a07"></a>餓鬼、人之前三趣，於地居天亦存四胎。其中：</p><a 
name="0208a08" id="0208a08"></a><p><span class="lg">　　　　於有色有三十九及九〔之色〕，<a 
name="0208a09" id="0208a09"></a><br>　　　　又殊勝者有七十色<a 
name="0208a10" id="0208a10"></a><br>　　　　或於濕〔生〕及化生之胎<a 
name="0208a11" id="0208a11"></a><br>　　　　劣者有三十〔色〕。</span></p><a 
name="0208a12" id="0208a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>先述化生胎於有色之諸梵天（色界天），由眼、耳、基之十法及命九法等四<a 
name="0208a13" id="0208a13"></a><span class="linehead" title="PTS.Vism.553"> [P.553] </span>聚三十九色共於結生色而生起。其次，除去有色之梵天，其他於濕生、化生、胎生<a 
name="0208a14" id="0208a14"></a>之勝者依眼、耳、鼻、舌、身、基、性之十法而有七十〔色〕。而其等常於諸天中。<a 
name="0209a01" id="0209a01"></a>其中：色、香、味、食素及〔地、水、火、風〕四界、眼淨、命等，此十之色量、<a 
name="0209a02" id="0209a02"></a>色聚，名為眼十法。應知其餘亦同樣。</p><a 
name="0209a03" id="0209a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，劣者，為生盲、〔生〕聾、無鼻、非男〔非女〕者，依舌、身、基之十法<a 
name="0209a04" id="0209a04"></a>而三十之色生起。又對勝、劣之間者亦應知適當之分別。</p><a 
name="0209a05" id="0209a05"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>已知如以上，更於：</p><a 
name="0209a06" id="0209a06"></a><p><span class="lg">　　　　蘊、所緣、趣為因，〔又〕依受、喜、尋、伺，<a 
name="0209a07" id="0209a07"></a><br>　　　　而當&#x5FA7;知死〔心〕與結生〔心〕等之別、無別。</span></p><a 
name="0209a08" id="0209a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>依此等之蘊等應知〔色之〕混合、不混合故為二種結生及其〔結生〕直後之死等其<a 
name="0209a09" id="0209a09"></a>別、無別之差別義。〔此〕云何？曰：於某時四蘊於無色〔界〕死之直後四蘊之結生，<a 
name="0209a10" id="0209a10"></a>即〔其〕所緣亦與〔死心之所緣〕無有差別。於某時非大（欲界）之外〔蘊〕為所<a 
name="0209a11" id="0209a11"></a>緣於〔死之直後〕，大（無色界）之內〔蘊〕為所緣之〔結生〕。以上唯先於無色地<a 
name="0209a12" id="0209a12"></a>述〔結生之〕方法。其次，某時四蘊於無色〔界〕死之直後，有五蘊欲界之結生。<a 
name="0209a13" id="0209a13"></a>某時五蘊於欲界之死，又於色界死之後，四蘊有無色〔界〕之結生。以上以過去為<a 
name="0209a14" id="0209a14"></a>所緣於死之〔直後轉起〕現在為所緣而結生。或善趣之死〔之直後〕，或有惡趣之結<a 
name="0210a01" id="0210a01"></a>生。無因〔心〕死〔之直後〕有有因〔心〕之結生。二因〔心〕死〔之直後〕有三<a 
name="0210a02" id="0210a02"></a>因〔心〕之結生。捨俱〔心〕死〔之直後〕有喜俱〔心〕之結生。無喜〔心〕死〔之<a 
name="0210a03" id="0210a03"></a>直後〕有有喜〔心〕之結生。無尋〔心〕死〔之直後〕有有尋〔心〕之結生。無伺<a 
name="0210a04" id="0210a04"></a>〔心〕死〔之直後〕有有伺〔心〕之結生。無尋無伺〔心〕死〔之直後〕有有尋有<a 
name="0210a05" id="0210a05"></a>伺〔心〕之結生。如斯各各適當於相反之配合。</p><a 
name="0210a06" id="0210a06"></a><p><span class="lg">　　　　唯得斯緣之法<a 
name="0210a07" id="0210a07"></a><br>　　　　至他之有。其〔生者〕，<a 
name="0210a08" id="0210a08"></a><br>　　　　於此不由（過去有）而移轉，<a 
name="0210a09" id="0210a09"></a><br>　　　　無因不生於此。</span></p><a 
name="0210a10" id="0210a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.554"> [P.554] </span>即得斯緣唯此色、非色之法，言進行生起至他之有。非〔至〕有情，非〔至於〕<a 
name="0210a11" id="0210a11"></a>命。又其〔生者〕非於過去有移轉於此，又若無由〔過去有〕亦即無現前於此。</p><a 
name="0210a12" id="0210a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>以此事很明瞭說明人死、結生之次第。即於過去有，於自然下手而近於死者，<a 
name="0210a13" id="0210a13"></a>切斷一切四肢五體連結之關結——給於難堪瀕死之苦痛——不堪於刀劍之落來，如<a 
name="0210a14" id="0210a14"></a>於炎熱所曝多羅（棕梠）之青葉，次第正使身體憔悴，消滅眼等之諸根，有時唯依<a 
name="0211a01" id="0211a01"></a>存於心基之身根、意根、命根，〔唯〕一剎那依止於殘存心基之識，重、數息、近〔死〕、<a 
name="0211a02" id="0211a02"></a>宿作諸〔業〕任何之一——得〔無明等之〕緣稱為行——業，又於此使現起以緣業<a 
name="0211a03" id="0211a03"></a>相、趣相之境而轉起。如斯進行轉起之〔識〕，由於未捨斷渴愛、無明，由無明隱蔽<a 
name="0211a04" id="0211a04"></a>〔其〕過患，於彼〔惡趣等之〕境，令向渴愛，俱生之諸行〔彼境〕投入〔此識〕。<a 
name="0211a05" id="0211a05"></a>其〔識〕於〔輪迴〕相續中，依渴愛而進行向〔惡趣等境〕，又依於諸行所投入，結<a 
name="0211a06" id="0211a06"></a>著於此岸之樹，如過越水路之懸繩，捨前之依止，依業所等起，以味著後之依止或<a 
name="0211a07" id="0211a07"></a>厭惡，依所緣〔緣〕等之緣而轉起。此時前之死亡故〔言為〕死，而後向其他有等<a 
name="0211a08" id="0211a08"></a>之結生故言為結生。此〔識〕非由前來於此，又無此業、行、向境等之因，當知<a 
name="0211a09" id="0211a09"></a>即不由〔前有〕而現前。</p><a 
name="0211a10" id="0211a10"></a><p><span class="lg">　　　　於此例喻山之回響等<a 
name="0211a11" id="0211a11"></a><br>　　　　非一之相續連結故亦非於多。</span></p><a 
name="0211a12" id="0211a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，於此之識非由前有來此，而屬過去有依諸因之生起，當例喻山響、燈光、<a 
name="0211a13" id="0211a13"></a>印章、映像等類之法。猶如山響、燈光、印章、影乃依聲等之因，如不行他處而存<a 
name="0211a14" id="0211a14"></a>在，此心亦如斯。而此時非一之相續連結故又非於多。即若有相續連結之時，〔連續<a 
name="0212a01" id="0212a01"></a>之前後者〕若一向是唯一，即不能由牛乳而酪之生。又〔若相續前後者〕一向是多<a 
name="0212a02" id="0212a02"></a>者，酪不應依存於牛乳。一切之因與〔此〕生起之關係與此同樣。若如斯若〔一向<a 
name="0212a03" id="0212a03"></a>為一多〕者，應奪去一切世間之言說，而且此不成。故此狀態亦不應一向為一多之<a 
name="0212a04" id="0212a04"></a>說。</p><a 
name="0212a05" id="0212a05"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.555"> [P.555] </span>於此〔反問〕曰：〔若〕如斯無由〔前有〕移轉至〔今有〕而現前者，於人<a 
name="0212a06" id="0212a06"></a>之身體所有之蘊滅故，又果緣之業不來此故，此果為他〔因之果〕應由他〔因〕而<a 
name="0212a07" id="0212a07"></a>成。若無〔業果之〕受用者，其果為誰之物耶？故〔前面之〕提說不善。〔答曰：〕<a 
name="0212a08" id="0212a08"></a>關於此如次之說：</p><a 
name="0212a09" id="0212a09"></a><p><span class="lg">　　　　於相續果此非他之〔因〕，非由他來，<a 
name="0212a10" id="0212a10"></a><br>　　　　諸種子之行乃此義之成就者。</span></p><a 
name="0212a11" id="0212a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>即於一相續果正在生起，於其處一向為一多不成故，〔其〕他之〔果〕亦非由他<a 
name="0212a12" id="0212a12"></a>而來者。但諸種子之行，為此義之成就者。即菴羅（芒果）之種子等所行作（行播<a 
name="0212a13" id="0212a13"></a>種等）之時，其種子之〔發芽成長〕之相續中得依緣，他日生起〔與種子同一〕特<a 
name="0212a14" id="0212a14"></a>別之果，不〔生起〕他種子之果。又雖依他〔種子〕行作之緣，亦不生起〔此特別<a 
name="0213a01" id="0213a01"></a>之果〕，其等種子或行作非得〔其儘〕果位。應知此〔識之因果〕亦如是。又於年<a 
name="0213a02" id="0213a02"></a>少若勤習學術、技藝、醫藥〔之學〕等，應知他日成大人之身等，果現前乃此意義<a 
name="0213a03" id="0213a03"></a>也。</p><a 
name="0213a04" id="0213a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>又「若無受用者，其果為誰之物耶？」就於〔反問者〕而言：</p><a 
name="0213a05" id="0213a05"></a><p><span class="lg">　　　　果之生起故世俗之受用者成，猶如<a 
name="0213a06" id="0213a06"></a><br>　　　　由於果實之生世俗〔言〕樹之實。</span></p><a 
name="0213a07" id="0213a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>即稱樹為法一部分之〔存在〕而樹果之生起，如言樹果或已成果。同樣稱為天<a 
name="0213a08" id="0213a08"></a>人，為蘊一部分之存在——稱為被受用物——依苦樂果之生起，天或人之受用，言<a 
name="0213a09" id="0213a09"></a>快樂或苦惱。故此時不要任何其他受用者。</p><a 
name="0213a10" id="0213a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>又〔反問〕雖有言：「如斯者，彼等諸行正存在，或不存在而當為果之緣。若<a 
name="0213a11" id="0213a11"></a>存在而為〔果之緣〕者，於轉起之剎那必有彼等之異熟。又若不存在而為〔果之緣〕<a 
name="0213a12" id="0213a12"></a>者，於轉起之前或後應常持果。」對彼如次言：</p><a 
name="0213a13" id="0213a13"></a><p><span class="lg">　　　　所造故有此等之緣，非常常持果。<a 
name="0213a14" id="0213a14"></a><br>　　　　就此應知被保證者等之譬喻。</span></p><a 
name="0214a01" id="0214a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>即諸行是所作故為自果之緣。為存在故又不存在故非為〔自果之緣〕。所謂：「<a 
name="0214a02" id="0214a02"></a><span class="linehead" title="PTS.Vism.556"> [P.556] </span>欲界善業所作積故異熟之眼識生起。」等。〔諸行〕適應為自果之緣而後不更感果，因<a 
name="0214a03" id="0214a03"></a>為異熟已熟故。為辨知此義，次當知被保證者之譬喻。即譬喻於世間為遂行何等之<a 
name="0214a04" id="0214a04"></a>目的而有保證〔金錢〕者，彼即行買物或借財。在彼唯行其事業為其目的遂行等之<a 
name="0214a05" id="0214a05"></a>緣，事業之存否非〔目的遂行之緣〕。又遂行其目的等之後非〔保證之〕保有者。何<a 
name="0214a06" id="0214a06"></a>故耶？因為〔目的之〕遂行等已所作故。如斯諸行所作故唯為自果之緣，且〔諸行〕<a 
name="0214a07" id="0214a07"></a>適應與果後，無〔更〕感果。</p><a 
name="0214a08" id="0214a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>以上依〔色之〕混合、不混合而正有二種轉起，說明結生識是依行之緣而轉起<a 
name="0214a09" id="0214a09"></a>之〔狀態〕。</p><a 
name="0214a10" id="0214a10"></a><p style="margin-left: 3em"><span class="headname">〔五〕〔行、識與緣之關係〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>今為破一切之此等三十二異熟識之癡惑：</p><a 
name="0214a11" id="0214a11"></a><p><span class="lg">　　　　於諸有等依結生、轉起而此等諸行以<a 
name="0214a12" id="0214a12"></a><br>　　　　何樣，對如何之〔異熟識〕為緣耶？應識知之。</span></p><a 
name="0214a13" id="0214a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其中，三有、四胎、五趣、七識住、九有情居，此為諸有等，此等「於諸有等結生與<a 
name="0214a14" id="0214a14"></a>轉起。」此等〔諸行〕「對於如何」之異熟識而「為緣耶？」又「於何樣」（幾通）為緣耶？<a 
name="0215a01" id="0215a01"></a>「當識知」其義。</p><a 
name="0215a02" id="0215a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其中，（一）〔福行與結生〕　先於福行，於欲界有八思差別之福行，概言之，<a 
name="0215a03" id="0215a03"></a>於欲有之善趣，於九異熟識之結生，多剎那之業緣及親依緣為二種之緣。色界有<a 
name="0215a04" id="0215a04"></a>五善思差別之福行，於色有之五結生〔多剎那之業緣及親依緣為二種之緣〕。</p><a 
name="0215a05" id="0215a05"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（二）〔福行與轉起〕　次如上述欲界之〔八福行〕，於欲有之善趣——除去捨<a 
name="0215a06" id="0215a06"></a>俱無因意識界——對七小異熟識於轉起，如前述之〔業緣及親依緣為〕二種之緣，<a 
name="0215a07" id="0215a07"></a>非於結生。同其〔福行〕對色有之五種異熟識於轉起同樣之〔二種之〕緣，而非<a 
name="0215a08" id="0215a08"></a><span class="linehead" title="PTS.Vism.557"> [P.557] </span>於結生。其次，〔福行是〕對於欲有之惡趣八小異熟識而轉起同樣為〔二種之〕緣，<a 
name="0215a09" id="0215a09"></a>非於結生。其中，於地獄，大目犍連長老往地獄旅行等之時，此〔福行〕是好所緣<a 
name="0215a10" id="0215a10"></a>結合之緣。又有大神變之畜生及餓鬼〔依福行〕唯得好所緣。又其〔福行〕於欲有<a 
name="0215a11" id="0215a11"></a>對善趣之十六善異熟而同樣轉起〔二種之〕緣，非於結生。又概言之，福行於色<a 
name="0215a12" id="0215a12"></a>有對十異熟識，同樣於轉起為緣，非於結生。</p><a 
name="0215a13" id="0215a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（三）〔非福行與結生、轉起〕　有十二不善思差別之非福行，於欲有對惡趣<a 
name="0215a14" id="0215a14"></a>之一〔無因異熟意〕識於結生〔二種之〕緣，非於轉起。〔其非福行除去上述之一識〕<a 
name="0216a01" id="0216a01"></a>對六〔不善異熟識〕於轉起〔為緣〕，非於結生。又〔非福行對惡趣之〕七不善異熟<a 
name="0216a02" id="0216a02"></a>識而轉起及於結生〔為緣〕。次〔非福行〕於欲有之善趣，同對其等七〔不善異熟識〕<a 
name="0216a03" id="0216a03"></a>而轉起為緣，非於結生。於色有對四異熟識於轉起同為緣，非於結生。而其〔非<a 
name="0216a04" id="0216a04"></a>福行〕於欲界見不好之色，聞〔不好之〕聲而〔轉起〕，於梵天界即無所謂不好之色<a 
name="0216a05" id="0216a05"></a>等。於欲界之天界亦同樣。</p><a 
name="0216a06" id="0216a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（四）〔不動行與結生、轉起〕　〔次〕不動行於無色有對四異熟識而轉起及於<a 
name="0216a07" id="0216a07"></a>結生為〔二種之〕緣。</p><a 
name="0216a08" id="0216a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>先於諸有由結生及轉起，此等諸行為何是〔異熟識〕之緣，於何樣為緣耶？應<a 
name="0216a09" id="0216a09"></a>知如上。當知對於胎等亦與此同樣。其次所述，由最初唯說明〔要〕門。</p><a 
name="0216a10" id="0216a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>即此等〔福、非福、不動之三〕行中，（一）先言「福行」於〔欲、色之〕二有與<a 
name="0216a11" id="0216a11"></a>結生，一切生自己之異熟。同樣於卵生等之四胎，於天、人之二趣，於多身多想、<a 
name="0216a12" id="0216a12"></a>多身一想、一身多想、一身一想之四識住——於無想有情居〔之福行〕唯色為行作<a 
name="0216a13" id="0216a13"></a>故〔以除此〕——於四有情居與結生，一切生自己之異熟。故此〔之福行〕於此等<a 
name="0216a14" id="0216a14"></a>之二有、四胎、二趣、四識住、四有情居對於二十一〔善〕異熟識，從〔其〕發生，<a 
name="0217a01" id="0217a01"></a>於結生及轉起，如上述由〔多剎那之業緣及親依緣〕為緣。</p><a 
name="0217a02" id="0217a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.558"> [P.558] </span>（二）其次「非福行」，唯於一欲有、於四胎，於其餘〔地獄、餓鬼、畜生之〕<a 
name="0217a03" id="0217a03"></a>三趣、於多身一想之一識住、同樣於一有情居由結生而異熟。故此〔非福行〕於一<a 
name="0217a04" id="0217a04"></a>有、四胎、三趣、一識住、一有情居對七〔不善〕異熟，於結生及轉起，如上述〔二<a 
name="0217a05" id="0217a05"></a>種之〕緣。</p><a 
name="0217a06" id="0217a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（三）次「不動行」，唯於一無色有、於一化生、於一天趣、於空無邊處等之三識住，<a 
name="0217a07" id="0217a07"></a>於空無邊處等之四有情居由結生而異熟。故此〔不動行〕於一有、一胎、一趣、三<a 
name="0217a08" id="0217a08"></a>識住、四有情居對於四〔無色界〕異熟，於結生及轉起，如上述為緣。如斯</p><a 
name="0217a09" id="0217a09"></a><p><span class="lg">　　　　於諸有情等由結生、轉起而此諸行於何樣<a 
name="0217a10" id="0217a10"></a><br>　　　　識知對如何之〔異熟識〕為緣耶？</span></p><a 
name="0217a11" id="0217a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>此是詳論「由行之緣而有識」之句。</p><a 
