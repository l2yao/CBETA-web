<div class="root" id="0035" sutra_name="清淨道論(第14卷-第23卷)" sutra_juan="[卷17]">　
<span class="linehead" title="PTS.Vism.516"></span><a 
name="0146a10" id="0146a10"></a><a 
name="0146a11" id="0146a11"></a><p style="margin-left: 2em"><span class="headname"><span class="linehead" title="PTS.Vism.517"> [P.517] </span>第十七品　慧地之解釋</span></p><a 
name="0146a12" id="0146a12"></a><a 
name="0146a13" id="0146a13"></a><p style="margin-left: 3em"><span class="headname">〔慧地之六．緣起之解釋〕</span></p><a 
name="0146a14" id="0146a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>如斯於〔前〕所說：「蘊、處、界、根、諦、緣起等種種之法是〔慧之〕地。」<a 
name="0147a01" id="0147a01"></a>此慧地之諸法中，餘下緣起及「等」之語所攝之緣已生法。故今說明其等之順序。</p><a 
name="0147a02" id="0147a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其中應先知無明等之法是「緣起」。即依世尊如次說：「諸比丘！云何是緣起？<a 
name="0147a03" id="0147a03"></a>諸比丘！無明之緣而有行，行之緣而有識，識之緣而有名色，名色之緣而有六處，<a 
name="0147a04" id="0147a04"></a>六處之緣而有觸，觸之緣而有受，受之緣而有愛，愛之緣而有取，取之緣而有有，<a 
name="0147a05" id="0147a05"></a>有之緣而有生，生之緣而老、死、愁、悲、苦、憂惱發生。如斯有此一切苦蘊之集，<a 
name="0147a06" id="0147a06"></a>諸比丘！此云緣起。」即依世尊如次說：「諸比丘！云何是緣已生法。諸比丘！老<a 
name="0147a07" id="0147a07"></a>死是無常、有為、緣已生、盡滅法、衰滅法、離貪法、滅法也。諸比丘！生……乃<a 
name="0147a08" id="0147a08"></a>至……有、取、愛、受、觸、六處、名色、識、行……諸比丘！無明是無常、有為、<a 
name="0147a09" id="0147a09"></a>緣已生、盡滅法、衰滅法、離貪法、滅法也。諸比丘！此等云緣已生法。」</p><a 
name="0147a10" id="0147a10"></a><p style="margin-left: 4em"><span class="headname"><span class="linehead" title="PTS.Vism.518"> [P.518] </span>一、〔緣起語義之一〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次此〔緣起及緣已生法之〕略解如下，當知「緣起」<a 
name="0147a11" id="0147a11"></a>是緣之法。「緣已生法」是依各各之緣而生之法。此云何可知耶？應依世尊之語而知。<a 
name="0147a12" id="0147a12"></a>即世尊於經說示緣起與緣已生法，「諸比丘！云何是緣起？諸比丘！生之緣而有老<a 
name="0147a13" id="0147a13"></a>死。諸如來之出世又諸如來雖不出世，〔緣起之〕界（自性）是住立、法住性、法決<a 
name="0147a14" id="0147a14"></a>定性，此緣性也。如來現正覺、現觀此。現正覺、現觀之後，而講說、說示、施設、<a 
name="0148a01" id="0148a01"></a>確立、開顯、分別、顯示〔此〕。『汝等當見！』宣示：『諸比丘！生之緣而有老死。』<a 
name="0148a02" id="0148a02"></a>諸比丘！有之緣而有生……乃至……諸比丘！無明之緣而有行。諸如來之出世……<a 
name="0148a03" id="0148a03"></a>乃至……分別、顯示，『汝等當見！』宣示：『諸比丘！無明之緣而有行。』諸如來<a 
name="0148a04" id="0148a04"></a>之出世……乃至……分別、顯示，『汝等當見！』宣示：『諸比丘！無明之緣而有<a 
name="0148a05" id="0148a05"></a>行。』諸比丘！此中所有如性、不違如性、不他性、此緣性，諸比丘！此言為緣起。」<a 
name="0148a06" id="0148a06"></a>如斯說示緣起是如性等之同義語，說緣之法是緣起也。</p><a 
name="0148a07" id="0148a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>故「緣起」是老死等諸法之緣為相，與苦連結為味，當知邪道（輪迴）為現起。<a 
name="0148a08" id="0148a08"></a>而此〔緣起〕各各不多不少，依緣而各各之法而發生故言為「如性」。至諸緣和合時，<a 
name="0148a09" id="0148a09"></a>雖須臾由此而生之諸法無不發生故〔言為〕「不違如性」。依他法（甲）〔應生〕之緣，<a 
name="0148a10" id="0148a10"></a>他法（乙）不生起故，〔言為〕「不他性」。如上述此等老死等之緣故，又緣之合聚故，<a 
name="0148a11" id="0148a11"></a>言為「此緣性」。〔此緣性〕之語義如次，此等〔老死等〕之緣是此緣。此緣即此緣性<a 
name="0148a12" id="0148a12"></a>也。又此緣之合聚是此緣性。而此〔緣起之語〕相，當&#x5FA7;求於文典。</p><a 
name="0148a13" id="0148a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.519"> [P.519] </span>其次，某些人不顧慮〔數論〕外學所&#x5FA7;計自性、神我等之原因，依緣之正生起<a 
name="0148a14" id="0148a14"></a>是緣起，說緣起唯斯之生起。此為不正當。何故耶？（一）〔斯說〕於經無故，（二）<a 
name="0149a01" id="0149a01"></a>於經相違故，（三）不生甚深之道理故。（四）破壞語故。</p><a 
name="0149a02" id="0149a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>即（一）「經無有」〔說〕唯生起為緣起。</p><a 
name="0149a03" id="0149a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（二）言緣起其〔唯生起〕者是部分住〔者說〕，此「陷於與經相違故」。云何〔相<a 
name="0149a04" id="0149a04"></a>違〕耶？謂：「時，世尊於初夜作意順逆之緣起。」等乃世尊之語故，最初住正覺之<a 
name="0149a05" id="0149a05"></a>緣起作意是部分住，是彼〔世尊〕之一部分住。所謂：「諸比丘！我最初之住正覺，<a 
name="0149a06" id="0149a06"></a>同依部分〔住〕而住。」其時，〔世尊〕見緣相而住，非見唯生起〔而住〕。所謂「彼<a 
name="0149a07" id="0149a07"></a>〔我〕『依邪見之緣亦有覺受，依正見之緣亦有覺受，依邪思惟之緣亦有覺受〔云云〕』<a 
name="0149a08" id="0149a08"></a>而如斯知解」。如斯言緣起唯生起者，〔說〕部分住陷於與經相違。又與迦旃延經相<a 
name="0149a09" id="0149a09"></a>違。即於迦旃延經有：「迦旃延！有如實正慧見世間之集（因）者，於世間無非有<a 
name="0149a10" id="0149a10"></a>性。」順緣起為世間〔發生〕之緣故是世間之集，為說明斷見之斷絕，非〔說〕唯生<a 
name="0149a11" id="0149a11"></a>起。然，由見唯生起，斷見不能斷絕，緣之不斷時，果為不斷故，只是依見緣之不<a 
name="0149a12" id="0149a12"></a>斷〔斷見即斷絕〕，如斯言緣起唯是生起者，即陷於與迦旃經相違。</p><a 
name="0149a13" id="0149a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（三）其次，「不生甚深之道理故」，依世尊如次說：「阿難！此緣起是甚深又<a 
name="0149a14" id="0149a14"></a>甚深也。」而甚深是四種〔甚深〕，此於後說明。〔故〕其〔緣起〕唯於生起而非能<a 
name="0150a01" id="0150a01"></a>存在。又彼等此緣起以四種之道理（四句分別）所莊嚴及讚說，但其四種道理亦於<a 
name="0150a02" id="0150a02"></a>唯生起非能存在。故不生甚深之道理，所以緣起非唯生起。</p><a 
name="0150a03" id="0150a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.520"> [P.520] </span>（四）其次，「破壞語故」，此「緣」（依緣）之語是結合於過去時，對同一主語<a 
name="0150a04" id="0150a04"></a>而成〔其〕目的。如謂：「緣眼與諸色而眼識生起。」〔之情形〕。然，言此〔緣起語〕<a 
name="0150a05" id="0150a05"></a>時，〔緣（依緣）之語不要主語〕，結合狀況之「起」語，〔於兩者〕無同一之主語故，<a 
name="0150a06" id="0150a06"></a>〔若緣起唯有生起之意義者〕，即導致語之破壞，不成何等之義。所以破壞語故緣起<a 
name="0150a07" id="0150a07"></a>非唯生起。</p><a 
name="0150a08" id="0150a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>此時，以〔緣起之語〕結合「有」之語，〔緣起者〕是為「緣起有」〔之意義〕，但<a 
name="0150a09" id="0150a09"></a>不合理。何故耶？不結合〔斯「有」之語〕故，又結合於「有」之語者，於生起即〔更〕<a 
name="0150a10" id="0150a10"></a>有生起之過失故，即言：「諸比丘！我為汝等說示緣起。諸比丘！云何是緣起……<a 
name="0150a11" id="0150a11"></a>乃至……諸比丘！此言為緣起。」此等〔緣起之〕語亦無有一與「有」之結合。</p><a 
name="0150a12" id="0150a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>又〔緣起之語〕非與生起〔之語結合〕。若然，於生起應該〔更〕生起。</p><a 
name="0150a13" id="0150a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>亦有人思惟：「此緣之狀態是此緣性。狀態是行相，無明等是行現前之因。其〔狀<a 
name="0150a14" id="0150a14"></a>態〕是在行之變化名為緣起。」但彼等之說不合理。何故耶？語無明等為因故。即依<a 
name="0151a01" id="0151a01"></a>世尊：「因此，阿難！所有之生是此老死之因，此是因緣，此是集，此是緣……乃<a 
name="0151a02" id="0151a02"></a>至……所有無明是行之……緣。」如斯說無明等是因，非〔說〕彼等之變化為〔因〕。<a 
name="0151a03" id="0151a03"></a>故知緣起是緣之法。所說此〔緣起是緣之法〕，當知是正當之說。</p><a 
name="0151a04" id="0151a04"></a><p style="margin-left: 4em"><span class="headname">二、〔緣起語義之二〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>於此所謂緣起，因此文字蔭暗之〔錯誤〕，想念生起而說<a 
name="0151a05" id="0151a05"></a>此唯是生起，但知此語有如次之義，應寂滅斯〔想念〕。即世尊言：</p><a 
name="0151a06" id="0151a06"></a><p><span class="lg">　　　　彼〔緣〕轉起之法聚　　　　說〔緣起〕二種〔意義〕<a 
name="0151a07" id="0151a07"></a><br>　　　　是故彼緣之〔法聚〕　　　　用果〔之語〕說〔緣起〕。</span></p><a 
name="0151a08" id="0151a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>曰：主張依緣性轉起此法聚〔言為〕緣起之此語，有二種〔之意義〕。即（一）此〔緣<a 
name="0151a09" id="0151a09"></a><span class="linehead" title="PTS.Vism.521"> [P.521] </span>起〕是為〔有情之〕利益、安樂而使了解故，諸賢者以此使〔諸有情〕有了解之價<a 
name="0151a10" id="0151a10"></a>值而〔言為〕「緣。」又生起者，俱〔生起而〕非單獨，正生起而非無因故〔言為〕<a 
name="0151a11" id="0151a11"></a>「起」，如斯「緣而且為起故」為緣起。（二）又俱生起故為「起」。依緣之和合，不排拒<a 
name="0151a12" id="0151a12"></a>〔緣之和合〕故為「緣」。如斯此緣而且為起故亦為緣起。而此因之聚為彼〔緣已生<a 
name="0151a13" id="0151a13"></a>法聚之〕緣故為彼緣。為彼緣故亦是〔此因之法聚〕，如言痰塊是世間痰〔病〕之緣<a 
name="0151a14" id="0151a14"></a>塊。又如樂教法之緣而諸佛之出世，言：「諸佛出世樂。」當知緣起是依果而說。</p><a 
name="0152a01" id="0152a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（三）或者：</p><a 
name="0152a02" id="0152a02"></a><p><span class="lg">　　　　此因聚由因　　　　向〔果〕故言「緣」<a 
name="0152a03" id="0152a03"></a><br>　　　　因諸俱生法　　　　生起故言「起」</span></p><a 
name="0152a04" id="0152a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>曰：使行等現前，依無明等一一因之名目而說此因聚，此由成共通〔一定〕果之義，<a 
name="0152a05" id="0152a05"></a>依不缺之義，諸和合支（因聚）於互相依此（因）赴向於〔果義〕而言為「緣。」<a 
name="0152a06" id="0152a06"></a>其〔因聚〕是諸俱在而無互相別離令生起法故言為「起」。如斯緣而且為起故亦為緣<a 
name="0152a07" id="0152a07"></a>起。</p><a 
name="0152a08" id="0152a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（四）又有其他之理由</p><a 
name="0152a09" id="0152a09"></a><p><span class="lg">　　　　此緣性互相為緣　　　　平等俱時諸法生<a 
name="0152a10" id="0152a10"></a><br>　　　　是故於此依牟尼　　　　如斯說〔緣起之語〕</span></p><a 
name="0152a11" id="0152a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>曰：依無明之名目而說諸緣之中，所生起行等法之諸緣，此無互相緣，於互相欠缺<a 
name="0152a12" id="0152a12"></a>時，即不能使生起〔諸法〕，故為「緣。」又此緣性令生起諸法，平等而非偏頗，俱時<a 
name="0152a13" id="0152a13"></a>而不依前後，〔故為「起」〕。依巧於言說之牟尼隨順於義說如斯——說緣起之義<a 
name="0152a14" id="0152a14"></a>——。</p><a 
name="0153a01" id="0153a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>又〔世尊〕如斯說：</p><a 
name="0153a02" id="0153a02"></a><p><span class="lg">　　　　依前句常〔論〕等非有，又依後句<a 
name="0153a03" id="0153a03"></a><br>　　　　斷〔論〕等之害破，由於兩者說真理。</span></p><a 
name="0153a04" id="0153a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.522"> [P.522] </span>「依前句。」說明緣之和合是依「緣」之句。轉起之諸法依屬緣之和合而起故，如常<a 
name="0153a05" id="0153a05"></a>〔論〕、無因〔論〕、〔自性、微、時等之〕異因〔論〕、自在天論之類，〔依緣之句〕<a 
name="0153a06" id="0153a06"></a>說明「常〔論〕等之非有」。又「依後句」，依「起」之句說明諸法之生起。於緣之和合<a 
name="0153a07" id="0153a07"></a>而諸法之生起故，以破斷〔論〕、非有〔論〕、無作〔論〕等，即依〔起之句〕說明：<a 
name="0153a08" id="0153a08"></a>「斷〔論〕等之害破。」「依兩者」，語緣與起之全體。由各各之緣和合而不斷相續、<a 
name="0153a09" id="0153a09"></a>各各諸法發生故，〔說明緣與起之〕中道，捨斷所謂：「作者與受者是同一，作者<a 
name="0153a10" id="0153a10"></a>與受者是相異。」之〔邪〕論。不住著於世人之用語，不超越〔世俗之〕名稱「說明此<a 
name="0153a11" id="0153a11"></a>真理」。以上先述緣起語之義。</p><a 
