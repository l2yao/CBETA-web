name="0222a12" id="0222a12"></a><p style="margin-left: 2em"><span class="headname">〔三、依識之緣而有名色〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>所謂於「識之緣而有名色」之句：</p><a 
name="0222a13" id="0222a13"></a><p><span class="lg">　　　　〔一〕名色之分別。〔二〕於有等之轉起。<a 
name="0222a14" id="0222a14"></a><br>　　　　〔三〕依攝。〔四〕依緣之方法應識知決定說。</span></p><a 
name="0223a01" id="0223a01"></a><p style="margin-left: 3em"><span class="headname">〔一〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「名色之分別」中，「名」者，〔名是〕面向於所緣故，是受等之三蘊。「色」<a 
name="0223a02" id="0223a02"></a>是四大種及四大種所造之色。彼等之分別於蘊之解釋既說。如斯先識知此名色分<a 
name="0223a03" id="0223a03"></a>別之決定說。</p><a 
name="0223a04" id="0223a04"></a><p style="margin-left: 3em"><span class="headname">〔二〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「於有等轉起」之〔句〕中，「名」是除去一切之有、胎、識住、〔及無想有<a 
name="0223a05" id="0223a05"></a>情之〕一有情居，其餘於〔八〕有情居中轉起。「色」於二有、四胎、五趣、前四識<a 
name="0223a06" id="0223a06"></a>住、〔前〕五有情居中轉起。此名色正如斯轉起之時，（一）無性（無男女性）之胎<a 
name="0223a07" id="0223a07"></a><span class="linehead" title="PTS.Vism.559"> [P.559] </span>生者及卵生者於結生剎那基、身十法之二色相續目及三非色蘊現前故，若詳說<a 
name="0223a08" id="0223a08"></a>其等者，當知色色（此時指基十法、身十法）之二十法及三非色蘊之此等二十三<a 
name="0223a09" id="0223a09"></a>法是由識之緣〔而生起〕名色。而除去重複者，由一相續目取掉九色法即成十四<a 
name="0223a10" id="0223a10"></a>〔法〕。（二）於有性者加性十法而三十三〔法現前〕。對其等亦除去重複者，由二相<a 
name="0223a11" id="0223a11"></a>續目取掉十八色法而成十五〔法〕。</p><a 
name="0223a12" id="0223a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（三）其次，於化生有情之中，梵眾天等之結生剎那，眼、耳、基之十法、命<a 
name="0223a13" id="0223a13"></a>根之九法、四色相續目、三非色蘊之現前故，若詳說其等者，當知色色之三十九法、<a 
name="0223a14" id="0223a14"></a>三非色蘊之此等四十二法由識之緣〔而生起〕名色。而除去重複者，由三相續目取<a 
name="0224a01" id="0224a01"></a>掉二十七法而成十五〔法〕。</p><a 
name="0224a02" id="0224a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（四）其次，於欲有其餘之化生者、濕生者，又〔內六〕處之完具有性者之<a 
name="0224a03" id="0224a03"></a>結生剎那，〔眼、耳、鼻、舌、身、基、性之〕七色相續目及三非色蘊現前故，詳說<a 
name="0224a04" id="0224a04"></a>其等者，當知色色之七十法、三非色蘊之此等七十三法由識之緣〔而生起〕名色。<a 
name="0224a05" id="0224a05"></a>而除去重複者，由六色相續目取掉五十四法而成十九〔法〕。此是勝者〔有情之〕<a 
name="0224a06" id="0224a06"></a>情形。（五）其次，於劣者各各減去所缺之色相續目，不論是簡略或詳細，當知由識<a 
name="0224a07" id="0224a07"></a>之緣〔而生起〕名色。</p><a 
name="0224a08" id="0224a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（六）其次，於無色〔有之結生〕者，唯三非色蘊〔現前〕。（七）無想〔有之<a 
name="0224a09" id="0224a09"></a>結生〕者，唯色命根九法〔現前〕。以上先述結生之方法。</p><a 
name="0224a10" id="0224a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（八）其次，於轉起，於色之轉起所有方處之結生心於存續之剎那，共結生心<a 
name="0224a11" id="0224a11"></a>轉起之時節（寒暑等），時節等起之單純八法現前。然而結生心不能令色等起。然，<a 
name="0224a12" id="0224a12"></a>譬喻落入懸崖之人不可能有〔救上〕他人之緣，如斯其〔結生心〕因為基〔色〕弱<a 
name="0224a13" id="0224a13"></a><span class="linehead" title="PTS.Vism.560"> [P.560] </span>而〔自〕弱故，不能令色等起。然，由結生心之後最初之有分以來，心等起之單純<a 
name="0224a14" id="0224a14"></a>八法〔現前〕。（九）於聲之現前時，結生之後轉起時節及由心而聲九法〔現前〕。<a 
name="0225a01" id="0225a01"></a>（一&#x3007;）而於段食所活之諸胎生有情</p><a 
name="0225a02" id="0225a02"></a><p><span class="lg">　　　　由彼母所食之食物飲料，<a 
name="0225a03" id="0225a03"></a><br>　　　　在彼母胎者於其處維持〔生命〕。</span></p><a 
name="0225a04" id="0225a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>之語故，母親所吸收之食物循環於身體時〔食等起之單純八法現前〕。（二）於諸化<a 
name="0225a05" id="0225a05"></a>生〔有情〕，〔結生後〕先嚥下最初生於自己口中之唾時，食等起之單純八法〔現前〕。</p><a 
name="0225a06" id="0225a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>如斯〔此等諸有情〕食等起之單純八法、時節、心等起之——〔聲現前而〕殊勝<a 
name="0225a07" id="0225a07"></a>時——二之〔聲〕九法之二十六種、業等起之——於一一剎那進行三回生〔住滅〕<a 
name="0225a08" id="0225a08"></a>——於前所說〔眼、耳、鼻、舌、身、基、性十法之〕七十種為九十六種法；及<a 
name="0225a09" id="0225a09"></a>加上三非色法，總計九十九法〔現前〕。或聲〔之現前〕是不定時而現前故，〔若不<a 
name="0225a10" id="0225a10"></a>數此，〕即除掉〔時節、心等起之〕二種〔聲九法〕，當知此等八十一法，於一切<a 
name="0225a11" id="0225a11"></a>有情之發生由識之緣〔而生起〕名色。即彼等〔有情〕於睡眠時、於放逸時、於食<a 
name="0225a12" id="0225a12"></a>時、於飲時、於晝夜之此等〔八十一法〕由識之緣而轉起。而其等由識之緣當於後<a 
name="0225a13" id="0225a13"></a>說明。</p><a 
name="0225a14" id="0225a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，此時之業生色者，此於有、胎、趣、〔識〕住、有情居中，雖於最初之住<a 
name="0226a01" id="0226a01"></a>立者，但無有依〔時節、心、食之〕三與等起之支持即不能持續。又三與等起之〔色〕<a 
name="0226a02" id="0226a02"></a>無其〔業生色〕之支持即不〔能持續〕。如斯雖風之吹來，如由四方固立之蘆束〔而<a 
name="0226a03" id="0226a03"></a>不倒〕，如嵌於大海何處之破損車，雖被怒濤所擊〔亦不動〕。此等〔四等起色〕互<a 
name="0226a04" id="0226a04"></a>相支持不倒而住立，即一年、二年……乃至……百年至彼等有情之壽盡、又福盡止<a 
name="0226a05" id="0226a05"></a>而轉起。當知如斯於有情由此〔名色〕轉起之決定說。</p><a 
name="0226a06" id="0226a06"></a><p style="margin-left: 3em"><span class="headname">〔三〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「依此〔名色〕之攝」，於無色〔有〕之轉起、結生及五蘊有（欲、色界）<a 
name="0226a07" id="0226a07"></a>之轉起，由識之緣唯名〔生起〕。又於無想〔有〕之一切處及五蘊有之轉起，由識之<a 
name="0226a08" id="0226a08"></a><span class="linehead" title="PTS.Vism.561"> [P.561] </span>緣而唯色〔生起〕。又於五蘊有之一切處，由識之緣而名色〔生起〕。其一切之名、<a 
name="0226a09" id="0226a09"></a>色、名色——〔名色是有名與色之〕各方特質而存〔名色之〕一——令攝於名色之語。<a 
name="0226a10" id="0226a10"></a>當知〔所說〕者「由識之緣而有名色」。</p><a 
name="0226a11" id="0226a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>於無想〔有〕，無識之故，不適合說〔由識之緣而有名色〕，但非不適合。即：</p><a 
name="0226a12" id="0226a12"></a><p><span class="lg">　　　　識是名色因，此有異熟與非異熟<a 
name="0226a13" id="0226a13"></a><br>　　　　二種，是故為適合。</span></p><a 
name="0226a14" id="0226a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>曰：識是名色之因，此異熟與非異熟之別故為二種。而於無想有情之此〔色〕是業<a 
name="0227a01" id="0227a01"></a>等起之故，於五蘊有轉起由行作識之緣〔而生起〕色，同樣於五蘊有轉起之〔色〕，<a 
name="0227a02" id="0227a02"></a>乃於善等之心剎那，由業令等起者，故此為適合。當知如斯此由〔名色〕攝之決定<a 
name="0227a03" id="0227a03"></a>說。</p><a 
name="0227a04" id="0227a04"></a><p style="margin-left: 3em"><span class="headname">〔四〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「此由〔名色之〕緣。」曰：</p><a 
name="0227a05" id="0227a05"></a><p><span class="lg">　　　　〔異〕熟識對於名為九種緣<a 
name="0227a06" id="0227a06"></a><br>　　　　對於基色是九種對餘之色是八種〔緣〕。<a 
name="0227a07" id="0227a07"></a><br>　　　　行作識對於色為一種〔緣〕。<a 
name="0227a08" id="0227a08"></a><br>　　　　又其他之識對各各〔之名色〕於適宜〔為緣〕。</span></p><a 
name="0227a09" id="0227a09"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>即（一）於結生或轉起，稱為異熟之此名——〔即〕結生或其他之異熟識——對<a 
name="0227a10" id="0227a10"></a>彼色混合或不混合〔之名〕，為俱生、互相、依止、相應、異熟、食、根、有、不離<a 
name="0227a11" id="0227a11"></a>去緣之九種緣。（二）結生時之〔異熟識〕對於基色，為俱生、互相、依止、異熟、<a 
name="0227a12" id="0227a12"></a>食、根、不相應、有、不離去緣之九種緣。（三）又除去基色而對其餘之色，上之九<a 
name="0227a13" id="0227a13"></a>〔緣〕中，取掉互相緣，餘之八緣為緣。</p><a 
name="0227a14" id="0227a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（四）其次，行作識是對無想有情之色，又於五蘊有對於業生色，若依經之<a 
name="0228a01" id="0228a01"></a>教說，唯親依〔緣〕之一緣。（五）其餘由最初之有分以來之一切識，當知對各各之名<a 
name="0228a02" id="0228a02"></a>色以適宜者為緣。而為示其詳細緣之方法，不得不說一切之發趣說故，〔今〕不關<a 
name="0228a03" id="0228a03"></a>說此。</p><a 
name="0228a04" id="0228a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>就其〔名色之緣〕，然者，有〔反問者〕，結生之名色，由如何知是依識之緣<a 
name="0228a05" id="0228a05"></a><span class="linehead" title="PTS.Vism.562"> [P.562] </span>〔而生起〕耶？但〔關於此〕依經典及道理而〔應知〕。即依於經中：「諸法隨心<a 
name="0228a06" id="0228a06"></a>轉。」等理趣而成對受等以識為緣。其次依道理</p><a 
name="0228a07" id="0228a07"></a><p><span class="lg">　　　　於此由見心生之色<a 
name="0228a08" id="0228a08"></a><br>　　　　證明對未見之色亦以識為緣。</span></p><a 
name="0228a09" id="0228a09"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>即心之欣樂或不欣樂之時，可見此是隨順色之生起。由所見之〔色〕，亦可推知未<a 
name="0228a10" id="0228a10"></a>見之〔色〕。如斯於此由自<span class="corr" title='[已&gt;己]'>己</span>見之心生色而〔推〕，當知對未見之結生色，亦以識為<a 
name="0228a11" id="0228a11"></a>緣。又對業等起〔之色〕，如對彼心等起〔之色〕，識為緣是發趣論之所述。當知<a 
name="0228a12" id="0228a12"></a>由如斯此〔名色〕緣之決定說。</p><a 
name="0228a13" id="0228a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>此詳論「由識之緣而有名色」之句。</p><a 
name="0228a14" id="0228a14"></a><p style="margin-left: 2em"><span class="headname">〔四、依名色之緣而有六處〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>所謂於「名色之緣而有六處」之句：</p><a 
name="0229a01" id="0229a01"></a><p><span class="lg">　　　　名是三蘊，色為〔四大〕種、基等。<a 
name="0229a02" id="0229a02"></a><br>　　　　結合其等〔之名色〕，彼同為〔六處〕之緣。</span></p><a 
name="0229a03" id="0229a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>即為六處之緣。此名色中，「名」是受等之三蘊。其次，「色」是屬於自己之〔身<a 
name="0229a04" id="0229a04"></a>體〕相續者，決定而云四大種、六基、命根，如是當知是〔大〕種、基等。而其名、<a 
name="0229a05" id="0229a05"></a>色、名色〔之其中〕唯存一而為「名色」。第六處與六處〔之其中〕唯存一而為「六處」，<a 
name="0229a06" id="0229a06"></a>當知〔其名色〕為六處之緣。何以故？於無色〔有〕唯名為緣，而且唯以第六處〔為<a 
name="0229a07" id="0229a07"></a>緣〕，不為其他〔處之緣〕故。於分別論說：「由名色之緣而有第六處。」</p><a 
name="0229a08" id="0229a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>對此有〔反問〕者，然，此如何知名色是六處之緣耶？〔答曰：〕名色存時而<a 
name="0229a09" id="0229a09"></a>〔六處〕存也。即各各之名或色存時而有各各之處，非由其他〔而存〕。而其〔名色〕<a 
name="0229a10" id="0229a10"></a>存時其〔六處〕存者，此於〔以下之〕緣論法當明之。故：</p><a 
name="0229a11" id="0229a11"></a><p><span class="lg">　　　　<span class="linehead" title="PTS.Vism.563"> [P.563] </span>於結生又轉起於何有何之緣？<a 
name="0229a12" id="0229a12"></a><br>　　　　有何之緣聰慧者當推知。</span></p><a 
name="0229a13" id="0229a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>云何？</p><p style="margin-left: 3em"><span class="headname">〔一〕〔名之緣〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（一）〔無色有結生之時〕　先述結生，名是對於第六處，<a 
name="0229a14" id="0229a14"></a>至少以俱生、互相、依止、相應、異熟、有、不離去緣之七種緣為緣。雖於此時，<a 
name="0230a01" id="0230a01"></a>但或者為因緣，或者為食緣之〔緣〕，如亦有其他之緣，由此當知其情形或多或少。</p><a 
name="0230a02" id="0230a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（二）〔無色有轉起之時〕　雖於轉起，但異熟〔之名〕是前所述之〔俱生等之<a 
name="0230a03" id="0230a03"></a>七種緣〕為緣。其次，不為其他〔異熟之名〕，至少如前述由〔七〕緣除去異熟緣之<a 
name="0230a04" id="0230a04"></a>六種緣為緣。而此時，或者為因緣，或者為食緣而〔為緣〕，如有其他之緣，由此當<a 
name="0230a05" id="0230a05"></a>知情形或多或少。</p><a 
name="0230a06" id="0230a06"></a><p><span class="lg">　　　　於他之有，名於結生對於第六〔處〕亦然。<a 
name="0230a07" id="0230a07"></a><br>　　　　對其他〔之色處等〕為六種緣。</span></p><a 
name="0230a08" id="0230a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（三）〔五蘊有結生之時〕　即於無色〔有〕以外五蘊有〔之結生〕，其異熟之<a 
name="0230a09" id="0230a09"></a>名，隨伴於心基，對第六意識，如說無色〔有〕之時，至少為七種緣。然對其他眼<a 
name="0230a10" id="0230a10"></a>處等之五處，其〔名〕隨伴大種，以俱生、依止、異熟、不相應、有、不離去之六<a 
name="0230a11" id="0230a11"></a>種緣為緣。然此時或為因緣，或為食緣〔之緣〕，如有其他之緣，由此當知情形或多<a 
name="0230a12" id="0230a12"></a>或少。</p><a 
name="0230a13" id="0230a13"></a><p><span class="lg">　　　　又於轉起〔異〕熟，對〔異〕熟同樣為緣，<a 
name="0230a14" id="0230a14"></a><br>　　　　非〔異〕熟對非異熟之第六〔處〕而為六種緣。</span></p><a 
name="0231a01" id="0231a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（四）〔五蘊有轉起之時〕　即於五蘊有之轉起如〔其〕結生。（a）異熟之名<a 
name="0231a02" id="0231a02"></a>對於異熟之第六處，至少同樣以七種緣為緣。（b）其次，非異熟之〔名〕對非異熟<a 
name="0231a03" id="0231a03"></a>之第六〔處〕，由此至少取掉異熟緣而為六種緣，而其多、少如前述類推可知。（c）<a 
name="0231a04" id="0231a04"></a><span class="linehead" title="PTS.Vism.564"> [P.564] </span>同於〔五蘊〕有之異熟，對餘之五〔處〕而為四種緣，非異熟亦如斯說明。即其處<a 
name="0231a05" id="0231a05"></a>〔五蘊有〕之轉起，眼淨等之基及其他之異熟名，對餘之眼處等之五〔處〕，而以後<a 
name="0231a06" id="0231a06"></a>生、不相應、有、不離去緣之四種緣為緣。（d）如異熟不異熟對〔眼等之五處〕<a 
name="0231a07" id="0231a07"></a>亦同樣之說明。故善等之別有〔心、心所〕，亦當知對彼等〔眼等之五處〕為四種緣，<a 
name="0231a08" id="0231a08"></a>如斯先述名於結生或轉起，對何者之處為緣耶？何情形為緣耶應知之。</p><a 
name="0231a09" id="0231a09"></a><p style="margin-left: 3em"><span class="headname">〔二〕〔色之緣〕</span></p><a 
name="0231a10" id="0231a10"></a><p><span class="lg">　　　　茲此色於無色之有，不為任何一<a 
name="0231a11" id="0231a11"></a><br>　　　　處之緣，亦於五蘊有〔之轉起〕<a 
name="0231a12" id="0231a12"></a><br>　　　　於色〔有之〕結生，基乃對於第六處<a 
name="0231a13" id="0231a13"></a><br>　　　　為六種緣，大種對於五〔處〕總為四種〔緣〕。</span></p><a 
name="0231a14" id="0231a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>即於（一）色（五蘊有）之結生，基色對第六意處，為俱生、互相、依止、不<a 
name="0232a01" id="0232a01"></a>相應、有、不離去緣之六種緣。（二）其次，四大種於〔五蘊有之〕結生及轉起，就<a 
name="0232a02" id="0232a02"></a>於所生起各各之處，總而言之，對於眼處等之五，以俱生、依止、有、不離去緣之<a 
name="0232a03" id="0232a03"></a>四種緣為緣。</p><a 
name="0232a04" id="0232a04"></a><p><span class="lg">　　　　命、食於〔結生、〕轉起，<a 
name="0232a05" id="0232a05"></a><br>　　　　對此等〔五處〕為三種〔緣〕，<a 
name="0232a06" id="0232a06"></a><br>　　　　其等〔五處〕對於第六〔處〕為六種〔緣〕，<a 
name="0232a07" id="0232a07"></a><br>　　　　基對此為五種〔緣〕。</span></p><a 
name="0232a08" id="0232a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（三）其次，於〔五蘊有之〕結生及轉起，（a）色之命對此等眼等之五〔處〕，<a 
name="0232a09" id="0232a09"></a>以有、不離去、根〔緣〕之三種緣為緣。（b）又食以有、不離去、食〔緣〕之三種<a 
name="0232a10" id="0232a10"></a>緣為緣。而其〔食〕，依食所活有情之食，唯循環於身體之轉起〔為緣〕。非於結生。</p><a 
name="0232a11" id="0232a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（四）其次，其等眼處等之五，對稱為眼、耳、鼻、舌、身識之第六〔意處〕，<a 
name="0232a12" id="0232a12"></a>於轉起以依止、前生、根、不相應、有、不離去〔緣〕之六種緣為緣。非於結生。<a 
name="0232a13" id="0232a13"></a>（五）其次，基色除去五識對其餘之意處，於轉起以依止、前生、不相應、有、不<a 
name="0232a14" id="0232a14"></a>離去〔緣〕之五種緣為緣，非於結生。當知如斯色於結生或轉起，對何何之處為緣<a 
name="0233a01" id="0233a01"></a>耶？於何狀態為緣耶？</p><a 
name="0233a02" id="0233a02"></a><p style="margin-left: 3em"><span class="headname">〔三〕〔名色之緣〕</span></p><a 
name="0233a03" id="0233a03"></a><p><span class="lg">　　　　<span class="linehead" title="PTS.Vism.565"> [P.565] </span>名色之兩者何者對何〔處〕為緣？<a 
name="0233a04" id="0233a04"></a><br>　　　　又何狀態〔為緣〕，辦知者當識知所有之狀態。</span></p><a 
name="0233a05" id="0233a05"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>先謂於五蘊有之結生，三蘊及基色之名色，對第六處以俱生、互相、依止、異<a 
name="0233a06" id="0233a06"></a>熟、相應、不相應、有、不離去緣為緣。此唯此時之〔要〕門。而用既述之方法類<a 
name="0233a07" id="0233a07"></a>推一切而得知故，對此不示詳細。</p><a 
name="0233a08" id="0233a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>此詳論由名色之緣而有六處之句。</p><a 
name="0233a09" id="0233a09"></a><p style="margin-left: 2em"><span class="headname">〔五、由六處之緣而有觸〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>所謂於「由六處之緣而有觸」之句：</p><a 
name="0233a10" id="0233a10"></a><p><span class="lg">　　　　觸略說之即眼觸等之六，<a 
name="0233a11" id="0233a11"></a><br>　　　　詳說之如識之三十二。</span></p><a 
name="0233a12" id="0233a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>即云「由六處之緣而有觸」〔之句中〕，「觸以略說之」，即眼觸、耳觸、鼻觸、舌觸、<a 
name="0233a13" id="0233a13"></a>身觸、意觸，此等為「眼觸等」之六。然，「詳說之」，眼觸等是五之善異熟、五之不<a 
name="0233a14" id="0233a14"></a>善異熟之十，及其餘二十二之世間異熟與相應之二十二。如斯一切由行之緣而說<a 
name="0234a01" id="0234a01"></a>〔生起〕，「如識之三十二」。其次，此三十二種觸之緣在六處中：</p><a 
name="0234a02" id="0234a02"></a><p><span class="lg">　　　　第六〔處〕共內之眼等〔六處〕，又共<a 
name="0234a03" id="0234a03"></a><br>　　　　外之六〔處〕，諸聰慧者主張六處。</span></p><a 
name="0234a04" id="0234a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其中，先論此〔緣起說〕是有執受（身心）之轉起故，唯屬自己相續之緣及緣<a 
name="0234a05" id="0234a05"></a>所起〔以說為緣起支〕所說明之人人，言：「由第六處之緣而有觸。」是從聖典〔說〕<a 
name="0234a06" id="0234a06"></a>故於無色〔有〕之第六處，攝一切故於其他〔之欲色有〕之六處是觸之緣，含括〔第<a 
name="0234a07" id="0234a07"></a>六處〕部分之自性以存〔六處之〕一，主張六處是第六〔處〕共內之眼等。然，其<a 
name="0234a08" id="0234a08"></a>第六處與六處〔總括而〕稱為六處。</p><a 
name="0234a09" id="0234a09"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，說明：「唯緣已起（果）雖屬於〔自己之〕一相續，緣亦有相續之以外者。」<a 
name="0234a10" id="0234a10"></a>之人人，任何處亦為觸之緣，以說明此一切〔為觸之緣〕故，彼第六〔處〕共內〔之<a 
name="0234a11" id="0234a11"></a><span class="linehead" title="PTS.Vism.566"> [P.566] </span>眼處等〕，加上外之色處等而主張〔其等〕為六處。然，此時第六處與〔內〕六處及<a 
name="0234a12" id="0234a12"></a>〔外〕六處，此等中為一而稱六處。</p><a 
name="0234a13" id="0234a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>茲有〔反問者〕，非由一切處發生一觸，又非依一處而一切觸之〔發生〕。然，<a 
name="0234a14" id="0234a14"></a>所謂唯說一：「由六處之緣而觸。」此何故耶？對此如次返答。〔非〕由一切〔之處發<a 
name="0235a01" id="0235a01"></a>生〕一〔觸〕，非由一〔處〕發生一切〔觸〕，此是真實。但由多〔處〕而發生一〔觸〕。<a 
name="0235a02" id="0235a02"></a>譬如眼觸是稱為眼處、色處、眼識之意處，由與其他相應之法而〔發生〕，一切時亦<a 
name="0235a03" id="0235a03"></a>當如斯應用於適宜。故：</p><a 
name="0235a04" id="0235a04"></a><p><span class="lg">　　　　一觸是由多處發生此一語之<a 
name="0235a05" id="0235a05"></a><br>　　　　說示於此依如同者（世尊）所說。</span></p><a 
name="0235a06" id="0235a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「依一語之說示」者，是「由六處之緣而有觸」此一語之說示，由多之處有一觸，「依如<a 
name="0235a07" id="0235a07"></a>同者所說」之義。</p><a 
name="0235a08" id="0235a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，對於「處」：</p><a 
name="0235a09" id="0235a09"></a><p><span class="lg">　　　　五是六種〔之緣〕，其後一是<a 
name="0235a10" id="0235a10"></a><br>　　　　九種〔之緣〕，外之六是<a 
name="0235a11" id="0235a11"></a><br>　　　　隨其發生。對〔斯之意觸〕<a 
name="0235a12" id="0235a12"></a><br>　　　　應辨知緣義。</span></p><a 
name="0235a13" id="0235a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其解明如次。（一）先述眼處等之五，對眼觸等之別有五種之觸以依止、前生、<a 
name="0235a14" id="0235a14"></a>根、不相應、不離去緣之六種緣為緣。（二）其後，一之異熟意處，對多之別有異熟<a 
name="0236a01" id="0236a01"></a>意觸，以俱生、互相、依止、異熟、食、根、相應、有、不離去〔緣〕之九種緣為<a 
name="0236a02" id="0236a02"></a>緣。（三）其次對於外〔處〕，色處是對眼觸，以所緣、前生、有、不離去〔緣〕之<a 
name="0236a03" id="0236a03"></a>四種緣為緣。聲觸等對耳觸等亦同樣。（四）其次，彼等〔色等之五處〕及法處〔所<a 
name="0236a04" id="0236a04"></a>攝之色〕，是對於意觸同樣於〔所緣、前生、有、不離去緣之四種緣為緣〕。（五）又<a 
name="0236a05" id="0236a05"></a>〔非現在色等之五處及法處，對於意處〕唯所緣緣〔為緣〕。如斯隨發生外之六，<a 
name="0236a06" id="0236a06"></a>對〔斯此意觸〕緣之義應辨知。</p><a 
name="0236a07" id="0236a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>此詳論「由六處之緣而有觸」之句。</p><a 
name="0236a08" id="0236a08"></a><p style="margin-left: 2em"><span class="headname">〔六、由觸之緣而有受〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>於「觸之緣而有受」之句：</p><a 
name="0236a09" id="0236a09"></a><p><span class="lg">　　　　受門之故而說眼觸所生等之六<a 
name="0236a10" id="0236a10"></a><br>　　　　其區分成為八十九</span></p><a 
name="0236a11" id="0236a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>曰：於此「受」句之分別，「眼觸所生之受，耳、鼻、舌、身、意觸所生之受」，於<a 
name="0236a12" id="0236a12"></a><span class="linehead" title="PTS.Vism.567"> [P.567] </span>如斯之門故說為六受。其次，若〔言〕區分者，與八十九心相應故為八十九。</p><a 
name="0236a13" id="0236a13"></a><p><span class="lg">　　　　而此等諸受中，說茲異熟〔心〕<a 
name="0236a14" id="0236a14"></a><br>　　　　相應唯三十二受之意義。<a 
name="0237a01" id="0237a01"></a><br>　　　　其中五門〔之觸〕對五〔受〕為八種緣<a 
name="0237a02" id="0237a02"></a><br>　　　　餘之〔意〕觸為一種亦於意門同。</span></p><a 
name="0237a03" id="0237a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其中，（一）眼觸等之觸於五門，以對眼淨等之基（所依）之五受，以俱生、互<a 
name="0237a04" id="0237a04"></a>相、依止、異熟、食、相應、有、不離去〔緣〕之八種緣為緣。（二）其次彼眼觸等<a 
name="0237a05" id="0237a05"></a>之觸於一一門為領受、推度、彼所緣〔作用〕而轉起，對其他諸欲界異熟受，以親<a 
name="0237a06" id="0237a06"></a>依〔緣〕之一種緣為緣。「於其意門亦同」，（三）於意門，稱為彼俱生意觸之觸，對<a 
name="0237a07" id="0237a07"></a>彼所緣而轉起諸欲界異熟受，亦同八種緣為緣。又〔其俱生意觸〕對結生、有分、<a 
name="0237a08" id="0237a08"></a>死而轉起三地之異熟受亦〔同八種緣為緣〕。（四）其次，意門轉〔心〕相應之意<a 
name="0237a09" id="0237a09"></a>觸，對於意門為所依緣而轉起彼欲界諸受，唯以親依〔緣〕之一種緣為緣。</p><a 
name="0237a10" id="0237a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>此詳論「由觸之緣而有受」之句。</p><a 
name="0237a11" id="0237a11"></a><p style="margin-left: 2em"><span class="headname">〔七、由受之緣而有渴愛〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>於「由受之緣而有渴愛」之句：</p><a 
name="0237a12" id="0237a12"></a><p><span class="lg">　　　　茲渴愛由色愛等之別而說六，<a 
name="0237a13" id="0237a13"></a><br>　　　　其一一轉起之行相故而成三種。</span></p><a 
name="0237a14" id="0237a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>即於此句，猶如子由父之名而言長者子、婆羅門子，由所緣之名而分別「渴愛」以說<a 
name="0238a01" id="0238a01"></a>為色愛、聲愛、香愛、味愛、觸愛、法愛。而其等渴愛中一一之渴愛，轉起行相故<a 
name="0238a02" id="0238a02"></a>而為欲愛、有愛、無有愛之三種。即「色愛」，是色所緣現於眼之視野有欲樂味而樂<a 
name="0238a03" id="0238a03"></a><span class="linehead" title="PTS.Vism.568"> [P.568] </span>味轉起時，名為「欲愛」，其〔色〕所緣看為「恒、常」而與常見共轉起時，名為「有愛」<a 
name="0238a04" id="0238a04"></a>——然，與常見俱起之貪言為有愛——，又其〔色〕所緣看為「斷絕、滅亡」而與斷<a 
name="0238a05" id="0238a05"></a>見共轉起時，言為「無有愛」——然，與斷見俱起之貪言為無有愛——。對於「聲愛」<a 
name="0238a06" id="0238a06"></a>等亦然。〔如斯〕此等〔六愛〕而成十八渴愛。〔又〕此於內色等十八，於外〔色等〕<a 
name="0238a07" id="0238a07"></a>十八故成為三十六。如斯過去有三十六、未來三十六、現在三十六故為一百零八之<a 
name="0238a08" id="0238a08"></a>渴愛。其等更簡略而依色等之所緣而為六，當知由欲愛等而成三渴愛。而此等有情，<a 
name="0238a09" id="0238a09"></a>如對〔自己之〕子之愛著，由我執而〔尊敬子之〕諸保姆，依色等之所緣而進行生<a 
name="0238a10" id="0238a10"></a>起愛著受，由對受之我執，給與色等之所緣，如供給畫家、音樂家、香料店、調理<a 
name="0238a11" id="0238a11"></a>師、織師不老不死藥，而大尊敬醫師等故，當知此等由一切受之緣而有渴愛。</p><a 
name="0238a12" id="0238a12"></a><p><span class="lg">　　　　而此唯一異熟之樂受的意義<a 
name="0238a13" id="0238a13"></a><br>　　　　故此〔樂受〕對於渴愛是一種緣</span></p><a 
name="0238a14" id="0238a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>一種是以親依緣為緣。或者</p><a 
name="0239a01" id="0239a01"></a><p><span class="lg">　　　　苦者冀求樂，樂者越欲求〔樂〕，<a 
name="0239a02" id="0239a02"></a><br>　　　　又捨是寂靜故說為樂。<a 
name="0239a03" id="0239a03"></a><br>　　　　因此三受亦為渴愛之緣<a 
name="0239a04" id="0239a04"></a><br>　　　　由受而有渴愛是大仙之所說。<a 
name="0239a05" id="0239a05"></a><br>　　　　又〔渴愛〕雖以受為緣但無睡眠即無，<a 
name="0239a06" id="0239a06"></a><br>　　　　因此〔梵行〕已住之婆羅門無此〔渴愛〕。</span></p><a 
name="0239a07" id="0239a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>此詳論「由受之緣而有渴愛」之句</p><a 
