name="0155a03" id="0155a03"></a><p style="margin-left: 4em"><span class="headname">三、〔各緣起支之解釋〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，世尊說示緣起，依「無明之緣而有行」等之方<a 
name="0155a04" id="0155a04"></a>法，〔作〕經典義之解釋者，分別論者入於會眾，無誹謗諸阿闍梨，不逸脫自宗義，<a 
name="0155a05" id="0155a05"></a>不至於他宗之義，不拒經於外，隨順於律，眺望大法經，令把握說法之義，再返<a 
name="0155a06" id="0155a06"></a>復依其他諸異門而解釋義，應進行義之解釋。而緣起義之解釋，如諸古人言：</p><a 
name="0155a07" id="0155a07"></a><p><span class="lg">　　　　諦與有情、結生、緣相之四法<a 
name="0155a08" id="0155a08"></a><br>　　　　是極難見又難說。</span></p><a 
name="0155a09" id="0155a09"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>本來困難，故除去證得聖典〔之義〕的人人，緣起之義不容易解說，須置於念頭，</p><a 
name="0155a10" id="0155a10"></a><p><span class="lg">　　　　我今欲作緣相之解釋<a 
name="0155a11" id="0155a11"></a><br>　　　　<span class="linehead" title="PTS.Vism.523"> [P.523] </span>如跳入海洋無住立。<a 
name="0155a12" id="0155a12"></a><br>　　　　然飾種種說示法之此教法，<a 
name="0155a13" id="0155a13"></a><br>　　　　未曾斷絕往昔阿闍梨之道。<a 
name="0155a14" id="0155a14"></a><br>　　　　是故依止此兩者，我始解說<a 
name="0156a01" id="0156a01"></a><br>　　　　此〔緣起之〕義諦聽等持。</span></p><a 
name="0156a02" id="0156a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>然，依往昔之諸阿闍梨如次說，</p><a 
name="0156a03" id="0156a03"></a><p><span class="lg">　　　　希求依我聽者誰，<a 
name="0156a04" id="0156a04"></a><br>　　　　前後（永久）得勝〔法〕。<a 
name="0156a05" id="0156a05"></a><br>　　　　若前後得勝〔法〕者<a 
name="0156a06" id="0156a06"></a><br>　　　　彼可至不老與死</span></p><a 
name="0156a07" id="0156a07"></a><p style="margin-left: 2em"><span class="headname">〔一、無明之緣而有行〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>先對於「無明之緣而有行」等〔之句〕，</p><a 
name="0156a08" id="0156a08"></a><p><span class="lg">　　　　〔一〕說示之別、〔二〕義、〔三〕相、〔四〕依一種等、<a 
name="0156a09" id="0156a09"></a><br>　　　　〔五〕依〔緣起〕支之差別，當識知決定說。</span></p><a 
name="0156a10" id="0156a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其中，〔一〕「由說示之別」，四人搬運蔓草者，以取蔓草，最初又由中央始而最<a 
name="0156a11" id="0156a11"></a>後，又最後又由中央始而至最初，依世尊說示四種之緣起。即（一）譬搬運蔓草之<a 
name="0156a12" id="0156a12"></a>四人中，於最初見蔓草之根，彼切斷其根，以〔蔓草之〕一切拉近身邊，取而使用，<a 
name="0156a13" id="0156a13"></a>如斯世尊：「諸比丘！斯無明之緣而有行……乃至……生之緣而有老死。」由最初<a 
name="0156a14" id="0156a14"></a>始至最後說示緣起。（二）又譬彼等〔四〕人中，最初見蔓草之中央，彼切斷中央，<a 
name="0157a01" id="0157a01"></a>以上方部分拉近身邊，取而使用，如斯世尊：「歡喜彼受者、觀迎者、在耽著者而<a 
name="0157a02" id="0157a02"></a>生起歡喜。對受觀喜即是取。其取之緣而有有。有之緣而有生。」此由中央始至最後<a 
name="0157a03" id="0157a03"></a>而說示。（三）又譬彼等〔四〕人之中，一人見最初蔓草之端，彼捕其端，由端至根<a 
name="0157a04" id="0157a04"></a>取一切而使用，世尊如斯說：「生之緣而有老死。」「諸比丘！依生之緣而有老死<a 
name="0157a05" id="0157a05"></a>否？又〔汝等〕對此如何思惟耶」？「尊師！生之緣而有老死，我等對此『生之緣而<a 
name="0157a06" id="0157a06"></a><span class="linehead" title="PTS.Vism.524"> [P.524] </span>有老死』如斯思惟」。「有之緣而有生……乃至……又無明之緣而有行，乃斯說也。<a 
name="0157a07" id="0157a07"></a>諸比丘！無明之緣而有行否？又〔汝等〕對此如何思惟耶」？是由最後始至最初說示<a 
name="0157a08" id="0157a08"></a>緣起。（四）又譬此等〔四〕人之中，一人最初見蔓草之中央，彼切斷其中央而取下<a 
name="0157a09" id="0157a09"></a>部分，下取至根止而使用，如斯世尊說：「諸比丘！此等之四食，是以何為因緣，<a 
name="0157a10" id="0157a10"></a>以何為集，以何為生因，以何為發生因耶？此等四食是渴愛為因緣，渴愛為集，渴<a 
name="0157a11" id="0157a11"></a>愛為生因，渴愛為發生因。渴愛以何為因緣……受……觸……六處……名色……識<a 
name="0157a12" id="0157a12"></a>……行以何為因緣……〔以何為發生因耶〕？行是以無明為因緣……乃至……以無明<a 
name="0157a13" id="0157a13"></a>為發生因。」此是由中央始至最初之說示。</p><a 
name="0157a14" id="0157a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>然，何故如斯說示耶？緣起為&#x5FA7;賢善故，又〔世尊〕自說示到達極妙故。即緣<a 
name="0158a01" id="0158a01"></a>起是&#x5FA7;賢善也。〔四種之說示〕依何者亦必至通達真理。又世尊與四無畏、〔四〕無<a 
name="0158a02" id="0158a02"></a>礙解相應，達種種甚深之狀態故，說示到達之極妙。彼說亦到達之極妙故，依種種<a 
name="0158a03" id="0158a03"></a>方法而說法。而（一）特別由最初始彼〔世尊〕之順說，〔世尊〕觀察所化之人人（諸<a 
name="0158a04" id="0158a04"></a>弟子），暗於轉起（諸法之生成）原因之分別，為示由各自原因之轉起，又為示生起<a 
name="0158a05" id="0158a05"></a>之次第而說者應當知。（三）由最初始之逆說，〔世尊〕眺望此「世人實陷於苦難，<a 
name="0158a06" id="0158a06"></a>依生、老、死、死滅、再生」等方法而陷於苦難之世人，於〔成道之〕前分通達〔緣<a 
name="0158a07" id="0158a07"></a>起〕，其各各老死之苦，〔如〕自己所證得，為示有原因〔而說示〕。（四）由中央始<a 
name="0158a08" id="0158a08"></a>至最初〔說示緣起〕，此食之因緣差別〔教說〕，為示說而&#x9061;至過去世，更又過去世<a 
name="0158a09" id="0158a09"></a>以來之因果次第。（二）次由中央始至最後〔說示緣起〕，為示〔說〕此於現在世，<a 
name="0158a10" id="0158a10"></a>由未來世之因等起以來之未來世〔狀態〕。</p><a 
name="0158a11" id="0158a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.525"> [P.525] </span>其等中，所化之人人暗於轉起之原因，為示由各自之原因之轉起，由最初始說<a 
name="0158a12" id="0158a12"></a>順說，當知於此提示〔順緣起〕。然，何故此〔順緣起〕中，無明為最初說耶？如自<a 
name="0158a13" id="0158a13"></a>性論者（數論派）之自性，無明亦〔自〕無原因而〔生〕，為世間之根本原因耶？不<a 
name="0158a14" id="0158a14"></a>然，非無原因。然，說：「漏之集故有無明之集。」為無明之原因。</p><a 
name="0159a01" id="0159a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>然，有教說言〔無明〕為根本原因，此云何？於輪轉說〔無明〕為初始。即世<a 
name="0159a02" id="0159a02"></a>尊說輪轉說以〔無明及有愛〕二法為初始而說。（一）〔即〕時而以無明〔為初始〕。<a 
name="0159a03" id="0159a03"></a>所謂：「諸比丘！『此以前無有無明、於〔此〕以後而發生』，不承認無明之前際。<a 
name="0159a04" id="0159a04"></a>諸比丘！雖如上所言，而『依此緣而有無明』而承認此。」（二）時而有愛〔為初始〕。<a 
name="0159a05" id="0159a05"></a>所謂：「諸比丘！『此以前無有有愛，於〔此〕以後而發生』，不承認有愛之前際。<a 
name="0159a06" id="0159a06"></a>諸比丘！雖如上所言，而承認謂：『依此緣而有有愛。』」</p><a 
name="0159a07" id="0159a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>然，世尊說輪轉說，何故此等二法為初始而說耶？〔此等二法〕至善趣、惡趣<a 
name="0159a08" id="0159a08"></a>業之特別因故。（一）即無明是至惡趣業之特別因。何故耶？恰如受火所熱，棍棒所<a 
name="0159a09" id="0159a09"></a>打擲，於疲勞所打敗之屠牛，其疲勞激痛故而無樂味，但自己持不利，如飲熱湯，<a 
name="0159a10" id="0159a10"></a>被無明所打敗之凡夫，雖煩惱之熱苦故無樂味，又令隨惡趣故自己持不利，行殺生<a 
name="0159a11" id="0159a11"></a>等至惡趣種種之業。（二）其次，有愛是至善趣業之特別因。何故耶？譬前述之牛渴<a 
name="0159a12" id="0159a12"></a>飲冷水，吞飲冷水有樂味且除去自己之疲勞，打敗有愛之凡夫，無煩惱之熱苦故有<a 
name="0159a13" id="0159a13"></a>樂味，且令至善趣故，除去自己惡趣之勞苦，行離殺生等至善趣之種種業。</p><a 
name="0159a14" id="0159a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.526"> [P.526] </span>其次，為輪轉說之初始之此等〔無明、有愛二〕法中，在某情形世尊說示一法<a 
name="0160a01" id="0160a01"></a>為根本。所謂：「諸比丘！以上，無明為親因而有行，行為親因而有識。」等。又說：<a 
name="0160a02" id="0160a02"></a>「諸比丘！取於諸法見樂味而住者，即渴愛增大。渴愛之緣而有取。」等。在某情形<a 
name="0160a03" id="0160a03"></a>〔說示〕兩者為根本。所謂：「諸比丘！有無明蓋，且與渴愛相應之愚者得成此身。<a 
name="0160a04" id="0160a04"></a>此身與外之名色為二，緣於二而有觸、六處。觸其等〔觸與六處〕之愚者覺受苦樂。」<a 
name="0160a05" id="0160a05"></a>等。</p><a 
name="0160a06" id="0160a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>此等說示之中，言：「無明之緣而有行。」當知今唯說示無明之一法為根本。</p><a 
name="0160a07" id="0160a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>當知如斯先說示此〔緣起差〕別之決定說。</p><a 
name="0160a08" id="0160a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔二〕「由義」，依無明等句之義。謂：</p><a 
name="0160a09" id="0160a09"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔無明〕（一）身惡行等遂行〔此〕為不當故言不可有。為不可得之義。其<a 
name="0160a10" id="0160a10"></a>不可有而有故為「無明。」（二）身善行等其相反故言為可有。其可有而不有故為「無<a 
name="0160a11" id="0160a11"></a>明」。（三）不知蘊聚之義、處努力之義、界之空義、根之增上義、諦之如實義故亦<a 
name="0160a12" id="0160a12"></a>是「無明」。（四）不知由逼惱等所說苦等四種之義故亦是「無明」。（五）於無終邊輪<a 
name="0160a13" id="0160a13"></a>迴，有情走向一切胎、趣、有、識住、有情居中故亦是「無明」。（六）走向於不存在<a 
name="0160a14" id="0160a14"></a>第一義之女、男等中，不走於存在〔第一義〕之蘊等中故亦是「無明」。（七）又隱蔽<a 
name="0161a01" id="0161a01"></a>眼識等之基（所依）、所緣及緣起、緣已生法故亦是「無明」。</p><a 
name="0161a02" id="0161a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>果由彼緣而來故為「緣」。「緣」者非無，謂不排拒之義。「來」者是生起且轉起之<a 
name="0161a03" id="0161a03"></a>義。又緣之義是資助之義。其無明且為緣者，即是「無明之緣」。「依其無明之緣」〔而<a 
name="0161a04" id="0161a04"></a>有行〕。</p><a 
name="0161a05" id="0161a05"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔行〕　行作有為故是「行」。又（一）依無明之緣的行，（二）以所述行之語行<a 
name="0161a06" id="0161a06"></a>而為二種行。其中（一）依無明之緣而有行，是福、非福、不動行之三與身、語、<a 
name="0161a07" id="0161a07"></a>心行之三，此等六皆唯是世間之善、不善之思。其次，（二）以所述行之語行，<a 
name="0161a08" id="0161a08"></a><span class="linehead" title="PTS.Vism.527"> [P.527] </span>〔一〕有為行、〔二〕所行作之行、〔三〕行作之行、〔四〕加行之行作，謂此等四種。<a 
name="0161a09" id="0161a09"></a>其中：</p><a 
name="0161a10" id="0161a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔一〕於「諸行無常」等〔句〕所說，一切有緣之法是「有為行」。〔二〕於諸義<a 
name="0161a11" id="0161a11"></a>疏所說業所生之〔欲、色、無色〕三地之色、非色法是「所行作之行」。彼等亦謂包<a 
name="0161a12" id="0161a12"></a>攝於「諸行無常」〔句〕中。然，其等於各別之敘述時即不承認。〔三〕其次三地之善、<a 
name="0161a13" id="0161a13"></a>不善之思言為「行作之行」。其所述時，於「諸比丘！至此無明者是行作福善之行」<a 
name="0161a14" id="0161a14"></a>等中可以認識。〔四〕其次身心之精進言為「加行之行作」。此於「車輪只要續〔迴<a 
name="0162a01" id="0162a01"></a>轉〕之行作即〔迴轉〕行去，如固定於車軸而停止」等〔之文〕所說。</p><a 
name="0162a02" id="0162a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>非唯此等〔四者〕，更又其等方法而說：「友！毘舍佉！入定於想受滅之比丘，<a 
name="0162a03" id="0162a03"></a>語行於最初滅，而身行、心行〔滅〕。」依行之語而述多行。其等中之所有行無不包攝<a 
name="0162a04" id="0162a04"></a>於有為行。</p><a 
name="0162a05" id="0162a05"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>由此更於「行之緣而有識」等〔文句〕所說〔語句之義〕，亦當知由上述之方法。<a 
name="0162a06" id="0162a06"></a>唯〔在上之說明〕，所不說者於〔次說〕。識知故為「識」。趣向故為「名」。惱壞故為<a 
name="0162a07" id="0162a07"></a>「色」。伸展來者及引導擴大者為「處」。接觸故為「觸」，覺受故為「受」。渴故為「渴愛」。<a 
name="0162a08" id="0162a08"></a>取故為「取」。有故為「有」。生故為「生」。老故為「老」。由此〔人人〕死故為「死」。<a 
name="0162a09" id="0162a09"></a>憂愁為「愁」。非泣為「悲」。苦故為「苦」。又由生、住二種之破壞故為「苦」。不快之<a 
name="0162a10" id="0162a10"></a>狀態為「憂」。激甚之憂惱為「惱」。「發生」者是生起。「發生」之語非單與愁等〔之<a 
name="0162a11" id="0162a11"></a><span class="linehead" title="PTS.Vism.528"> [P.528] </span>句結合〕，當知與一切句之結合。若不然，言「無明之緣而有行」時，即不知〔無明之<a 
name="0162a12" id="0162a12"></a>緣而有行〕是為何耶？然，有與「發生」〔句之〕結合時，其無明而且為緣，彼無明<a 
name="0162a13" id="0162a13"></a>之緣而有行發生，即示為緣與緣已生之差別，〔其他〕一切情形亦同此。「如斯」是示<a 
name="0162a14" id="0162a14"></a>已所說示之方法之〔語〕。此不依自在天之化作等，示由無明之原因。「此」如上所述<a 
name="0163a01" id="0163a01"></a>者。「一切之」是不雜，又全體也。「苦蘊之」是苦之聚合，不是有情，不是樂淨等。<a 
name="0163a02" id="0163a02"></a>「集」是生。「有」是存在。</p><a 
name="0163a03" id="0163a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>當知如斯此〔緣起經〕義之決定說。</p><a 
name="0163a04" id="0163a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔三〕「由相等」，是依無明等之相等。所謂「無明」是無智為〔特〕相，癡蒙為<a 
name="0163a05" id="0163a05"></a>味（作用），〔所緣自性之〕隱蔽為現起（現狀），漏為足處（直接因）。「行」是行作<a 
name="0163a06" id="0163a06"></a>為相，營務為味，思為現起，無明為足處。「識」是識知為相，〔名色之〕前行為味，<a 
name="0163a07" id="0163a07"></a>結生為現起，行為足處，又基（所依）、所緣為足處。「名」是趣向為相，與〔識〕相<a 
name="0163a08" id="0163a08"></a>應為味，不簡別為現起，識為足處。「色」是惱壞為相，散在為味，無記為現起，識<a 
name="0163a09" id="0163a09"></a>為足處。「六處」是努力為相，見等為味，基（識之所依）、（認識之門）為現起，名<a 
name="0163a10" id="0163a10"></a>色為足處。「觸」是接觸為相，觸擊為味，合集為現起，六處為足處。「受」是領納為<a 
name="0163a11" id="0163a11"></a>相，境味之受用為味，苦樂為現起，觸為足處。「渴愛」是因為相，歡喜為味，無飽<a 
name="0163a12" id="0163a12"></a>足為現起，受為足處。「取」是把取為相，不放為味，依強烈渴愛之〔惡〕見為現起，<a 
name="0163a13" id="0163a13"></a>渴愛為足處。「有」是業、業果為相，〔他〕令存在〔自〕存在為味，善、不善、無記<a 
name="0163a14" id="0163a14"></a>為現起，取為足處。「生」等之相等，當知由諦之解釋而所說者。</p><a 
name="0164a01" id="0164a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>當知如斯依此〔緣起支〕相等之決定說。</p><a 
name="0164a02" id="0164a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔四〕「由一種等」，此〔緣起支〕中，「無明」是無智、無見、癡等性故為一種。<a 
name="0164a03" id="0164a03"></a>不行道、邪行道故為二種。又有行、無行故為〔二〕種。三受相應故為三種。四<a 
name="0164a04" id="0164a04"></a>諦之不通達故為四種。隱蔽五趣之過患故為五種。又〔轉起六〕門、〔六〕所緣故為<a 
name="0164a05" id="0164a05"></a><span class="linehead" title="PTS.Vism.529"> [P.529] </span>〔六種〕。當知對〔其他〕一切非色〔支亦同此〕六種。</p><a 
name="0164a06" id="0164a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「行」是有漏持法等異熟法故為一種。善、不善故為二種。又小、大、劣、中、<a 
name="0164a07" id="0164a07"></a>邪〔定〕、正定故亦為〔二種〕。〔三〕福行等之性故為三種。轉起〔卵生、胎生、濕<a 
name="0164a08" id="0164a08"></a>生、化生之〕四胎故為四種。至五趣故為五種。</p><a 
name="0164a09" id="0164a09"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「識」是世間（有漏）異熟等之性故為一種。有因、無因之故為二種。三有之<a 
name="0164a10" id="0164a10"></a>所攝，三受相應，無因、二因、三因之故為三種。由〔四〕胎、〔五〕趣為四種、五<a 
name="0164a11" id="0164a11"></a>種。</p><a 
name="0164a12" id="0164a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「名色」是識之依止故，業之緣故為一種。有所緣、無所緣之故為二種。過去等<a 
name="0164a13" id="0164a13"></a>之故為三種。由〔四〕胎、〔五〕趣故為四種、五種。</p><a 
name="0164a14" id="0164a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「六處」是〔心、心所〕發生合流之處故為一種。種淨、識之故為二種。境之<a 
name="0165a01" id="0165a01"></a>到達、不到達、非二之故為三種。〔四〕胎、〔五〕趣之所攝之故為四種、五種。<a 
name="0165a02" id="0165a02"></a>依此方法當知「觸」等之一種等狀態。</p><a 
name="0165a03" id="0165a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>當知如斯由此〔緣起支〕一種等之決定說。</p><a 
name="0165a04" id="0165a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔五〕「由〔緣起〕支差別」，此〔緣起支之〕中，愁等為示說有之輪不斷。然，<a 
name="0165a05" id="0165a05"></a>老死所襲之愚人發生其〔愁〕等。所謂：「諸比丘！無聞之凡夫觸身苦受而愁、疲、<a 
name="0165a06" id="0165a06"></a>悲、搥腦哭泣、陷於蒙昧。」只要其〔愁〕之轉起，即有無明之〔轉起〕。更又無明<a 
name="0165a07" id="0165a07"></a>之緣而有行。如斯而連續有之輪。故當知其〔愁〕等與老死一起，而緣起支唯十二。<a 
name="0165a08" id="0165a08"></a>如斯識知由〔緣起〕支差別之決定說。以上先略說〔緣起支之差別〕。</p><a 
name="0165a09" id="0165a09"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.530"> [P.530] </span>其次之詳說如下，「無明」，若依經典所說是對苦等四〔諦〕處之無智。若依阿<a 
name="0165a10" id="0165a10"></a>毘達磨說，對前際等八處之〔無智〕。即如次說：「其中，云何是無明？對於苦之<a 
name="0165a11" id="0165a11"></a>無智……乃至……對至苦滅之道無智，對前際無智，對後際無智，對前後際無智，<a 
name="0165a12" id="0165a12"></a>對此緣性、緣已生法之無智。」其中，假令除去出世間〔滅道〕二諦，於餘之處，雖<a 
name="0165a13" id="0165a13"></a>由所緣生起無明，於此依隱蔽唯〔無明〕之義。然，其〔無智〕生起而隱蔽苦諦，<a 
name="0165a14" id="0165a14"></a>不得通達〔苦諦〕如真、自然之相。於同樣稱集、滅、道、前際之過去五蘊，稱後<a 
name="0166a01" id="0166a01"></a>際之未來五蘊，稱前後際其兩者，稱此緣性、緣已生法之此緣性（緣起）及隱蔽緣<a 
name="0166a02" id="0166a02"></a>已生法，此是無明、此是行，如斯不得通達此〔緣起及緣已生法之〕如真、自然之<a 
name="0166a03" id="0166a03"></a>相。故言：「對苦之無智……乃至……對此緣性、緣已生法之無智。」</p><a 
name="0166a04" id="0166a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「行」是福等之三及身行等之三，此於前略說之六。而詳說之，（一）「福行」是<a 
name="0166a05" id="0166a05"></a>依施戒等而轉起八欲界善思，依修習而轉起五色界善思等之十三思。（二）「非福<a 
name="0166a06" id="0166a06"></a>行」是依殺生等而轉起之十二不善思。（三）「不動行」，依修習而轉起四無色界善思。<a 
name="0166a07" id="0166a07"></a>斯〔福行等之〕三行是二十九思。其次，於他之三〔行〕。（四）「身行」是身思。（五）<a 
name="0166a08" id="0166a08"></a>「語行」是語思，（六）「心行」是意思。此三法於剎那營作業，為示說依福行等門而轉<a 
name="0166a09" id="0166a09"></a>起。即八欲界善思與十二不善思等二十思，令等起身表，依身門而轉起者，〔此〕名<a 
name="0166a10" id="0166a10"></a><span class="linehead" title="PTS.Vism.531"> [P.531] </span>為身行。彼等〔二十思〕令等起語表，依語門而轉起者，〔此〕名為語行。然，此〔二<a 
name="0166a11" id="0166a11"></a>十思〕中，神通之思為別事，〔謂行之緣而有識時之結生〕，不為識之緣故不含攝<a 
name="0166a12" id="0166a12"></a>〔於此〕。如神通之思〔於此亦含括〕掉舉思。故此〔掉舉思〕亦由識之緣所除去。<a 
name="0166a13" id="0166a13"></a>然，〔加神通思、掉舉思〕之此等一切是依無明之緣〔而生行〕也。其次，一切之二<a 
name="0166a14" id="0166a14"></a>十九思，非令等起〔身語之〕兩表，而於意門生起者，〔此〕為心行。此〔身行等之〕<a 
name="0167a01" id="0167a01"></a>三法是入於〔福行等之〕三法故，當知依義者唯由福行等而〔說明〕無明之緣性。</p><a 
name="0167a02" id="0167a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>茲有〔問者〕然，依無明之緣，如何而知此等之行耶？〔答曰：〕無明有時，<a 
name="0167a03" id="0167a03"></a>即〔行〕有故。即不捨斷對苦等稱無明之無智者，由對苦乃至前際等之無智，樂想<a 
name="0167a04" id="0167a04"></a>輪迴之苦，以勵進其為〔苦〕因〔福行等之〕三種行。又對集依無智，行伴苦因之<a 
name="0167a05" id="0167a05"></a>渴愛，思惟為樂行，策勵〔其行〕。又對滅、道依無智而非苦滅，想苦之滅是趣〔梵<a 
name="0167a06" id="0167a06"></a>天界等之〕殊勝，又非〔苦〕滅之道，想為滅道是祭祀、苦行等，而希求苦之滅，<a 
name="0167a07" id="0167a07"></a>依勵行祭祀、苦行等方面之三種行。又彼〔無智者〕是對四諦不捨斷無明故，特別<a 
name="0167a08" id="0167a08"></a>混著生、老、病、死等之甚多過患而稱為福果，不知苦是苦而為獲得其〔福果〕<a 
name="0167a09" id="0167a09"></a>以勵行身、語、心行等之福行。恰如欲〔得〕天女者，〔跳進〕天〔女〕所居之斷崖。<a 
name="0167a10" id="0167a10"></a>其福果雖想為快樂，但不見其中令生大熱惱之壞苦性及不樂味之性者，此依上所<a 
name="0167a11" id="0167a11"></a>述勵行〔祭祀、苦行等之〕福行。恰如癡蛾飛入燈炎，如貪蜜滴者嘗塗蜜之刀口。<a 
name="0167a12" id="0167a12"></a>又不見對受用諸欲受報之過患者，依有樂想而為煩惱所克服，勵行轉起〔身語心之〕<a 
name="0167a13" id="0167a13"></a>三門之非福行。恰如愚人玩糞，如欲死者之服毒。不知覺無色之報〔於諸行〕有行<a 
name="0167a14" id="0167a14"></a>〔苦〕、壞苦者，勵行常等顛倒心行之不動行。恰如迷了方向者，向惡鬼之市鎮行路。<a 
name="0168a01" id="0168a01"></a><span class="linehead" title="PTS.Vism.532"> [P.532] </span>如斯因有無明而有行。無〔無明〕故而無〔行〕，當知此等之行是由無明之緣而生。<a 
name="0168a02" id="0168a02"></a>又如次所說：「諸比丘！至於不知無明者，行作福行、亦行作非福行、行作不動行。<a 
name="0168a03" id="0168a03"></a>諸比丘！斷無明者生起明故，彼離無明及生起明故無行作福行。」</p><a 
name="0168a04" id="0168a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>茲〔問者〕曰：我等先已理解無明是行之緣，對如何之行說有如何之緣耶？〔答<a 
name="0168a05" id="0168a05"></a>曰：〕有關此，依世尊如次說：「（一）因緣。（二）所緣緣。（三）增上緣。（四）<a 
name="0168a06" id="0168a06"></a>無間緣。（五）等無間緣。（六）俱生緣。（七）互相緣。（八）依緣。（九）親依緣。<a 
name="0168a07" id="0168a07"></a>（一&#x3007;）前生緣。（一一）後生緣。（一二）習行緣。（一三）業緣。（一四）異熟緣。<a 
name="0168a08" id="0168a08"></a>（一五）食緣。（一六）根緣。（一七）禪緣。（一八）道緣。（一九）相應緣。（二&#x3007;）<a 
name="0168a09" id="0168a09"></a>不相應緣。（二一）有緣。（二二）非有緣。（二三）離去緣。（二四）不離去緣。」以<a 
name="0168a10" id="0168a10"></a>上說二十四緣。</p><a 
name="0168a11" id="0168a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其中，〔甲、二十四緣之說明〕（一）〔因緣〕　其因而且為其緣故名「緣」。言<a 
name="0168a12" id="0168a12"></a>為因而成為緣，因之狀態為緣，對所緣緣等亦相同此。其〔因緣之〕中，「因」〔一〕<a 
name="0168a13" id="0168a13"></a>是論式之部分、〔二〕原因、〔三〕根之同義語。即〔一〕於世間謂宗、因、〔喻、合、<a 
name="0168a14" id="0168a14"></a>結〕等時之論式部分而言為因。〔二〕其次於〔佛〕教謂：「由因所發生之一切法。」<a 
name="0169a01" id="0169a01"></a>等情形之原因〔言為因〕。〔三〕「謂三是善因，三是不善因」等情形，〔善、不善之〕<a 
name="0169a02" id="0169a02"></a>根言為因。於此是〔根之〕意義。其次，此〔因緣〕中，「緣」者其語義如次，緣此<a 
name="0169a03" id="0169a03"></a>而有行故為緣——不排拒此作用之義——。然，某法（甲）緣他法（乙），不排拒此<a 
name="0169a04" id="0169a04"></a><span class="linehead" title="PTS.Vism.533"> [P.533] </span>而存立、又生起者，言此（乙）是彼（甲）之緣。緣、因、原因、因緣、生成、發<a 
name="0169a05" id="0169a05"></a>生〔因〕等，其文雖異其義為一。以上，由根之義言「因」，由資助之義言為「緣」，<a 
name="0169a06" id="0169a06"></a>略說之，根之意義為資助之法為「因緣」。此對於稻等如稻之種子等，對珠光等如珠<a 
name="0169a07" id="0169a07"></a>之色澤等，對善等令成善等之性。此是〔離婆多等〕諸阿闍梨之意趣。</p><a 
name="0169a08" id="0169a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔主張說以因緣為善等之性因是不相宜〕然，若如斯〔因緣令成善等之性〕，〔一〕<a 
name="0169a09" id="0169a09"></a>〔此〕對與其等起諸色即不成因緣。然，此〔因緣〕不使彼等〔色等〕成善等之性，<a 
name="0169a10" id="0169a10"></a>而〔因緣對於色法亦〕非不為〔因〕緣。然，如斯說：「因是與因相應之諸法及對<a 
name="0169a11" id="0169a11"></a>其等起之諸色而為因緣之緣。」〔二〕又諸無因心不〔只是〕無此〔因〕，且成無記之<a 
name="0169a12" id="0169a12"></a>性。〔三〕又雖有諸有因〔心〕，但關係於如理作意〔而成〕善等之性，此非關於相<a 
name="0169a13" id="0169a13"></a>應之因〔而成〕。〔四〕若相應於因中為自性，而有善等之性者，關於相應〔法〕中<a 
name="0169a14" id="0169a14"></a>之因，應該是無貪之善或為無記〔之一面〕。然，亦有〔善、無記之〕兩者。是故如<a 
name="0170a01" id="0170a01"></a>於相應〔法〕中，於因中亦不以善等之性〔為自性，於如理作意等之關係而〕&#x5FA7;求。</p><a 
name="0170a02" id="0170a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>而成善等性者不解為因之根義，而解為成善住立狀態時，即無任何矛盾。然，<a 
name="0170a03" id="0170a03"></a>得因緣諸法，如生出根之樹木，堅固而善住立，無因〔之諸法〕，如芽生已盡之胡麻<a 
name="0170a04" id="0170a04"></a>植物，不能善住立。斯根是為資助之意義，資助成善住立狀態之法，應知〔此〕為<a 
name="0170a05" id="0170a05"></a>因緣。</p><a 
name="0170a06" id="0170a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（二）〔所緣緣〕　其〔因緣〕以外之〔二十三緣〕中，「所緣緣」是依所緣而資<a 
name="0170a07" id="0170a07"></a>助法。此「色處是對眼識界」之〔文〕始，「緣各各之法（甲）而各各法之心、心<a 
name="0170a08" id="0170a08"></a>所法（乙）生者，其各各之法（甲）對各各之法〔乙〕而為所緣緣之緣」以連結故，<a 
name="0170a09" id="0170a09"></a>任何法無不為〔所緣緣〕。譬喻力弱之人依靠杖或繩而起且站立，此心、心所法是色<a 
name="0170a10" id="0170a10"></a>等之所緣為緣始能生起且住立。故對一切心、心所〔法〕而為所緣之法，當知〔此〕<a 
name="0170a11" id="0170a11"></a>為所緣緣。</p><a 
name="0170a12" id="0170a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.534"> [P.534] </span>（三）〔增上緣〕　「增上緣」為優勢資助法之義。其〔一〕俱生〔增上緣〕、〔二〕<a 
name="0170a13" id="0170a13"></a>所緣〔增上緣〕之二種。其中，〔一〕「欲增上是欲與相應之諸法及對此等起之諸<a 
name="0170a14" id="0170a14"></a>色為增上緣之緣」等之語故，稱為欲、精進、心、觀之四法，當知是「俱生增上緣」。<a 
name="0171a01" id="0171a01"></a>然，〔此四法〕非一起〔為增上緣〕。然，欲之堅固，以欲為主之心轉起時，唯欲為<a 
name="0171a02" id="0171a02"></a>增上〔緣〕，而其他即不然。對其餘〔之精進、心、觀〕亦同樣。〔二〕其次或重法<a 
name="0171a03" id="0171a03"></a>（甲），若非色〔之心心所〕法（乙）轉起者，其（甲）為彼等（乙）之「所緣增上<a 
name="0171a04" id="0171a04"></a>〔緣〕也」。故說：「重各各之法而各各法之心心所法（乙）生起者，各各之法（甲）<a 
name="0171a05" id="0171a05"></a>對於各各之法（乙）為增上緣之緣。」</p><a 
name="0171a06" id="0171a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（四）〔無間緣〕、（五）〔等無間緣〕　「無間緣」是於無間而資助法。「等無間緣」<a 
name="0171a07" id="0171a07"></a>是等無間而資助法。而人對此二緣雖作種種之臆說，但其真義如下，即眼識之無間<a 
name="0171a08" id="0171a08"></a>（之後）而有意界，意界之無間而有意識界等心之決定，不是其他是依此前前之心<a 
name="0171a09" id="0171a09"></a>而成故，各自之無間適應得令生起〔此〕心生起之法為無間緣。故說：「此無間緣<a 
name="0171a10" id="0171a10"></a>是以此眼識界相應之諸〔心所〕法，對意界及相應此之諸〔心所〕法，而〔作用〕<a 
name="0171a11" id="0171a11"></a>為無間緣之緣。」等。無間緣此即不外是等無間緣。即〔皆是生之共義語〕如於積集、<a 
name="0171a12" id="0171a12"></a>相續〔之文字〕，又如增語、詞之二法，此〔無間緣、等無間緣亦〕唯文字之異，<a 
name="0171a13" id="0171a13"></a>但其義為不異。</p><a 
name="0171a14" id="0171a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>又依世之無間而有無間緣，依時之無間而謂有等無間緣，〔有如離婆多等〕諸<a 
name="0172a01" id="0172a01"></a>阿闍梨之所說，但此是與從「滅盡〔定〕出定者之非想非非想處善，是對果定而為<a 
name="0172a02" id="0172a02"></a>等無間緣之緣」等〔文〕有矛盾。又對此，〔異說者〕雖言：「〔非想非非想處善〕非<a 
name="0172a03" id="0172a03"></a>去失令等起諸法之能力，但由修習力所障礙故，諸法才不能無間生起。」此時不外成<a 
name="0172a04" id="0172a04"></a>為時無間之無而已。然，依修習力而此時，無有時無間性，我等即如斯說。而無時<a 
name="0172a05" id="0172a05"></a><span class="linehead" title="PTS.Vism.535"> [P.535] </span>無間性故，即不得有等無間性。然，言：「由時之無間而等無間緣。」是彼等〔異說者〕<a 
name="0172a06" id="0172a06"></a>之主張。故不執著〔異說者之說〕，應了解唯依文字上有此〔無間緣、等無間緣之〕<a 
name="0172a07" id="0172a07"></a>相違，於其義即無〔差別〕。何故耶？曰：於此等謂無間（於中間者）為無間，無形<a 
name="0172a08" id="0172a08"></a>故能為無間故為等無間。</p><a 
name="0172a09" id="0172a09"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（六）〔俱生緣〕　「俱生緣」是〔法〕生起之時，依資助而俱生起之法。如燈對<a 
name="0172a10" id="0172a10"></a>〔其〕光，此非色蘊等有六種。所謂：「〔一〕四非色蘊互相為俱生緣之緣。〔二〕<a 
name="0172a11" id="0172a11"></a>四大種是互相為〔俱生緣之緣〕〔三〕入胎剎那之名、色是互相為〔俱生緣之緣〕。<a 
name="0172a12" id="0172a12"></a>〔四〕心、心所法是心等起之諸色為〔俱生緣之緣〕。〔五〕〔四〕大種對諸所造色為<a 
name="0172a13" id="0172a13"></a>〔俱生緣之緣〕。〔六〕有色之諸法（心基）是對非色之諸〔心、心所〕法，有時是<a 
name="0172a14" id="0172a14"></a>為俱生緣之緣，有時（對無色界之心、心所法等）是非為俱生緣之緣。」此〔有色之<a 
name="0173a01" id="0173a01"></a>諸法〕是言心基之事。</p><a 
name="0173a02" id="0173a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（七）〔互相緣〕　「互相緣」是互相為資助生起、支持之法。互相為支持如三根<a 
name="0173a03" id="0173a03"></a>之棒。此非色蘊等之三種。所謂：「〔一〕四非色蘊〔互相〕為互相緣之緣。〔二〕<a 
name="0173a04" id="0173a04"></a>四大種是〔互相為互相緣之緣〕。〔三〕入胎剎那之名、色是〔互相〕為互相緣之緣。」</p><a 
name="0173a05" id="0173a05"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（八）〔依緣〕　「依緣」是由依處之相及依止之相為資助之法。如土地、布帛對<a 
name="0173a06" id="0173a06"></a>樹木、繪<span class="corr" title='[晝&gt;畫]'>畫</span>等。「〔一〕四非色蘊為互相緣之緣」，當知如是對俱生〔緣〕而說亦<a 
name="0173a07" id="0173a07"></a>同樣。然，於何狀態第六分有〔六〕「眼處是對眼識界〔及各各相應之心所法〕……<a 
name="0173a08" id="0173a08"></a>乃至……耳、鼻、舌、身處是對〔耳、鼻、舌〕、身識界及其相應之諸〔心所〕法為<a 
name="0173a09" id="0173a09"></a>依緣之緣。或依色（心基）而意界與意識界之作用時，其色對於意界、意識界及與<a 
name="0173a10" id="0173a10"></a>其等相應之諸〔心所〕法為依緣之緣」而如是分別。</p><a 
name="0173a11" id="0173a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.536"> [P.536] </span>（九）〔親依緣〕　「親依緣」是先對此有如次之語義。有依屬之性故，依自果所<a 
name="0173a12" id="0173a12"></a>依止而不相拒故言依。譬喻有甚悲痛之激惱，如斯強力之依止為親依，此是強力原<a 
name="0173a13" id="0173a13"></a>因之同義語。當知強力原因為資助之法是親依緣。此有〔一〕所緣親依、〔二〕無間<a 
name="0173a14" id="0173a14"></a>親依、〔三〕自然親依之三種。其中：</p><a 
name="0174a01" id="0174a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔一〕「所緣親依」　「行布施、受持戒、行布薩業、重其〔等〕而觀察，重宿<a 
name="0174a02" id="0174a02"></a>作意而觀察，由禪出定重禪而觀察。諸有學重種姓而觀察，重清白〔心〕而觀<a 
name="0174a03" id="0174a03"></a>察，諸有學由道〔定〕出定，重道而觀察」，依如斯之方法，先不附所緣親依與所緣<a 
name="0174a04" id="0174a04"></a>增上之差異而分別。其中，或重所緣而心、心所生起，其〔所緣〕決定諸所緣中強<a 
name="0174a05" id="0174a05"></a>力之所緣。如斯於唯應所重之義〔言為〕所緣增上。依強力原因之義而〔言為〕<a 
name="0174a06" id="0174a06"></a>所緣親依，斯知此等之差異。</p><a 
name="0174a07" id="0174a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔二〕「無間親依」　亦「前前之諸善蘊是對後後之諸善蘊為親依緣之緣」等方<a 
name="0174a08" id="0174a08"></a>法，不附加無間緣之差異而分別。然，於〔二十四緣〕論母之概說，彼等之中，對<a 
name="0174a09" id="0174a09"></a>於無間〔緣〕，依「眼識界及其相應之諸〔心所〕法是對意界及其相應之諸〔心所〕<a 
name="0174a10" id="0174a10"></a>法為無間緣之緣」等之方法而〔述〕。對於親依〔緣〕，依「前前之諸善法是對後後<a 
name="0174a11" id="0174a11"></a>之諸善法為親依緣之緣」等之方法而述故，於概說有差異。〔然〕，此若依義亦為同<a 
name="0174a12" id="0174a12"></a>一。雖如是，於各自之無間（之後）〔其〕適應之心生起有令轉起之能力故，〔此名<a 
name="0174a13" id="0174a13"></a>為〕無間緣，前心對後心之生起而有強力故，當知〔此名為〕無間親依。猶如於因<a 
name="0174a14" id="0174a14"></a>緣等中雖無何等法而生起心，但無有無間心而心即不得生起。故〔無間心〕為強力<a 
name="0175a01" id="0175a01"></a><span class="linehead" title="PTS.Vism.537"> [P.537] </span>緣。斯各自之無間依令生起適應〔此〕心而言為無間緣，依強力之原因謂無間親依，<a 
name="0175a02" id="0175a02"></a>當知如斯此等之差違。</p><a 
name="0175a03" id="0175a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔三〕其次，「自然親依」是自然之親依者為自然親依。自然是於自己之相續中<a 
name="0175a04" id="0175a04"></a>〔自然〕完成故，又受習信、戒等，又〔寒暑等之〕時節、食物等之自然物故，能<a 
name="0175a05" id="0175a05"></a>為親依者是為自然親依。與所緣〔親依〕、無間〔親依〕之義不混雜。對此「自然<a 
name="0175a06" id="0175a06"></a>親依是親依於信而行布施、受持戒、行布薩業、令生起禪定、生起觀、生起道、生<a 
name="0175a07" id="0175a07"></a>起神通、生起定。親依於戒、聞、捨、慧……乃至……令生起定。信、戒、聞、捨、<a 
name="0175a08" id="0175a08"></a>慧是對於信、戒、聞、捨、慧而為親依緣之緣」等方法，而知為多種類之區別。斯此<a 
name="0175a09" id="0175a09"></a>等之信等於自然〔令生善法〕故，且由強力原因之義，而為親依故名為親依緣。</p><a 
name="0175a10" id="0175a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（一&#x3007;）〔前生緣〕　「前生緣」是初生起作用而為資助之法。其〔眼等之〕五門<a 
name="0175a11" id="0175a11"></a>有基（所依）、所緣、心基之十一種。所謂：「眼處是對眼識及其相應之諸〔心所〕<a 
name="0175a12" id="0175a12"></a>法為前生緣之緣。耳、鼻、舌、身處、色、聲、香、味、觸處是對〔耳、鼻、舌、<a 
name="0175a13" id="0175a13"></a>身識界、眼、耳、鼻、舌、〕身識界及其相應之諸〔心所〕法為前生緣之緣。色、<a 
name="0175a14" id="0175a14"></a>聲、香、味、觸處是對意界〔及其相應之諸心所法〕為〔前生緣之緣〕。意界、意識<a 
name="0176a01" id="0176a01"></a>界為依止而轉起之彼色（心基），是對意界及其相應之諸〔心所〕法為前生緣之緣。<a 
name="0176a02" id="0176a02"></a>對意識界及其相應之諸〔心所〕法，有時為前生緣之緣，有時不為前生緣之緣。」</p><a 
name="0176a03" id="0176a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（一一）〔後生緣〕　「後生緣」是前生諸色法支持者之意義，為資助而非色法。<a 
name="0176a04" id="0176a04"></a>如鷲兒之身體有意欲食物之思。故言：「後生之諸心、心所法是對前生之此身為<a 
name="0176a05" id="0176a05"></a><span class="linehead" title="PTS.Vism.538"> [P.538] </span>後生緣之緣。」</p><a 
name="0176a06" id="0176a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（一二）〔習行緣〕　「習行緣」是習行之意義，而來於〔自己之〕無間（直後）<a 
name="0176a07" id="0176a07"></a>為令練達、強力資助之法。如〔學〕典籍等前之練習。此由善、不善、唯作之速行<a 
name="0176a08" id="0176a08"></a>有三種。所謂：「前前之諸善法對於後後之諸善法為習行緣之緣。前前之諸不善法<a 
name="0176a09" id="0176a09"></a>……乃至……諸唯作無記法對於後後之諸唯作無記法為習行緣之緣。」</p><a 
name="0176a10" id="0176a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（一三）〔業緣〕　「業緣」稱為心之加行由作業為資助之法，與其多剎那之善、<a 
name="0176a11" id="0176a11"></a>不善之思俱生一切思而為二種。所謂：「善、不善業對於異熟之諸蘊及業作之諸色<a 
name="0176a12" id="0176a12"></a>為業緣之緣。俱生之思對於相應之諸〔心、心所〕法及由此等起之諸色為業緣之緣。」</p><a 
name="0176a13" id="0176a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（一四）〔異熟緣〕　「異熟緣」是依無動寂靜為資助之異熟法。其〔五門轉〕作<a 
name="0176a14" id="0176a14"></a>用之時，對於〔異熟識〕與等起之〔諸色〕，又結生之時，對於業作之諸色，又於一<a 
name="0177a01" id="0177a01"></a>切時，對於〔異熟與〕相應之諸法為緣。所謂：「為異熟無記之一蘊對於〔其他〕<a 
name="0177a02" id="0177a02"></a>三〔異熟無記〕蘊及心等起之諸色而為異熟緣之緣……乃至……結生剎那為異熟無<a 
name="0177a03" id="0177a03"></a>記之一蘊，對於〔其他之〕三蘊及業作之諸色……三蘊對於〔其他〕一蘊……二<a 
name="0177a04" id="0177a04"></a>蘊對於〔其他之〕二蘊及業作之諸色為異熟緣之緣。〔異熟識之〕諸蘊對於〔心〕基<a 
name="0177a05" id="0177a05"></a>為異熟緣之緣。」</p><a 
name="0177a06" id="0177a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（一五）〔食緣〕　「食緣」對於色、非色而支持者為資助之四食。所謂：「段<a 
name="0177a07" id="0177a07"></a>食是對此身為食緣之緣。〔觸、意思、識之〕非色〔三〕食對於相應之諸〔心、心所〕<a 
name="0177a08" id="0177a08"></a>法及其等起之諸法為食緣之緣。」其次於〔發趣論之〕問分亦說：「結生剎那之異<a 
name="0177a09" id="0177a09"></a>熟無記食對其相應之諸蘊及業作之諸色為食緣之緣。」</p><a 
name="0177a10" id="0177a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.539"> [P.539] </span>（一六）〔根緣〕　「根緣」是增上（為主）之意義而為資助者，除女根、男根為<a 
name="0177a11" id="0177a11"></a>二十根。其中眼根等〔之五〕唯對於非色法，餘〔之十五〕對於色、非色而為〔根〕<a 
name="0177a12" id="0177a12"></a>緣。所謂：「眼根對於眼識界〔及其相應之諸心所法〕……乃至……耳、鼻、舌、<a 
name="0177a13" id="0177a13"></a>身根對於〔耳、鼻、舌、〕身識界及其相應之諸法為根緣之緣。色命根對於業作之<a 
name="0177a14" id="0177a14"></a>諸色為根緣之緣。非色之諸根對於相應之諸〔心、心所〕法及其相應之諸法為根緣<a 
name="0178a01" id="0178a01"></a>之緣。」又於問分亦說：「結生剎那之異熟無記根對於相應諸蘊及業作之諸色為根<a 
name="0178a02" id="0178a02"></a>緣之緣。」</p><a 
name="0178a03" id="0178a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（一七）〔禪緣〕　「禪緣」以審慮之意義為資助者，除去二之〔前〕五識中〔存<a 
name="0178a04" id="0178a04"></a>於身識〕樂、苦受之二，分一切之善〔、不善、無記〕等之七禪支。所謂：「諸<a 
name="0178a05" id="0178a05"></a>禪支對於與禪相應之諸〔心、心所〕法及其等起之諸色為禪緣之緣。」又於問分亦說：<a 
name="0178a06" id="0178a06"></a>「結生剎那異熟無記之諸禪支對於相應諸蘊及業作之諸色為禪緣之緣。」</p><a 
name="0178a07" id="0178a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（一八）〔道緣〕　「道緣」由此處彼處導出之意義為資助者，分為善〔、不善、<a 
name="0178a08" id="0178a08"></a>無記〕等之十二道支。所謂：「諸道支對於與道相應之諸〔心、心所〕法及其等<a 
name="0178a09" id="0178a09"></a>起之諸色為道緣之緣。」又於問分亦說：「結生剎那異熟無記之諸道支對於相應諸<a 
name="0178a10" id="0178a10"></a>蘊及業作之諸色為道緣之緣。」而知此等禪〔緣〕與道緣之二，是於無因之二〔之前〕<a 
name="0178a11" id="0178a11"></a>五識心中不可得。</p><a 
name="0178a12" id="0178a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（一九）〔相應緣〕　「相應緣」以基（所依）為一，所緣為一，於同時生同時滅<a 
name="0178a13" id="0178a13"></a>為相應狀態為資助之諸非色法。所謂：「四非色蘊是互相為應緣之緣。」</p><a 
name="0178a14" id="0178a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（二&#x3007;）〔不相應緣〕　「不相應緣」不以一基（所依）等之狀態為資助者，有色<a 
name="0179a01" id="0179a01"></a>之諸法對於非色之諸〔心、心所〕法，又非色〔之諸心、心所法〕對於有色〔之諸<a 
name="0179a02" id="0179a02"></a>法〕為〔緣〕。由〔一〕俱生、〔二〕後生、〔三〕前生為三種。即如次說：「俱生<a 
name="0179a03" id="0179a03"></a><span class="linehead" title="PTS.Vism.540"> [P.540] </span>之諸善蘊對於心等起之諸色而為不相應緣之緣。後生之諸善蘊對於前生之此身為不<a 
name="0179a04" id="0179a04"></a>相應緣之緣。」又就無記句之俱生〔不相應緣〕之分別亦說：「結生剎那異熟無記<a 
name="0179a05" id="0179a05"></a>之諸蘊對於業作之諸色為不相應緣之緣。諸〔非色〕蘊對於基〔色〕、又基（色）<a 
name="0179a06" id="0179a06"></a>對於諸〔非色〕蘊為不相應緣之緣。」其次，前生〔不相應緣〕當知唯眼根等之基。<a 
name="0179a07" id="0179a07"></a>所謂：「前生之眼處對於〔後生之〕眼識〔及其相應之諸心所法〕……乃至……<a 
name="0179a08" id="0179a08"></a>〔前生之〕身處對於〔後生之〕身識〔及其相應之諸心所法〕為不相應緣之緣。〔前<a 
name="0179a09" id="0179a09"></a>生之心〕基對於〔後生〕異熟無記、唯作無記之諸蘊……乃至……〔前生之心〕基<a 
name="0179a10" id="0179a10"></a>對於〔後生〕善之諸蘊……乃至……〔前生之心〕基對於〔後生〕不善之諸蘊為不<a 
name="0179a11" id="0179a11"></a>相應緣之緣。」</p><a 
name="0179a12" id="0179a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（二一）〔有緣〕　「有緣」由有狀態以現在為相，對同樣〔狀態之某〕法為支持<a 
name="0179a13" id="0179a13"></a>者資助之法。其〔有緣〕依非色蘊、大種、名色、心心所、大種、處、基之七種，<a 
name="0179a14" id="0179a14"></a>為論母而概說。所謂：「〔一〕四非色蘊於互相為有緣之緣。〔二〕四大種是於〔互<a 
name="0180a01" id="0180a01"></a>相〕，〔三〕入胎之剎那名色是互相……〔四〕心、心所法對於心等起之諸色……〔五〕<a 
name="0180a02" id="0180a02"></a>諸大種對於諸所造色……〔六〕眼處對於眼識界〔及其相應之諸心所法〕……乃至<a 
name="0180a03" id="0180a03"></a>……身處……乃至……色處……乃至……觸處，對於身識界及其相應之諸〔心所〕<a 
name="0180a04" id="0180a04"></a>等為有緣之緣。色處……乃至……觸處，對於意界及其相之諸〔心所〕法為〔有緣<a 
name="0180a05" id="0180a05"></a>之緣〕。〔七〕意界、意識界所依止而作用之彼色（心基）對於意界、意識界及其相<a 
name="0180a06" id="0180a06"></a>應之諸〔心所〕法為有緣之緣。」又於問分亦概說俱生、前生、後生、食、根〔之五<a 
name="0180a07" id="0180a07"></a>有緣〕，先就於〔一〕俱生，依「一蘊對於〔其他〕三蘊及其等起之諸色而為有緣<a 
name="0180a08" id="0180a08"></a>之緣」等方法進行解釋。〔二〕就於前生對前生之眼等而〔眼識等為有緣，對前生之<a 
name="0180a09" id="0180a09"></a>諸蘊而心基為有緣等之方法〕而進行解釋。〔三〕就於後生前生之此身，對於後生<a 
name="0180a10" id="0180a10"></a><span class="linehead" title="PTS.Vism.541"> [P.541] </span>之心、心所〔法〕為緣而進行解釋。〔四～五〕就於食、根，段食是對於此身為<a 
name="0180a11" id="0180a11"></a>有緣之緣，色命根是對業作之諸色為有緣之緣而如是解釋。</p><a 
name="0180a12" id="0180a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（二二）〔非有緣〕　「非有緣」是於自己無間（直後）與生起某非色諸法轉起之<a 
name="0180a13" id="0180a13"></a>機會而為資助無間（直後）滅之非色法。所謂：「等無間（直後）滅之諸心、心所<a 
name="0180a14" id="0180a14"></a>法對於現在之諸心、心所法為非有緣之緣。」</p><a 
name="0181a01" id="0181a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（二三）〔離去緣〕　「離去緣」由離去為資助故同樣於彼等〔非有緣之法〕。所<a 
name="0181a02" id="0181a02"></a>謂：「等無間（直前）離去之諸心、心所法對於現在之諸心、心所法為離去緣之緣。」</p><a 
name="0181a03" id="0181a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（二四）〔不離去緣〕　「不離去緣」亦不離去之狀態為資助故不外是有緣之法。<a 
name="0181a04" id="0181a04"></a>然，成為善妙所示，或為化導所化（諸弟子）而於〔有緣、非有緣之其他〕說此〔離<a 
name="0181a05" id="0181a05"></a>去緣、不離去緣之〕二法。恰如說無因〔、有因之〕二法，〔更說〕因不相應〔、因<a 
name="0181a06" id="0181a06"></a>相應之〕二法。</p><a 
name="0181a07" id="0181a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔乙、無明與緣行之關係〕　如斯此等二十四緣中，此無明是：</p><a 
name="0181a08" id="0181a08"></a><p><span class="lg">　　　　對福〔行〕為二種緣，<a 
name="0181a09" id="0181a09"></a><br>　　　　又對他之〔非福行〕多種<a 
name="0181a10" id="0181a10"></a><br>　　　　〔為緣〕，對其後〔之不動行〕<a 
name="0181a11" id="0181a11"></a><br>　　　　唯為一種緣。</span></p><a 
name="0181a12" id="0181a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其中，「對於福〔行為〕二種〔緣〕」是所緣緣及親依緣為二種之緣。即（一）其<a 
name="0181a13" id="0181a13"></a>〔無明，善凡夫〕思惟無明是「盡滅〔法〕、衰滅〔法〕」之時，對欲界之諸福行而為<a 
name="0181a14" id="0181a14"></a>所緣緣之緣，以神通（他心智）心知有癡心時，對色界之福行而為〔所緣緣之緣〕。<a 
name="0182a01" id="0182a01"></a>（二）其次，為超越無明而完遂施等欲界之福業事者，〔為超越無明〕令生起色界禪<a 
name="0182a02" id="0182a02"></a>者，於彼等兩者〔無明〕是為親依緣之緣。又癡迷於無明故，冀求欲有、色有之幸<a 
name="0182a03" id="0182a03"></a>福而行彼等〔欲色界之〕福〔行〕者，亦〔無明是為親依緣之緣〕。</p><a 
name="0182a04" id="0182a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>「又對其〔之非福行為〕多種之〔緣〕」。〔無明〕對於非福行為多種之緣。云何？<a 
name="0182a05" id="0182a05"></a>曰：此〔無明〕緣無明而貪等之生起時為所緣緣之〔緣〕，重〔無明〕而欣賞時為所<a 
name="0182a06" id="0182a06"></a>緣增上〔緣〕、所緣親依〔緣之緣〕。癡迷於無明不見〔無明〕之過患，而行殺生者<a 
name="0182a07" id="0182a07"></a>為親依緣〔之緣〕，對〔不善之〕第二速行等為無間〔緣〕、等無間〔緣〕、無間親依<a 
name="0182a08" id="0182a08"></a>〔緣〕、習行〔緣〕、非有〔緣〕、離去緣之〔緣〕，何等行彼不善者為因〔緣〕、俱生<a 
name="0182a09" id="0182a09"></a>〔緣〕、互相〔緣〕、依〔緣〕、相應〔緣〕、有〔緣〕、不離去緣之〔緣〕。為如斯多<a 
name="0182a10" id="0182a10"></a>種之緣。</p><a 
name="0182a11" id="0182a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.542"> [P.542] </span>「對其後〔之不動行〕為一種緣。」〔無明〕對不動行唯為親依緣之一種緣。而其<a 
name="0182a12" id="0182a12"></a>〔無明〕之親依緣之狀態，當知此於福行所述相同。</p><a 
name="0182a13" id="0182a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>於此〔反問〕曰：然者云何？無明之唯一為行之緣耶？或其他亦有〔行之〕<a 
name="0182a14" id="0182a14"></a>緣耶？又云何？此情形若唯一〔無明〕為〔行之緣〕者，即陷於一原因說。又其他<a 
name="0183a01" id="0183a01"></a>若有〔無明之〕緣者，言：「無明之緣而有行。」此一原因之說明是不適當。〔答曰：〕<a 
name="0183a02" id="0183a02"></a>非不適當。何故耶？</p><a 
name="0183a03" id="0183a03"></a><p><span class="lg">　　　　於此由一〔因〕非有一〔果〕<a 
name="0183a04" id="0183a04"></a><br>　　　　非有多〔果〕。又由多〔原因〕<a 
name="0183a05" id="0183a05"></a><br>　　　　非有一果。但〔此〕<a 
name="0183a06" id="0183a06"></a><br>　　　　一因果之說明是有意義。</span></p><a 
name="0183a07" id="0183a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>即由一原因或非有一果、非有多〔果〕。又由多原因非一〔果〕，唯是由多因、唯有<a 
name="0183a08" id="0183a08"></a>多〔果〕。何以故？稱為時節、土地、種子、水等，由多原因而可見稱為色、香、味、<a 
name="0183a09" id="0183a09"></a>等某芽之果生起。又此「無明之緣而有行，由行之緣而有識。」進行一因果之說明，<a 
name="0183a10" id="0183a10"></a>於此有意義有目的。即世尊有時以主要、有以明瞭、有時以不共〔其他〕及說示<a 
name="0183a11" id="0183a11"></a>微妙為適合所化（諸弟子），宣說唯一之因與果。即「由觸之緣而有受」，〔其因果〕<a 
name="0183a12" id="0183a12"></a>為主要故說示唯一之因與果。然，觸是如觸之樣而受確定，故為受主要之因，又受<a 
name="0183a13" id="0183a13"></a>是如受之樣而觸確定，故為觸主要之果。「病是與痰等起」，甚明瞭故說唯一之因。<a 
name="0183a14" id="0183a14"></a>然，此時痰明瞭而業等不〔明瞭〕。「諸比丘！所有不善之諸法皆是此不如理作意<a 
name="0184a01" id="0184a01"></a>為根本〔因〕」，為不共〔他〕故說唯一之因。然，不如理作意唯不善根，非與〔其<a 
name="0184a02" id="0184a02"></a>他善等〕共〔存〕，乃共通於基（所依）、所緣〔其他之善等〕。</p><a 
name="0184a03" id="0184a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>故此行原因之基、所緣、俱生法等雖存在於其他，但又「觀樂味者增大渴愛」，<a 
name="0184a04" id="0184a04"></a>言：「由無明之集而有漏之集。」之語故，〔如〕渴愛等之行因雖存於他，但〔其等中<a 
name="0184a05" id="0184a05"></a>無明之〕行因為主要故，又言：「諸比丘！無知而至於無明者以行作福行。」〔之語<a 
name="0184a06" id="0184a06"></a><span class="linehead" title="PTS.Vism.543"> [P.543] </span>而無明為行之因〕甚明瞭故，且不共〔其他〕故，當知說此無明為行之因。對於一<a 
name="0184a07" id="0184a07"></a>因一果之說明〔目的〕依以上詳釋之語，應知於其〔他之〕一切〔緣起支〕說明一<a 
name="0184a08" id="0184a08"></a>因一果之目的。</p><a 
name="0184a09" id="0184a09"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>於此〔反問〕曰：雖如斯一向持有罪不好果之無明，為妥當於緣福〔行〕、不動<a 
name="0184a10" id="0184a10"></a>行耶？然，尼無婆（苦味樹）之種子不能生甘蔗。〔答曰：〕云何有妥當？即於世間<a 
name="0184a11" id="0184a11"></a>之中：</p><a 
name="0184a12" id="0184a12"></a><p><span class="lg">　　　　有相違與不相違、似同與不同，<a 
name="0184a13" id="0184a13"></a><br>　　　　而諸法之緣成就，然而彼等異熟即不然。</span></p><a 
name="0184a14" id="0184a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>諸法之緣，處所、自性、作用等之相違與不相違於世間而成就。即前心對於後心而<a 
name="0185a01" id="0185a01"></a>〔其〕處所為相違之緣。又以前工巧等之學習對於而後轉起工巧等之行為，〔其處所<a 
name="0185a02" id="0185a02"></a>為相違之緣〕。業對於色是相違之緣。又牛乳等對於酪等〔其自性為相違之緣〕。光<a 
name="0185a03" id="0185a03"></a>明對於眼識〔其〕作用是相違之〔緣〕。又砂糖對酒〔其作用是相違之緣〕。其次眼、<a 
name="0185a04" id="0185a04"></a>色等對於眼識等〔其〕處所為相違之緣。前之速行等對後之速行等〔其〕自性相違，<a 
name="0185a05" id="0185a05"></a>又作用是相違之〔緣〕。如緣之相違不相違之成就，斯〔緣之〕似同與不同亦〔成就〕。<a 
name="0185a06" id="0185a06"></a>即稱時節、食等似同之色為〔似同〕色之緣，又稻之種子等為〔似同之〕稻果之〔緣〕。<a 
name="0185a07" id="0185a07"></a>又色為不同非色之〔緣〕，非色為〔不同〕色之緣。牛毛、赤羊毛、角、酪、胡麻、<a 
name="0185a08" id="0185a08"></a>麥粉等為〔不同〕之吉祥草、香草〔、音〕等之〔緣〕。或對諸法（甲）而相違、<a 
name="0185a09" id="0185a09"></a>不相違、似同、不同，有彼諸緣（乙）時，彼等諸法（甲）非唯其等諸法（乙）之<a 
name="0185a10" id="0185a10"></a>異熟。如斯此無明為異熟，一向持不好之果，為自性雖有罪，但對此等一切之福、<a 
name="0185a11" id="0185a11"></a>〔非福、不動〕行等，於適宜之處所、作用、自性之相違、不相違為緣，又當知似<a 
name="0185a12" id="0185a12"></a>同、不同之緣為緣。</p><a 
name="0185a13" id="0185a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>此〔無明〕緣之狀態。即依「稱為無明是對苦之未捨斷無智者，依無智對於苦乃<a 
name="0185a14" id="0185a14"></a>至前際等，以輪迴之苦為樂想，勵行其〔輪迴之〕因〔福、非福、不動〕之三種行」<a 
name="0186a01" id="0186a01"></a>等方法而說。</p><a 
name="0186a02" id="0186a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>又如次亦有其他之教說：</p><a 
name="0186a03" id="0186a03"></a><p><span class="lg">　　　　對生死輪迴，又對諸行之相，<a 
name="0186a04" id="0186a04"></a><br>　　　　又對諸緣已生法迷妄者<a 
name="0186a05" id="0186a05"></a><br>　　　　<span class="linehead" title="PTS.Vism.544"> [P.544] </span>行作此〔福、非福、不動〕之三種行故<a 
name="0186a06" id="0186a06"></a><br>　　　　無明是彼等〔諸行〕之緣。</span></p><a 
name="0186a07" id="0186a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>然，對此等〔輪迴〕而迷妄者而行此等三種之行耶？〔曰：〕（一）先對於死之<a 
name="0186a08" id="0186a08"></a>迷妄者，所有諸蘊破壞之時，不解為死〔之意義〕，而妄計：「有情之死，有情是轉<a 
name="0186a09" id="0186a09"></a>移往他之身體。」等。（二）對於生之迷妄者，所有諸蘊之現前時為生，不解為生〔之<a 
name="0186a10" id="0186a10"></a>意義〕，而妄計：「有情之生，於有情出現新之身體。」等。（三）迷妄於輪迴，彼：</p><a 
name="0186a11" id="0186a11"></a><p><span class="lg">　　　　無諸蘊及諸界、處之斷絕<a 
name="0186a12" id="0186a12"></a><br>　　　　以連續轉起言為輪迴。</span></p><a 
name="0186a13" id="0186a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>如斯所說明不解為輪迴之意思，妄計：「此有情由此世界至他世界，由他世界來此世<a 
name="0186a14" id="0186a14"></a>界。」等。（四）迷妄於諸行相者，不解為〔色、受等〕諸行之自性相是〔無常、無我<a 
name="0187a01" id="0187a01"></a>等之〕同相，以妄計諸行是我、我所、恒常、樂、淨。（五）迷妄諸緣已生法者，不<a 
name="0187a02" id="0187a02"></a>解為依無明等而行等之轉起，妄計「我是知」或「不知」、「彼（自我）行」或「令行」、<a 
name="0187a03" id="0187a03"></a>「〔自我〕於結生生起」、「微、自在天等依羯邏藍等之狀態形成彼（自我）之身體成<a 
name="0187a04" id="0187a04"></a>具諸根。彼（自我）具諸根而被觸受、覺受、渴愛、取所擊。彼更於他之有而生存」，<a 
name="0187a05" id="0187a05"></a>又「一切有情是運命、偶然之事情而轉變」。彼為盲目於無明而如上之妄計，譬喻盲<a 
name="0187a06" id="0187a06"></a>者行於地上，道或非道、高地或低地、平坦之處、凹凸之處〔不知而〕行，斯福〔行〕<a 
name="0187a07" id="0187a07"></a>或非福〔行〕、不動行亦〔不知而〕行作。故如次說：</p><a 
name="0187a08" id="0187a08"></a><p><span class="lg">　　　　譬喻生盲之人不導他<a 
