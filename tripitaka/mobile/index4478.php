name="0242a12" id="0242a12"></a><p style="margin-left: 2em"><span class="headname">〔八、由渴愛之緣而有取〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>於「由渴愛之緣而有取」之句：</p><a 
name="0242a13" id="0242a13"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>取是四，其等〔一〕由義之分別，</p><a 
name="0242a14" id="0242a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔二〕由法之廣略，〔三〕由順序當辨知。</p><a 
name="0243a01" id="0243a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span><span class="linehead" title="PTS.Vism.569"> [P.569] </span>對其〔取〕如次有辨別說。先述此等之四取：欲取、見取、戒禁取、我語取。</p><a 
name="0243a02" id="0243a02"></a><p style="margin-left: 3em"><span class="headname">〔一〕〔由義之分別〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>由彼等之義有次之分別。〔即〕稱為事（欲之對象）以<a 
name="0243a03" id="0243a03"></a>取所欲，故為「欲取。」又欲且取故為欲取。（up&#x0101;d&#x0101;na）取者是堅執，即此時如於惱<a 
name="0243a04" id="0243a04"></a>（up&#x0101;y&#x0101;sa）、接近（upaka&#x1E6D;&#x1E6D;ha）等〔之語〕，upa 之語是堅之義。同樣於見且取故<a 
name="0243a05" id="0243a05"></a>為「見取」。又取見故為見取。同樣取戒禁故為「戒禁取」。又戒禁且取故為戒禁取。<a 
name="0243a06" id="0243a06"></a>如斯住著牛戒、牛禁等為淨者，唯自身為取〔此〕。同樣由此〔人人〕之語故為語。<a 
name="0243a07" id="0243a07"></a>由此〔人人〕之取故為取。語何？又取何耶？自取自己之語為「我語取」。由此〔人<a 
name="0243a08" id="0243a08"></a>人〕唯取我語是我故為我語取。此先由其等〔四取〕之義而分別。</p><a 
name="0243a09" id="0243a09"></a><p style="margin-left: 3em"><span class="headname">〔二〕〔由法廣略〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，對於法之廣說，先述「欲取」，「其中，欲取者云<a 
name="0243a10" id="0243a10"></a>何？對於諸欲所有之欲欲、欲貪、欲歡喜、欲渴愛、欲愛情、欲熱惱、欲昏迷、欲<a 
name="0243a11" id="0243a11"></a>縛著，以此言為欲取」故，略言之，是言激烈之渴愛，激烈之渴愛，是前之渴愛為親<a 
name="0243a12" id="0243a12"></a>依緣所強化後之渴愛。然，言某人：「如於暗中伸手之盜賊，冀求未達其境為渴愛。<a 
name="0243a13" id="0243a13"></a>如彼〔盜賊〕取物品，已達到所取之境，此為〔欲〕取。而彼等〔欲取之〕諸法是<a 
name="0243a14" id="0243a14"></a>少欲、知足之相反。又〔其等欲取是〕&#x5FA7;求〔之苦〕、守護之苦的根本。」次餘之三<a 
name="0244a01" id="0244a01"></a>取略言之，〔所有皆〕唯是見。若詳言之，對前色〔愛〕等說一百零八種之強烈渴愛<a 
name="0244a02" id="0244a02"></a>為欲取。有十事之邪見是「見取」。所謂：「其中，見取者云何？（一）不布施、（二）<a 
name="0244a03" id="0244a03"></a>不獻供、（三）〔不祭祀、（四）無善作、惡作諸業之果報、（五）無此世、（六）無他<a 
name="0244a04" id="0244a04"></a>世、（七）無母、（八）無父、（九）無化生之有情、（一&#x3007;）世間無正生活、正行道，<a 
name="0244a05" id="0244a05"></a>且自通達作證此世他世〕，而宣說於〔世人之沙門、婆羅門〕，如斯所有之見……乃<a 
name="0244a06" id="0244a06"></a>至……執顛倒，此言為見取。」其次，由戒禁而有淨之執取是「戒禁取」。所謂「其<a 
name="0244a07" id="0244a07"></a><span class="linehead" title="PTS.Vism.570"> [P.570] </span>中，戒禁取者何云。……由戒而有淨，由禁而有淨，由戒禁而有淨，如斯所有之見<a 
name="0244a08" id="0244a08"></a>……乃至……執顛倒，此言為戒禁取」。有二十事之有身見是「我語取」。所謂：「其中，<a 
name="0244a09" id="0244a09"></a>我語取者云何？於此無聞之凡夫〔不見諸聖者、不熟達聖法、不化導於聖法中、不<a 
name="0244a10" id="0244a10"></a>見諸善人、不熟達善人之法〕、不化導於善人法中，（一）色是我，〔（二）我有色，<a 
name="0244a11" id="0244a11"></a>（三）我中有色，（四）於色中有我〕而觀，〔（五～八）是受……（九～一二）<a 
name="0244a12" id="0244a12"></a>是想……（一三～一六）是行……（一七）識是我，（一八）我有識，（一九）於<a 
name="0244a13" id="0244a13"></a>我中有識，（二&#x3007;）於識中有我而觀。如斯所有之見〕……乃至……執顛倒，此言為<a 
name="0244a14" id="0244a14"></a>我語取。」此是〔四取〕法之廣說。</p><a 
name="0245a01" id="0245a01"></a><p style="margin-left: 3em"><span class="headname">〔三〕〔由順序〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，由順序者，〔此取〕者（一）生趣順序、（二）捨斷之<a 
name="0245a02" id="0245a02"></a>順序、（三）說示之順序等三種。其中，（一）於無始之輪迴，言無有「此生起於最<a 
name="0245a03" id="0245a03"></a>初」故，諸煩惱生起之順序不直接而說。唯於間接，在一有之中概而先行我執（我語<a 
name="0245a04" id="0245a04"></a>取）之常、斷之住者（見取）。由此執「我是常」，令我清淨為戒禁取。執「〔此我〕斷<a 
name="0245a05" id="0245a05"></a>絕」，不顧他世者是欲取。如斯於最初有我語取，由此而有見〔取〕、戒禁〔取〕、欲<a 
name="0245a06" id="0245a06"></a>取。於此等〔四取〕之一有，為「生起之順序」。（二）又此〔四取〕中見取等〔之三〕<a 
name="0245a07" id="0245a07"></a>應於由須陀洹道而斷故為最初所捨斷。欲取是由阿羅漢道當斷故為最後〔捨斷〕。此<a 
name="0245a08" id="0245a08"></a>等〔四取〕之「捨斷順序」。（三）其次於此等〔四取〕中，欲取是大境，為明瞭故而<a 
name="0245a09" id="0245a09"></a>於最初說。即〔欲取〕是八〔貪〕心相應故為大境。餘他是四種〔瞋、癡〕心相應<a 
name="0245a10" id="0245a10"></a>故為小境。大概言之，人人樂阿賴耶（五取蘊）故欲取是甚明瞭，餘他不然。或<a 
name="0245a11" id="0245a11"></a>欲取為得達諸〔事〕欲而數數行祭典等喧鬧。其〔喧鬧〕於彼〔欲取者為見取〕故<a 
name="0245a12" id="0245a12"></a>，其〔欲取〕之後有見取。離其〔見取〕為戒禁〔取〕、我語取之二種。其二者之<a 
name="0245a13" id="0245a13"></a>中，戒禁取是見牛之所作，又&#x9DC4;之所作而可知故為麤於最初說示，我語取為細之故<a 
name="0245a14" id="0245a14"></a>於最後〔說示〕。此乃此等〔四取〕「說示之順序」。</p><a 
name="0246a01" id="0246a01"></a><p><span class="lg">　　　　渴愛對此中初〔之欲取〕為一種之緣，<a 
name="0246a02" id="0246a02"></a><br>　　　　對於其餘之三為七種或八種之〔緣〕。</span></p><a 
name="0246a03" id="0246a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其次，如斯所說示此四取中，對最初之欲取，欲愛為親依〔緣〕唯一種為緣。<a 
name="0246a04" id="0246a04"></a>〔欲取〕由渴愛對於喜歡之諸境而生起故。而〔渴愛〕對餘之三〔取〕，以俱生、互<a 
name="0246a05" id="0246a05"></a><span class="linehead" title="PTS.Vism.571"> [P.571] </span>相、依止、相應、有、不離去、因緣之七種緣為〔緣〕。又加上親依〔緣〕為八種<a 
name="0246a06" id="0246a06"></a>緣。而其〔渴愛對三取〕為親依緣時，〔渴愛與取〕必不俱生。</p><a 
name="0246a07" id="0246a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>此詳論「由渴愛之緣而有取」之句。</p><a 
name="0246a08" id="0246a08"></a><p style="margin-left: 2em"><span class="headname">〔九、由取之緣而有有〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>於「由取之緣而有有」之句：</p><a 
name="0246a09" id="0246a09"></a><p><span class="lg">　　　　〔一〕由義，〔二〕由法、〔三〕由有意義、〔四〕由區分與〔五〕攝、〔六〕<a 
name="0246a10" id="0246a10"></a><br>　　　　何者為何之緣耶之決定說當識知。</span></p><a 
name="0246a11" id="0246a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>其中，〔一〕〔由義〕有故為「有。」其〔有〕是「業有」、「起有」之二種。所謂「<a 
name="0246a12" id="0246a12"></a>二種之有，乃業有、起有」。其中，業即有是為業有。同樣於生起即有是為起有。而<a 
name="0246a13" id="0246a13"></a>此時生起有故為有。其次譬如樂之原因故說：「佛之出世樂。」業有之原因故，當<a 
name="0246a14" id="0246a14"></a>知由果之言說有。如斯當知先由〔有〕義之決定說。</p><a 
name="0247a01" id="0247a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔二〕其次，「由法」，（一）先「業有」是稱為思與思相應之貪欲等業之諸法。所<a 
name="0247a02" id="0247a02"></a>謂「其中，業有者云何？〔是屬於小地、大地之〕福行、非福行、不動行，此言為<a 
name="0247a03" id="0247a03"></a>業有。至一切有之業，為業有」。此中，「福行」者，是十三之思。「非福行」者，是<a 
name="0247a04" id="0247a04"></a>十二〔之思〕。「不動行」者，是四之思。如斯由言「屬於小地、大地」〔之句〕，說其<a 
name="0247a05" id="0247a05"></a>等思之異熟之優劣。又由「至一切有之業」〔之句〕，說思與相應之貪欲等。</p><a 
name="0247a06" id="0247a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（二）其次「起有」，略言之，乃由業而生之諸蘊，區別而言即成九種。所謂「<a 
name="0247a07" id="0247a07"></a>其中，起有者云何？是欲有、色有、無色有、想有、無想有、非想非非想有、一蘊<a 
name="0247a08" id="0247a08"></a><span class="linehead" title="PTS.Vism.572"> [P.572] </span>有、四蘊有、五蘊有也，此言為起有」。其中稱欲之有是「欲有」，「色〔有〕、無色有」<a 
name="0247a09" id="0247a09"></a>等亦同此。想而為有，又想有此有故為想有。又與此相反即為「無想有」。無麤想故，<a 
name="0247a10" id="0247a10"></a>有細〔想〕而為〔非想非非想〕，非想非非想在此有故為「非想非非想有」。充足於一<a 
name="0247a11" id="0247a11"></a>色蘊之有為「一蘊有」，又其有有一蘊（要素）故為一蘊有。「四蘊有、五蘊有」亦同<a 
name="0247a12" id="0247a12"></a>此。此中，欲有是五取蘊。色有亦同樣。無色有是四〔取蘊〕。想有是四、五〔取蘊〕。<a 
name="0247a13" id="0247a13"></a>無想有是一〔取蘊〕。非想非非想有是四〔取蘊〕。一蘊有等屬於取蘊之一、四、五<a 
name="0247a14" id="0247a14"></a>蘊。當知如斯由此〔取〕法之決定說。</p><a 
name="0248a01" id="0248a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔三〕「由有意義」，（一）如於〔今之〕有解釋，於〔前〕行解釋之福行等已<a 
name="0248a02" id="0248a02"></a>說了。雖如斯，由前過去之業而今世之結生為緣故，今時依現在之業而為未來結生<a 
name="0248a03" id="0248a03"></a>之緣故，〔今〕更說是有意義。（二）又如前說：「其中，福行者云何？即欲界〔、<a 
name="0248a04" id="0248a04"></a>色界〕之善思……。」由此方法，唯思說為行。然，於此言：「至一切有業。」之語故，<a 
name="0248a05" id="0248a05"></a>與思想應之〔諸法〕亦〔說為有〕。（三）又於前唯識緣之業說為行。今令生於無想<a 
name="0248a06" id="0248a06"></a>有之〔業〕亦〔說為有〕。（四）又於「由無明之緣而有行」之〔句〕，唯福行等之善、<a 
name="0248a07" id="0248a07"></a>不善法而說，今言：「由取之緣而有有。」之時，亦包攝起有之故，就善、不善、異熟<a 
name="0248a08" id="0248a08"></a>法而說，何要多言哉。故由所有之要點，於此再說是有意義。如斯由此〔有之〕意<a 
name="0248a09" id="0248a09"></a>義為決定說當識知之。</p><a 
name="0248a10" id="0248a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔四〕〔由區分〕「由區分與包攝」，是由取之緣而〔生〕有之區分，又由包攝。<a 
name="0248a11" id="0248a11"></a>即由欲取之緣令生欲有，所作之業為此「業有」。由此生之諸蘊是「起有」。對於色、<a 
name="0248a12" id="0248a12"></a><span class="linehead" title="PTS.Vism.573"> [P.573] </span>無色有亦同此。如斯由欲取之緣而有二欲有，想有及五蘊有內含於此。〔由欲取之緣〕<a 
name="0248a13" id="0248a13"></a>而有二色有、想有、無想有、一蘊有、五蘊有內含於此。〔由欲取之緣〕而有二無色<a 
name="0248a14" id="0248a14"></a>有、想有、非想非非想有、四蘊有內含於此。斯〔由欲取之緣〕而有六有及其所內<a 
name="0249a01" id="0249a01"></a>含之〔諸有〕。如欲取之緣而有六有及其所內含之〔諸有〕，由餘取之緣亦同樣。如<a 
name="0249a02" id="0249a02"></a>斯由取之緣而〔生有〕以區分之，即有二十四有及其所內含之〔諸有〕。</p><a 
name="0249a03" id="0249a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔五〕「由包攝」，總括業有與起有〔為有〕，由欲取之緣而有一欲有及其所內含<a 
name="0249a04" id="0249a04"></a>之〔諸有〕。同樣〔由欲之緣〕而有色、無色有而為三有。同樣由餘〔之三〕取亦〔各<a 
name="0249a05" id="0249a05"></a>三有〕。如斯由取之緣而有十二有及其所內含之〔諸有〕。又總括而言，由取之緣而<a 
name="0249a06" id="0249a06"></a>至欲有之業為業有，由此生諸蘊為起有。對色、無色有亦同此。如斯由取之緣而有<a 
name="0249a07" id="0249a07"></a>二欲有、二色有、二無色有及其所內含之〔諸有〕故，由他之異門包攝之即為六有。<a 
name="0249a08" id="0249a08"></a>或不從業有、起有之區分，有欲有等之三有及其所內含之〔諸有〕。又不從欲有等之<a 
name="0249a09" id="0249a09"></a>區分，有業有、起有之二有。又不從業、起之區分，言由取之緣而有是唯一有之有。<a 
name="0249a10" id="0249a10"></a>當知如斯由取之緣而有之區分、包攝為決定說。</p><a 
name="0249a11" id="0249a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔六〕「又何者為何之緣耶？」〔此緣起支之〕中，何取為何〔有〕之緣耶？當<a 
name="0249a12" id="0249a12"></a>識知其決定說之義。然，於此何者為何之緣耶？何物必為何物之緣。即凡夫如狂者，<a 
name="0249a13" id="0249a13"></a>彼不伺察：「此是妥當，此是不妥當。」由何等之取而冀求何等之有、行何等之業。<a 
name="0249a14" id="0249a14"></a>故某人人由戒禁取說無有色、無色有，此非〔真實〕之解。但應解為由有一切〔故〕<a 
name="0250a01" id="0250a01"></a>而有一切〔有〕即：</p><a 
name="0250a02" id="0250a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（一）茲某者從他聞，又從〔自己之〕見，思：「諸欲於此人界〔極成〕於剎帝<a 
name="0250a03" id="0250a03"></a><span class="linehead" title="PTS.Vism.574"> [P.574] </span>大家等，又極成於六欲天界。」為得達彼等〔諸欲〕，由聽聞不正法等之所欺，思<a 
name="0250a04" id="0250a04"></a>惟：「由此業可得諸欲。」由欲取而行身惡行等。彼遂惡行之果而生惡趣。或冀求現<a 
name="0250a05" id="0250a05"></a>世諸欲，或護持所獲得，由欲取而行身惡行等。彼遂惡行之果而生惡趣。其時彼生<a 
name="0250a06" id="0250a06"></a>起因之業為業有。由業而生起諸蘊為起有，而想有、五蘊有內含於此。</p><a 
name="0250a07" id="0250a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（二）其次又其他者，由聽聞正法等而令智增大，思惟：「由此業得諸欲。」由<a 
name="0250a08" id="0250a08"></a>欲取而行身善行等。彼遂善行之果生於諸〔欲界〕天中或人中。此時彼生起因之業<a 
name="0250a09" id="0250a09"></a>為業有。由業生諸蘊為起有，而想有、五蘊有內含於此。以上〔之二例〕欲取是欲<a 
name="0250a10" id="0250a10"></a>有及對種種之區分內含〔有〕為緣</p><a 
name="0250a11" id="0250a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（三）又他者，聞：「由色、無色有其〔欲有〕更極成諸欲。」又〔如斯〕&#x5FA7;計，<a 
name="0250a12" id="0250a12"></a>由欲取令生色、無色定，依定力而生色、無色梵天界。其時彼生起因之業為業有。<a 
name="0250a13" id="0250a13"></a>由業生諸蘊為起有，而想〔有〕、無想〔有〕、非想非非想〔有〕、一〔蘊有〕、四〔蘊<a 
name="0250a14" id="0250a14"></a>有〕、五蘊有內含於此。以上欲取為色、無色有及對種種之區分內含〔有〕為緣。</p><a 
name="0251a01" id="0251a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（四）又他者言：「此我者，是欲界之成得有（善趣），又色、無色有之何者斷<a 
name="0251a02" id="0251a02"></a>絕時，善當斷絕。」取斷見行至此業。彼之業為業有，由業生諸蘊為起有，而無想有<a 
name="0251a03" id="0251a03"></a>等內含於此。以上見取有欲、色、無色有及對種種之區分內含〔有〕為緣。</p><a 
name="0251a04" id="0251a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（五）又他者，言：「此我者是欲界之成得有（善趣），又於色、無色有之何者，<a 
name="0251a05" id="0251a05"></a>離去熱惱為樂者（幸福者）。」由我語取而至行此業。彼其業是業有。由〔業〕生諸<a 
name="0251a06" id="0251a06"></a><span class="linehead" title="PTS.Vism.575"> [P.575] </span>蘊為起有，而想有等內含於此。以上是我語取三有及對種種區分內含〔有〕為緣。</p><a 
name="0251a07" id="0251a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>（六）其他者，言：「此戒禁者是欲界之成得有（善趣），又於色、無色有之何<a 
name="0251a08" id="0251a08"></a>者令遂樂果。」依戒禁取而至行此業。彼之業為業有，由此而生諸蘊為起有，而想有<a 
name="0251a09" id="0251a09"></a>等內含於此。以上最戒禁取三有及對種種之區分內含〔有〕為緣。如斯當知此〔緣<a 
name="0251a10" id="0251a10"></a>起支之〕何者為何之緣耶之決定說。</p><a 
name="0251a11" id="0251a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>〔七〕其次，此〔緣起支之中〕，若為「何〔取〕對何有而何樣為緣耶」？</p><a 
name="0251a12" id="0251a12"></a><p><span class="lg">　　　　取對於色、無色有而為親依緣，其<a 
name="0251a13" id="0251a13"></a><br>　　　　對於欲有而俱生〔緣為緣〕當識知。</span></p><a 
name="0251a14" id="0251a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>即此四種取（一）對於色、無色有，又對屬於欲有之業有中之善業及起有，為<a 
name="0252a01" id="0252a01"></a>親依緣之一種緣。（二）於欲有中對自己相應不善之業有，於俱生、互相、依止、<a 
name="0252a02" id="0252a02"></a>相應、有、不離去、因緣所區分以俱生〔緣〕等之〔七種〕緣為緣。（三）又〔於欲<a 
name="0252a03" id="0252a03"></a>有中對自己〕不相應〔不善之業有〕，唯是親依緣〔為緣〕。</p><a 
name="0252a04" id="0252a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>此詳論「由取緣而有有」之句。</p><a 
name="0252a05" id="0252a05"></a><p style="margin-left: 2em"><span class="headname">〔一&#x3007;、一一由有之緣而有生，由生之緣而有老死等〕</span></p><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>於「有之緣而有生」等，<a 
name="0252a06" id="0252a06"></a>當知生等之決定說於諦之解說方法。而此時之「有」，唯為業有之意義。然，對於<a 
name="0252a07" id="0252a07"></a>生之緣故非是起有。其次，〔有對於生〕以業緣、親依緣之二種緣為緣。</p><a 
name="0252a08" id="0252a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>茲有〔質問〕者：「然，所謂有是生之緣，應如何而知耶？」〔答曰：〕外緣<a 
name="0252a09" id="0252a09"></a>雖是相等，但見於〔生〕有劣勝等之差別故。即父、母之白（精液）、赤（卵子）、<a 
name="0252a10" id="0252a10"></a>食物等之外諸緣雖是相等，現有雙生兒或劣勝等之差別。非無其因，〔若無因者〕於<a 
name="0252a11" id="0252a11"></a><span class="linehead" title="PTS.Vism.576"> [P.576] </span>一切時之一切者即無有有故。〔又〕於諸有情內之相續中，令生此而其他之原因，依<a 
name="0252a12" id="0252a12"></a>業有而無他因故，必是業有為〔差別〕因。然，業是有情之劣勝等差別之因。故世<a 
name="0252a13" id="0252a13"></a>尊言：「業所謂依劣勝之性而分諸有情。」故說：「有是生之緣。」應當知。</p><a 
name="0252a14" id="0252a14"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>又無生之時亦無有名為老死愁等之諸法。然，有生時之老死，所觸於老死之苦<a 
name="0253a01" id="0253a01"></a>法，即關係愚人老死〔愁等之諸法〕，又所觸〔老死以外〕各各之苦法者，亦有不關<a 
name="0253a02" id="0253a02"></a>係於〔老死〕愁等之諸法故，當知此生是老死及愁等之緣。而其〔生對老死等〕，由<a 
name="0253a03" id="0253a03"></a>親依〔緣〕之點唯一種緣為緣。</p><a 
name="0253a04" id="0253a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>此詳論「由有之緣而有生」等。</p><a 
