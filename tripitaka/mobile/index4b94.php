<a 
name="0736b02" id="0736b02"></a><a 
name="0736b03" id="0736b03"></a><p style="margin-left: 2em"><span class="juannum">No. 791</span></p><a 
name="0736b04" id="0736b04"></a><p><span class="juanname">佛說出家緣經</span></p><a 
name="0736b05" id="0736b05"></a><a 
name="0736b06" id="0736b06"></a><p style="margin-left: 4em"><span class="byline">後漢安息國三藏安世高譯</span></p><a 
name="0736b07" id="0736b07"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>如是我聞：</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>一時婆伽婆在王舍城耆闍崛山，<a 
name="0736b08" id="0736b08"></a>與大比丘僧俱，千二百五十。是時難提優婆<a 
name="0736b09" id="0736b09"></a>塞與五百優婆塞，出王舍城上耆闍崛山，往<a 
name="0736b10" id="0736b10"></a>詣佛所，頭面禮足，長跪叉手問佛：「大德！願聞<a 
name="0736b11" id="0736b11"></a>優婆塞五戒；毀犯正戒，有何惡事？」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛答：「難提<a 
name="0736b12" id="0736b12"></a>優婆塞！楚害生命有十惡事。何等十？現世常<a 
name="0736b13" id="0736b13"></a>懷害心；後世毒心隆赫，恒被痛害；怨怨不息，<a 
name="0736b14" id="0736b14"></a>人不喜見；思慮多惡，見者怖懼；眠不安寐；夢<a 
name="0736b15" id="0736b15"></a>則惶怖；覺則驚悸；死時狂勃種短命栽；身壞<a 
name="0736b16" id="0736b16"></a>命終生地獄中；設得為人多病少命。是為十<a 
name="0736b17" id="0736b17"></a>惡事。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>「偷劫他財，有十惡事。何等十？貪餮深<a 
name="0736b18" id="0736b18"></a>重，恒為眾疑；能興重惡，行則非時，動則非<a 
name="0736b19" id="0736b19"></a>法；親友惡逆，賢良&#x8E08;遠；敗戒果敢；常懼王<a 
name="0736b20" id="0736b20"></a>伺，須財市命；種遺寶物；貧弊之業；身壞命終<a 
name="0736b21" id="0736b21"></a>生地獄中；設得為人，飢寒困苦，致財良難，雖<a 
name="0736b22" id="0736b22"></a>獲少財，五事共之——王、賊、水、火及惡妻子；今乃<a 
name="0736b23" id="0736b23"></a>藏埋，會亦當失。是為十惡事。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>「邪婬有十惡事。<a 
name="0736b24" id="0736b24"></a>何等十？常為其夫伺捕楚毒，室家不和；善法<a 
name="0736b25" id="0736b25"></a>消竭，不善法增；危敗軀身；不檢其妻，不守<a 
name="0736b26" id="0736b26"></a>財賄；常為人疑，宗親不信；種業如是；門不貞<a 
name="0736b27" id="0736b27"></a>潔；身壞命終生地獄中；設得為女，非獨一主，<a 
name="0736b28" id="0736b28"></a>設得為男，馳騁邪婬，自失其妻。是十惡事。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>「妄<a 
name="0736b29" id="0736b29"></a>語中有十惡事。何等十？口常臭爛；善神背叛，<a 
name="0736c01" id="0736c01"></a>凶鬼易陵；實言流世，眾所不信；俗為重事，不<a 
name="0736c02" id="0736c02"></a>在言議；未有實事惡名遠聞，為人輕毀，不起<a 
name="0736c03" id="0736c03"></a>恭肅；雖言有實，人不奉用；多懷愁怖；種誹謗<a 
name="0736c04" id="0736c04"></a>業；身死命終生地獄中；設得為人，常被誹謗。<a 
name="0736c05" id="0736c05"></a>是十惡事。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>「飲酒有三十五惡。何等三十五？散<a 
name="0736c06" id="0736c06"></a>盡財賄；致眾苦患；怨諍增重；裸露形軀；惡名<a 
name="0736c07" id="0736c07"></a>遐邇；慧明日減；應得不得；已得便失；顯揚惡<a 
name="0736c08" id="0736c08"></a>事；要務頓發憂慼之本；恍惚變沒；顏貌鄙<a 
name="0736c09" id="0736c09"></a>惡；輕慢尊長；不知供養沙門、婆羅門；自於室<a 
name="0736c10" id="0736c10"></a>家不辨尊卑；不宗敬佛；不崇大法；不敬事僧；<a 
name="0736c11" id="0736c11"></a>返親惡人；遠離明能；崩墜邪道；無慚愧心；不<a 
name="0736c12" id="0736c12"></a>護根門；惛荒婬欲；眾所不愛；人不喜見；德士<a 
name="0736c13" id="0736c13"></a>宿舊咸來咎責；集造眾惡；要用之勢，不豫識<a 
name="0736c14" id="0736c14"></a>任；智德隱避；像類不別；去泥洹遠；種狂惑<a 
name="0736c15" id="0736c15"></a>業；身死命終生地獄中；設得為人，愚癡頑瞶。」</p><a 
name="0736c16" id="0736c16"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>是時難提優婆塞及五百清信士，諸天、世人、<a 
name="0736c17" id="0736c17"></a>四輩之眾，聞佛所說，畢命受持，頭面遶竟，踊<a 
name="0736c18" id="0736c18"></a>悅而去。</p><a 
name="0736c19" id="0736c19"></a><p><span class="juanname">佛說出家緣經</span></p>
