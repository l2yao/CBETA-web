name="0209a22" id="0209a22"></a><a pin_name="9 無住品"></a>乃至一切陀羅尼門不應攝受，一切三摩地門<a 
name="0209a23" id="0209a23"></a><a pin_name="9 無住品"></a>不應攝受；陀羅尼門既不應攝受便非陀羅<a 
name="0209a24" id="0209a24"></a><a pin_name="9 無住品"></a>尼門，三摩地門既不應攝受便非三摩地門。<a 
name="0209a25" id="0209a25"></a><a pin_name="9 無住品"></a>所以者何？本性空故。其所攝受修行圓滿般<a 
name="0209a26" id="0209a26"></a><a pin_name="9 無住品"></a>若波羅蜜多亦不應攝受，如是般若波羅蜜多<a 
name="0209a27" id="0209a27"></a><a pin_name="9 無住品"></a>既不應攝受便非般若波羅蜜多。所以者何？<a 
name="0209a28" id="0209a28"></a><a pin_name="9 無住品"></a>本性空故。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>「如是菩薩摩訶薩修行般若波羅<a 
name="0209a29" id="0209a29"></a><a pin_name="9 無住品"></a>蜜多時，應以本性空觀一切法。作此觀時，於<a 
name="0209b01" id="0209b01"></a><a pin_name="9 無住品"></a>一切法心無行處，是名菩薩摩訶薩無所攝<a 
name="0209b02" id="0209b02"></a><a pin_name="9 無住品"></a>受三摩地。此三摩地微妙殊勝廣大無量，能<a 
name="0209b03" id="0209b03"></a><a pin_name="9 無住品"></a>集無邊無礙作用，不共一切聲聞、獨覺。其所<a 
name="0209b04" id="0209b04"></a><a pin_name="9 無住品"></a>成辦一切相智亦不應攝受，如是一切相智<a 
name="0209b05" id="0209b05"></a><a pin_name="9 無住品"></a>既不應攝受便非一切相智。所以者何？以內<a 
name="0209b06" id="0209b06"></a><a pin_name="9 無住品"></a>空故，外空故，內外空故，空空故，大空故，勝<a 
name="0209b07" id="0209b07"></a><a pin_name="9 無住品"></a>義空故，有為空故，無為空故，畢竟空故，無<a 
name="0209b08" id="0209b08"></a><a pin_name="9 無住品"></a>際空故，散空故，無變異空故，本性空故，自<a 
name="0209b09" id="0209b09"></a><a pin_name="9 無住品"></a>相空故，共相空故，一切法空故，不可得空故，<a 
name="0209b10" id="0209b10"></a><a pin_name="9 無住品"></a>無性空故，自性空故，無性自性空故。何以<a 
name="0209b11" id="0209b11"></a><a pin_name="9 無住品"></a>故？世尊！是一切相智非取相修得。所以者<a 
name="0209b12" id="0209b12"></a><a pin_name="9 無住品"></a>何？諸取相者皆是煩惱。何等為相？所謂色相，<a 
name="0209b13" id="0209b13"></a><a pin_name="9 無住品"></a>受、想、行、識相，乃至一切陀羅尼門相、一切三<a 
name="0209b14" id="0209b14"></a><a pin_name="9 無住品"></a>摩地門相，於此諸相而取著者名為煩惱。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>「若<a 
name="0209b15" id="0209b15"></a><a pin_name="9 無住品"></a>取相修得一切相智者，勝軍梵志於一切智<a 
name="0209b16" id="0209b16"></a><a pin_name="9 無住品"></a>智不應信解。何等名為彼信解相？謂於般<a 
name="0209b17" id="0209b17"></a><a pin_name="9 無住品"></a>若波羅蜜多深生淨信，由勝解力思惟觀察<a 
name="0209b18" id="0209b18"></a><a pin_name="9 無住品"></a>一切智智，不以相方便，亦不以非相方便，以<a 
name="0209b19" id="0209b19"></a><a pin_name="9 無住品"></a>相與非相俱不可取故。是勝軍梵志雖由<a 
name="0209b20" id="0209b20"></a><a pin_name="9 無住品"></a>信解力歸趣佛法，名隨信行者，而能以本性<a 
name="0209b21" id="0209b21"></a><a pin_name="9 無住品"></a>空悟入一切智智。既悟入已，不取色相，不<a 
name="0209b22" id="0209b22"></a><a pin_name="9 無住品"></a>取受、想、行、識相，乃至不取一切陀羅尼門相，<a 
name="0209b23" id="0209b23"></a><a pin_name="9 無住品"></a>不取一切三摩地門相。何以故？以一切法自<a 
name="0209b24" id="0209b24"></a><a pin_name="9 無住品"></a>相皆空，能取、所取俱不可得故。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>「如是梵志不<a 
name="0209b25" id="0209b25"></a><a pin_name="9 無住品"></a>以內得現觀而觀一切智智，不以外得現觀<a 
name="0209b26" id="0209b26"></a><a pin_name="9 無住品"></a>而觀一切智智，不以內外得現觀而觀一切<a 
name="0209b27" id="0209b27"></a><a pin_name="9 無住品"></a>智智，不以無智得現觀而觀一切智智，不以<a 
name="0209b28" id="0209b28"></a><a pin_name="9 無住品"></a>餘得現觀而觀一切智智，亦不以不得現觀<a 
name="0209b29" id="0209b29"></a><a pin_name="9 無住品"></a>而觀一切智智。所以者何？是勝軍梵志不<a 
name="0209c01" id="0209c01"></a><a pin_name="9 無住品"></a>見所觀一切智智，不見能觀般若，不見觀者<a 
name="0209c02" id="0209c02"></a><a pin_name="9 無住品"></a>及觀所依處。是勝軍梵志非於內色觀一切<a 
name="0209c03" id="0209c03"></a><a pin_name="9 無住品"></a>智智，非於內受、想、行、識觀一切智智；非於外<a 
name="0209c04" id="0209c04"></a><a pin_name="9 無住品"></a>色觀一切智智，非於外受、想、行、識觀一切智<a 
name="0209c05" id="0209c05"></a><a pin_name="9 無住品"></a>智；非於內外色觀一切智智，非於內外受、想、<a 
name="0209c06" id="0209c06"></a><a pin_name="9 無住品"></a>行、識觀一切智智；亦非離色觀一切智智，亦<a 
name="0209c07" id="0209c07"></a><a pin_name="9 無住品"></a>非離受、想、行、識觀一切智智。乃至非於內一<a 
name="0209c08" id="0209c08"></a><a pin_name="9 無住品"></a>切陀羅尼門觀一切智智，非於內一切三摩<a 
name="0209c09" id="0209c09"></a><a pin_name="9 無住品"></a>地門觀一切智智；非於外一切陀羅尼門觀<a 
name="0209c10" id="0209c10"></a><a pin_name="9 無住品"></a>一切智智，非於外一切三摩地門觀一切智<a 
name="0209c11" id="0209c11"></a><a pin_name="9 無住品"></a>智；非於內外一切陀羅尼門觀一切智智，非<a 
name="0209c12" id="0209c12"></a><a pin_name="9 無住品"></a>於內外一切三摩地門觀一切智智；亦非離<a 
name="0209c13" id="0209c13"></a><a pin_name="9 無住品"></a>一切陀羅尼門觀一切智智，亦非離一切三<a 
name="0209c14" id="0209c14"></a><a pin_name="9 無住品"></a>摩地門觀一切智智。何以故？若內、若外、若<a 
name="0209c15" id="0209c15"></a><a pin_name="9 無住品"></a>內外、若離內外皆不可得故。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>「是勝軍梵志以<a 
name="0209c16" id="0209c16"></a><a pin_name="9 無住品"></a>如是等諸離相門，於一切智智深生信解；由<a 
name="0209c17" id="0209c17"></a><a pin_name="9 無住品"></a>此信解，於一切法皆無取著，以諸法實相<a 
name="0209c18" id="0209c18"></a><a pin_name="9 無住品"></a>不可得故。如是梵志以離相門，於一切智<a 
name="0209c19" id="0209c19"></a><a pin_name="9 無住品"></a>智得信解已，於一切法皆不取相，亦不思<a 
name="0209c20" id="0209c20"></a><a pin_name="9 無住品"></a>惟無相，諸法以相、無相法皆不可得故。如<a 
name="0209c21" id="0209c21"></a><a pin_name="9 無住品"></a>是梵志由勝解力，於一切法不取不捨，實<a 
name="0209c22" id="0209c22"></a><a pin_name="9 無住品"></a>相法中無取捨故。時，彼梵志於自信解乃<a 
name="0209c23" id="0209c23"></a><a pin_name="9 無住品"></a>至於涅槃亦不取著。所以者何？以一切法<a 
name="0209c24" id="0209c24"></a><a pin_name="9 無住品"></a>本性皆空不可取故。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>「世尊！菩薩摩訶薩般<a 
name="0209c25" id="0209c25"></a><a pin_name="9 無住品"></a>若波羅蜜多亦復如是，於一切法無所取著，<a 
name="0209c26" id="0209c26"></a><a pin_name="9 無住品"></a>能從此岸到彼岸故；若於諸法少有取著，則<a 
name="0209c27" id="0209c27"></a><a pin_name="9 無住品"></a>於彼岸非為能到。是故菩薩摩訶薩修行般<a 
name="0209c28" id="0209c28"></a><a pin_name="9 無住品"></a>若波羅蜜多時，不取一切色，不取一切受、想、<a 
name="0209c29" id="0209c29"></a><a pin_name="9 無住品"></a>行、識，以一切法無所取故；乃至不取一切陀<a 
name="0210a01" id="0210a01"></a><a pin_name="9 無住品"></a>羅尼門，不取一切三摩地門，亦以一切法無<a 
name="0210a02" id="0210a02"></a><a pin_name="9 無住品"></a>所取故。是菩薩摩訶薩雖於一切色，一切受、<a 
name="0210a03" id="0210a03"></a><a pin_name="9 無住品"></a>想、行、識，乃至一切陀羅尼門、一切三摩地門，若<a 
name="0210a04" id="0210a04"></a><a pin_name="9 無住品"></a>總若別皆無所取；而以本願所行四念住乃<a 
name="0210a05" id="0210a05"></a><a pin_name="9 無住品"></a>至八聖道支未圓滿故，及以本願所證佛十<a 
name="0210a06" id="0210a06"></a><a pin_name="9 無住品"></a>力乃至一切相智未成辦故，於其中間終不<a 
name="0210a07" id="0210a07"></a><a pin_name="9 無住品"></a>以不取一切相故而般涅槃。是菩薩摩訶薩<a 
name="0210a08" id="0210a08"></a><a pin_name="9 無住品"></a>雖能圓滿四念住乃至八聖道支，及能成辦佛<a 
name="0210a09" id="0210a09"></a><a pin_name="9 無住品"></a>十力乃至一切相智，而不見四念住乃至八<a 
name="0210a10" id="0210a10"></a><a pin_name="9 無住品"></a>聖道支，及不見佛十力乃至一切相智。何以<a 
name="0210a11" id="0210a11"></a><a pin_name="9 無住品"></a>故？是四念住即非四念住，乃至八聖道支即<a 
name="0210a12" id="0210a12"></a><a pin_name="9 無住品"></a>非八聖道支，及佛十力即非佛十力，乃至一<a 
name="0210a13" id="0210a13"></a><a pin_name="9 無住品"></a>切相智即非一切相智，以一切法非法、非非<a 
name="0210a14" id="0210a14"></a><a pin_name="9 無住品"></a>法故。是菩薩摩訶薩修行般若波羅蜜多時，<a 
name="0210a15" id="0210a15"></a><a pin_name="9 無住品"></a>於一切法雖無所取，而能成辦一切事業。</p><a 
