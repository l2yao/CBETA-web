<a 
name="0129b01" id="0129b01"></a><a 
name="0129b02" id="0129b02"></a><p style="margin-left: 2em"><span class="juannum">No. 2738 [cf. No. 235]</span></p><a 
name="0129b03" id="0129b03"></a><p><span class="juanname">金剛經疏</span></p><a 
name="0129b04" id="0129b04"></a><a 
name="0129b05" id="0129b05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>故女人以嗔為力。欲有所索必先嗔故。國王<a 
name="0129b06" id="0129b06"></a>以嬌豪為力。羅漢以精進為力。諸佛以大悲<a 
name="0129b07" id="0129b07"></a>為力。比丘以忍為力。故遺教經云。能行忍者。<a 
name="0129b08" id="0129b08"></a>乃可名為有力大人。七佛偈云。忍辱第一道。<a 
name="0129b09" id="0129b09"></a>佛說無為最出家惱他人不名為沙門。忍雖<a 
name="0129b10" id="0129b10"></a>有三。今此中取無生法忍。何以故。依經起行。<a 
name="0129b11" id="0129b11"></a>無人法二執故。文中有三。一能忍。二無苦。三<a 
name="0129b12" id="0129b12"></a>有樂。此明能忍也。無生法忍。唯諸佛之所窮<a 
name="0129b13" id="0129b13"></a>故。名如來忍辱波羅蜜。非餘人之能究故。說<a 
name="0129b14" id="0129b14"></a>非波羅蜜。今言忍辱波羅蜜者。是如來清淨<a 
name="0129b15" id="0129b15"></a>善根。非波羅蜜者。非二乘人善根。若然者<a 
name="0129b16" id="0129b16"></a>依經起行。既生無生之勝忍。豈見苦受之所<a 
name="0129b17" id="0129b17"></a>倦哉。故偈曰。</p><a 
name="0129b18" id="0129b18"></a><p>　<span class="lg">能忍於苦行　　以苦行有善<a 
name="0129b19" id="0129b19"></a><br>　彼福不可量　　如是最勝義</span></p><a 
name="0129b20" id="0129b20"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>能忍於苦行者。依經起苦行能忍也。以苦行<a 
name="0129b21" id="0129b21"></a>有善者。由此苦行以清淨忍善根為體故。言<a 
name="0129b22" id="0129b22"></a>有善也。彼福不可量者。彼忍善根非餘人所<a 
name="0129b23" id="0129b23"></a>惻量也。如是最勝義者。唯如來之所量也。故<a 
name="0129b24" id="0129b24"></a>曰忍辱波羅蜜說非忍辱波羅蜜。</p><a 
name="0129b25" id="0129b25"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>經曰。何以故至無壽者相者。自下明無苦。一<a 
name="0129b26" id="0129b26"></a>立無苦二證無苦。此立也。前明有若而能忍。<a 
name="0129b27" id="0129b27"></a>此明有忍故無苦。何者若見自我他我之異。<a 
name="0129b28" id="0129b28"></a>便見能害所害之殊。我於爾時依經修行。既<a 
name="0129b29" id="0129b29"></a>無兩我之執。則不見歌利為能害我身為所<a 
name="0129c01" id="0129c01"></a>害。既無兩害之體。苦受從誰兩生。論經云。<a 
name="0129c02" id="0129c02"></a>無我相人相眾生相壽者。相無相亦非無相。<a 
name="0129c03" id="0129c03"></a>真諦云。無我等相者無自相也。無相者無他<a 
name="0129c04" id="0129c04"></a>也。亦非無相者非不有悲智相也。無著云。能<a 
name="0129c05" id="0129c05"></a>忍者謂達法無我。故如經如來說羼提波羅<a 
name="0129c06" id="0129c06"></a>蜜。案此謂無生法忍也。又云。云何知忍相若<a 
name="0129c07" id="0129c07"></a>他於已起惡等時。無有我等相。不生嗔想。亦<a 
name="0129c08" id="0129c08"></a>不於羼提波羅蜜中生有想。於非波羅蜜中<a 
name="0129c09" id="0129c09"></a>生無想。案此解稍異。今明於忍度上不作法<a 
name="0129c10" id="0129c10"></a>想。於非忍上不作非法想。故曰無相亦非無<a 
name="0129c11" id="0129c11"></a>相也。此等雖有不饒益事。而是法思惟忍攝<a 
name="0129c12" id="0129c12"></a>也。歌利王事。此中應廣說。</p><a 
name="0129c13" id="0129c13"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>經曰。何以故我於往昔至應生嗔恨者證也。<a 
name="0129c14" id="0129c14"></a>更曉無苦義。節節支解害事極也。不生嗔恨<a 
name="0129c15" id="0129c15"></a>忍力猛也。嗔恨因我執而生。我執既無嗔恨。<a 
name="0129c16" id="0129c16"></a>從誰而作。然嗔是大惑。恨是小纏。大惑即可<a 
name="0129c17" id="0129c17"></a>報之以殺心。小纏但可加之以楚毒。由嗔具<a 
name="0129c18" id="0129c18"></a>五義故名大。恨不具故名小。如心說。</p><a 
name="0129c19" id="0129c19"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>經曰。須菩提又念過去至無壽者相者。明有<a 
name="0129c20" id="0129c20"></a>樂也。前明有忍故無苦。此明無苦故有樂。何<a 
name="0129c21" id="0129c21"></a>者作忍仙人顯有慈悲。無我等相明無嗔恨。<a 
name="0129c22" id="0129c22"></a>無嗔恨故無苦。有慈悲故有樂。偈曰。</p><a 
name="0129c23" id="0129c23"></a><p>　<span class="lg">離我及恚相　　實無於苦惱<a 
name="0129c24" id="0129c24"></a><br>　苦樂有慈悲　　如是苦行果</span></p><a 
name="0129c25" id="0129c25"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>案上半明無苦無自他相故。下半明有樂有<a 
name="0129c26" id="0129c26"></a>悲智相故。由悲有樂。由智無苦。悲智和合故<a 
name="0129c27" id="0129c27"></a>名共也。</p><a 
name="0129c28" id="0129c28"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>經曰。是故須菩提至三菩提心者。自下第二<a 
name="0129c29" id="0129c29"></a>更防其過也。夫見苦而行苦者。則心疲而退<a 
name="0130a01" id="0130a01"></a>沒。忘苦而行苦者。則心輕而進昇。為防退沒<a 
name="0130a02" id="0130a02"></a>之行所以。教發無住之心也。文中有四。一示<a 
name="0130a03" id="0130a03"></a>無住心。二示無住境。三示無住障。四示無住<a 
name="0130a04" id="0130a04"></a>行。此明無住心也。夫菩提以無住為體。若能<a 
name="0130a05" id="0130a05"></a>忘相發心。心即泯同無住。所以離相發心。即<a 
name="0130a06" id="0130a06"></a>無住之體也。無著云。為顯示流轉苦對治故<a 
name="0130a07" id="0130a07"></a>說應離一切想等。案流轉者謂生死流轉。若<a 
name="0130a08" id="0130a08"></a>著相行施。則於流轉中有疲乏之苦。今為對<a 
name="0130a09" id="0130a09"></a>治言離一切相也。偈曰。</p><a 
name="0130a10" id="0130a10"></a><p>　<span class="lg">為不捨心起　　修行及堅固<a 
name="0130a11" id="0130a11"></a><br>　為忍波羅蜜　　習彼能學心</span></p><a 
name="0130a12" id="0130a12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>為不捨心起者有二。一不捨菩提心。二不捨<a 
name="0130a13" id="0130a13"></a>無相心也。故言應離一切相發阿耨菩提心。<a 
name="0130a14" id="0130a14"></a>此則不捨心也。修行者謂不住色聲香味觸<a 
name="0130a15" id="0130a15"></a>而修行也。堅固者若離於相修行堅固也。論<a 
name="0130a16" id="0130a16"></a>釋發心修行何所為為忍波羅蜜。習彼能學<a 
name="0130a17" id="0130a17"></a>心。案忍波羅蜜者。如來忍度也。能學心者。初<a 
name="0130a18" id="0130a18"></a>地心也。今為求如來忍度發心修行。求初地<a 
name="0130a19" id="0130a19"></a>能學之心也。又忍波羅蜜者。初地心也。能學<a 
name="0130a20" id="0130a20"></a>心者。地前心也。故習地前無住心也。故曰習<a 
name="0130a21" id="0130a21"></a>彼能學心。故論曰。入初地羼提波羅蜜。此<a 
name="0130a22" id="0130a22"></a>名不住心。案不住者。不住法非法也。故無著<a 
name="0130a23" id="0130a23"></a>云。於非法及法無我中皆不住故。</p><a 
name="0130a24" id="0130a24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>經曰。不應住色生心至應生無所住心者。此<a 
name="0130a25" id="0130a25"></a>示無住境也。不住色生心。令離報恩障。不住<a 
name="0130a26" id="0130a26"></a>聲香味觸法生心。令離果報障。應生無所住<a 
name="0130a27" id="0130a27"></a>也。令離相疑障也。真諦云。此中應具離三<a 
name="0130a28" id="0130a28"></a>障。一己身。二報恩。三果報。己身者若著己身<a 
name="0130a29" id="0130a29"></a>即起二見。謂常見斷見。由常故不求未來。由<a 
name="0130b01" id="0130b01"></a>斷故不見未來。故障修行也。餘二可見。</p><a 
name="0130b02" id="0130b02"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>經曰。若心有住則為非住者。此示無住障也。<a 
name="0130b03" id="0130b03"></a>若心有住住前二境則為非住不住菩提。若<a 
name="0130b04" id="0130b04"></a>有此心便成障也。真諦云。若心有住者住三<a 
name="0130b05" id="0130b05"></a>執也。三執者。一常見。二斷見。三有無見。為<a 
name="0130b06" id="0130b06"></a>離常見故不令住法。為離斷見故不令住非<a 
name="0130b07" id="0130b07"></a>法。為離有無見故即令住中道。前一破凡夫<a 
name="0130b08" id="0130b08"></a>執分別性為有。次一破凡夫執依他性為無。<a 
name="0130b09" id="0130b09"></a>後一破二乘執真實性亦有亦無。若不離此<a 
name="0130b10" id="0130b10"></a>三執。即不與菩提相應故言非住也。</p><a 
name="0130b11" id="0130b11"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>經曰。是故佛說至布施者。此示無住行。夫昇<a 
name="0130b12" id="0130b12"></a>高者必自述涉深者必自淺。是故欲入無住<a 
name="0130b13" id="0130b13"></a>之心位者。應修無住之行也。無住心位初地<a 
name="0130b14" id="0130b14"></a>已上。無住行位道種已還。故攝論云。願樂<a 
name="0130b15" id="0130b15"></a>位六波羅蜜。雖是世間法能引出世心。真諦<a 
name="0130b16" id="0130b16"></a>云。無住心者即真心。若與忍辱相應位在三<a 
name="0130b17" id="0130b17"></a>地。若與施相應位在初地。若與戒相應位在<a 
name="0130b18" id="0130b18"></a>二地。四地已上精進禪定智慧方便願力智。<a 
name="0130b19" id="0130b19"></a>隨義應知。為不捨此真心故。離一切相發心<a 
name="0130b20" id="0130b20"></a>修行也。通論地上束為四僧俱修六度。一無<a 
name="0130b21" id="0130b21"></a>羞僧。謂破戒人。二啞羊僧。謂凡夫持戒。三<a 
name="0130b22" id="0130b22"></a>有羞僧。謂二乘人四第一義僧。謂真菩薩。初<a 
name="0130b23" id="0130b23"></a>三地修世間行相。同凡夫不堪斷決譬之啞<a 
name="0130b24" id="0130b24"></a>羊。次四地修出世行相。同二乘不復作惡譬<a 
name="0130b25" id="0130b25"></a>之有羞。後三地修出出世行。是菩薩自位不<a 
name="0130b26" id="0130b26"></a>離雜道故稱第一義。一切淨土中有凡夫二<a 
name="0130b27" id="0130b27"></a>乘者。悉須作此判之。若就實為論。則淨土<a 
name="0130b28" id="0130b28"></a>中無復凡夫二乘故。往生論曰。女人及根缺。<a 
name="0130b29" id="0130b29"></a>二乘種不生也。寄相則有故。觀經明三輩九<a 
name="0130c01" id="0130c01"></a>品。則凡夫二乘亦往生也。波頗云。應佛淨土<a 
name="0130c02" id="0130c02"></a>有凡夫二乘。報佛淨土則無。真諦云。凡夫<a 
name="0130c03" id="0130c03"></a>二乘於穢土中見阿彌陀佛。諸菩薩於淨土<a 
name="0130c04" id="0130c04"></a>中見阿彌陀佛。案此二說。報土則一向純淨。<a 
name="0130c05" id="0130c05"></a>應土則有染有淨。</p><a 
name="0130c06" id="0130c06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>經曰。須菩提菩薩至應如是布施者。自下約<a 
name="0130c07" id="0130c07"></a>無住斷疑兩重。一約斷疑。二約自行斷。外化<a 
name="0130c08" id="0130c08"></a>者疑曰。若其無住為誰修行。若見眾生可為<a 
name="0130c09" id="0130c09"></a>應名住著眾生。今明約俗可言有為。約真即<a 
name="0130c10" id="0130c10"></a>無所為故。偈曰。</p><a 
name="0130c11" id="0130c11"></a><p>　<span class="lg">修行為眾生　　如是因當識<a 
name="0130c12" id="0130c12"></a><br>　眾生及彼事　　遠離亦應知</span></p><a 
name="0130c13" id="0130c13"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>上半明俗。下半明真。文有兩重。一約俗二約<a 
name="0130c14" id="0130c14"></a>真。此約俗也。為利益一切眾生應如是布施<a 
name="0130c15" id="0130c15"></a>者。明約俗有眾生可言有為故。言為利益一<a 
name="0130c16" id="0130c16"></a>切眾生應如是布施。何以故。由約俗諦陰界<a 
name="0130c17" id="0130c17"></a>入等有故。言利益者三檀分別。即登眾生捨<a 
name="0130c18" id="0130c18"></a>三種苦。得三種樂。由資生檀故。令捨貧窮苦<a 
name="0130c19" id="0130c19"></a>得巨富樂。由無畏檀故。令捨怖畏苦得安穩<a 
name="0130c20" id="0130c20"></a>樂由法檀故。令捨生死苦得涅槃樂。菩薩所<a 
name="0130c21" id="0130c21"></a>以能三檀利益眾生者。六真七似故。能利益<a 
name="0130c22" id="0130c22"></a>眾生。六真者。謂以六度利益眾生也。以施<a 
name="0130c23" id="0130c23"></a>令成善器。以戒令其禁守。以忍耐與違惡。以<a 
name="0130c24" id="0130c24"></a>進助其順善。以定化令入法。以智斷其疑網。<a 
name="0130c25" id="0130c25"></a>如偈說令器及禁耐惡與助善入法亦斷疑六<a 
name="0130c26" id="0130c26"></a>行饒益事。七似者。一似母。二似父。三似善<a 
