<div class="root" id="0001" sutra_name="天竺國菩提達摩禪師論" sutra_juan="[卷1]">　
<a 
name="0032a01" id="0032a01"></a><a 
name="0032a02" id="0032a02"></a><p style="margin-left: 2em"><span class="headname">天竺國菩提達摩禪師論</span></p><a 
name="0032a03" id="0032a03"></a><p style="margin-left: 4em"><span class="byline">整理者　方廣錩</span></p><a 
name="0032a04" id="0032a04"></a><a 
name="0032a05" id="0032a05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="headname">　　〔題解〕</span></p><a 
name="0032a06" id="0032a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>《天竺國菩提達摩禪師論》，又名《達摩禪師論》，中國僧人假<a 
name="0032a07" id="0032a07"></a>託禪宗初祖菩提達摩所撰典籍，著者不詳，一卷。</p><a 
name="0032a08" id="0032a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>在敦煌遺書中，至今已經發現兩種題名為《達摩禪師論》的<a 
name="0032a09" id="0032a09"></a>文獻。其一為日本橋本凝胤所藏，首殘尾存，尾題作「達摩禪師<a 
name="0032a10" id="0032a10"></a>論」，係唐高宗開耀元年（681）所書，後被收入關口真大《達摩大<a 
name="0032a11" id="0032a11"></a>師之研究》一書中。該文獻論述徐緩、唯淨、唯善等三種安樂法<a 
name="0032a12" id="0032a12"></a>門，主張禪師必須自守戒行。另一種為日本田中良昭於法國敦<a 
name="0032a13" id="0032a13"></a>煌特藏中發現的伯2039號。該號首尾完整，首題作「天竺國菩<a 
name="0032a14" id="0032a14"></a>提達摩禪師論」，尾題作「達摩禪師論」。論述禪門之法，先從「禪<a 
name="0032a15" id="0032a15"></a>定門」順序至「安心門」，共列舉了十五種禪門之法的名稱。接著<a 
name="0032a16" id="0032a16"></a>從「安心門」逆序至「禪定門」，逐一解釋這些禪門之法的意義。<a 
name="0032a17" id="0032a17"></a>從內容看，上述兩種《達摩禪師論》應是同名的不同文獻。這兩<a 
name="0032a18" id="0032a18"></a>種文獻均未為我國歷代經錄所著錄，亦未為歷代大藏經所收。</p><a 
name="0032a19" id="0032a19"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>最近我們發現北京圖書館所藏敦煌遺書北新1254號（首殘<a 
name="0032a20" id="0032a20"></a>尾脫）與北新1255號（首脫尾殘）所抄均為禪文獻，紙質、字體完<a 
name="0032a21" id="0032a21"></a>全相同，本為同卷。後因紙縫粘接處脫落，遂成為兩件。綴接後<a 
name="0032a22" id="0032a22"></a>文字相連無間。所抄寫的第一個文獻首殘尾全，無尾題，故不知<a 
name="0032a23" id="0032a23"></a>名。第二個文獻首尾完整，首題作「禪策問答」，無尾題。第三個<a 
name="0032a24" id="0032a24"></a>文獻首尾完整，首題作「息諍論」，無尾題。最後還抄寫了一則禪<a 
name="0032a25" id="0032a25"></a>問答及若干禪詩。其後，經田中良昭先生鑒定，第一個闕名殘文<a 
name="0033a01" id="0033a01"></a>獻即《天竺國菩提達摩禪師論》的又一個抄本。</p><a 
name="0033a02" id="0033a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>新1254號與新1255號本為一卷，依次所抄均為禪文獻，故<a 
name="0033a03" id="0033a03"></a>應屬傳入敦煌的宗密《禪藏》的組成部分。對研究早期禪宗及宗<a 
name="0033a04" id="0033a04"></a>密《禪藏》均有一定的價值。</p><a 
name="0033a05" id="0033a05"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>與伯2039號相比，北圖本缺失首尾題及首部約三百餘字，<a 
name="0033a06" id="0033a06"></a>但在對諸種法門一一解釋之後，末尾多了一段總結性文字，稱這<a 
name="0033a07" id="0033a07"></a>些禪門之法均為往古大德禪師所作。最後並有一段關於大、小<a 
name="0033a08" id="0033a08"></a>乘禪區別的問答。共約多出六百餘字。從文獻形態看，這些多<a 
name="0033a09" id="0033a09"></a>出的文字顯然是後人增補的。因此，可以肯定地說，北圖本的產<a 
name="0033a10" id="0033a10"></a>生比伯2039號的時代要晚。從兩本共同的部分看，內容略有差<a 
name="0033a11" id="0033a11"></a>異，文字互有優劣，可資互校。</p><a 
name="0033a12" id="0033a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>整理本綜合伯2039號及北新1254號，錄為全本。其中首<a 
name="0033a13" id="0033a13"></a>部北圖本缺失部分，以伯2039號為底本，無校本；其後兩本共同<a 
name="0033a14" id="0033a14"></a>部分，以北新1254號為底本，以伯2039號為甲本；最後北圖本<a 
name="0033a15" id="0033a15"></a>獨有部分以北圖本為底本，無校本。底、校本的換用情況，在校<a 
name="0033a16" id="0033a16"></a>記中隨文說明。</p><a 
name="0033a17" id="0033a17"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>古代佛典在傳抄中增衍嬗變，甚為常見。有的在流傳中形<a 
name="0033a18" id="0033a18"></a>成異本，應分別整理，如本書整理發表的《天公經》、《佛母經》等；<a 
name="0033a19" id="0033a19"></a>有的雖然有差異，但不足以成為異本，可資互校。即使已經成為<a 
name="0033a20" id="0033a20"></a>異本，因其出於同源，亦可以相互參校。因此，對同一主題內容<a 
name="0033a21" id="0033a21"></a>的不同抄本，應整理為一本，還是分作異本，有時成為佛典整理<a 
name="0033a22" id="0033a22"></a>中的一個難點。對《天竺國菩提達摩禪師論》，本書擬先將北圖<a 
name="0033a23" id="0033a23"></a>本與伯2039號合校為一本，發表在第一輯；然後將上述兩號分<a 
name="0033a24" id="0033a24"></a>別整理，發表在第二輯。以此作為整理這一類佛典的一種嘗試，<a 
name="0033a25" id="0033a25"></a>敬請讀者批評。</p><a 
name="0033a26" id="0033a26"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>北圖本曾於1995年3月8日在日本京都禪文化研究所集<a 
name="0033a27" id="0033a27"></a>體討論，此次整理吸收了討論會上的不少校訂意見，特別吸收了<a 
name="0034a01" id="0034a01"></a>西口芳男先生對本文獻的校訂意見與註釋，特此說明。並向田<a 
name="0034a02" id="0034a02"></a>中良昭、西口芳男及參加討論會的其他諸位先生表示感謝。</p><a 
name="0034a03" id="0034a03"></a><a 
name="0034a04" id="0034a04"></a><p style="margin-left: 2em"><span class="headname">〔錄文〕</span></p><a 
name="0034a05" id="0034a05"></a><a 
name="0034a06" id="0034a06"></a><p><span class="juanname">天竺國菩提達摩禪師論一卷</span></p><a 
name="0034a07" id="0034a07"></a><a 
name="0034a08" id="0034a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>禪門之法，如經論所說，乃有多義，非直一名。一名禪定門，<a 
name="0034a09" id="0034a09"></a>亦名制心門，亦名照心門，亦名覺心門，亦名察心門，亦名正<a 
name="0034a10" id="0034a10"></a>心門，亦名知心門，亦名了心門，亦名達心門，亦名徵心門，亦名<a 
name="0034a11" id="0034a11"></a>息心門，亦名定心門，亦名悟心門，亦名住心門，亦名安心門。</p><a 
name="0034a12" id="0034a12"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>何名「安心門」者？由常看守心故，熟看諸境種種相<a 
name="0034a13" id="0034a13"></a>貌，一切境界悉知不從外來。迷是自心變作，知境界唯是自心作<a 
name="0034a14" id="0034a14"></a>此，觀自然漸合唯識觀智。唯識者，遮詮為義。遮卻雜染虛妄之<a 
name="0034a15" id="0034a15"></a>法，詮取真如佛性者。不去不來，不生不滅，不取不捨，不垢<a 
name="0034a16" id="0034a16"></a>不淨，無為無染，無有自性，清淨湛然，常名唯識觀智。故言「亦<a 
name="0034a17" id="0034a17"></a>名安心門」。此出《唯識論》。</p><a 
name="0034a18" id="0034a18"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>又言「住心門」者，由常看守心故，心即不起。無動故，心<a 
name="0035a01" id="0035a01"></a>即安住。《維摩經》云：「心常安住，無礙解脫。」故言「住心門」。</p><a 
name="0035a02" id="0035a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>言「悟心門」者，由久看，心不起動，即自心體，即與道合。心<a 
name="0035a03" id="0035a03"></a>虛空寂，無礙為道。故言「悟心門」。</p><a 
name="0035a04" id="0035a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>言「定心門」者，由常看守心故，於五欲境界，不亂不<a 
name="0035a05" id="0035a05"></a>惑。由看心故，中不令亂。故《維摩經》云：「念定總<a 
name="0035a06" id="0035a06"></a>持，辯才不斷。」故云「亦名定心門」。</p><a 
name="0035a07" id="0035a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>言「息心門」者，由常看守心故，息妄緣念，歸真<a 
name="0036a01" id="0036a01"></a>寂定，故云「亦名息心門」。</p><a 
name="0036a02" id="0036a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>言「徵心門」者，由常看守心故，即見心中心、心數<a 
name="0036a03" id="0036a03"></a>法，舉緣妄想，卻徵緣心，虛妄不可得故。故云「亦名<a 
name="0036a04" id="0036a04"></a>徵心門」。</p><a 
name="0036a05" id="0036a05"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>言「達心門」者，由常看守心故，漸達自心，本性清淨，<a 
name="0036a06" id="0036a06"></a>不為一切煩惱諸垢之所染污，猶如虛空。故云「亦名達心<a 
name="0036a07" id="0036a07"></a>門」。</p><a 
name="0036a08" id="0036a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>言「了心門」者，由常看守心故，了自己心，無障無<a 
name="0037a01" id="0037a01"></a>礙，靈通迅速，而體常住不動，畢竟寂滅，即涅槃相。故云<a 
name="0037a02" id="0037a02"></a>「亦名了心門」。</p><a 
name="0037a03" id="0037a03"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>言「知心門」者，由常看守心故，知心去來，知心生<a 
name="0037a04" id="0037a04"></a>時，知心滅時；知過去心已滅不可得，未來心未至不可得，<a 
name="0037a05" id="0037a05"></a>現在心不住不可得。由常看守心故，知心去、來、生、滅悉常<a 
name="0037a06" id="0037a06"></a>善。故云「知心門」。</p><a 
name="0037a07" id="0037a07"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>言「正心門」者，由常看守心故，不令妄念輒生，正念不<a 
name="0037a08" id="0037a08"></a>移。故云「亦名正心門」。</p><a 
name="0038a01" id="0038a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>言「察心門」者，由常看守心故，察煩惱賊，六根之<a 
name="0038a02" id="0038a02"></a>中六個頭首大賊。六賊者，眼愛美色，耳貪好聲，鼻貪好<a 
name="0038a03" id="0038a03"></a>香，舌貪美味，身貪滑細，意貪六塵。六塵者，色、聲、<a 
name="0038a04" id="0038a04"></a>香、味、觸、法也。若貪著生愛，即為所燒。故智者察六<a 
name="0038a05" id="0038a05"></a>塵賊，不令得入。譬如關令守門，端坐專察，門中有人來去，悉須<a 
name="0038a06" id="0038a06"></a>察慮，不得一人輒盜來去。察心亦爾。所云「察」者，即是覺<a 
name="0038a07" id="0038a07"></a>察之義，覺察心口善惡等念，悉無遺漏。若有善念，即隨生<a 
name="0038a08" id="0038a08"></a>滅；若有惡念，急手覺察，挫制斷除。常自覺察身心<a 
name="0039a01" id="0039a01"></a>過失，故云「察心門」。</p><a 
name="0039a02" id="0039a02"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>言「覺心門」者，由常看守心故，即覺自心體性真<a 
name="0039a03" id="0039a03"></a>如，無色無形，非常非斷，非外非內，亦非中間，離諸色相，不出<a 
name="0039a04" id="0039a04"></a>不沒，不來不去，不生不滅，不垢不淨，亦非方圓、大小、長<a 
name="0039a05" id="0039a05"></a>短，離有離無，畢竟空寂。此是自家真如心、本性清淨心，不<a 
name="0039a06" id="0039a06"></a>可以言說分別顯示。《維摩經》云：「如自觀身實相，觀佛亦<a 
name="0039a07" id="0039a07"></a>然。」心亦前際不來，後際不去，今則不住。與佛同體，與法<a 
name="0039a08" id="0039a08"></a>相應。自體無為，即合僧義，即是僧寶。動成物執，即為法<a 
name="0039a09" id="0039a09"></a>義，即是法寶。常覺，即為佛義，即是佛寶。照見心中三<a 
name="0040a01" id="0040a01"></a>寶，復覺道在身中，若心內覓，不久見佛；若著相外<a 
name="0040a02" id="0040a02"></a>求，累劫施功，去道轉遠。《華嚴經》云：自歸依佛，自歸依法、<a 
name="0040a03" id="0040a03"></a>僧，此是心中一體三寶。《維摩經》云：「若自觀者，名為正觀；<a 
name="0040a04" id="0040a04"></a>若他觀者，名為邪觀。」正觀者，謂自觀身心，得禪<a 
name="0040a05" id="0040a05"></a>定解脫道，故名正觀。邪觀者，謂身心之外，妄取境界，或<a 
name="0040a06" id="0040a06"></a>見諸佛、菩薩、青、黃、赤、白、光明等事，並是想心妄見，與道<a 
name="0041a01" id="0041a01"></a>相違。故名邪觀。又言覺心者，是覺悟之心，即是真<a 
name="0041a02" id="0041a02"></a>佛，即是菩提。《無量壽觀經》云：「是心是佛，是心作佛。」<a 
name="0041a03" id="0041a03"></a>《念佛三昧經》云：「念佛祇是念心，求心即是求佛。」所以者<a 
name="0041a04" id="0041a04"></a>何？心識無形，佛無相貌。《維摩經》云：「煩惱即是菩提。」謂覺煩<a 
name="0041a05" id="0041a05"></a>惱性空，無有處所，名為菩提。故名「覺心門」。</p><a 
name="0041a06" id="0041a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>亦名「照心門」者，慧日明朗，照自心源。不以日月<a 
name="0041a07" id="0041a07"></a>所照為明。《觀世音經》云：「慧日破諸闇。」故名「照心<a 
name="0041a08" id="0041a08"></a>門」。</p><a 
name="0042a01" id="0042a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>言「制心門」者，心為身之主，成敗之事，皆由自心，<a 
name="0042a02" id="0042a02"></a>造惡並是心作，善則天堂所攝，惡則地獄所收，不離生<a 
name="0042a03" id="0042a03"></a>死。大士發心，善惡俱斷，降伏自心，入無生正觀。《遺教經》云：「制<a 
name="0042a04" id="0042a04"></a>心一處，無事不辨。」故云「亦名制心門」。</p><a 
name="0042a05" id="0042a05"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>言「禪定解脫心門」者，禪定能絕念，定即無思。心無思念，體性<a 
name="0042a06" id="0042a06"></a>明淨，離諸結縛，名為解脫。《法華經》云：「禪定解脫等，不可思議<a 
name="0042a07" id="0042a07"></a>法。」故云「禪定解脫心門」。</p><a 
name="0042a08" id="0042a08"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>此大聖無相禪觀門，並是往古大德禪師所作，一依經論修學，非<a 
name="0042a09" id="0042a09"></a>是今時謬說。</p><a 
name="0042a10" id="0042a10"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>問曰：禪有大小以不？</p><a 
name="0043a01" id="0043a01"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>答曰：禪觀門有數種。一、大乘觀門，二、小乘觀門。小乘聲<a 
name="0043a02" id="0043a02"></a>聞觀法，即數息安般。安般即有次第：第一禪、第二禪、第三禪、<a 
name="0043a03" id="0043a03"></a>第四禪，即有所求，即有所見，即有所得。得生人天，得生非想非非<a 
name="0043a04" id="0043a04"></a>想天，受快樂，報盡還墮三塗。即聲聞觀法。若依大乘觀法，無求無<a 
name="0043a05" id="0043a05"></a>慾，無言無說，寂然無相；不生不滅，不來不去，無漏無為，湛然常住。<a 
name="0043a06" id="0043a06"></a>若依菩薩五門觀法，行時定、住時定、坐時定、臥時定、偃息時定、著<a 
name="0043a07" id="0043a07"></a>衣時定、喫食時定、語笑時定，一切時中，無有間念。若依小乘觀法，<a 
name="0043a08" id="0043a08"></a>身心俱動，即有出定，即有入定。若依菩薩觀法，無有出入，湛然一<a 
name="0043a09" id="0043a09"></a>相，無有變異，身雖動作，心常不動。《維摩經》云：「心常安住，無礙<a 
name="0043a10" id="0043a10"></a>解脫。」人身喻如何物？喻如車輪。人有十八識，車有十八輻。輪行<a 
name="0043a11" id="0043a11"></a>千里轉，車軸恒如故，鉤心常不動。若無鉤心，車即破壞，不能運載。<a 
name="0043a12" id="0043a12"></a>《頭陀經》云：「五陰以為車，無相以為牛，調御以為心，運載諸群生，<a 
name="0043a13" id="0043a13"></a>趣向般若洲。」人亦如是，亦如十八識。車輪者，人身也。車有十<a 
name="0043a14" id="0043a14"></a>八輻，人有十八識。車鉤心者，佛法也。車軸，人心也。內有六<a 
name="0043a15" id="0043a15"></a>根，外有六塵，中間有六識。六根者，眼、耳、鼻、舌、身、意，名為六<a 
name="0043a16" id="0043a16"></a>根。六塵者，色、聲、香、味、觸、法，名為六塵。六識者，眼識、耳識、<a 
name="0043a17" id="0043a17"></a>鼻識、舌識、身識、意識，名為六識。六根、六識、六塵各六，共為十<a 
name="0043a18" id="0043a18"></a>八，亦名十八界。雖行住坐臥，舉動施為，心王常不動。心王若<a 
name="0043a19" id="0043a19"></a>動，即流浪生死，不能運載法之財寶。《維摩經》云：「不著世間如蓮<a 
name="0044a01" id="0044a01"></a>花。常善入於空寂行，達諸法相無罣礙，稽首如空無所依。」蓮花<a 
name="0044a02" id="0044a02"></a>雖在淤泥中生，不被泥之所污染。行人雖在五欲煩惱泥中坐，不被<a 
name="0044a03" id="0044a03"></a>煩惱所染。</p><a 
name="0044a04" id="0044a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>偈贊云：「如蓮花，不著水，心清淨，超於彼。」彼者，彼岸。浮囊<a 
name="0044a05" id="0044a05"></a>者，心。守城者，不令賊入。賊者，六根是也。守護心，不令賊入。</p><a 
name="0044a06" id="0044a06"></a><a 
name="0044a07" id="0044a07"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>〔錄文完〕</p>
