name="0172a21" id="0172a21"></a><a pin_name="3 分別世品"></a>蘊諸行。妄起我見。由我見故復起我慢<a 
name="0172a22" id="0172a22"></a><a pin_name="3 分別世品"></a>　不知即是無明支也。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>為自受樂至無明<a 
name="0172a23" id="0172a23"></a><a pin_name="3 分別世品"></a>緣行者。此出行支體。眾生為自受樂．及非<a 
name="0172a24" id="0172a24"></a><a pin_name="3 分別世品"></a>苦樂故。作身．語．意各三種福．非福．不動業。<a 
name="0172a25" id="0172a25"></a><a pin_name="3 分別世品"></a>謂為自身受欲界當樂故造諸福業。為受<a 
name="0172a26" id="0172a26"></a><a pin_name="3 分別世品"></a>當來色界下三定樂．及四定已上非苦樂。故<a 
name="0172a27" id="0172a27"></a><a pin_name="3 分別世品"></a>造不動業。為受欲界現在世中五欲樂故。<a 
name="0172a28" id="0172a28"></a><a pin_name="3 分別世品"></a>造作殺等諸非福業。如是名為由無明為<a 
name="0172a29" id="0172a29"></a><a pin_name="3 分別世品"></a>緣能起於行。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>由引業力至通於六識者。<a 
name="0172b01" id="0172b01"></a><a pin_name="3 分別世品"></a>出識支體。既言引業。明知行支是引業非<a 
name="0172b02" id="0172b02"></a><a pin_name="3 分別世品"></a>滿業。由彼過去引業力故。六識相續流轉<a 
name="0172b03" id="0172b03"></a><a pin_name="3 分別世品"></a>如火焰行。相續不斷住彼．彼趣。此之六<a 
name="0172b04" id="0172b04"></a><a pin_name="3 分別世品"></a>識相續不斷。憑附中有馳赴所生處。結生<a 
name="0172b05" id="0172b05"></a><a pin_name="3 分別世品"></a>有身名行緣識。此識通於中．生二有。生有<a 
name="0172b06" id="0172b06"></a><a pin_name="3 分別世品"></a>雖唯意識於中有位通起六識。若作此釋。<a 
name="0172b07" id="0172b07"></a><a pin_name="3 分別世品"></a>善順契經分別識支通於六識。若依說一<a 
name="0172b08" id="0172b08"></a><a pin_name="3 分別世品"></a>切有部。識支唯生有一剎那不通中有。故唯<a 
name="0172b09" id="0172b09"></a><a pin_name="3 分別世品"></a>意識。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>識為先故至如是說故者。出名色<a 
name="0172b10" id="0172b10"></a><a pin_name="3 分別世品"></a>體。由識為先故於此趣中次有名色生具<a 
name="0172b11" id="0172b11"></a><a pin_name="3 分別世品"></a>足五蘊。展轉相續遍一期生乃至命終總名<a 
name="0172b12" id="0172b12"></a><a pin_name="3 分別世品"></a>名色。此名色位長。於此位中立六處等。即<a 
name="0172b13" id="0172b13"></a><a pin_name="3 分別世品"></a>引經證。於大因緣經．辨緣起經等。皆說名<a 
name="0172b14" id="0172b14"></a><a pin_name="3 分別世品"></a>色具足五蘊遍一期生。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>如是名色至說<a 
name="0172b15" id="0172b15"></a><a pin_name="3 分別世品"></a>為六處者。出六處體。於名色位漸有眼等<a 
name="0172b16" id="0172b16"></a><a pin_name="3 分別世品"></a>名為六處。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>次與境合至順樂等觸者。<a 
name="0172b17" id="0172b17"></a><a pin_name="3 分別世品"></a>出觸支體。既六根生次與境合便有識生。<a 
name="0172b18" id="0172b18"></a><a pin_name="3 分別世品"></a>根．境．識合有順樂受等三觸。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>依此便生<a 
name="0172b19" id="0172b19"></a><a pin_name="3 分別世品"></a>樂等三受者。出受支體。依此觸故便生苦．<a 
name="0172b20" id="0172b20"></a><a pin_name="3 分別世品"></a>樂．捨三受。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>從此三受至生無色愛者。出<a 
name="0172b21" id="0172b21"></a><a pin_name="3 分別世品"></a>愛支體。從此三受引生三愛。謂由欲界苦<a 
name="0172b22" id="0172b22"></a><a pin_name="3 分別世品"></a>逼惱故。有於樂受發生欲界愛。或有於<a 
name="0172b23" id="0172b23"></a><a pin_name="3 分別世品"></a>色界初．二．三定樂。第四非苦樂受。發生色<a 
name="0172b24" id="0172b24"></a><a pin_name="3 分別世品"></a>界愛。或有唯於無色界非苦樂受。發生無<a 
name="0172b25" id="0172b25"></a><a pin_name="3 分別世品"></a>色界愛。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>從欣受愛起欲等取者。此下出<a 
name="0172b26" id="0172b26"></a><a pin_name="3 分別世品"></a>取支體。就中。一總標。二別解。此即總標。<a 
name="0172b27" id="0172b27"></a><a pin_name="3 分別世品"></a>從前樂．捨欣受愛後。次起欲等四取。經部<a 
name="0172b28" id="0172b28"></a><a pin_name="3 分別世品"></a>四取以貪為體。與大乘同。若說一切有部<a 
name="0172b29" id="0172b29"></a><a pin_name="3 分別世品"></a>以百八煩惱為體。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>此中欲者至依之說<a 
name="0172c01" id="0172c01"></a><a pin_name="3 分別世品"></a>我故者。此下第二別解。一明四境。二出取<a 
name="0172c02" id="0172c02"></a><a pin_name="3 分別世品"></a>體。此即明四境　一欲者。謂色等五妙欲<a 
name="0172c03" id="0172c03"></a><a pin_name="3 分別世品"></a>境　二見者。謂六十二見。如梵網經廣說。<a 
name="0172c04" id="0172c04"></a><a pin_name="3 分別世品"></a>至隨眠品當列標釋　三戒禁者。戒謂戒<a 
name="0172c05" id="0172c05"></a><a pin_name="3 分別世品"></a>約。即是內道遠離惡戒。禁謂禁斷。即是<a 
name="0172c06" id="0172c06"></a><a pin_name="3 分別世品"></a>外道狗牛等禁。如諸離繫外道受持種種露<a 
name="0172c07" id="0172c07"></a><a pin_name="3 分別世品"></a>形．拔髮。遠離衣等所繫縛。故名為離繫<a 
name="0172c08" id="0172c08"></a><a pin_name="3 分別世品"></a>　婆羅門外道。受持手執杖行被烏鹿皮<a 
name="0172c09" id="0172c09"></a><a pin_name="3 分別世品"></a>　播輸&#x9262;多外道此云牛主。主謂天主。摩醯首<a 
name="0172c10" id="0172c10"></a><a pin_name="3 分別世品"></a>羅天。乘牛而行故名牛主。此外道學彼天<a 
name="0172c11" id="0172c11"></a><a pin_name="3 分別世品"></a>法。從彼為名故名牛主。此外道受持頂上<a 
name="0172c12" id="0172c12"></a><a pin_name="3 分別世品"></a>持一髻子身體塗灰　般利伐羅勺迦外<a 
name="0172c13" id="0172c13"></a><a pin_name="3 分別世品"></a>道此云遍出。即顯出家義。是出家外道。受<a 
name="0172c14" id="0172c14"></a><a pin_name="3 分別世品"></a>持執三杖行。擬安衣服．瓶．&#x9262;等物。并剪鬢<a 
name="0172c15" id="0172c15"></a><a pin_name="3 分別世品"></a>髮無義苦行。等者等取諸餘外道&#x7ADD;名為禁<a 
name="0172c16" id="0172c16"></a><a pin_name="3 分別世品"></a>　四我語者。謂三界內身依之說我故故名<a 
name="0172c17" id="0172c17"></a><a pin_name="3 分別世品"></a>我語。不同說一切有部但約上二界依之<a 
name="0172c18" id="0172c18"></a><a pin_name="3 分別世品"></a>說我。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>有餘師說至名為我語者。經部異<a 
name="0172c19" id="0172c19"></a><a pin_name="3 分別世品"></a>師。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>云何此二獨名我語者。問。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>由此<a 
name="0172c20" id="0172c20"></a><a pin_name="3 分別世品"></a>二種至我及我所者。答。由此二種說有我<a 
name="0172c21" id="0172c21"></a><a pin_name="3 分別世品"></a>故名之為我。我非有故但有語言故名我<a 
name="0172c22" id="0172c22"></a><a pin_name="3 分別世品"></a>語。經言異生隨假言說起我執。於中實無<a 
name="0172c23" id="0172c23"></a><a pin_name="3 分別世品"></a>我及我所。明知我體非有。但有語言。</p><a 
name="0172c24" id="0172c24"></a><a pin_name="3 分別世品"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>於前四種至所謂欲貪者。此即第二正出取<a 
name="0172c25" id="0172c25"></a><a pin_name="3 分別世品"></a>體。前說四種是所取境。今正出彼能緣取<a 
name="0172c26" id="0172c26"></a><a pin_name="3 分別世品"></a>體。所謂欲貪。即貪名欲。貪通於三界。非<a 
name="0172c27" id="0172c27"></a><a pin_name="3 分別世品"></a>是欲界貪也。經言欲貪名取。明知不通餘<a 
name="0172c28" id="0172c28"></a><a pin_name="3 分別世品"></a>法。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>由取為緣至說名為有者。出有支體<a 
name="0172c29" id="0172c29"></a><a pin_name="3 分別世品"></a>及引經證。能招後有果業說名為有。</p><a 
name="0173a01" id="0173a01"></a><a pin_name="3 分別世品"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>有為緣故至說名為生者。出生支體。有為緣<a 
name="0173a02" id="0173a02"></a><a pin_name="3 分別世品"></a>故。識相續流轉趣未來生。如前識支所說道<a 
name="0173a03" id="0173a03"></a><a pin_name="3 分別世品"></a>理。憑附中有馳赴所生。結生有身。具足<a 
name="0173a04" id="0173a04"></a><a pin_name="3 分別世品"></a>五蘊說名為生。大分雖同識支非無差別。<a 
name="0173a05" id="0173a05"></a><a pin_name="3 分別世品"></a>識名是狹唯說六識。生名是寬故通五蘊<a 
name="0173a06" id="0173a06"></a><a pin_name="3 分別世品"></a>　又解此生支如前識．名色支。既言識相續<a 
name="0173a07" id="0173a07"></a><a pin_name="3 分別世品"></a>流同前識支復言具足五蘊同前名色此<a 
name="0173a08" id="0173a08"></a><a pin_name="3 分別世品"></a>言生者。總顯未來生始從中有初念。乃至<a 
name="0173a09" id="0173a09"></a><a pin_name="3 分別世品"></a>命終一期生也。於此生位建立老死　又<a 
name="0173a10" id="0173a10"></a><a pin_name="3 分別世品"></a>解老死前言生。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>以生為緣至廣說如經<a 
name="0173a11" id="0173a11"></a><a pin_name="3 分別世品"></a>者。出老死體。於初生後即立老死　又解<a 
name="0173a12" id="0173a12"></a><a pin_name="3 分別世品"></a>於生支位中建立老死。其老死相髮白面皺<a 
name="0173a13" id="0173a13"></a><a pin_name="3 分別世品"></a>等。種種差別廣說如經　又解至髮白等已<a 
name="0173a14" id="0173a14"></a><a pin_name="3 分別世品"></a>去方名老。後名死。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>如是純言至諸苦<a 
name="0173a15" id="0173a15"></a><a pin_name="3 分別世品"></a>蘊生者。上來經部出十二支體。復釋經文<a 
name="0173a16" id="0173a16"></a><a pin_name="3 分別世品"></a>如是純大苦蘊集。以經言無明緣行。乃至<a 
name="0173a17" id="0173a17"></a><a pin_name="3 分別世品"></a>生緣老死。如是純大苦蘊集。所以便釋。</p><a 
name="0173a18" id="0173a18"></a><a pin_name="3 分別世品"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>毘婆沙宗如前已說者。上來經部解十二緣<a 
name="0173a19" id="0173a19"></a><a pin_name="3 分別世品"></a>起　若毘婆沙宗。解十二緣起如前已說。</p><a 
