name="0537a25" id="0537a25"></a><a pin_name="29 歎佛品"></a>曰、所言柔軟初無麤獷。六曰、其聲悲和猶<a 
name="0537a26" id="0537a26"></a><a pin_name="29 歎佛品"></a>如哀鸞。七曰、身心隨時音聲如梵，會中人<a 
name="0537a27" id="0537a27"></a><a pin_name="29 歎佛品"></a>聞莫不咨受。八曰、音響如佛可眾生心。<a 
name="0537a28" id="0537a28"></a><a pin_name="29 歎佛品"></a>是為八。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>「若有書是《普曜經典》，著於竹帛，<a 
name="0537a29" id="0537a29"></a><a pin_name="29 歎佛品"></a>不&#x6061;惜經心不懷&#x59AC;，眾人嗟歎三十四<a 
name="0537b01" id="0537b01"></a><a pin_name="29 歎佛品"></a>行，名德流布；若得學是經典，得八大藏。何<a 
name="0537b02" id="0537b02"></a><a pin_name="29 歎佛品"></a>謂為八：一、得意藏，未曾忘捨。二曰、所<a 
name="0537b03" id="0537b03"></a><a pin_name="29 歎佛品"></a>得心藏，無所不解分別經法。三、得往來<a 
name="0537b04" id="0537b04"></a><a pin_name="29 歎佛品"></a>藏，普解一切諸佛經法。四、得總持藏，一切<a 
name="0537b05" id="0537b05"></a><a pin_name="29 歎佛品"></a>所聞皆能識念。五曰、得辯才藏，為諸眾<a 
name="0537b06" id="0537b06"></a><a pin_name="29 歎佛品"></a>生班宣經典皆歡喜受。六曰、得法藏，將<a 
name="0537b07" id="0537b07"></a><a pin_name="29 歎佛品"></a>護正法。七曰、得隨意法藏，未曾斷絕三<a 
name="0537b08" id="0537b08"></a><a pin_name="29 歎佛品"></a>寶法教。八曰、得奉行法藏，則&#x8F19;逮得無所<a 
name="0537b09" id="0537b09"></a><a pin_name="29 歎佛品"></a>從生之忍。是為八大法藏。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>「若有受持是<a 
name="0537b10" id="0537b10"></a><a pin_name="29 歎佛品"></a>《普曜經典》諷誦受持，得具八業。何謂為八：<a 
name="0537b11" id="0537b11"></a><a pin_name="29 歎佛品"></a>一曰、施業，無慳&#x59AC;心。二曰、戒業，具足諸願。<a 
name="0537b12" id="0537b12"></a><a pin_name="29 歎佛品"></a>三曰、聞業，合集智慧至阿惟顏。四曰、寂業，<a 
name="0537b13" id="0537b13"></a><a pin_name="29 歎佛品"></a>懃於一切定意正受。五曰、見業，悉能具&#x89A9;三<a 
name="0537b14" id="0537b14"></a><a pin_name="29 歎佛品"></a>達之智。六曰、福業，具於相好訓誨佛國。七<a 
name="0537b15" id="0537b15"></a><a pin_name="29 歎佛品"></a>曰、慧業，為眾說法應病授藥。八曰、大哀業，<a 
name="0537b16" id="0537b16"></a><a pin_name="29 歎佛品"></a>為於十方殖眾德本而無懈惓。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>「受持是<a 
name="0537b17" id="0537b17"></a><a pin_name="29 歎佛品"></a>《普曜法》，心自念言：『使一切眾生皆逮是法。』<a 
name="0537b18" id="0537b18"></a><a pin_name="29 歎佛品"></a>以是德本復得八大福。何謂為八：一曰、轉<a 
name="0537b19" id="0537b19"></a><a pin_name="29 歎佛品"></a>輪聖王成大福德，&#x89A9;見無極建立篤信。二<a 
name="0537b20" id="0537b20"></a><a pin_name="29 歎佛品"></a>曰、得作四天王。三曰、得作帝釋。四曰、得<a 
name="0537b21" id="0537b21"></a><a pin_name="29 歎佛品"></a>作炎天王。五曰、得作兜術天子。六曰、得<a 
name="0537b22" id="0537b22"></a><a pin_name="29 歎佛品"></a>作無憍樂天。七曰、得作化自在天。八曰、得<a 
name="0537b23" id="0537b23"></a><a pin_name="29 歎佛品"></a>作如來，斷諸不善法具足諸善本。是為<a 
name="0537b24" id="0537b24"></a><a pin_name="29 歎佛品"></a>八。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>「若有受此《普曜經》者，若至心聽耳貫係<a 
name="0537b25" id="0537b25"></a><a pin_name="29 歎佛品"></a>意，得八清淨心。何謂為八：一曰、常行慈<a 
name="0537b26" id="0537b26"></a><a pin_name="29 歎佛品"></a>心消除瞋恚。二曰、常行愍哀除眾患害。三<a 
name="0537b27" id="0537b27"></a><a pin_name="29 歎佛品"></a>曰、常行喜悅除諸不樂。四曰、常行於護<a 
name="0537b28" id="0537b28"></a><a pin_name="29 歎佛品"></a>除諸結著。五曰、修四禪行在於欲界而得<a 
name="0537b29" id="0537b29"></a><a pin_name="29 歎佛品"></a>自在。六曰、行四無色定而得由己。七曰、<a 
name="0537c01" id="0537c01"></a><a pin_name="29 歎佛品"></a>得五神通遊諸佛土，除諸罣礙眾蓋之患。<a 
name="0537c02" id="0537c02"></a><a pin_name="29 歎佛品"></a>八曰、逮得勇伏定意獨步三界。是為八。」</p><a 
name="0537c03" id="0537c03"></a><a pin_name="29 歎佛品"></a><a pin_name=""></a><a pin_name="30 囑累品"></a><p style="margin-left: 2em"><span class="headname">囑累品第三十</span></p><a 
name="0537c04" id="0537c04"></a><a pin_name="30 囑累品"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>爾時世尊告賢者大迦葉、賢者阿難、彌勒菩<a 
name="0537c05" id="0537c05"></a><a pin_name="30 囑累品"></a>薩：「我從無數劫遵習是法，乃成無上正真<a 
name="0537c06" id="0537c06"></a><a pin_name="30 囑累品"></a>之道。囑累汝等，以手相付，受持諷誦廣為<a 
name="0537c07" id="0537c07"></a><a pin_name="30 囑累品"></a>人說。」於時世尊欲使此法普悉周布十方，<a 
name="0537c08" id="0537c08"></a><a pin_name="30 囑累品"></a>說是偈言：</p><a 
name="0537c09" id="0537c09"></a><a pin_name="30 囑累品"></a><p><span class="lg">「佛目所&#x89A9;眾生類，　　皆得羅漢如身子；<a 
name="0537c10" id="0537c10"></a><a pin_name="30 囑累品"></a><br>　若有供養億千劫，　　乃復前進如恒沙。<a 
name="0537c11" id="0537c11"></a><a pin_name="30 囑累品"></a><br>　加復供養辟支佛，　　若有一日奉斯經；<a 
