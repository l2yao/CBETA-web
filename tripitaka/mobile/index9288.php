name="0291b15" id="0291b15"></a>般。著甚死急。道。&#x9262;裏有飯。桶裏有水。佛果<a 
name="0291b16" id="0291b16"></a>道。漱口三年。正為汝輩。雪竇道。多口阿師難<a 
name="0291b17" id="0291b17"></a>下嘴。元來小膽爾看。天童擘破面皮。頌云。</p><a 
name="0291b18" id="0291b18"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>&#x9262;裏飯桶裏水<span class="note">(&#x6900;盛杓戽)</span>開口見膽求知已<span class="note">(只為分明極翻<a 
name="0291b19" id="0291b19"></a>令得所遲)</span>擬思便落二三機<span class="note">(天童第四)</span>對面忽成千萬里<a 
name="0291b20" id="0291b20"></a><span class="note">(是必早&#x5EFB;程)</span>韶陽師較些子<span class="note">(未敢相保)</span>斷金之義兮誰與<a 
name="0291b21" id="0291b21"></a>相同<span class="note">(心不負人)</span>匪石之心兮獨能如此<span class="note">(面無慚色)</span>。</p><a 
name="0291b22" id="0291b22"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>師云。僧問忠國師。如何是佛法大意。師云。文<a 
name="0291b23" id="0291b23"></a>殊堂裏萬菩薩。僧云。學人不會。師云。大悲千<a 
name="0291b24" id="0291b24"></a>手眼。佛鑑頌。時年蔬菜賤。滿地蘿葡頭。一文<a 
name="0291b25" id="0291b25"></a>買一箇。得者飽齁齁。這裏便見天童與雲門<a 
name="0291b26" id="0291b26"></a>出一隻手。提折&#x811A;鐺子也。周易繫辭。君子之<a 
name="0291b27" id="0291b27"></a>道。或出或處。或默或語。二人同心。其利斷金。<a 
name="0291b28" id="0291b28"></a>同心之言。其臭如蘭。注云。金堅剛之物。而能<a 
name="0291b29" id="0291b29"></a>斷之。言利之甚也。毛詩邶柏舟云。我心匪石。<a 
name="0291c01" id="0291c01"></a>不可轉也。我心匪席。不可卷也。注石雖堅尚<a 
name="0291c02" id="0291c02"></a>可轉。席雖平尚可卷。言已心志堅平過于石<a 
name="0291c03" id="0291c03"></a>席。且道。忠國師。佛鑑。雲門。天童。恁麼氣急<a 
name="0291c04" id="0291c04"></a>作甚。赤心片片知人少。覿面堆堆&#x89A9;者稀。</p><a 
name="0291c05" id="0291c05"></a><p style="margin-left: 2em"><span class="headname">第百則瑯琊山河</span></p><a 
name="0291c06" id="0291c06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>示眾云。一言可以興邦。一言可以喪邦。此藥<a 
name="0291c07" id="0291c07"></a>亦能殺人。此藥亦能活人。仁者見之謂之仁。<a 
name="0291c08" id="0291c08"></a>智者見之謂之智。且道。利害在甚麼處。</p><a 
name="0291c09" id="0291c09"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>舉。僧問瑯琊覺和尚。清淨本然。云何忽生<a 
name="0291c10" id="0291c10"></a>山河大地<span class="note">(迷時三界有)</span>覺云清淨本然云何忽生<a 
name="0291c11" id="0291c11"></a>山河大地<span class="note">(悟後十方空)</span>。</p><a 
name="0291c12" id="0291c12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>師云。汾陽無德昭禪師。北地苦寒。因罷夜參。<a 
name="0291c13" id="0291c13"></a>梵僧乘雲而至勸。不可失時。此眾雖不多。六<a 
name="0291c14" id="0291c14"></a>人大器。道廕人天。陽明日上堂云。胡僧金錫<a 
name="0291c15" id="0291c15"></a>光。為法到汾陽。六人成大器。勸請為敷揚。時<a 
name="0291c16" id="0291c16"></a>大愚芝。慈明圓。瑯琊覺。法華舉。天勝泰。石<a 
name="0291c17" id="0291c17"></a>霜永等。皆在席下。滁州瑯琊山。開化廣照禪<a 
name="0291c18" id="0291c18"></a>師。諱慧覺。西洛人。父為衡陽太守。捐館。扶<a 
name="0291c19" id="0291c19"></a>襯歸洛。過澧州。登藥山古剎瞻禮。觀其游處。<a 
name="0291c20" id="0291c20"></a>宛若舊居。緣此出家。得法于汾陽。應緣滁水。<a 
name="0291c21" id="0291c21"></a>與雪竇明覺。同時唱道。天下指為二甘露門。<a 
name="0291c22" id="0291c22"></a>逮今淮南遺化如昔。湖南祇林和尚。纔見僧<a 
name="0291c23" id="0291c23"></a>來便云。魔來魔來。以木劍揮之。潛入方丈。如<a 
name="0291c24" id="0291c24"></a>是十二年。後置劍無言。有僧問。十二年前為<a 
name="0291c25" id="0291c25"></a>甚麼降魔。林云。賊不打貧兒家。僧云。十二年<a 
name="0291c26" id="0291c26"></a>後為甚麼不降魔。林云。賊不打貧兒家。此名<a 
name="0291c27" id="0291c27"></a>一劍下分身之意。首楞嚴第四。富樓那問。若<a 
name="0291c28" id="0291c28"></a>復世間一切根塵。陰處界等。皆如來藏。清淨<a 
name="0291c29" id="0291c29"></a>本然。云何忽生山河大地諸有為相。次第遷<a 
name="0292a01" id="0292a01"></a>流。終而復始。說者云。若解則已知。覺體本<a 
name="0292a02" id="0292a02"></a>妙。無明本空。山河大地。如空花相。若惑則能<a 
name="0292a03" id="0292a03"></a>所妄分。強覺俄起。三細為世。四輪成界。瑯琊<a 
name="0292a04" id="0292a04"></a>云。我則不然。清淨本然。云何忽生山河大地。<a 
name="0292a05" id="0292a05"></a>此喚騎賊馬赶賊。奪賊槍殺賊。薦福信云。先<a 
name="0292a06" id="0292a06"></a>行不到。末後太過。萬松道。徐六檐板。各見一<a 
name="0292a07" id="0292a07"></a>邊。要除見滲漏。須見天童始得。頌云。</p><a 
name="0292a08" id="0292a08"></a><p>　<span class="lg">見有不有<span class="note">(一般麵草)</span>　翻手覆手<span class="note">(由人故造)</span>　瑯琊山裏人<span class="note">(叉手<a 
name="0292a09" id="0292a09"></a>云慧覺)</span><br>　　不落瞿曇後<span class="note">(一語傷人千刀攪腹)</span></span></p><a 
name="0292a10" id="0292a10"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>師云。見有不有。其有自朽。見怪不怪。其怪自<a 
name="0292a11" id="0292a11"></a>壞。摩訶衍論。十四祖龍樹祖師造也。乃云一<a 
name="0292a12" id="0292a12"></a>切諸法。一切因緣故應有。一切諸法。一切因<a 
name="0292a13" id="0292a13"></a>緣故不應有。此翻手覆手也。瑯琊上堂云。見<a 
name="0292a14" id="0292a14"></a>聞覺知。俱為生死之因。見聞覺知。俱為解脫<a 
name="0292a15" id="0292a15"></a>之本。譬如師子返擲。南北東西。且無定止。汝<a 
name="0292a16" id="0292a16"></a>等諸人若也不會。且莫辜負釋迦老子。吽。此<a 
name="0292a17" id="0292a17"></a>所以不落瞿曇後也。瞿曇梵語。具云喬答摩。<a 
name="0292a18" id="0292a18"></a>此云地勝。謂除天外在地人中。最殊勝故。方<a 
name="0292a19" id="0292a19"></a>今後五百歲。去聖時遙。人多懈怠。如何得不<a 
name="0292a20" id="0292a20"></a>落後去。裂破古今。</p><a 
