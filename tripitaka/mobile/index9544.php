name="0190b14" id="0190b14"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>收。山僧於法無罣礙。祇欠諸人些禪債。今朝一一為<a 
name="0190b15" id="0190b15"></a>酬償。要者來支無後悔。以拂子打一圓相曰。上不似<a 
name="0190b16" id="0190b16"></a>下。下&#x5374;似上。上下打合。總不似像。會麼。將謂有許多<a 
name="0190b17" id="0190b17"></a>債主。元來&#x5374;無。咦。孤峰夜半猿啼切。曒月&#x643A;星拱北<a 
name="0190b18" id="0190b18"></a>辰。</p><a 
name="0190b19" id="0190b19"></a><p style="text-indent: 0em; margin-left: 0em"><span class="headname">　　　上堂</span></p><a 
name="0190b20" id="0190b20"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>素號無明實不明。憑條拄杖<span class="gaiji">&#x9A10;<!--gaiji,驗,1[騇-古+(一/心)],2&#x9A10;,3--></span>&#x8E08;親。佛祖撞來只是<a 
name="0190b21" id="0190b21"></a>打。看他那箇解翻身。直須藏身處沒&#x8E2A;跡。沒&#x8E2A;跡處<a 
name="0190b22" id="0190b22"></a>莫藏身始得。作麼生是藏身處沒蹤跡。會麼。拾得撫<a 
name="0190b23" id="0190b23"></a>掌笑呵呵。寒山忘&#x5374;來時道。珍重。</p><a 
name="0190b24" id="0190b24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="headname">　　　上堂</span></p><a 
name="0190c01" id="0190c01"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>一切佛祖異口同音。共唱宗乘。各宜諦聽。有能聞者<a 
name="0190c02" id="0190c02"></a>出眾舉似。老僧麈尾當堂分付。不然向第二門頭聽<a 
name="0190c03" id="0190c03"></a>取一頌。&#x8657;空不自行。倒使崑崙走。四處無可投。藏身<a 
name="0190c04" id="0190c04"></a>於北斗。驚起烏兔飛。乾坤兩眉皺。野干盡潛蹤。獅子<a 
name="0190c05" id="0190c05"></a>無時吼。凡聖齊教立下風。彌勒當來續其後。珍重。</p><a 
name="0190c06" id="0190c06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="headname">　　　上堂</span></p><a 
name="0190c07" id="0190c07"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>曹溪一派來。雲水兩行集。畢竟作麼生。大事都了畢。<a 
name="0190c08" id="0190c08"></a>如其未透。但看黃龍心參雲峰悅三年無所得。因辭<a 
name="0190c09" id="0190c09"></a>去。悅曰汝往依黃蘗心。至黃蘗四年不大發明。又辭<a 
name="0190c10" id="0190c10"></a>再上雲峰。會悅謝世仍止石霜。因閱傳燈至僧問多<a 
name="0190c11" id="0190c11"></a>福。如何是多福一叢竹。福曰一莖兩莖斜。僧曰不會。<a 
name="0190c12" id="0190c12"></a>福曰三莖四莖曲。於此開悟。徹見二師用處。徑回黃<a 
name="0190c13" id="0190c13"></a>蘗。方展具。蘗曰子<span class="corr" title='[巳&gt;已]'>已</span>入吾室矣。心踴躍曰。大事本來<a 
name="0190c14" id="0190c14"></a>如是。和尚何得教人看話頭百計搜尋。蘗曰。若不教<a 
name="0190c15" id="0190c15"></a>爾如此究尋到無心處自見自肯。即吾埋沒汝也。師<a 
name="0190c16" id="0190c16"></a>曰。大眾。且道作麼生是大事了畢。良久云。奮然揣出<a 
name="0190c17" id="0190c17"></a>&#x8657;空骨。驚起須彌折斷腰。</p><a 
name="0190c18" id="0190c18"></a><p style="text-indent: 0em; margin-left: 0em"><span class="headname">　　　上堂</span></p><a 
name="0190c19" id="0190c19"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>師彈指一下云。大眾作麼生會。眾無語。師曰。不會出<a 
name="0190c20" id="0190c20"></a>世師。空勞一彈指。最無分曉句。真是難接嘴。倚天長<a 
name="0190c21" id="0190c21"></a>&#x5292;逼人寒。不是其人徒側耳。方知一顆摩尼珠。解用<a 
name="0190c22" id="0190c22"></a>須是寒山子。下座。</p><a 
name="0190c23" id="0190c23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="headname">　　　上堂</span></p><a 
name="0190c24" id="0190c24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>昔外道問世尊曰。昨日說何法。曰說定法。外道曰今<a 
name="0191a01" id="0191a01"></a>日說何法。尊曰不定法。外道曰。昨日說定法。今日如<a 
name="0191a02" id="0191a02"></a>何說不定法。尊曰。昨日定。今日不定。師曰。外道相似<a 
name="0191a03" id="0191a03"></a>瞎聾。喚鐘作甕。世尊潦倒正似連雲推月下西江。大<a 
name="0191a04" id="0191a04"></a>眾作麼生。復曰。昨日說定。今日不定。相如有病而無<a 
name="0191a05" id="0191a05"></a>症。勾惹兒孫奉重多。爭似龐公仆地。靈照女相扶急<a 
name="0191a06" id="0191a06"></a>急如律令。若是老僧。聊與拄杖。何故。知恩者少。負恩<a 
name="0191a07" id="0191a07"></a>者多。</p><a 
name="0191a08" id="0191a08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="headname">　　　上堂</span></p><a 
name="0191a09" id="0191a09"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>祖師云。凡所見色皆是見心。心不自心因色故有。知<a 
name="0191a10" id="0191a10"></a>色空故心即不生。如是則隨時著衣喫飯任運過時。<a 
name="0191a11" id="0191a11"></a>更有何事。然雖如是。亦要轉身始得。不然依舊似朱<a 
name="0191a12" id="0191a12"></a>頂守空池去也。差別門庭作麼生過。當知天上有地。<a 
name="0191a13" id="0191a13"></a>地下有天。憎中有愛。愛中有憎。不管東三西四。要會<a 
name="0191a14" id="0191a14"></a>失後忘前。老僧恁麼舉似。大眾會麼。良久云。拈起&#x8657;<a 
name="0191a15" id="0191a15"></a>空尋本有。抒乾滄海見真元。機關之上觀師利。火燄<a 
name="0191a16" id="0191a16"></a>之中看普賢。參。</p><a 
name="0191a17" id="0191a17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="headname">　　　上堂</span></p><a 
name="0191a18" id="0191a18"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>舉二僧參見雪峰公案畢曰。雪峰峰門易入。巖頭巖<a 
name="0191a19" id="0191a19"></a>上希登。壁立千仞難近傍。誰能同死及同生。末後句。<a 
name="0191a20" id="0191a20"></a>見如盲。祇這是大火坑。穿雲士也。到此須當審細。<span class="corr" title='[擭&gt;攫]'><span class="gaiji">攫<!--gaiji,攫,1[爪*隻],2&#x3E15;,3--></span></span><a 
name="0191a21" id="0191a21"></a>浪客也。彼見決。要知先。紅爐自有真消息。一任金&#x936E;<a 
name="0191a22" id="0191a22"></a>是與非。下座。</p><a 
name="0191a23" id="0191a23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="headname">　　　上堂</span></p><a 
name="0191a24" id="0191a24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>昨日有今朝。今朝無昨日。三世一切佛。不會波羅蜜。<a 
name="0191b01" id="0191b01"></a>文殊思不來。普賢想不及。何況參學人。安能明得失。<a 
name="0191b02" id="0191b02"></a>八臂大那吒。高聲連&#x544C;屈。且道屈個甚麼。良久云。初<a 
name="0191b03" id="0191b03"></a>三十一。中九下七。</p><a 
name="0191b04" id="0191b04"></a><p style="margin-left: 3em"><span class="headname">解制上堂</span></p><a 
name="0191b05" id="0191b05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>三月安居。九旬禁足。不求佛法僧。豈為人天福。說箇<a 
name="0191b06" id="0191b06"></a>願一切眾生同證菩提。大似父死呼公哭。畢竟作麼<a 
name="0191b07" id="0191b07"></a>生。除是波斯能嚙鏃。珍重。</p><a 
name="0191b08" id="0191b08"></a><p style="margin-left: 3em"><span class="headname">小參</span></p><a 
name="0191b09" id="0191b09"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>昨夜三更睡不著。翻身<span class="gaiji">爬<!--gaiji,爬,1[爬-爪+瓜],2,3--></span>得烏龜&#x811A;。世間是箇起事<a 
name="0191b10" id="0191b10"></a>精。一出伸兮一出縮。捉來拷打問原因。罪坐靈山黃<a 
name="0191b11" id="0191b11"></a>面人。無端把朵曇花弄。坑累東西諸有情。即今還有<a 
name="0191b12" id="0191b12"></a>不被坑累者麼。試道看。僧曰和尚請尊重。師曰這師<a 
name="0191b13" id="0191b13"></a>僧&#x5374;被渠坑累。復擊禪床下座。</p><a 
name="0191b14" id="0191b14"></a><p style="text-indent: 0em; margin-left: 0em"><span class="headname">　　　小參</span></p><a 
name="0191b15" id="0191b15"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>因齋慶讚宗風舉。吹散迷雲千萬里。本自無生無不<a 
name="0191b16" id="0191b16"></a>生。長鯨吞&#x5374;須彌嘴。三十三天自失驚。無端&#x5374;把脩<a 
name="0191b17" id="0191b17"></a>羅叱。惹發脩羅起戰爭。戰輸藏入藕絲裏。文殊舉目<a 
name="0191b18" id="0191b18"></a>兩眉攢。普賢合掌生歡喜。未得勝者免憂。<span class="corr" title='[巳&gt;已]'>已</span>得勝者<a 
name="0191b19" id="0191b19"></a>休喜。返覆看渠渠是誰。箇中何我復何你。噫。更須知<a 
name="0191b20" id="0191b20"></a>有安樂一句。作麼生道。海浪從教去潑天。&#x8657;空送倒<a 
name="0191b21" id="0191b21"></a>休扶起。</p><a 
name="0191b22" id="0191b22"></a><p style="margin-left: 3em"><span class="headname">師到斗峰請陞座</span></p><a 
name="0191b23" id="0191b23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>師良久曰。恁麼是否。若是。請尊重於斯。不會。老僧再<a 
name="0191b24" id="0191b24"></a>加榼<span class="gaiji">[木*(天/韭)]<!--gaiji,,1[木*(天/韭)],2&#x23702;,3--></span>。若論此事。本自分明。從上諸聖曲垂方便。所<a 
name="0191c01" id="0191c01"></a>以云。天是天。地是地。山是山。水是水。男是男。女是女。<a 
name="0191c02" id="0191c02"></a>僧是僧。俗是俗。又云。天非天。地非地。山非山。水非水。<a 
name="0191c03" id="0191c03"></a>男非男。女非女。僧非僧俗非俗。又云。即天即地。即山<a 
name="0191c04" id="0191c04"></a>即水。即男即女。即僧即俗。又云。非天非地。非山非水。<a 
name="0191c05" id="0191c05"></a>非男非女。非僧非俗。恁麼也得。不恁麼也得。恁麼不<a 
name="0191c06" id="0191c06"></a>恁麼總皆得。雖然如是。更須知有不落此限一句。作<a 
name="0191c07" id="0191c07"></a>麼生道。噫。五宗話到無生處。直是須彌暗點頭。久立<a 
name="0191c08" id="0191c08"></a>珍重。</p><a 
name="0191c09" id="0191c09"></a><p style="margin-left: 3em"><span class="headname">師到書林僊亭庵請陞座</span></p><a 
name="0191c10" id="0191c10"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>拈香竟云。今日十方三世諸佛向香雲&#x8462;上放大光<a 
name="0191c11" id="0191c11"></a>明。助宣第一義諦。俾一切眾生共證薩婆若海。大眾<a 
name="0191c12" id="0191c12"></a>見否。若云見。&#x8657;空落地成幾片。更聞否。若云聞。未入<a 
name="0191c13" id="0191c13"></a>維摩不二門。豈不知此事如擊石火似閃電光。擬議<a 
name="0191c14" id="0191c14"></a>將來關山萬里。更有緣契者。如九龍聚會五虎攢羊。<a 
name="0191c15" id="0191c15"></a>五虎攢羊意在乎食。九龍聚會意在乎珠。設有能于<a 
name="0191c16" id="0191c16"></a>驪<span class="corr" title='[能&gt;龍]'>龍</span>頷下取珠。猛虎口中奪食。於此門中即得一橛。<a 
name="0191c17" id="0191c17"></a>且道那一橛作麼生。噫。超方須是英靈子。敵聖還他<a 
name="0191c18" id="0191c18"></a>師子兒。下座。</p><a 
name="0191c19" id="0191c19"></a><a 
