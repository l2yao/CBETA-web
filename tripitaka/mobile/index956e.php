<a 
name="0169b01" id="0169b01"></a><p style="margin-left: 2em"><span class="juannum">No. 1642</span></p><a 
name="0169b02" id="0169b02"></a><p><span class="juanname">金剛針論</span></p><a 
name="0169b03" id="0169b03"></a><a 
name="0169b04" id="0169b04"></a><p style="margin-left: 4em"><span class="byline">法稱菩薩造</span></p><a 
name="0169b05" id="0169b05"></a><p style="margin-left: 4em"><span class="byline">西天譯經三藏朝散大夫<a 
name="0169b06" id="0169b06"></a>試鴻臚少卿傳教大師臣<a 
name="0169b07" id="0169b07"></a>法天奉　詔譯</span></p><a 
name="0169b08" id="0169b08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>如婆羅門言。眾典之內。四圍陀正。又於此<a 
name="0169b09" id="0169b09"></a>中念為其正。又此念中能所詮正。又於此中<a 
name="0169b10" id="0169b10"></a>能詮為正。唯此最上無法過此。世若無此業<a 
name="0169b11" id="0169b11"></a>云何作。由此能詮。若愛若恚從此而生。如一<a 
name="0169b12" id="0169b12"></a>切姓婆羅門上。今此言詮亦復如是。</p><a 
name="0169b13" id="0169b13"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>此理不然。所以者何。彼婆羅門何姓何命。復<a 
name="0169b14" id="0169b14"></a>云何知。行業云何。如何得此婆羅門名。又此<a 
name="0169b15" id="0169b15"></a>圍陀。云何稱正。帝釋元因。云何傍生傍生云<a 
name="0169b16" id="0169b16"></a>何。生於月天。日天元因。復生傍生。風天火<a 
name="0169b17" id="0169b17"></a>天水天元因。展轉往來。云何如是。</p><a 
name="0169b18" id="0169b18"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又彼妄執。天中死已復生天中。人中死已復<a 
name="0169b19" id="0169b19"></a>生人中。傍生亦然。四圍陀內作此說者。皆非<a 
name="0169b20" id="0169b20"></a>正理。此命是何。何因名命。婆羅門等亦復如<a 
name="0169b21" id="0169b21"></a>是。</p><a 
name="0169b22" id="0169b22"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又汝外道婆羅門言。正典所說。婆囉帝山產<a 
name="0169b23" id="0169b23"></a>七禽獸。那娑囉陀及別鹿迦陵惹哩山。彼<a 
name="0169b24" id="0169b24"></a>山所有鸚鵡鷺<span class="gaiji">[斯/鳥]<!--gaiji,,1[斯/鳥],2&#x2A197;,3--></span>鵝鹿之類。生在人中俱嚕<a 
name="0169b25" id="0169b25"></a>乞曬<span class="note">(二合此云福地)</span>。從彼死已生在婆羅門中。解四圍<a 
name="0169b26" id="0169b26"></a>論。</p><a 
name="0169b27" id="0169b27"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>此等禽獸鹿鵝鴛鴦。出生人中。彼獸之命。是<a 
name="0169b28" id="0169b28"></a>婆羅門非婆羅門。所以者何。彼命若是而非<a 
name="0169b29" id="0169b29"></a>禽獸。彼命若非彼生婆羅。此言非理。</p><a 
name="0169c01" id="0169c01"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>婆羅門執。四圍陀論是萬法本。亦號真如。非<a 
name="0169c02" id="0169c02"></a>於餘姓而許受食。</p><a 
name="0169c03" id="0169c03"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>於首陀處數數受利。正違自宗。何名淨行。由<a 
name="0169c04" id="0169c04"></a>此亦非真婆羅門。</p><a 
name="0169c05" id="0169c05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又四圍陀婆羅門法。妄執正命及於正法婆<a 
name="0169c06" id="0169c06"></a>羅門種。亦復非理。云何正法。種姓間雜。何名<a 
name="0169c07" id="0169c07"></a>最上。所以者何。非最間雜。其事云何。</p><a 
name="0169c08" id="0169c08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>且如父名那洛乞叉。其子乃名兵&#x8A90;羅仙。又<a 
name="0169c09" id="0169c09"></a>如父名阿&#x8A90;悉帝。其子亦名阿&#x8A90;悉帝<span class="note">(二合)</span>。又<a 
name="0169c10" id="0169c10"></a>如父名布沙野<span class="note">(二合)</span>左。其子乃名嬌尸迦。又如<a 
name="0169c11" id="0169c11"></a>其父名俱舍子。其子名為僧薩多<span class="note">(二合)</span>&#x8A90;。又如<a 
name="0169c12" id="0169c12"></a>父名迦癡那。其子亦名迦癡那。又如父名婆左<a 
name="0169c13" id="0169c13"></a>虞臘麼<span class="note">(二合)</span>。其子乃名嬌怛麼。又如父名迦<a 
name="0169c14" id="0169c14"></a>羅舍<span class="note">(引)</span>。其子乃名訥嚕<span class="note">(二合)</span>拏左哩野<span class="note">(二合)</span>。又如<a 
name="0169c15" id="0169c15"></a>父名底逸底哩。其子乃名底帝哩<span class="gaiji">[口*女]<!--gaiji,,1[口*女],2&#x20BC6;,3--></span><span class="note">(尼所引)</span>迦。<a 
name="0169c16" id="0169c16"></a>又如父名捺囉<span class="note">(二合)</span><span class="gaiji">[牟*含]<!--gaiji,,1[牟*含],2&#x246A5;,3--></span>。子名仙覺。又如母名野<a 
name="0169c17" id="0169c17"></a>鹿採魚人生。其子乃名&#x56A9;野<span class="note">(二合)</span>僧子覺乞曬<a 
name="0169c18" id="0169c18"></a><span class="note">(二合引)</span>。父首陀姓其子乃名尾濕<span class="note">(二合)</span>彌怛覽<span class="note">(二合)</span>。<a 
name="0169c19" id="0169c19"></a>母是氈陀羅。子名&#x56A9;瑟&#x59F9;<span class="note">(二合)</span>。母名烏哩<a 
name="0169c20" id="0169c20"></a>&#x56A9;<span class="note">(二合)</span>尸。天所生女非婆羅門。如上所說。何因<a 
name="0169c21" id="0169c21"></a>固執言婆羅門人間最上。又如所執戌嚕<a 
name="0169c22" id="0169c22"></a><span class="note">(二合)</span>底經正亦非理。是故所有婆羅門法。道理<a 
name="0169c23" id="0169c23"></a>亦非。</p><a 
name="0169c24" id="0169c24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如所執。婆羅門法。新肉紫礦及鹽等物。<a 
name="0169c25" id="0169c25"></a>戌陀應受。汝婆羅門。勿宜受之今何不爾。</p><a 
name="0169c26" id="0169c26"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如彼計。乳賣婆羅門行虛空墮落非婆羅<a 
name="0169c27" id="0169c27"></a>門。食肉墮空非理亦爾。是故應知。買賣乳肉<a 
name="0169c28" id="0169c28"></a>婆羅門。非戌陀之法。由此當知。一切非食肉<a 
name="0169c29" id="0169c29"></a>乳等人及非買賣。皆總得名婆羅門邪。是故<a 
name="0170a01" id="0170a01"></a>應知。乳肉計賣非婆羅門。妄執非法。</p><a 
name="0170a02" id="0170a02"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又世間姓妄執最上。亦非正法。如剎帝利。毘<a 
name="0170a03" id="0170a03"></a>舍戌達各執最上。應皆總名婆羅門姓。</p><a 
name="0170a04" id="0170a04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又執苦身名婆羅門。諸有苦身一切總應名<a 
name="0170a05" id="0170a05"></a>婆羅門。</p><a 
name="0170a06" id="0170a06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又彼妄執殺婆羅門而獲罪重。害彼眷屬獲<a 
name="0170a07" id="0170a07"></a>罪亦爾。復執彼從淨天口生。剎帝利姓彼天<a 
name="0170a08" id="0170a08"></a>身上。毘舍首陀身足而生。若殺於彼故獲重<a 
name="0170a09" id="0170a09"></a>罪。</p><a 
name="0170a10" id="0170a10"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>彼執非理。所以者何。應殺餘姓其罪非有。害<a 
name="0170a11" id="0170a11"></a>餘眷屬非有亦然。由是妄執不契正理。</p><a 
name="0170a12" id="0170a12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又彼所執。破壞彼行。破壞檀行。及彼受施。若<a 
name="0170a13" id="0170a13"></a>智若身皆獲重罪。</p><a 
name="0170a14" id="0170a14"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>此不應然。所以者何。身智之中何者得名號<a 
name="0170a15" id="0170a15"></a>婆羅門。應首陀等皆有身智。悉應得名婆羅<a 
name="0170a16" id="0170a16"></a>門邪。</p><a 
name="0170a17" id="0170a17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又彼妄執。解四圍陀及彌<span class="gaiji">[牟*含]<!--gaiji,,1[牟*含],2&#x246A5;,3--></span>婆。并僧佉論<a 
name="0170a18" id="0170a18"></a>尾世史迦。乃至諸論皆悉了達。名婆羅門。</p><a 
name="0170a19" id="0170a19"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>此理亦非。如首陀等。亦解彼論曉了彼義。應<a 
name="0170a20" id="0170a20"></a>皆得名婆羅門邪。</p><a 
name="0170a21" id="0170a21"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若修苦行名婆羅門。彼首陀等亦能行之。應<a 
name="0170a22" id="0170a22"></a>亦得名婆羅門邪。</p><a 
name="0170a23" id="0170a23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>解諸術數名婆羅門。彼採魚人及諸樂人。了<a 
name="0170a24" id="0170a24"></a>解術數種種差別。亦可得名婆羅門邪。</p><a 
name="0170a25" id="0170a25"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>是故應知。行非婆羅門。業非婆羅門。檀行受<a 
name="0170a26" id="0170a26"></a>者非婆羅門。</p><a 
name="0170a27" id="0170a27"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>彼剎帝利毘舍首陀。亦能行之。應皆得名婆<a 
name="0170a28" id="0170a28"></a>羅門邪。是故應知。非族非業非行非生。乃至<a 
name="0170a29" id="0170a29"></a>於德名婆羅門。</p><a 
name="0170b01" id="0170b01"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>彼因何立日如軍那花。亦似白月。離一切染<a 
name="0170b02" id="0170b02"></a>善修勝行。威儀無缺戒行具足。善伏諸根除<a 
name="0170b03" id="0170b03"></a>斷煩惱。無我無人離諸執著。及貪瞋癡悉皆<a 
name="0170b04" id="0170b04"></a>遠離。如是乃名真婆羅門。</p><a 
name="0170b05" id="0170b05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又離愛染乃至畜生。不生貪著修清淨行。名<a 
name="0170b06" id="0170b06"></a>婆羅門。</p><a 
name="0170b07" id="0170b07"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>是故得知此速骨嚕<span class="note">(二合)</span>大仙所說。此婆羅門<a 
name="0170b08" id="0170b08"></a>非姓非業非德非行。亦非工巧。如旃陀羅。善<a 
name="0170b09" id="0170b09"></a>四圍陀工巧藝能德行具足。應可得名婆羅<a 
name="0170b10" id="0170b10"></a>門邪。是故應知。非命非姓非智非身。亦非業<a 
name="0170b11" id="0170b11"></a>行名婆羅門。</p><a 
name="0170b12" id="0170b12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如首陀。苦行修學解四圍陀。獲五通仙。汝<a 
name="0170b13" id="0170b13"></a>婆羅門云何奉事此下種姓。又彼仙道四姓<a 
name="0170b14" id="0170b14"></a>皆得。云何餘姓名非最上。</p><a 
name="0170b15" id="0170b15"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如帝釋。往修善業得生彼天。本下種姓。彼<a 
name="0170b16" id="0170b16"></a>經正文作如是說。此婆伽晚及於帝釋。彼下<a 
name="0170b17" id="0170b17"></a>種姓。如是徵詰。一准於前。又彼所說。大自<a 
name="0170b18" id="0170b18"></a>在天。及於天后口中。生彼帝釋諸天及器世<a 
name="0170b19" id="0170b19"></a>間。非從世間生大自在及生天后。本能生末<a 
name="0170b20" id="0170b20"></a>非末生本。是故此言違彼正說。本下種姓。云<a 
name="0170b21" id="0170b21"></a>何妄執從彼而生。故知非理。又如首陀命終<a 
name="0170b22" id="0170b22"></a>生彼大自在天。汝婆羅門。云何奉事彼下種<a 
name="0170b23" id="0170b23"></a>姓。</p><a 
name="0170b24" id="0170b24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如汝說。婆羅門法。服氣餌藥苦行絕食。名<a 
name="0170b25" id="0170b25"></a>婆羅門。</p><a 
name="0170b26" id="0170b26"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>彼首陀等亦能行之。此應得名婆羅門邪。</p><a 
name="0170b27" id="0170b27"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又彼所執。於首陀處手中受食經於一月。現<a 
name="0170b28" id="0170b28"></a>身變為首陀之身。後報生中決定作狗。</p><a 
name="0170b29" id="0170b29"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又婆羅門娶首陀女以為其妻。父母家神皆<a 
name="0170c01" id="0170c01"></a>悉遠離。死入地獄。</p><a 
name="0170c02" id="0170c02"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>此執非理。婆羅門姓與彼首陀有何差別。</p><a 
name="0170c03" id="0170c03"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>如迦癡那大仙。從於鹿胎而生。苦行修學乃<a 
name="0170c04" id="0170c04"></a>證仙道。此仙豈可從婆羅門而乃生邪。</p><a 
name="0170c05" id="0170c05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>如&#x56A9;野<span class="note">(二合)</span>娑大仙。從採魚女之所生故。苦<a 
name="0170c06" id="0170c06"></a>行修學而成仙道。此仙豈是婆羅門姓。是故<a 
name="0170c07" id="0170c07"></a>妄執不契正理。</p><a 
name="0170c08" id="0170c08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如&#x56A9;斯瑟吒<span class="note">(二合)</span>大仙。從於烏哩&#x56A9;<span class="note">(二合)</span>尸天<a 
name="0170c09" id="0170c09"></a>女所生。苦行修學乃獲仙道。此仙豈是婆羅<a 
name="0170c10" id="0170c10"></a>門生。</p><a 
name="0170c11" id="0170c11"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如鹿角大仙。生於鹿胎。修習苦行而成仙<a 
name="0170c12" id="0170c12"></a>道。此仙豈是婆羅門邪。</p><a 
name="0170c13" id="0170c13"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如尾濕&#x56A9;<span class="note">(二合)</span>彌怛嚕<span class="note">(二合)</span>大仙。從於旃陀羅<a 
name="0170c14" id="0170c14"></a>家女之所生。此仙豈是婆羅門耶。是故應知。<a 
name="0170c15" id="0170c15"></a>調伏諸根不執我人。勤修梵行遠離染欲。永<a 
name="0170c16" id="0170c16"></a>息諸惑。由此方名真婆羅門。而非從彼族姓<a 
name="0170c17" id="0170c17"></a>而生。如何妄執。婆羅門姓世間最上。戒行清<a 
name="0170c18" id="0170c18"></a>潔族姓無雜。以此妄執非最為最。是故當知。<a 
name="0170c19" id="0170c19"></a>彼婆羅門。非姓非命非族非行非業非生名<a 
name="0170c20" id="0170c20"></a>婆羅門。</p><a 
name="0170c21" id="0170c21"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如多人本下種姓。持戒修福而得生天。何<a 
name="0170c22" id="0170c22"></a>因族姓乃生天邪。</p><a 
name="0170c23" id="0170c23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如汝宗迦癡曩大仙。尾野<span class="note">(二合)</span>娑大仙。&#x56A9;尸<a 
name="0170c24" id="0170c24"></a>瑟吒<span class="note">(二合)</span>大仙。覺善大仙。尾濕&#x56A9;<span class="note">(二合)</span>大仙。彌怛<a 
name="0170c25" id="0170c25"></a>囉<span class="note">(二合)</span>大仙。曩囉那大仙。此等大仙皆從下姓<a 
name="0170c26" id="0170c26"></a>種族而生。苦行修因乃獲仙道。何故妄執種<a 
name="0170c27" id="0170c27"></a>姓非雜世間最上。是故虛言應非信受。</p><a 
name="0170c28" id="0170c28"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如彼執。婆羅門姓梵王口生。剎帝利姓梵<a 
name="0170c29" id="0170c29"></a>天臂生。毘舍種姓梵天髀生。從於梵足乃生<a 
name="0171a01" id="0171a01"></a>首陀。是故虛妄多作是執。又執苦行堅守其<a 
name="0171a02" id="0171a02"></a>志名婆羅門。應採魚人染師皮作及首陀等。<a 
name="0171a03" id="0171a03"></a>堅志苦行。應皆總名婆羅門邪。</p><a 
name="0171a04" id="0171a04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又執彼形編其髻髮。腰帶索繫手執木杖。衣<a 
name="0171a05" id="0171a05"></a>素儉食名婆羅門。餘戌陀等亦能行之。應此<a 
name="0171a06" id="0171a06"></a>總名婆羅門邪。又執四姓皆從梵生。如何父<a 
name="0171a07" id="0171a07"></a>一子姓乃別。應可首陀乃至餘族一父所生<a 
name="0171a08" id="0171a08"></a>子姓應殊。此既不爾。彼云何然。</p><a 
name="0171a09" id="0171a09"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又婆羅門從一梵天口中而生。姊妹兄弟自<a 
name="0171a10" id="0171a10"></a>相交契。世所呵厭。汝能行之。云何清淨。是<a 
name="0171a11" id="0171a11"></a>故妄執非淨稱淨。如一父母而生四子非可<a 
name="0171a12" id="0171a12"></a>別姓。如何妄執。此婆羅門。此剎帝利。此是<a 
name="0171a13" id="0171a13"></a>毘舍。此是首陀。云何一父子姓各別。是故四<a 
name="0171a14" id="0171a14"></a>姓妄執差別。</p><a 
name="0171a15" id="0171a15"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>非如象馬牛羊駝鹿師子虎狼形足各異。此<a 
name="0171a16" id="0171a16"></a>是牛跡乃至象跡可分差別。又如一樹出生<a 
name="0171a17" id="0171a17"></a>花果。可無有異。非餘花卉。生處不同非可令<a 
name="0171a18" id="0171a18"></a>同。汝今四姓道理亦然。若婆羅門若剎帝利<a 
name="0171a19" id="0171a19"></a>乃至首陀。皆從一父之所生故。云何妄執四<a 
name="0171a20" id="0171a20"></a>姓差別。</p><a 
name="0171a21" id="0171a21"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>復有天王名喻地瑟致<span class="note">(二合)</span>囉。虔恭合掌來詣<a 
name="0171a22" id="0171a22"></a>仙人吠娑波灑頭面禮足而白大仙。云何<a 
name="0171a23" id="0171a23"></a>得名婆羅門德。復云何名婆羅門相。差別之<a 
name="0171a24" id="0171a24"></a>相復有幾種願今演說令我了解。</p><a 
name="0171a25" id="0171a25"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>時彼仙人吠娑波灑。乃告王言。忍辱精進靜<a 
name="0171a26" id="0171a26"></a>慮般若。此乃名為婆羅門德。遠離貪瞋及諸<a 
name="0171a27" id="0171a27"></a>殺害一切有情。是名第一婆羅門相。於他所<a 
name="0171a28" id="0171a28"></a>有一切財物而非貪受。是名第二婆羅門相。<a 
name="0171a29" id="0171a29"></a>遠離暴惡性行溫和。不封我人。捨離繫縛及<a 
name="0171b01" id="0171b01"></a>諸欲染。是名第三婆羅門相。於人天女乃至<a 
name="0171b02" id="0171b02"></a>傍生恒離染著。是名第四婆羅門相。又復<a 
name="0171b03" id="0171b03"></a>成熟一切有情恒起悲愍。調伏諸根清淨最<a 
name="0171b04" id="0171b04"></a>勝。是名第五婆羅門相。如是五種悉皆具足。<a 
name="0171b05" id="0171b05"></a>名婆羅門。若封彼我非具五相。皆名首陀。仙<a 
name="0171b06" id="0171b06"></a>人復告喻地瑟恥<span class="note">(二合)</span>囉言。非族非姓及修苦<a 
name="0171b07" id="0171b07"></a>行成婆羅門。彼旃陀等具足五相。亦得名為<a 
name="0171b08" id="0171b08"></a>真婆羅門。由如是理。彼婆羅門亦名首陀。首<a 
name="0171b09" id="0171b09"></a>陀亦名真婆羅門。</p><a 
name="0171b10" id="0171b10"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>彼喻地瑟恥<span class="note">(二合)</span>囉白仙人言。彼婆羅門行不殺<a 
name="0171b11" id="0171b11"></a>行獲果清淨。此乃少分名婆羅門。</p><a 
name="0171b12" id="0171b12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>仙人復告喻地瑟恥<span class="note">(二合)</span>囉言。此四姓別。皆由<a 
name="0171b13" id="0171b13"></a>過去宿業因緣。猶如世間胎生有情。一切皆<a 
name="0171b14" id="0171b14"></a>從穢處根生。有何差別。是故戒行復修德<a 
name="0171b15" id="0171b15"></a>業。名婆羅門。乃至首陀修於德行。成婆羅<a 
name="0171b16" id="0171b16"></a>門。</p><a 
name="0171b17" id="0171b17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若婆羅門不修德業。此亦得名下劣首陀。</p><a 
name="0171b18" id="0171b18"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又此五根能起惡業。恒應調伏。猶如大海沈<a 
name="0171b19" id="0171b19"></a>溺有情。應求濟度令超彼岸。爾時喻地瑟恥<a 
name="0171b20" id="0171b20"></a><span class="note">(二合)</span>囉王。聞仙所說。了解踊躍。以此所聞迴施<a 
name="0171b21" id="0171b21"></a>一切無邊有情。悉令曉悟。非為自身及貪己<a 
name="0171b22" id="0171b22"></a>命。我今日夜修習忍辱。遠離眷屬及於嫉&#x59AC;。<a 
name="0171b23" id="0171b23"></a>一切欲境更不耽著。趣求解脫恒修淨行。</p><a 
name="0171b24" id="0171b24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>仙人復告喻地瑟恥<span class="note">(二合)</span>囉言。不殺有情遠離<a 
name="0171b25" id="0171b25"></a>貪瞋清淨無比。如是名為婆羅門行。調伏諸<a 
name="0171b26" id="0171b26"></a>根。布施忍辱。真實梵行。悲念愍護一切有<a 
name="0171b27" id="0171b27"></a>情。修習智慧。如是名為婆羅門行。離邪苦行。<a 
name="0171b28" id="0171b28"></a>應有情機所有眾苦。如是名為婆羅門行。</p><a 
name="0171b29" id="0171b29"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又婆羅門&#x8A90;野怛哩<span class="note">(二合)</span>經&#x546A;中說。苦行離執<a 
name="0171c01" id="0171c01"></a>調伏諸根。四時行施愛念有情。捨離睡眠。恒<a 
name="0171c02" id="0171c02"></a>修淨行經於千劫。方得名為真婆羅門。</p><a 
name="0171c03" id="0171c03"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>仙人復告喻地瑟恥<span class="note">(二合)</span>囉言。若人解了四圍<a 
name="0171c04" id="0171c04"></a>陀論。名婆羅門。稱姓最上。餘首陀姓亦能了<a 
name="0171c05" id="0171c05"></a>解。何非最上。</p><a 
name="0171c06" id="0171c06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>譬如四姓同遊聖境。所有蹤跡。不可分別此<a 
name="0171c07" id="0171c07"></a>人之蹤非彼人跡。一姓四姓亦復如是。由假<a 
name="0171c08" id="0171c08"></a>施設本無差別。</p><a 
name="0171c09" id="0171c09"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如世間牛馬等形。相狀雖異。男女二根同<a 
name="0171c10" id="0171c10"></a>類不殊。彼婆羅門與剎帝利毘舍首陀。一姓<a 
name="0171c11" id="0171c11"></a>四姓相望亦然。</p><a 
name="0171c12" id="0171c12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如一人血肉屎尿手足諸根。與眾多人所<a 
name="0171c13" id="0171c13"></a>有血肉同類亦然。</p><a 
name="0171c14" id="0171c14"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如蓮花剎怛哩花月螺光色可分差別。於<a 
name="0171c15" id="0171c15"></a>餘四姓色相無異。如何差別。又如牛馬乃至<a 
name="0171c16" id="0171c16"></a>象鹿行於染欲。而非交契可分差別。今婆羅<a 
name="0171c17" id="0171c17"></a>門與剎帝利毘舍首陀。互相交契而行染欲。<a 
name="0171c18" id="0171c18"></a>皆同胎生有何差別。</p><a 
name="0171c19" id="0171c19"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如婆羅門所生之女。對餘婆羅門同姓姊<a 
name="0171c20" id="0171c20"></a>妹。云何交契。姊妹兄弟夫妻乃爾。世間首陀<a 
name="0171c21" id="0171c21"></a>非行此法。</p><a 
name="0171c22" id="0171c22"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>譬如世間憂曇&#x9262;樹。花果枝葉雖復眾多根<a 
name="0171c23" id="0171c23"></a>身無異。非能分別此彼之花。汝婆羅門亦復<a 
name="0171c24" id="0171c24"></a>如是。非可交會同姓姊妹。世所呵厭非可行<a 
name="0171c25" id="0171c25"></a>之。</p><a 
name="0171c26" id="0171c26"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如捨離身語不善恒修淨業名婆羅門。破<a 
name="0171c27" id="0171c27"></a>毘舍等亦能行之。得彼大仙名&#x56A9;私瑟&#x59F9;<span class="note">(二合)</span>。</p><a 
name="0171c28" id="0171c28"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如世間之火能燒柴薪而無分別。今婆羅<a 
name="0171c29" id="0171c29"></a>門對餘諸姓無異亦然。</p><a 
name="0172a01" id="0172a01"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如彼宗彌野<span class="note">(二合)</span>娑大仙。本是採魚父之所生。<a 
name="0172a02" id="0172a02"></a>亦非是彼婆羅門生。</p><a 
name="0172a03" id="0172a03"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如半拏&#x56A9;王。兄弟五人同一母生父乃<a 
name="0172a04" id="0172a04"></a>各別。此由宿業同母別父非由於姓。而妄執<a 
name="0172a05" id="0172a05"></a>別。</p><a 
name="0172a06" id="0172a06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如世間鹽處於水。形雖可隱鹽味非無。宿<a 
name="0172a07" id="0172a07"></a>業隨身隱顯亦然。如是妄執。諸有智人。應當<a 
name="0172a08" id="0172a08"></a>審悉非可依信。</p><a 
name="0172a09" id="0172a09"></a><p><span class="juanname">金剛針論</span></p>
