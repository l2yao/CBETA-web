name="1192c28" id="1192c28"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛藏經念佛品云　惡知識。</p><a 
name="1192c29" id="1192c29"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛告舍利弗。若有比丘教餘比丘。汝當念佛<a 
