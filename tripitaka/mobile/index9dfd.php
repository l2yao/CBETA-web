name="1194a29" id="1194a29"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>法華經云　無上寶聚不求自得<span class="note">(若人不求種種世法。亦不求<a 
name="1194b01" id="1194b01"></a>佛法。即是真求法人)</span>真佛者<span class="note">(識心見性悟理之人即是真佛)</span>。</p><a 
name="1194b02" id="1194b02"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>金剛經云　離一切諸相即名諸佛<span class="note">(離有離無故。不垢不<a 
name="1194b03" id="1194b03"></a>淨故。無生無體故)</span>。</p><a 
name="1194b04" id="1194b04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>法身禮。</p><a 
name="1194b05" id="1194b05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>入佛境界經云　佛常在世間。而不染世法。<a 
name="1194b06" id="1194b06"></a>以不分別世間故。敬禮無所觀。虛空無中邊。<a 
name="1194b07" id="1194b07"></a>諸佛身亦然。心同虛空故。敬禮無所觀。一切<a 
