name="1194a19" id="1194a19"></a>色身見佛。舍利弗。如來不應以色身見。亦復<a 
name="1194a20" id="1194a20"></a>不應以音聲見。舍利弗。若人以色身見佛。是<a 
name="1194a21" id="1194a21"></a>去佛遠。所以者何。佛不名見名為見佛<span class="note">(不依有見無見<a 
name="1194a22" id="1194a22"></a>即是不依正見)</span>。</p><a 
name="1194a23" id="1194a23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>維摩經云　夫求法者不著佛求不著法求不<a 
