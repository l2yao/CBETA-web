name="1194a25" id="1194a25"></a>染著非求法也。法名無為。若行有為。是求有<a 
name="1194a26" id="1194a26"></a>為。非求法也。法離見聞覺知。若行見聞覺<a 
name="1194a27" id="1194a27"></a>知。是即見聞覺知非求法也。唯舍利弗。夫求<a 
