name="1193b05" id="1193b05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛藏經云　惡知識。</p><a 
name="1193b06" id="1193b06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>舍利弗。人以清淨信等諸根出家學道。遇惡<a 
name="1193b07" id="1193b07"></a>知識。惡知識者。常好調戲輕躁無羞。言語散<a 
