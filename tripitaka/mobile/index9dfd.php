name="1193b15" id="1193b15"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>念誦結護法普通諸部</p><a 
name="1193b16" id="1193b16"></a><p style="text-indent: 0em; margin-left: 6em"><span class="linehead"></span>三藏金剛智授與灌頂弟子</p><a 
