name="1193c15" id="1193c15"></a>界。作此觀者。一切佛法恒沙功德不由他悟。<a 
name="1193c16" id="1193c16"></a>此一法攝無量。剎那悟入諸法中。自在無礙。<a 
name="1193c17" id="1193c17"></a>從地至地漸漸昇進。學此觀者。不得專守念<a 
