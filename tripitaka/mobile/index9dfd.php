name="1196a20" id="1196a20"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>有諍法。</p><a 
name="1196a21" id="1196a21"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>思益經云　梵天言。云何比丘多諍訟。答言。<a 
name="1196a22" id="1196a22"></a>是好是惡此名諍訟。是理是非理此名諍訟。<a 
name="1196a23" id="1196a23"></a>是垢是淨此名諍訟。是善是不善此名諍訟。<a 
name="1196a24" id="1196a24"></a>是戒是毀戒此名諍訟。是應作是不應作此<a 
name="1196a25" id="1196a25"></a>名諍訟。以是法得道以是法得果此名諍訟。<a 
name="1196a26" id="1196a26"></a>梵天若於法中有高下心貪著取受。皆是諍<a 
name="1196a27" id="1196a27"></a>訟。樂諍訟者。無沙門法。樂沙門法者。無有妄<a 
name="1196a28" id="1196a28"></a>想貪著。梵天言。云何比丘親近於佛。答言。若<a 
name="1196a29" id="1196a29"></a>比丘於諸法中不見有法若近若遠。是名親<a 
name="1196b01" id="1196b01"></a>近於佛。梵天言。云何比丘給侍於佛。答言。若<a 
name="1196b02" id="1196b02"></a>比丘身口意無所作。是名給侍於佛。</p><a 
name="1196b03" id="1196b03"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>樂瓔珞莊嚴方便品<span class="note">(一部一卷成。亦名轉女身菩薩問答經)</span>若其無<a 
name="1196b04" id="1196b04"></a>諍不可言說。以何等故說名無諍。須菩提言。<a 
name="1196b05" id="1196b05"></a>姊。如來世尊為聲聞弟子假名字說。女言。大<a 
name="1196b06" id="1196b06"></a>德須菩提。若有假名即有諍訟。若有諍訟即<a 
name="1196b07" id="1196b07"></a>有顛倒。若有顛倒非沙門法。須菩提言。姊何<a 
name="1196b08" id="1196b08"></a>等是沙門法。女言。大德須菩提。無有文字。無<a 
name="1196b09" id="1196b09"></a>有諍訟。無有顛倒。是沙門法。亦不分別是法。<a 
name="1196b10" id="1196b10"></a>是非法。是沙門法。又不分別憶想不憶想。是<a 
name="1196b11" id="1196b11"></a>沙門法。離一切著。是沙門法。無心離意識。是<a 
name="1196b12" id="1196b12"></a>沙門法。知足是沙門法。離有為法。是沙門法。<a 
name="1196b13" id="1196b13"></a>諸法如虛空。是沙門法。說是沙門法時。中<a 
name="1196b14" id="1196b14"></a>間所集聽法諸天。有四十天子。遠塵離垢得<a 
name="1196b15" id="1196b15"></a>法眼淨。</p><a 
name="1196b16" id="1196b16"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛藏經云<span class="note">(佛弟子終有為著相。非沙門法)</span>非沙門自稱沙門。不<a 
name="1196b17" id="1196b17"></a>應供養而受供養。名為常賊立憧相賊。名為<a 
name="1196b18" id="1196b18"></a>自在殺害人賊。是人所食一口皆不淨。唯清<a 
name="1196b19" id="1196b19"></a>有向道得道果者能消供養。舍利弗。我滅度<a 
name="1196b20" id="1196b20"></a>後如是等人滿閻浮提。專行求利以自生活<a 
name="1196b21" id="1196b21"></a>　有為法。</p><a 
name="1196b22" id="1196b22"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>金剛經云　一切有為法。如夢幻泡影。如露<a 
name="1196b23" id="1196b23"></a>亦如電。應作如是觀。</p><a 
name="1196b24" id="1196b24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>涅槃經云　有為功德多諸過患。</p><a 
name="1196b25" id="1196b25"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛藏經云　我所說諸法。隨順第一義。有為<a 
name="1196b26" id="1196b26"></a>不堅牢。如夢之所見。</p><a 
name="1196b27" id="1196b27"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>金剛三昧經云　於有為法如避火坑。</p><a 
name="1196b28" id="1196b28"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>楞伽經云　若依止少法而有少法起。若依<a 
name="1196b29" id="1196b29"></a>止於事此法即便壞。</p><a 
name="1196c01" id="1196c01"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>禪門經云　大乘妙義至理空曠。有為眾生<a 
name="1196c02" id="1196c02"></a>而不能入。</p><a 
name="1196c03" id="1196c03"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>大佛頂經云　圓滿菩提歸無所得。</p><a 
name="1196c04" id="1196c04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>般若子經云　無智亦無得。以無所得故即<a 
name="1196c05" id="1196c05"></a>菩提薩埵。</p><a 
name="1196c06" id="1196c06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>修多羅般若經云　佛告須菩提。若有善男<a 
name="1196c07" id="1196c07"></a>子善女人。發菩提心有所得者。即是邪見。邪<a 
name="1196c08" id="1196c08"></a>見之人永不見佛。斷一切智名為佛智見。凡<a 
name="1196c09" id="1196c09"></a>夫有為。佛是無為。如是見者非平等見。見凡<a 
name="1196c10" id="1196c10"></a>夫法空佛法亦空。如是見者是名正見。</p><a 
name="1196c11" id="1196c11"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>思益經云　世尊。云何具足六波羅蜜。梵天<a 
name="1196c12" id="1196c12"></a>若不念施。不依戒。不分別忍。不敢精進。不住<a 
name="1196c13" id="1196c13"></a>禪定。不二於惠。是名具足六波羅蜜。</p><a 
name="1196c14" id="1196c14"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>金剛三昧經云　空心不動具六波羅。</p><a 
name="1196c15" id="1196c15"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>此大乘頓教法門。為一切眾生本來自有佛<a 
name="1196c16" id="1196c16"></a>性。千經萬論大小乘教文字言說。只指眾生<a 
name="1196c17" id="1196c17"></a>本性見成佛道。此法亦名反源。亦名反照。亦<a 
name="1196c18" id="1196c18"></a>名反流。亦名迴向。亦名無生。亦名無漏。亦名<a 
name="1196c19" id="1196c19"></a>不起。亦名法離見聞覺知。</p><a 
name="1196c20" id="1196c20"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>楞伽經云　如水瀑流盡。波浪即不起。如是<a 
name="1196c21" id="1196c21"></a>意識滅。種種識不生。</p><a 
name="1196c22" id="1196c22"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>大佛頂經云　元是一精明。分為六和合。一<a 
name="1196c23" id="1196c23"></a>處成休復。六用皆不行。一根既反源。六根成<a 
name="1196c24" id="1196c24"></a>解脫。反聞聞自性。性成無上道。反見見自心。<a 
name="1196c25" id="1196c25"></a>見性成佛道。</p><a 
name="1196c26" id="1196c26"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又云。若棄生滅守於真常。常光現時塵根識<a 
name="1196c27" id="1196c27"></a>心應時消落。想相為塵。識情為垢。二俱遠離。<a 
name="1196c28" id="1196c28"></a>則汝法眼應時清明。云何不成無上知覺<a 
name="1196c29" id="1196c29"></a>　佛告阿難。如是清淨持禁戒人心無貪婬。於<a 
name="1197a01" id="1197a01"></a>外六塵不多流溢。因不流溢旋源自歸。鹿<a 
name="1197a02" id="1197a02"></a>既不緣根無所偶。反流全一六用不行。十方<a 
name="1197a03" id="1197a03"></a>國土皎然清淨。</p><a 
name="1197a04" id="1197a04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>楞伽經云　遠離於心識。亦離於五法。復離<a 
name="1197a05" id="1197a05"></a>於自性。是為佛種性。由依本識故。而有諸識<a 
name="1197a06" id="1197a06"></a>生。由依內識故。有似外影現。無智恒分別。有<a 
name="1197a07" id="1197a07"></a>為及無為。□□□□□離質亦無影。藏識若<a 
name="1197a08" id="1197a08"></a>清淨。諸識浪不生。依法身有報。而起淨分別。<a 
name="1197a09" id="1197a09"></a>若不了無我。依教不依義。理教中求我。是妄<a 
name="1197a10" id="1197a10"></a>垢惡見。不了故說有。唯妄取無餘。離聖教正<a 
name="1197a11" id="1197a11"></a>理。欲滅惑反增。是外道狂言。智者不應說。教<a 
name="1197a12" id="1197a12"></a>由理故顯。當依此教理。勿更餘分別。</p><a 
name="1197a13" id="1197a13"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>維摩經云　文殊師利菩薩從無住本立一切<a 
name="1197a14" id="1197a14"></a>法。</p><a 
name="1197a15" id="1197a15"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>金剛經云　一切諸佛及諸佛阿耨多羅三藐<a 
name="1197a16" id="1197a16"></a>三菩提法。皆從此經出<span class="note">(此經者即此心。是不生不滅故。不垢不淨故。離有離無<a 
name="1197a17" id="1197a17"></a>故。影體俱離故。不依性相故。亦不順生。亦不依寂。亦不順有相。亦不依無相不沈不浮故)</span>。</p><a 
name="1197a18" id="1197a18"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>文殊行經云　復次舍利弗。若有人言。過去<a 
name="1197a19" id="1197a19"></a>未來於實際中。說有依處說無依處者。當知。<a 
name="1197a20" id="1197a20"></a>彼輩誹謗如來獲大重罪。所以者何。彼真實<a 
name="1197a21" id="1197a21"></a>際無憶無念亦無墮落<span class="note">(無憶無念者即不順浮。亦無墮落者。即不沈空滯寂。此<a 
name="1197a22" id="1197a22"></a>法不順有心。不依無心。順有心者即是波浪。依無心者即是□□空見外道有無不二即是正見)</span>。</p><a 
name="1197a23" id="1197a23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>楞伽經云　天乘及梵乘。聲聞緣覺乘。諸佛<a 
name="1197a24" id="1197a24"></a>如來乘。諸乘我所說。乃至有心起。諸乘未究<a 
name="1197a25" id="1197a25"></a>境。彼心轉滅以。無乘及乘者。無有乘建立。我<a 
name="1197a26" id="1197a26"></a>說為大乘。</p><a 
name="1197a27" id="1197a27"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>天乘非乘。非名亦非字。非諦非解脫。</p><a 
name="1197a28" id="1197a28"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>我無上大乘超過於名言。其義甚明了。愚夫<a 
name="1197a29" id="1197a29"></a>不覺知。內自證不動。是無上大乘。</p><a 
name="1197b01" id="1197b01"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>決定毘尼經云　見凡夫有為佛。是無為。如<a 
name="1197b02" id="1197b02"></a>是見者不平等見。見凡夫法空佛法亦空。如<a 
name="1197b03" id="1197b03"></a>是見者是名平等見。見犯比丘不入地獄。清<a 
name="1197b04" id="1197b04"></a>淨行者不證涅槃。如是見者名平等見。</p><a 
name="1197b05" id="1197b05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>寶積經云　云何真供養佛。佛言。無佛想無<a 
name="1197b06" id="1197b06"></a>法想無僧想。是名日日真供養佛。佛不名作。<a 
name="1197b07" id="1197b07"></a>不名色。不名想。無作無色無想。是名真供<a 
name="1197b08" id="1197b08"></a>養佛。</p><a 
name="1197b09" id="1197b09"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>金剛三昧經序品第一　爾時世尊。大眾圍<a 
name="1197b10" id="1197b10"></a>遶。說大乘經。名一味真實際本覺利行。若聞<a 
name="1197b11" id="1197b11"></a>是經。乃至受持一四句偈。是人則為入佛智<a 
name="1197b12" id="1197b12"></a>地。能以方便教化眾生。為一切眾生作大知<a 
name="1197b13" id="1197b13"></a>識。佛說此經已。結跏趺坐。即入金剛三昧。身<a 
name="1197b14" id="1197b14"></a>心不動。爾時眾中。有一比丘。名曰阿伽陀。從<a 
name="1197b15" id="1197b15"></a>坐而起。合掌<span class="gaiji">[跍*月]<!--gaiji,,1[跍*月],2&#x4812;,3--></span>跪。欲宣義意。而說偈言。</p><a 
name="1197b16" id="1197b16"></a><p>　<span class="lg">大慈滿足尊　　智慧通無礙<a 
name="1197b17" id="1197b17"></a><br>　廣度眾生故　　說於一諦義<a 
name="1197b18" id="1197b18"></a><br>　無量諸菩薩　　皆悉度眾生<a 
name="1197b19" id="1197b19"></a><br>　為眾廣染問　　知法寂滅相<a 
