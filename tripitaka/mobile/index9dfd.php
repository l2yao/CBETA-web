name="1194a13" id="1194a13"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>假佛一切形像是。</p><a 
name="1194a14" id="1194a14"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>金剛經云　凡所有相皆是虛妄。若以色見<a 
