name="1197b28" id="1197b28"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>無相法品第二　一覺了義難解難入。非諸<a 
name="1197b29" id="1197b29"></a>二乘之所知見。唯佛菩薩乃能知之。善男子。<a 
