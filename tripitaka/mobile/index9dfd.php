name="1194a06" id="1194a06"></a>皆愁憂不樂。</p><a 
name="1194a07" id="1194a07"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛藏經云　舍利弗。我法無諸難事。不乏飲<a 
