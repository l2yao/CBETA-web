name="0107c18" id="0107c18"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>牟尼寂靜觀。是則遠離生。是名為不取。今世後世淨。</p><a 
name="0107c19" id="0107c19"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>法身無性無生。非心非識。此牟尼寂靜。頓離一切<a 
