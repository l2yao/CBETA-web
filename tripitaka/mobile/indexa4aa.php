name="0107c24" id="0107c24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>○二正宗分。分二。初直指離言絕證。廣大微妙。第。</p><a 
name="0108a01" id="0108a01"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>一義法門。分二。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>△初問一百八句。</p><a 
name="0108a02" id="0108a02"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>爾時大慧菩薩偈讚佛<span class="corr" title='[巳&gt;已]'>已</span>。自說姓名。我名為大慧。通<a 
name="0108a03" id="0108a03"></a>達於大乘。今以百八義。仰諮尊中上。</p><a 
name="0108a04" id="0108a04"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>菩薩自言通達大乘。&#x8462;欲當時後世知所問義。皆<a 
