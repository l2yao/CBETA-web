name="0107b04" id="0107b04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>世間離生滅。猶如&#x8657;空華。智不得有無。而興大悲心。<a 
name="0107b05" id="0107b05"></a>一切法如幻。遠離於心識。智不得有無。而興大悲心。<a 
name="0107b06" id="0107b06"></a>遠離於斷常。世間恒如夢。智不得有無。而興大悲心。</p><a 
name="0107b07" id="0107b07"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>世間如&#x8657;華。一切法如幻。世間恒如夢。此即眾生<a 
name="0107b08" id="0107b08"></a>日用。頓見如來不思議境界。非智之所能有無也。<a 
name="0107b09" id="0107b09"></a>如來憫此眾生。不能自覺。枉隨心識。妄計斷常。是<a 
name="0107b10" id="0107b10"></a>興大悲。方便度脫。菩薩深領此意。故於讚佛。獨露<a 
name="0107b11" id="0107b11"></a>全經旨趣。此為善解其義也。</p><a 
name="0107b12" id="0107b12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>知人法無我。煩惱及爾<span class="gaiji">燄<!--gaiji,燄,1[舀*炎],2&#x269A8;,3--></span>。常清淨無相。而興大悲心。</p><a 
name="0107b13" id="0107b13"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>凡夫執陰中我。則煩惱依起。二乘觀陰無我。而所<a 
name="0107b14" id="0107b14"></a>知未忘。二皆屬迷。非有淺深也。此言人法無我。煩<a 
name="0107b15" id="0107b15"></a>惱爾<span class="gaiji">燄<!--gaiji,燄,1[舀*炎],2&#x269A8;,3--></span>。常自清淨。本皆讚佛之辭。然實指凡夫二<a 
name="0107b16" id="0107b16"></a>乘。當體全空。良由不覺。妄見流轉。如來深知。而起<a 
name="0107b17" id="0107b17"></a>悲度。雖無眾生得滅度者。而十無盡句。本願弘深<a 
name="0107b18" id="0107b18"></a>耳。按三細。初依不覺。心動。名業相。二依動。能見。名<a 
name="0107b19" id="0107b19"></a>轉相。三依見。境界妄現。名現相。此三屬八識。為無<a 
name="0107b20" id="0107b20"></a>始&#x8657;偽習氣。後依現相。對境分別。成六麤智相。為<a 
name="0107b21" id="0107b21"></a>法執俱生。二依智。起念不斷。名相續相。為法執分<a 
name="0107b22" id="0107b22"></a>別。此二法我。雖屬七識。然根極能見。依不覺起。最<a 
name="0107b23" id="0107b23"></a>為微細。十一地猶有二分所知愚。故非二乘所窺<a 
name="0107b24" id="0107b24"></a>也。所知。即此爾<span class="gaiji">燄<!--gaiji,燄,1[舀*炎],2&#x269A8;,3--></span>也。三依念。起著。名執取相。為人<a 
name="0107c01" id="0107c01"></a>執俱生。四依執。起計。名計名字相。為人執分別。此<a 
name="0107c02" id="0107c02"></a>二人我。起一切根本煩惱。亦屬七識。然依境分別。<a 
name="0107c03" id="0107c03"></a>為所執持。故二乘觀人無我。亦能除斷。而七識法<a 
name="0107c04" id="0107c04"></a>我不斷也。奘師以分別屬六識。俱生屬七識。然此<a 
name="0107c05" id="0107c05"></a>推原生起之由。故總言七識耳。五依惑造業。名起<a 
name="0107c06" id="0107c06"></a>業相。六依業。招報。名苦繫相。此二屬人天。然總三<a 
name="0107c07" id="0107c07"></a>細六麤。悉由不覺。覺即全空。所謂常清淨無相也。</p><a 
name="0107c08" id="0107c08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>一切無涅槃。無有涅槃佛。無有佛涅槃。遠離覺所覺。<a 
name="0107c09" id="0107c09"></a>若有若無有。是二悉俱離。</p><a 
name="0107c10" id="0107c10"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>前四偈。言眾生日用。悉是如來境界。此言如來涅<a 
name="0107c11" id="0107c11"></a>槃。不異眾生生死。故曰一切無涅槃也。若有涅槃。<a 
name="0107c12" id="0107c12"></a>寧無生死。既有生死。即有生死眾生。有眾生入生<a 
name="0107c13" id="0107c13"></a>死。有生死眾生。即有涅槃佛。有眾生入生死。即有<a 
name="0107c14" id="0107c14"></a>佛入涅槃。是覺與所覺。宛成相待。豈知一切眾生<a 
