name="0108a06" id="0108a06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>世間解之士。聞彼所說偈。觀察一切眾。告諸佛子言。<a 
name="0108a07" id="0108a07"></a>汝等諸佛子。今皆恣所問。我當為汝說。自覺之境界。</p><a 
name="0108a08" id="0108a08"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>自覺境界。十方三世如來所證。四卷反覆推明。總<a 
name="0108a09" id="0108a09"></a>不踰此。故首為揭示也。</p><a 
name="0108a10" id="0108a10"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>爾時大慧菩薩摩訶薩。承佛所聽。頂禮佛足。合掌恭<a 
name="0108a11" id="0108a11"></a>敬。以偈問曰。云何淨其念。云何念增長。<span class="note">(魏譯云。云何<a 
name="0108a12" id="0108a12"></a>淨諸覺。何因而有覺)</span>云何見癡惑。云何惑增長。<span class="note">(魏譯<a 
name="0108a13" id="0108a13"></a>云。何因見迷惑。何因有迷惑)</span>何故剎土化。相及諸外<a 
name="0108a14" id="0108a14"></a>道。云何無受次。何故名無受<span class="note">(魏譯云。寂靜及次第。唐<a 
name="0108a15" id="0108a15"></a>譯云。及無影次第)</span>。</p><a 
name="0108a16" id="0108a16"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>念。謂妄覺也。癡惑。謂不覺也。真如無性。不覺妄動。<a 
name="0108a17" id="0108a17"></a>見惑纔生。故有妄覺。欲淨妄覺。而窮妄覺之所起。<a 
name="0108a18" id="0108a18"></a>欲悟不覺。而究不覺之所從。始知真如無性。佛佛<a 
name="0108a19" id="0108a19"></a>秘旨。所宜首問也。剎土。即國土。化相。謂一切變化<a 
name="0108a20" id="0108a20"></a>六道之相。先言外道。後出無受。此欲辨邪正差別<a 
name="0108a21" id="0108a21"></a>也。無受。謂牟尼寂靜。不受一法。離諸影相。次。即諸<a 
name="0108a22" id="0108a22"></a>地次第。所謂無為法而有差別也。</p><a 
name="0108a23" id="0108a23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>何故名佛子。解脫至何所。誰縛誰解脫。何等禪境界。<a 
name="0108a24" id="0108a24"></a>云何有三乘。唯願為解說。緣起何所生。云何作所作。<a 
name="0108b01" id="0108b01"></a>云何俱異說。云何為增長。<span class="note">(唐譯云。云何諸有起)</span>云何<a 
name="0108b02" id="0108b02"></a>無色定。及與滅正受。云何為想滅。何因從定覺。云何<a 
name="0108b03" id="0108b03"></a>所作生。進去及持身。云何現分別。云何生諸地。</p><a 
name="0108b04" id="0108b04"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>解脫至何所。欲顯牟尼寂靜。無有縛脫。故復曰誰<a 
name="0108b05" id="0108b05"></a>縛誰解。言無解縛者。皆迷悟妄見耳。禪境界。指凡<a 
name="0108b06" id="0108b06"></a>聖諸禪定。緣起。謂因緣所生法也。作所作。謂能作<a 
name="0108b07" id="0108b07"></a>所作業果也。俱異說。指外道邪說也。增長。謂三有<a 
name="0108b08" id="0108b08"></a>因果何因增長也。無色定。謂四空。滅正受。謂滅盡。<a 
name="0108b09" id="0108b09"></a>想滅。謂無想定也。從定覺。謂如來何故從定而覺。<a 
name="0108b10" id="0108b10"></a>起諸作用。示身去住也。現分別。謂現說種種差別<a 
name="0108b11" id="0108b11"></a>之法。生諸地。謂建立諸地行相也。</p><a 
name="0108b12" id="0108b12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>破三有者誰。何處身云何。往生何所至。云何最勝子。<a 
name="0108b13" id="0108b13"></a>何因得神通。及自在三昧。云何三昧心。最勝為我說。</p><a 
name="0108b14" id="0108b14"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>破三有。總指三乘出三界有也。既出三界。住於何<a 
name="0108b15" id="0108b15"></a>處。受何等身。往生何所。言三乘聖人。同出三界。然<a 
name="0108b16" id="0108b16"></a>身土不同。應化各別也。最勝子。指諸菩薩。神通。謂<a 
name="0108b17" id="0108b17"></a>六神通。三昧。翻正受也。三昧心。謂住三昧之心。與<a 
name="0108b18" id="0108b18"></a>三昧同異也。</p><a 
name="0108b19" id="0108b19"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>云何名為藏。云何意及識。云何生與滅。云何見<span class="corr" title='[巳&gt;已]'>已</span>還。<a 
name="0108b20" id="0108b20"></a><span class="note">(魏譯云。何因見諸法。何因斷所見)</span>云何為種性。非種<a 
name="0108b21" id="0108b21"></a>及心量。云何建立相。及與非我義。云何無眾生。云何<a 
