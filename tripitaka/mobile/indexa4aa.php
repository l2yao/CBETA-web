name="0107a20" id="0107a20"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>爾時大慧菩薩。與摩帝菩薩。俱遊一切諸佛剎土。承<a 
name="0107a21" id="0107a21"></a>佛神力。從座而起。偏袒右肩。右膝著地。合掌恭敬。以<a 
