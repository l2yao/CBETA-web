name="0107a23" id="0107a23"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>諸佛剎土。即眾生剎土。此菩薩能於眾生日用。通<a 
name="0107a24" id="0107a24"></a>達如來境界。所謂見一切色。皆是佛色。聞一切聲。<a 
name="0107b01" id="0107b01"></a>皆是佛聲。游一切剎土。皆諸佛剎土也。以此上承<a 
