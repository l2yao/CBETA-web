name="0107a01" id="0107a01"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○初序分。分二。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>△初序經緣起。</p><a 
