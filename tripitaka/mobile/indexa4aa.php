name="0108b23" id="0108b23"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>藏。謂藏識。意。謂七識。識。謂六五識也。生滅。即諸根<a 
name="0108b24" id="0108b24"></a>識所現生滅。見。即生滅之見。還。謂還於無見也。同<a 
name="0108c01" id="0108c01"></a>此一心。而有藏意識之別。而有見生滅見不生滅<a 
name="0108c02" id="0108c02"></a>之異。此迷悟&#x8657;妄。悉無所有也。種性。三乘種性。非<a 
name="0108c03" id="0108c03"></a>種。謂無種性。指外道闡提也。心量。謂同一心而各<a 
name="0108c04" id="0108c04"></a>有劑量也。建立相。謂我相也。非我義。謂無我相也。<a 
name="0108c05" id="0108c05"></a>眾生。即我相異說。謂於我法即無眾生。而於世俗<a 
name="0108c06" id="0108c06"></a>說有者何也。</p><a 
name="0108c07" id="0108c07"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>云何為斷見。及常見不生。云何佛外道。其相不相違。<a 
name="0108c08" id="0108c08"></a>云何當來世。種種諸異部。云何空何因。云何剎那壞。</p><a 
name="0108c09" id="0108c09"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>斷常。皆外道邪見。見不生。謂二見不生。即如來正<a 
name="0108c10" id="0108c10"></a>法也。佛與外道相不相違。統一心平等而言之也。<a 
