name="0118a16" id="0118a16"></a>三摩提。隨入如來身。隨入如如化。神通自在。慈悲方<a 
name="0118a17" id="0118a17"></a>便。具足莊嚴。等入一切佛剎外道入處。離心意意識。<a 
name="0118a18" id="0118a18"></a>是菩薩漸次轉身。得如來身。</p><a 
name="0118a19" id="0118a19"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此八地至十地相也。八地斷俱生我執盡。證無功<a 
name="0118a20" id="0118a20"></a>用道。所作皆息。十方如來。同聲勸發。始以如幻觀。<a 
name="0118a21" id="0118a21"></a>觀三界如幻。得如幻三昧。度自心現無所有也。自<a 
name="0118a22" id="0118a22"></a>心現無所有。即無功用道。乃八地行相。至此始得<a 
name="0118a23" id="0118a23"></a>度越。至九地。方能說法利生。十地。方能分身十方。<a 
name="0118a24" id="0118a24"></a>承事諸佛。所謂得無礙般若波羅蜜也。彼生。指生<a 
name="0118b01" id="0118b01"></a>相無明也。十地以後。以金剛喻方便智力。斷生相<a 
name="0118b02" id="0118b02"></a>無明。證如來法身。真如常住。自然而有不思議業<a 
name="0118b03" id="0118b03"></a>用。具足方便。具足莊嚴。&#x5FA7;入一切佛剎。及外道入<a 
name="0118b04" id="0118b04"></a>處。所謂上與十方諸佛。同一慈力。下與六道眾生。<a 
name="0118b05" id="0118b05"></a>同一悲仰也。至此方稱離心意識。得方便二轉依<a 
name="0118b06" id="0118b06"></a>果。此菩薩頓悟自理。淨除業流。二俱殊勝。故曰漸<a 
name="0118b07" id="0118b07"></a>次得也。</p><a 
name="0118b08" id="0118b08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>大慧。是故欲得如來隨入身者。當遠離陰界入心因<a 
name="0118b09" id="0118b09"></a>緣。所作方便生住滅。妄想&#x8657;偽。唯心直進。觀察無始<a 
name="0118b10" id="0118b10"></a>&#x8657;偽過。妄想習氣因。三有思惟無所有。佛地無生。到<a 
name="0118b11" id="0118b11"></a>自覺聖趣。自心自在。到無開發行。如隨眾色摩尼。隨<a 
name="0118b12" id="0118b12"></a>入眾生微細之心。而以化身隨心量度。諸地漸次。相<a 
name="0118b13" id="0118b13"></a>續建立。是故。大慧。自悉檀善。應當修學。</p><a 
name="0118b14" id="0118b14"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此結重示修行方便也。心因緣者。心。識心。因緣。諸<a 
name="0118b15" id="0118b15"></a>識二種生也。謂陰界入諸心因緣。所起方便。一切<a 
name="0118b16" id="0118b16"></a>生滅。分別&#x8657;偽。所當遠離也。既遠離<span class="corr" title='[巳&gt;已]'>已</span>。當住唯心。<a 
name="0118b17" id="0118b17"></a>更無岐路。直進。言無岐路也。即以此心。觀察無明<a 
name="0118b18" id="0118b18"></a>發業之過。與分別人法。一切習氣。所起因相。思三<a 
name="0118b19" id="0118b19"></a>界如幻。達佛地無生。到自覺聖智。得心自在無功<a 
name="0118b20" id="0118b20"></a>用行。如隨色摩尼。隨入眾生心。而以化身。隨眾生<a 
name="0118b21" id="0118b21"></a>心量所受而為化度。依種種地。方便建立。此自悉<a 
name="0118b22" id="0118b22"></a>檀。所當修學也。悉檀。法施也。總之修行方便。先當<a 
name="0118b23" id="0118b23"></a>發明二種生滅。皆由無始不覺。遂成&#x8657;偽。住於唯<a 
name="0118b24" id="0118b24"></a>心。頓除習氣所起。人法二障。始知三界原空。直下<a 
name="0118c01" id="0118c01"></a>無生。不由功用也。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>上初略明八識因果邪正。以<a 
name="0118c02" id="0118c02"></a>表離妄所證竟。</p><a 
name="0118c03" id="0118c03"></a><p><span class="juanname">楞伽阿<span class="gaiji">跋<!--gaiji,跋,1[跳-兆+(乏-之+友)],2&#x47E6;,3--></span>多羅寶經心印卷一</span></p><a 
name="0118c04" id="0118c04"></a><a 
name="0118c05" id="0118c05"></a><div class="w"><div style="margin-left: 1em"><p style="margin-left: 3em"><span class="headname">音釋</span></p><a 
