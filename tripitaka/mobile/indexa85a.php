name="0842a20" id="0842a20"></a>成也故彼繫辭云易與天地準故能彌綸天地之道<a 
name="0842a21" id="0842a21"></a><span class="note">(如仰觀天文俯察地理等云云)</span>乃至範圍天地之化而不過曲成萬<a 
name="0842a22" id="0842a22"></a>物而不遺<span class="note">(韓康伯注云範圍者擬範天地而周備其理曲成者乘變以應物不繫一方者也則<a 
name="0842a23" id="0842a23"></a>物宜得矣)</span>今釋曰此意是明聖人則天法地以濟萬物故<a 
name="0842a24" id="0842a24"></a>得各隨其類普皆成益故云曲成從其易簡者正明<a 
name="0842b01" id="0842b01"></a>撮略之意意在簡略易解故云從其易簡易簡之文<a 
name="0842b02" id="0842b02"></a>亦是繫辭彼意說天地之道甚易甚簡無為而化順<a 
name="0842b03" id="0842b03"></a>物之情成其功業彼文云<span class="note">(其文&#x5374;在曲成之前今兼注引之理令自解)</span>乾<a 
name="0842b04" id="0842b04"></a>知太始坤作成物乾以易知坤以簡能<span class="note">(天地之道不為而善始不<a 
name="0842b05" id="0842b05"></a>勞而善成故曰易簡也)</span>易則易知簡則易從易知則有親易從<a 
name="0842b06" id="0842b06"></a>則有功<span class="note">(順萬物之情故曰有親通天下之志故曰有功)</span>有親則可久有功則<a 
name="0842b07" id="0842b07"></a>可大<span class="note">(有易簡之德則能成可久可大之功)</span>可久則賢人之德可大則賢<a 
name="0842b08" id="0842b08"></a>人之業<span class="note">(天地易簡萬物各載其形聖人不為&#x7FA3;方各遂其業德業既成則入於形器故以賢人目其德<a 
name="0842b09" id="0842b09"></a>業)</span>易簡而天下之理得矣<span class="note">(天下之理莫不由於易簡而各得順其分位也)</span>釋<a 
name="0842b10" id="0842b10"></a>曰既合天道而化則易簡也故道經云大道甚易行<a 
name="0842b11" id="0842b11"></a>而天下莫能行又云大道甚夷<span class="note">(夷平)</span>而人好徑故知但<a 
name="0842b12" id="0842b12"></a>一向尋求經意順理直釋不須廣敘諸家繁述名相<a 
name="0842b13" id="0842b13"></a>則易簡矣問甚深法義難解難入云何言易答順佛<a 
name="0842b14" id="0842b14"></a>善功方便於難解法中令得易解方名善說豈可以<a 
name="0842b15" id="0842b15"></a>著述演說令人皆不解會&#x5374;名妙耶所以優波&#x6BF1;多<a 
name="0842b16" id="0842b16"></a>說法聞者百千萬億之數皆得悟入馬鳴菩薩亦然<a 
name="0842b17" id="0842b17"></a>佛會中亦然今有矜誇難解以為深妙者是障道之<a 
name="0842b18" id="0842b18"></a>流也即知法有深妙而易解有麤淺而難解有淺而<a 
name="0842b19" id="0842b19"></a>易有深而難由說之差當述之巧拙也且如高僧傳<a 
name="0842b20" id="0842b20"></a>中慧休聽大乘經論一&#x5FA7;入神聽律三十&#x5FA7;未解豈<a 
name="0842b21" id="0842b21"></a>可云律部深於經論邪問既知如此今何不但作略<a 
name="0842b22" id="0842b22"></a>疏直釋經文而乃先為廣疏邪答上句豈不云根欲<a 
name="0842b23" id="0842b23"></a>性殊邪良由名相漸次之疏論<span class="corr" title='[巳&gt;已]'>已</span>&#x5FA7;人間彼一類人<a 
name="0842b24" id="0842b24"></a>固執先聞拒而不信故為開章一一會釋令歸一極<a 
