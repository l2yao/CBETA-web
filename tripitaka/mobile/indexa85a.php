name="0842c02" id="0842c02"></a>遺也疏更搜精要直注本經者直敘本宗經意以消<a 
name="0842c03" id="0842c03"></a>其文各隨科注於本文之下令覽者易見免別看經<a 
name="0842c04" id="0842c04"></a>本疏本尋求科段矣文前雖有略敘義門比大疏十<a 
name="0842c05" id="0842c05"></a>中刪八故云更搜精要。</p><a 
name="0842c06" id="0842c06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>疏庶即下三結成其益諸佛說教本是詮心良由心<a 
name="0842c07" id="0842c07"></a>有無量義門故展轉辨明便成多種事數學者尋文<a 
name="0842c08" id="0842c08"></a>尋義迷本所詮之心故今節節指歸於心令學者每<a 
