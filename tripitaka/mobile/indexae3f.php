name="0835c21" id="0835c21"></a>即具，故與後義俱立難易。初文三藏廣，餘<a 
name="0835c22" id="0835c22"></a>三例。初三藏中三：初正釋相，次「經言」下引<a 
name="0835c23" id="0835c23"></a>證，三「此則」下明當分意。初文可見。引證中<a 
name="0835c24" id="0835c24"></a>言「即脫瓔珞等」者，報身四十一地，戒定慧陀<a 
name="0835c25" id="0835c25"></a>羅尼以為瓔珞，寂滅忍為柔軟上服，大小相<a 
name="0835c26" id="0835c26"></a>海為嚴飾之具，丈六相好為&#x9E81;，生空法空為<a 
name="0835c27" id="0835c27"></a>弊，現有煩惱有為有漏，為塵土坌身，實無<a 
name="0835c28" id="0835c28"></a>生死煩惱，似有生死煩惱，為狀有所畏。如<a 
name="0835c29" id="0835c29"></a>《成論．無畏品》云：「如經云，善來比丘，隨順我<a 
