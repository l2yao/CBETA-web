name="0835c14" id="0835c14"></a>復未甄衡二途，其意以通而未灼然開<a 
name="0835c15" id="0835c15"></a>顯，以是義故，故須分於二義而答：當分<a 
name="0835c16" id="0835c16"></a>通於一代，於今便成相待；跨節唯在今經，<a 
name="0835c17" id="0835c17"></a>佛意非適今也。釋此二義，先標、次列、三釋。<a 
name="0835c18" id="0835c18"></a>釋中先當分、次跨節，一一皆約教行理三。初<a 
name="0835c19" id="0835c19"></a>當分者，順前問意，為顯跨節，以當分難四<a 
