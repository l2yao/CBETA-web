name="0835b10" id="0835b10"></a>舉大小兩乘能趣之行。次例中不可得即是<a 
name="0835b11" id="0835b11"></a>理也。舉劣況勝，觀息尚即是理，況大小兩乘<a 
name="0835b12" id="0835b12"></a>能趣之行？「當知」下結，又二：先結、次引證。次理<a 
name="0835b13" id="0835b13"></a>中三：初立、次引證、三結。初文不二故通、非一<a 
name="0835b14" id="0835b14"></a>故別。證中二文例立可知。先引《大論》，次引<a 
