name="0836a28" id="0836a28"></a>劣相對，故成六味，故略不說淨及不淨。」今<a 
name="0836a29" id="0836a29"></a>文從劣，故云「不說甘甜」等也。「棄王者服飾」<a 
name="0836b01" id="0836b01"></a>重語隱妙現&#x9E81;，亦與前隱功德現丈六<a 
name="0836b02" id="0836b02"></a>意同。如窮子見父豪貴尊嚴等，無量功德<a 
name="0836b03" id="0836b03"></a>莊嚴纓珞為王者服飾，當知秖是一種身口<a 
name="0836b04" id="0836b04"></a>行理更無別途。今棄此服飾，即當分義施<a 
name="0836b05" id="0836b05"></a>權意也。言「糞器」者，生滅道品。「若開方便」下正<a 
name="0836b06" id="0836b06"></a>明跨節相，此句總開。即向身向法向行等者，<a 
name="0836b07" id="0836b07"></a>指前方便身口行理等也。三結意中歷三<a 
name="0836b08" id="0836b08"></a>意結，初言「通是一音之教，而小大差別」者，<a 
name="0836b09" id="0836b09"></a>今此是法華跨節一音，不同小乘當分一音，<a 
name="0836b10" id="0836b10"></a>具如《止觀》第一記。故依《婆沙》但三藏佛一<a 
