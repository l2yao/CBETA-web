name="0837a24" id="0837a24"></a>通」者，在因則以伏斷為用，在果則以利物<a 
name="0837a25" id="0837a25"></a>為用，光宅意以〈壽量品〉為神通延壽利益<a 
name="0837a26" id="0837a26"></a>眾生。「即是」下總結。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>次「今古」下破，中三：先舉<a 
name="0837a27" id="0837a27"></a>難例易，次「因體」下正破，三「彼作」下結成難<a 
name="0837a28" id="0837a28"></a>勢。初文中「南方」者，南朝即江東是，舊總云南<a 
name="0837a29" id="0837a29"></a>朝，後分兩道，故云江東。「承肇什」者，取語便<a 
name="0837b01" id="0837b01"></a>耳。關中四子即生、肇、融、叡，後人承用四子之<a 
name="0837b02" id="0837b02"></a>義，故時人語曰「生肇發天真」，若以今意望<a 
name="0837b03" id="0837b03"></a>之，多附於通，不必全是，故云「多附」。「附」謂附<a 
name="0837b04" id="0837b04"></a>近，光宅亦附近肇什，故云「寧遠」。時人以為<a 
name="0837b05" id="0837b05"></a>光宅得旨，故抑云不遠，先破得旨之難，餘<a 
name="0837b06" id="0837b06"></a>易自啟。次正破中二：先因、次果。因中三重，謂<a 
name="0837b07" id="0837b07"></a>體、位、用，一一各以四一難之。初破因體中<a 
name="0837b08" id="0837b08"></a>先通責光宅，昔言通漫復汎許之，故云「可<a 
name="0837b09" id="0837b09"></a>然」。「何者」下正破，中三：初舉昔有四一，次舉<a 
name="0837b10" id="0837b10"></a>今無四一，三結難。初昔中二：先列、次結。初<a 
name="0837b11" id="0837b11"></a>文者，般若舉乘，乘即是教，故云「運載」。既言<a 
name="0837b12" id="0837b12"></a>一切靡不遍行及法界等，當知體廣。《般若》有<a 
name="0837b13" id="0837b13"></a>教一、《思益》有行一、《華嚴》有人一、《淨名》有理<a 
name="0837b14" id="0837b14"></a>一。「昔因如此」下結。「若言」下責今經無四一，<a 
name="0837b15" id="0837b15"></a>又二：先列、次釋，列中並是光宅疏意，下去例<a 
name="0837b16" id="0837b16"></a>知。「當知」下難勢，還以彼義而難於彼。次難<a 
name="0837b17" id="0837b17"></a>位中四：先來意、次標、三釋、四結。初二可知。<a 
name="0837b18" id="0837b18"></a>釋中亦二：先昔、次今。昔中亦先釋、次結。釋中<a 
name="0837b19" id="0837b19"></a>初文云「無上」是法，舉法取人。言「般若是無<a 
name="0837b20" id="0837b20"></a>上明&#x546A;」者，《大論》「問釋提桓因，何故以般若為<a 
name="0837b21" id="0837b21"></a>無上明&#x546A;？答：諸外道聖人有種種&#x546A;術利益<a 
name="0837b22" id="0837b22"></a>人民，誦是&#x546A;者能隨意所欲使諸鬼神，諸<a 
name="0837b23" id="0837b23"></a>僊有&#x546A;得大名聲，人民歸伏，貴是&#x546A;故，故<a 
name="0837b24" id="0837b24"></a>名般若以之為&#x546A;。帝釋白佛：是般若&#x546A;常<a 
name="0837b25" id="0837b25"></a>與眾生道德樂故，餘諸&#x546A;術能增長惡，般<a 
name="0837b26" id="0837b26"></a>若神&#x546A;能滅諸禪涅槃之著，何況貪等？是故<a 
name="0837b27" id="0837b27"></a>名為大明&#x546A;也。」「無上明」者，大或有上，故次<a 
name="0837b28" id="0837b28"></a>名無上。「無等等」者，等此無等，名無等等。皆<a 
name="0837b29" id="0837b29"></a>破無明，故並云「明」。又外道有&#x546A;能知他心，<a 
name="0837c01" id="0837c01"></a>有&#x546A;能飛變，有&#x546A;能住壽；於諸&#x546A;中，般若<a 
name="0837c02" id="0837c02"></a>出過無量無邊，故云「無等」。又諸佛於一切眾<a 
name="0837c03" id="0837c03"></a>生中無等，故般若名「無等」。又「無等等」者，妙覺<a 
name="0837c04" id="0837c04"></a>位也。無能等此無等之位，故云「無等等」。故<a 
name="0837c05" id="0837c05"></a>《淨名》中所歎菩薩皆是等覺，位&#x96A3;妙覺，故<a 
name="0837c06" id="0837c06"></a>云「近無等等佛自在慧」，此&#x546A;無上義同妙覺，<a 
name="0837c07" id="0837c07"></a>故云「無等等」。是為&#x546A;義無等等故也。次引<a 
name="0837c08" id="0837c08"></a>《大論》中應云「因行」而言「位」者，恐誤。受法性<a 
name="0837c09" id="0837c09"></a>身，行菩薩行，如前釋。引《淨名》中「近無等等」<a 
name="0837c10" id="0837c10"></a>者，佛名無等，無等中最，故云「等等」。等覺近<a 
name="0837c11" id="0837c11"></a>之，舉彼所近，故云「佛自在慧」。「十方魔王」者，<a 
name="0837c12" id="0837c12"></a>〈不思議品〉文，入地菩薩能作魔王，魔王不盡<a 
name="0837c13" id="0837c13"></a>是彼入地菩薩，故但云「多」，以實行者不能<a 
name="0837c14" id="0837c14"></a>惱於深位菩薩，故云「非驢所堪」。所以今云<a 
name="0837c15" id="0837c15"></a>「皆是」等也。次「淨名文」者，初住八相未是極<a 
name="0837c16" id="0837c16"></a>位故名為「雖」，是故仍須行菩薩道，自行化<a 
name="0837c17" id="0837c17"></a>他以進後位，此由初住見理故也。故舉理<a 
name="0837c18" id="0837c18"></a>以顯德，祕藏正當所見之理。「如是」下結。次<a 
name="0837c19" id="0837c19"></a>「若言」下明今經中亦應云「行」亦但云「位」者，<a 
name="0837c20" id="0837c20"></a>應是以位顯行，前文亦然。「當知」下結。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>次明<a 
name="0837c21" id="0837c21"></a>用中三：謂標、釋、結。釋中二：先昔、次今。先昔<a 
name="0837c22" id="0837c22"></a>者又二：釋、結，初，教。次明行長中初云「無明」<a 
name="0837c23" id="0837c23"></a>舉失顯得，「一切種智」正明能知之行，「一切<a 
name="0837c24" id="0837c24"></a>法」即兼舉所觀以顯能觀，「明無明」下出觀<a 
name="0837c25" id="0837c25"></a>相。「若知」下釋觀相，「是為」下結觀相者，並是<a 
name="0837c26" id="0837c26"></a>中道觀故，亦應合有用字，但是文略。次明<a 
name="0837c27" id="0837c27"></a>人一中云「一日」等者，並是斥奪之辭，舉能<a 
name="0837c28" id="0837c28"></a>奪所以明人勝。初云「一日行般若如日照世」<a 
name="0837c29" id="0837c29"></a>等者，《大品．習應品》云：「菩薩行般若一日，出<a 
name="0838a01" id="0838a01"></a>過二乘之上。二乘如螢火蟲，不作是念：我<a 
name="0838a02" id="0838a02"></a>力能照閻浮提，普令大明。二乘亦爾，不<a 
name="0838a03" id="0838a03"></a>作是念：我行六度乃至菩提。菩薩照世，洞<a 
name="0838a04" id="0838a04"></a>照法界如照三千，故云如日照世。故勝二<a 
name="0838a05" id="0838a05"></a>乘一切智光。」拙度菩薩尚未見空，故如螢火。<a 
name="0838a06" id="0838a06"></a>「若入薝蔔」等者，若入淨名常寂光土之室，故<a 
name="0838a07" id="0838a07"></a>唯聞諸佛法界萬德之香，尚不嗅地前教道<a 
name="0838a08" id="0838a08"></a>之香，豈樂拙度小行功德？故法身菩薩立種<a 
name="0838a09" id="0838a09"></a>智之身，如實相之座，不假燈王之威，見思<a 
name="0838a10" id="0838a10"></a>塵沙諸習先盡，故分段方便二土五塵不能<a 
name="0838a11" id="0838a11"></a>染於實報法身，故知此等皆是法身念不退<a 
name="0838a12" id="0838a12"></a>類，故云「阿越」。理一中云「色無邊故」等者，五<a 
name="0838a13" id="0838a13"></a>陰是理故，即陰是實相般若，故皆無邊。以由<a 
name="0838a14" id="0838a14"></a>理故令法無邊。「當知」下結。次「若謂」下破，今教<a 
name="0838a15" id="0838a15"></a>無四一故&#x9E81;，文闕人一，以行兼之，行必有<a 
name="0838a16" id="0838a16"></a>人故也。言「覆相」者，覆&#x5374;常住真實之相而<a 
name="0838a17" id="0838a17"></a>用於權，亦略用字，如前可知。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>次果體廣狹<a 
name="0838a18" id="0838a18"></a>四難者，亦標、釋、結。釋中二：先昔、次今。初又二：<a 
name="0838a19" id="0838a19"></a>初難、次結。初難中全具四一，文且引《般若》、《淨<a 
name="0838a20" id="0838a20"></a>名》，但是理一教一，餘二比於上下諸文，類<a 
name="0838a21" id="0838a21"></a>之可知，故略不說。次從「若謂今果」去難今<a 
name="0838a22" id="0838a22"></a>經果上四一不成。初不滿不了教一不成，<a 
name="0838a23" id="0838a23"></a>光宅疏中云「明一乘故滿了，不明佛性故<a 
name="0838a24" id="0838a24"></a>不滿不了」。從「何故復言」去行一不成。從「若<a 
name="0838a25" id="0838a25"></a>體廣」去人一不成。此等並是光宅疏意。言<a 
name="0838a26" id="0838a26"></a>「七百阿僧祇」者，《首楞嚴》下卷「堅首菩薩白佛<a 
name="0838a27" id="0838a27"></a>言：世尊！佛壽幾何？何時入滅？佛告堅首：東<a 
name="0838a28" id="0838a28"></a>方去此三萬二千佛土國名莊嚴，佛號照<a 
name="0838a29" id="0838a29"></a>明莊嚴自在王，十號具足，今現說法，如彼<a 
name="0838b01" id="0838b01"></a>佛所有壽量，我壽亦爾。又問：彼佛壽命幾何？<a 
name="0838b02" id="0838b02"></a>佛言：汝自往問，彼自當答。堅首承佛神力，<a 
name="0838b03" id="0838b03"></a>於一念頃即往彼國，頂禮遶佛白佛言：世<a 
name="0838b04" id="0838b04"></a>尊！壽命幾何？幾時入滅？彼佛答言：如釋迦壽，<a 
name="0838b05" id="0838b05"></a>我亦如是。汝欲知者，我壽七百阿僧祇劫，釋<a 
name="0838b06" id="0838b06"></a>迦亦爾。堅首還白佛竟。阿難言：如我解佛<a 
name="0838b07" id="0838b07"></a>所說，彼佛是釋迦異名。佛告阿難：汝承佛<a 
name="0838b08" id="0838b08"></a>力，乃知是事。乃至上方有土名一燈明等，<a 
name="0838b09" id="0838b09"></a>並釋迦分身。」從「若言」去理一不成。言應備<a 
name="0838b10" id="0838b10"></a>五眼見佛性者，據不見佛性則無佛眼。「當<a 
name="0838b11" id="0838b11"></a>知」下結。次果位中亦三：標、釋、結。釋中亦應先<a 
name="0838b12" id="0838b12"></a>舉昔有，次斥今無，文中從略直舉今無，準<a 
name="0838b13" id="0838b13"></a>前諸釋，驗知昔破。次果用中亦三：標、釋、結。釋<a 
name="0838b14" id="0838b14"></a>中亦不出昔直舉今文，準前可知。於中二：<a 
name="0838b15" id="0838b15"></a>先正破無四一，次廣破神通。初文二：先破、<a 
name="0838b16" id="0838b16"></a>次結，可知。</p>◎<a 
name="0838b17" id="0838b17"></a><p><span class="juanname">法華玄義釋籤卷第三</span></p>
