name="0836a02" id="0836a02"></a>癡人，食人涕唾。此似有瞋。自言：我是人中<a 
name="0836a03" id="0836a03"></a>師子。此似有慢。善持我法，如擎油&#x9262;。又<a 
name="0836a04" id="0836a04"></a>如語調達言：我尚不以法付舍利弗等，<a 
name="0836a05" id="0836a05"></a>況當與汝？此似有見也。」文中但略舉垢衣<a 
name="0836a06" id="0836a06"></a>一句耳。「語言勤作」等者，即是小乘，七科道品<a 
name="0836a07" id="0836a07"></a>即除見思糞之法器也。「語言」即念處，「勤作」即<a 
name="0836a08" id="0836a08"></a>正勤，「咄男子」等即如意，「好自安意」即五根，「所<a 
name="0836a09" id="0836a09"></a>以者何」等即五力，「更與作字」即八正，「雖忻此<a 
name="0836a10" id="0836a10"></a>遇」即七覺，「當加汝價」即&#x7157;法不能發真，如<a 
name="0836a11" id="0836a11"></a>意能發，故云「加價」。今文甚略。言「塗足油」者，<a 
name="0836a12" id="0836a12"></a>下偈文中得如意足觀，如油塗足則能履<a 
name="0836a13" id="0836a13"></a>水，能得神通。又油能除風，如定除亂，廣<a 
name="0836a14" id="0836a14"></a>如〈信解品〉疏釋。此則唯譬三藏說教行理可<a 
name="0836a15" id="0836a15"></a>知。次三教例中云「通別圓等亦爾」者，具如<a 
name="0836a16" id="0836a16"></a>下本門果妙中釋四種身相不同，融會可知。</p><a 
name="0836a17" id="0836a17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>次跨節中四：先斥當分，次「若開」下立跨節相，<a 
name="0836a18" id="0836a18"></a>三「如此」下結意，四「作如此」下融會。初文又二：<a 
name="0836a19" id="0836a19"></a>初正斥，次「秖隱」下明當分意。立當分者以<a 
name="0836a20" id="0836a20"></a>施權竟，權實相對故有四主，各各不同。故<a 
name="0836a21" id="0836a21"></a>明意者無別當分，秖是隱實施權且云當<a 
