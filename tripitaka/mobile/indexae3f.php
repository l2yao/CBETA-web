name="0836b12" id="0836b12"></a>義中分四教主，各有所說所行理體，故義易<a 
name="0836b13" id="0836b13"></a>解。理無種種，何故四教各有所詮？所詮異<a 
name="0836b14" id="0836b14"></a>故理不應異，故云難融。跨節義中不分四<a 
name="0836b15" id="0836b15"></a>教各別詮理，故云易明。諸經所說因果各<a 
name="0836b16" id="0836b16"></a>別，今越彼別義唯論一理，故相難解。若二<a 
name="0836b17" id="0836b17"></a>義相成則理相俱易，若二義隔越則二俱難<a 
name="0836b18" id="0836b18"></a>明，是故二義相須，當分乃成今經相待義邊，<a 
name="0836b19" id="0836b19"></a>跨節乃成今經開權義邊。又若方等般若及<a 
name="0836b20" id="0836b20"></a>華嚴等當分義者，仍是施權；若來至法華，當<a 
name="0836b21" id="0836b21"></a>分義邊成判權實，判已即廢，廢已即開，開廢<a 
name="0836b22" id="0836b22"></a>相即不可異時。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>二定妙法先後，中四：初標，<a 
