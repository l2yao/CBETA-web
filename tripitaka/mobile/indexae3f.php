<a 
name="0830b01" id="0830b01"></a><a 
name="0830b02" id="0830b02"></a><p><span class="juanname">法華玄義釋籤卷第三</span></p><a 
name="0830b03" id="0830b03"></a><a 
name="0830b04" id="0830b04"></a><p style="margin-left: 4em"><span class="byline">天台沙門湛然述</span></p><a 
name="0830b05" id="0830b05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>次解四悉中列章別釋，初標列十章通&#x4E98;<a 
name="0830b06" id="0830b06"></a>一代，於中為二：前七章通約諸經，後之三<a 
name="0830b07" id="0830b07"></a>章獨今經意。前七又四：初二正明悉檀，次二<a 
name="0830b08" id="0830b08"></a>名義同異，次二所起功能，次一自證與利他<a 
name="0830b09" id="0830b09"></a>得失。初二者，前總名、次別相，次二名義同異<a 
name="0830b10" id="0830b10"></a>中，初一名有同異，義一向同，次一名一向，<a 
name="0830b11" id="0830b11"></a>異而義同。次二初凡夫起觀，聖人起教，次唯<a 
name="0830b12" id="0830b12"></a>聖人說默，次一凡聖得失。次後三中初二明<a 
name="0830b13" id="0830b13"></a>今經意，後一明今經文，於前二中初一判<a 
name="0830b14" id="0830b14"></a>權實，次一開權實。得此意已，方知一代悉<a 
name="0830b15" id="0830b15"></a>檀不同。雖前七門義及今經妙未獨顯，別<a 
name="0830b16" id="0830b16"></a>明當部故有後三。思之思之！</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>初釋名中<a 
name="0830b17" id="0830b17"></a>三：初汎引古師，次引論破，三引南嶽正解。<a 
name="0830b18" id="0830b18"></a>初如文。次文者先引《地持》證謬，次正破謬。<a 
name="0830b19" id="0830b19"></a>初引《地持》四優檀那者，彼第十七〈菩提分品〉<a 
name="0830b20" id="0830b20"></a>云：「餘之一切所行所學悉入三三昧門，所謂<a 
name="0830b21" id="0830b21"></a>聲聞所行所學有四優檀那法，諸佛菩薩為<a 
name="0830b22" id="0830b22"></a>眾生故亦說此四，謂一切行苦是優檀那法，<a 
name="0830b23" id="0830b23"></a>乃至涅槃寂滅是優檀那法。諸佛菩薩具足<a 
name="0830b24" id="0830b24"></a>此法，復以此法轉授眾生是名優檀那。過<a 
name="0830b25" id="0830b25"></a>去寂默諸牟尼尊展轉相傳，是名優檀那。增<a 
name="0830b26" id="0830b26"></a>上涌出乃至具足出第一有，是名優檀那。」論<a 
name="0830b27" id="0830b27"></a>文無經字，但題云「地持經」，故今引云經也。<a 
name="0830b28" id="0830b28"></a>《地持》此四名為印者，如下所引《大論》所明法<a 
name="0830b29" id="0830b29"></a>印不同，大乘一印、小乘三印，此《地持》文雖是<a 
name="0830c01" id="0830c01"></a>大乘，欲明所攝之法，及為初行菩薩且脩<a 
name="0830c02" id="0830c02"></a>此四，四秖是三，更立苦印故為四也。二印<a 
name="0830c03" id="0830c03"></a>印生死，一印印涅槃，生死以無常為初印，<a 
name="0830c04" id="0830c04"></a>無我為後印，又名為宗，故知此與四悉名<a 
name="0830c05" id="0830c05"></a>義全別。「印是」下釋印義，教行兩文不同。說<a 
name="0830c06" id="0830c06"></a>屬世智故云「世智所說」。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>問：</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>約教約行俱云<a 
name="0830c07" id="0830c07"></a>傳授，有何差別？</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>答：</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>教是所傳之法，故云佛<a 
name="0830c08" id="0830c08"></a>菩薩具此法行；是能傳之功，故云經過去<a 
name="0830c09" id="0830c09"></a>寂默諸牟尼，秖是佛佛皆有化他傳教之<a 
name="0830c10" id="0830c10"></a>行。「增上」等者，並是歎釋宗義故也。言「第一<a 
name="0830c11" id="0830c11"></a>有」者，謂三界頂，此有難出，故云「第一」；居<a 
name="0830c12" id="0830c12"></a>極頂，故復云「第一」。次「彼明文」下正破。「如此」下<a 
name="0830c13" id="0830c13"></a>例破。「南嶽」下正釋，可見。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>二辨相中二：先引<a 
name="0830c14" id="0830c14"></a>大師釋，次私釋。初文四悉自為四段。初文<a 
name="0830c15" id="0830c15"></a>二：初釋、次料簡。初文中言「世界」者，謂五陰<a 
name="0830c16" id="0830c16"></a>差別，界入亦然。次文者「若無」下問，「人者」下答，<a 
name="0830c17" id="0830c17"></a>「第一義」下問，「答」下答也。「外人」下因釋。外人橫<a 
name="0830c18" id="0830c18"></a>計，不名世界、不達差別，但總計我，無因<a 
name="0830c19" id="0830c19"></a>謂自然，邪因謂梵天等。此四悉文全出《大<a 
name="0830c20" id="0830c20"></a>論》。為人中三：標、釋、結。釋中二：初正釋，次「如<a 
name="0830c21" id="0830c21"></a>雜業」下立傍正相以釋疑。初文云「或聽不<a 
name="0830c22" id="0830c22"></a>聽」者，「聽」謂聽許，雖復雙舉，意在於聽；聽是<a 
name="0830c23" id="0830c23"></a>所宜，宜即生善。次釋疑者，恐引此為疑：生<a 
name="0830c24" id="0830c24"></a>善文中復有破惡者何耶？於中先出相狀，<a 
name="0830c25" id="0830c25"></a>次正判。初文言「如雜業」等者，文引二經云：<a 
name="0830c26" id="0830c26"></a>如經中說，以雜業故雜生世間，及更有破<a 
name="0830c27" id="0830c27"></a>群那經。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>問：</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此二經云何通？</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>答：</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>有人不信罪<a 
name="0830c28" id="0830c28"></a>福墮於斷見，斷彼疑故說雜業等。若計<a 
name="0830c29" id="0830c29"></a>有神我墮於常見，如破群那來問佛言誰<a 
name="0831a01" id="0831a01"></a>受？若佛說言某受。更增其邪，是故不說受<a 
name="0831a02" id="0831a02"></a>者觸者，但說無觸無受。此之二人雖有此<a 
name="0831a03" id="0831a03"></a>疑，機在生善。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>次「此意」下正判，傍為二人不<a 
name="0831a04" id="0831a04"></a>信罪福破其斷常，正為二人令生善根，<a 
name="0831a05" id="0831a05"></a>故為人中非無破惡，但有傍正，故云正為<a 
name="0831a06" id="0831a06"></a>生善，傍為破惡。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>三對治中亦標、釋、結。釋中二：<a 
name="0831a07" id="0831a07"></a>初辨有無，次「對治」下正釋相。初文中云「有<a 
name="0831a08" id="0831a08"></a>法對治則有」等者，有能治所治，故名為「有」。<a 
name="0831a09" id="0831a09"></a>第一義中既無能所，故云「則無」。此中實性&#x4F39;<a 
name="0831a10" id="0831a10"></a>是對辨，為顯對治有能所耳。次結中云「說<a 
name="0831a11" id="0831a11"></a>此法藥」等者，通論四悉無非法藥。今從別<a 
name="0831a12" id="0831a12"></a>說，從對治邊立名便故且作此說。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>次第一<a 
name="0831a13" id="0831a13"></a>義中亦標、釋、結。釋中二：初立二種不同，次「不<a 
name="0831a14" id="0831a14"></a>可」下釋二種相。初文言「有二」者，意說今文<a 
name="0831a15" id="0831a15"></a>言第一義者，從教法邊正當可說，不可說<a 
name="0831a16" id="0831a16"></a>者當內證邊，非今文意，故須辨異。初釋不<a 
name="0831a17" id="0831a17"></a>可說中，初正出相，次引論證。初文者，證法<a 
name="0831a18" id="0831a18"></a>無憑須寄人辨，故云「諸佛等所得之法」，所<a 
name="0831a19" id="0831a19"></a>引偈者亦是論第一義文中引之。初之二句<a 
name="0831a20" id="0831a20"></a>明證法功能，能離言說妄想故也。次二句<a 
name="0831a21" id="0831a21"></a>者明所證法體，上句正明法體，次句引例<a 
name="0831a22" id="0831a22"></a>況釋，如過去諸佛所得涅槃不生滅法在我<a 
name="0831a23" id="0831a23"></a>身中。「說諸」下一偈與世界互辨，前明世界<a 
name="0831a24" id="0831a24"></a>亦與第一義互辨，故今亦然。次可說者，正明<a 
name="0831a25" id="0831a25"></a>今意四句皆實，故無非第一義也。「佛於」下<a 
name="0831a26" id="0831a26"></a>引證。「此亦是一家明四門入實」者，判也。謂一<a 
name="0831a27" id="0831a27"></a>切實等四句，次第以對有等四門，上皆名「等」<a 
name="0831a28" id="0831a28"></a>者，入實也。「故中論」下證入實意，謂為利根<a 
name="0831a29" id="0831a29"></a>故也。「快馬見鞭影」如《止觀》第二記。「若聞」下結，<a 
name="0831b01" id="0831b01"></a>反以無結有，故云「豈第一義耶」。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>十五番者，又<a 
name="0831b02" id="0831b02"></a>二：初正釋，次「問」下料簡。初文者，總十五番，謂<a 
name="0831b03" id="0831b03"></a>一事理，二假實，三善惡人，四善惡陰，五善惡<a 
name="0831b04" id="0831b04"></a>法，六三世，七內外凡，八見脩聖，九凡及聖，非<a 
name="0831b05" id="0831b05"></a>學非無學即凡位也。十至十三四悉各為一<a 
name="0831b06" id="0831b06"></a>番，十四以四悉通為一番，十五別約四諦。<a 
name="0831b07" id="0831b07"></a>此十五番為欲令人了四悉義遍一切法，若<a 
name="0831b08" id="0831b08"></a>得此意觸境皆成自行化他法皆具足，然須<a 
name="0831b09" id="0831b09"></a>細釋此十五番，皆使順於歡喜生善破惡入<a 
name="0831b10" id="0831b10"></a>真。又此十五番雖趣舉一法，不無次第。於<a 
name="0831b11" id="0831b11"></a>中為四：初之六番約所觀人法，次內外凡下<a 
name="0831b12" id="0831b12"></a>三番約能觀凡聖，三五番約所施方法，四一<a 
name="0831b13" id="0831b13"></a>番約所觀諦，非所觀法無以成能觀人，非<a 
name="0831b14" id="0831b14"></a>能觀人無以用所施法，非上三故不成於<a 
name="0831b15" id="0831b15"></a>諦，諦是所觀，義通能所人法迷悟故也。今略<a 
name="0831b16" id="0831b16"></a>出相狀，不可具記。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>初事理者，名通迷悟因<a 
name="0831b17" id="0831b17"></a>果等法，今且在迷為迷說於事中有理而<a 
name="0831b18" id="0831b18"></a>理異於事，直聞此法心生歡喜屬世界，聞<a 
name="0831b19" id="0831b19"></a>能生善破惡入真即屬後三。假實異故聞復<a 
name="0831b20" id="0831b20"></a>歡喜即屬世界。輪等譬實法，車即譬假名，<a 
name="0831b21" id="0831b21"></a>五陰及人共譬和合有人也。從人邊說即<a 
name="0831b22" id="0831b22"></a>為人。論既云「或說有人無人」，人能生善故<a 
name="0831b23" id="0831b23"></a>也。單說實法，實法虛幻，可以觀治。「雙非」下<a 
name="0831b24" id="0831b24"></a>明第一義，論中既云「言語道斷」，故云雙非<a 
name="0831b25" id="0831b25"></a>即第一義，故一一悉並引論證。此假實相對<a 
name="0831b26" id="0831b26"></a>為一番。次單約假人，次單約實法。從外凡<a 
name="0831b27" id="0831b27"></a>陰生內凡陰，以內外凡陰破三界陰，無漏<a 
name="0831b28" id="0831b28"></a>即初果等，陰雖有內外等不同，然陰終是所<a 
name="0831b29" id="0831b29"></a>觀之境。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>次「問」下略料簡。初問者，人名既通，何<a 
name="0831c01" id="0831c01"></a>故生善獨得人名？答意者，惡是所破，非所<a 
name="0831c02" id="0831c02"></a>為故不立人名，人能生善，非無惡人。次問<a 
name="0831c03" id="0831c03"></a>者，生善唯在為人，何故令人破惡？答意者，<a 
name="0831c04" id="0831c04"></a>有傍正故得名不同。次約三世中來世是<a 
name="0831c05" id="0831c05"></a>為人者，為人本生其宿善，現種微善當來<a 
name="0831c06" id="0831c06"></a>可生，以當望現故現名宿。若治現惡名<a 
name="0831c07" id="0831c07"></a>為對治，可見。次約凡位者，以四善根內<a 
name="0831c08" id="0831c08"></a>用對於外凡，名內外異。&#x7157;法已去，善陰漸<a 
name="0831c09" id="0831c09"></a>生，義當生善。總別念中以無常等治，故云<a 
name="0831c10" id="0831c10"></a>對治。以第一義通初後故，凡位中亦得通<a 
name="0831c11" id="0831c11"></a>用。次約聖位者，見道生理善，脩道治事惡，<a 
name="0831c12" id="0831c12"></a>既純約聖位，故聖位中極方名第一義。次<a 
name="0831c13" id="0831c13"></a>凡聖共立雙非即是內外凡位。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>次「世界」下料<a 
name="0831c14" id="0831c14"></a>簡釋疑，非十五番數，由前文云世界中無<a 
name="0831c15" id="0831c15"></a>第一義。今問：為人、對治為有第一義，第一<a 
name="0831c16" id="0831c16"></a>義有三耶？故今文云展轉相生，由觀世界<a 
name="0831c17" id="0831c17"></a>故生善，由生善故破惡，由惡破故見理，<a 
name="0831c18" id="0831c18"></a>理中則無世界等也。是則善生惡滅，若互<a 
name="0831c19" id="0831c19"></a>有無終不見理。次約四悉。各立四悉者，<a 
