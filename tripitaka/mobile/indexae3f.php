name="0835b17" id="0835b17"></a>句解釋，是則解脫有萬名字。「如天帝釋有千<a 
name="0835b18" id="0835b18"></a>種名」者，亦名憍尸迦，亦名婆蹉婆，亦名婆<a 
name="0835b19" id="0835b19"></a>佉婆，亦名因陀羅，亦名千眼天，亦名舍脂<a 
name="0835b20" id="0835b20"></a>夫，亦名金剛寶頂，亦名寶幢等。「名異」下結，<a 
name="0835b21" id="0835b21"></a>名異故別，禮一故通。別中云「今稱」等者，妙法<a 
name="0835b22" id="0835b22"></a>是別，異餘經故；經即是通，通名經故。故知<a 
name="0835b23" id="0835b23"></a>今經通別始自如是，終乎而退，莫非佛說，<a 
name="0835b24" id="0835b24"></a>俱是妙法，從始至終咸別並通。於此教通<a 
name="0835b25" id="0835b25"></a>別中詮於行理通別，故知本&#x8FF9;無非因果<a 
name="0835b26" id="0835b26"></a>及名實故，不可遍引，故略示方隅，&#x9E81;引<a 
name="0835b27" id="0835b27"></a>譬喻以示行之通別，及方便中實相等為理<a 
name="0835b28" id="0835b28"></a>通別。譬喻中者，諸子習因不同即是行別，等<a 
name="0835b29" id="0835b29"></a>賜大車直至道場即是行通，大車是所乘通，<a 
name="0835c01" id="0835c01"></a>理道場是所契實相，乃至大慧之名亦是名<a 
name="0835c02" id="0835c02"></a>別，一一別名咸從理立即是理通。此是今經<a 
name="0835c03" id="0835c03"></a>理之通別，具如下釋中更廣明之。四「約此」下<a 
name="0835c04" id="0835c04"></a>結。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>次料簡中先問、次答。問中所以但問教<a 
name="0835c05" id="0835c05"></a>者，教行理三展轉互通，教既居初，但從教<a 
name="0835c06" id="0835c06"></a>問，餘之二種憑教自顯。初廣略二解汎通諸<a 
name="0835c07" id="0835c07"></a>教，是故通以一佛為通；今即離為四佛不<a 
