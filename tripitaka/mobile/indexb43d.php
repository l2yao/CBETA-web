<span class="linehead" title="PTS.C&#x016B;l.590"></span><a 
name="0461a01" id="0461a01"></a><a 
name="0461a02" id="0461a02"></a><p style="margin-left: 2em"><span class="headname"><span class="linehead" title="PTS.C&#x016B;l.591"> [P.591] </span>第一百零一章　<span class="corr" title='[&gt;未完]'>未完</span></span></p><a 
name="0461a03" id="0461a03"></a><a 
name="0461a04" id="0461a04"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>西利羅奢提羅奢西哈是彼之弟，彼之歿後就楞伽國之王位。（一）上於王位之〔彼<a 
name="0461a05" id="0461a05"></a>王〕，於三寶有淨信，於專心聽聞正法，精進而明敏，兄之所作事，皆使增展世間〔與<a 
name="0461a06" id="0461a06"></a>佛〕教之物，人民之主無廢棄此，照行如前。（二、三）大名稱者，於殊勝齒舍利亦<a 
name="0461a07" id="0461a07"></a>飲食物無罷，供養如前。（四）於前為勝者之實子等（佛弟子）所設施與物，王之施<a 
name="0461a08" id="0461a08"></a>命隨其次第應照樣施之。（五）兄王之時，由沙敏達國來之比丘等，以優波利長老為<a 
name="0461a09" id="0461a09"></a>首於西利瓦達那城，由此，此〔外〕來之比丘等，優波利長老為始，於都之南方，<a 
name="0461a10" id="0461a10"></a>所知為具斯瑪羅瑪之〔處〕，依一白一羯磨以結界。（六、八上）</p><a 
name="0461a11" id="0461a11"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>見奇提西利王，其時所作城中布薩堂之朽，求善根之人王，先於此境地立尖角<a 
name="0461a12" id="0461a12"></a>塔，四周用更多之石材，築結界內垣。（八下、一&#x3007;）彼生類之主，立石柱設布薩堂，<a 
name="0461a13" id="0461a13"></a>施於四方來之僧伽。（一一）精通巴利，梵文學等種種之學，喜常住施，具相如欲神。<a 
name="0461a14" id="0461a14"></a>（一二）熟此音聲學之〔王〕阿沙提沙本生故事之詩，令書寫為西哈羅語。（一三）<a 
name="0462a01" id="0462a01"></a><span class="linehead" title="PTS.C&#x016B;l.592"> [P.592] </span>清淨心〔之王〕，依信心一夜點百千之燈明，以迎齒舍利。（一四）以迦絺那衣無差<a 
name="0462a02" id="0462a02"></a>別之布施聞有功德，彼〔王〕每年於僧伽行迦絺那衣之施，以青銅作安義羅沙像，<a 
name="0462a03" id="0462a03"></a>為王〔自〕等身之大，亦欲得勝者之&#x9262;，於康伽羅瑪，於〔人〕所尊敬之精舍內，<a 
name="0462a04" id="0462a04"></a>作喜樂殊妙之支提，（一五、一七）積如是等之善業，彼〔大〕地主，於第十八年，<a 
name="0462a05" id="0462a05"></a>隨業而去此〔世〕。（一八）</p><a 
name="0462a06" id="0462a06"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>羅奢提羅姜西哈之外甥人民之主，為剛毅之國主，有西利加瑪羅奢西哈，喜聽<a 
name="0462a07" id="0462a07"></a>勝者所語之法，〔大〕地之主，對齒舍利尊，供養摩尼珠真珠等，同幾多村落田畝。<a 
name="0462a08" id="0462a08"></a>（一九、二&#x3007;）以佛陀為始，不只幾次施與僧伽勝妙之食物，願無限之光榮。（二一）</p><a 
name="0462a09" id="0462a09"></a><p style="text-indent: 2em; margin-left: 0em"><span class="linehead"></span>依不良之交，〔王〕逢如次之變化，集協議之諸大臣與其他之甚多官吏，如〔滅〕<a 
name="0462a10" id="0462a10"></a>羅剎之人而滅之。（二三、二四上）數百阿羅僧祇之諸人，由各處捕來，無慈悲如死<a 
name="0462a11" id="0462a11"></a>王，串刺〔彼等〕，此等諸人傳來巨大之財產，此王破壞村落如盜賊之掠奪。（二四<a 
name="0462a12" id="0462a12"></a>下、二六上）如此王行種種之非法，怒而住於庫蘭婆之西哈羅人等由各處來，此邪<a 
name="0462a13" id="0462a13"></a>法之王，其即位過十八年時，生擒而追放於對岸。（二六下、二八）壓迫世間此〔王〕<a 
name="0462a14" id="0462a14"></a>追放於對岸而因義利西人收一王國於己手中。（二九）</p><a 
name="0463a01" id="0463a01"></a><p style="text-indent: 0em; margin-left: 4em"><span class="linehead"></span><span class="corr" title='[大&gt;小]'>小</span>王統史　畢</p><a 
name="0463a02" id="0463a02"></a><p style="text-indent: 0em; margin-left: 4em"><span class="linehead"></span>有成就吧！</p><a 
