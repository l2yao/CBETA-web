name="0677b23" id="0677b23"></a>法也。問曰。佛性有三。如來種亦有三耶。答曰。種亦有<a 
name="0677b24" id="0677b24"></a>三。經言。有身為種。六入為種。即苦道正因如來種。正<a 
name="0677c01" id="0677c01"></a>因佛性之異名也。經言。無明貪恚為種。六十二見為<a 
name="0677c02" id="0677c02"></a>和。即是了因如來種。了因佛性之異名也。經言。十不<a 
name="0677c03" id="0677c03"></a>善道為種。即緣因如來種。緣因佛性之異名也。種之<a 
