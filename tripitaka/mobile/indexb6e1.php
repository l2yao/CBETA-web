<a 
name="0675b05" id="0675b05"></a><a 
name="0675b06" id="0675b06"></a><a 
name="0675b07" id="0675b07"></a><p><span class="juanname">維摩詰經三觀玄義卷下</span></p><a 
name="0675b08" id="0675b08"></a><a 
name="0675b09" id="0675b09"></a><p style="margin-left: 4em"><span class="byline">隋天台山修禪寺沙門　智顗　撰</span></p><a 
name="0675b10" id="0675b10"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第四會乘義者。三觀即是三智。三智有二種。一別相<a 
name="0675b11" id="0675b11"></a>三智。二一心三智。一別相三智即開三乘。二一心三<a 
name="0675b12" id="0675b12"></a>智但是一佛乘也。第一正明別相三觀開三乘者。即<a 
name="0675b13" id="0675b13"></a>為二意。一正約三觀開三乘。二明十法成三乘。一正<a 
name="0675b14" id="0675b14"></a>約三觀開三乘者。即為三意。一約析法觀開三藏教<a 
name="0675b15" id="0675b15"></a>三乘。二約體法觀開通教三乘。三總約析體別相三<a 
name="0675b16" id="0675b16"></a>觀成別教大乘。一明析法觀開三藏教三乘者。三藏<a 
name="0675b17" id="0675b17"></a>教明三乘行人同析因緣假以入空。若聲聞。總相析<a 
name="0675b18" id="0675b18"></a>法入空。發真無漏。成一切智。名聲聞乘。若辟支佛。別<a 
name="0675b19" id="0675b19"></a>相析法入空。發真無漏。成一切智。名辟支佛乘。若菩<a 
name="0675b20" id="0675b20"></a>薩。總相別相析法入空而不斷結取證。多入俗假修<a 
name="0675b21" id="0675b21"></a>行六度。求一切智．佛智．自然智．無師智。即是三藏教<a 
name="0675b22" id="0675b22"></a>之大乘也。二明體假入空開通教三乘者。三乘之人<a 
name="0675b23" id="0675b23"></a>同體因緣假以入空。若發真無漏。斷見思惑。小乘根<a 
name="0675b24" id="0675b24"></a>鈍但除正使。成一切智。名聲聞乘。緣覺中根侵除習<a 
name="0675c01" id="0675c01"></a>氣。成一切智。名辟支佛乘。菩薩得一切智。入假修道<a 
name="0675c02" id="0675c02"></a>種智。教化眾生。求一切種智。即是通教之大乘。三明<a 
name="0675c03" id="0675c03"></a>總約析體別相三觀成別教大乘者。若別教菩薩。觀<a 
name="0675c04" id="0675c04"></a>因緣假。修別相三觀。次第成一切智．道種智。乃至修<a 
name="0675c05" id="0675c05"></a>中道觀見佛性。成一切種智。求常住涅槃。即是別教<a 
name="0675c06" id="0675c06"></a>大乘義也。</p><a 
name="0675c07" id="0675c07"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>二明具十法成乘者。三觀乃是乘之正體。若不約十<a 
name="0675c08" id="0675c08"></a>法和合。則乘義不成。所以者何。三乘悉能運出三界<a 
name="0675c09" id="0675c09"></a>火宅。必須正助眾善和合。故運用之義成也。就此即<a 
name="0675c10" id="0675c10"></a>為三意。一出十法名。二明次第成乘。三料簡。一出十<a 
name="0675c11" id="0675c11"></a>法名者。一明識正因緣生法。二真正發心。三止觀修<a 
name="0675c12" id="0675c12"></a>習。四破諸法遍。五善知通塞。六道品調適。七對治助<a 
name="0675c13" id="0675c13"></a>開三解脫門。八明識次位。九安忍強&#x8F2D;兩賊。十順道<a 
name="0675c14" id="0675c14"></a>法愛不生。三乘之人修學三觀。若具此十法。即成三<a 
name="0675c15" id="0675c15"></a>乘入涅槃也。二明次第成乘者。初所以順知正因緣<a 
name="0675c16" id="0675c16"></a>法者。知無明因緣生一切法。即是正因緣。異外人執<a 
name="0675c17" id="0675c17"></a>邪因緣無因緣生一切法也。次明真正發心者。三乘<a 
name="0675c18" id="0675c18"></a>行人明知正因緣所生三界火宅。覺悟生死。志求涅<a 
name="0675c19" id="0675c19"></a>槃。但菩薩大悲濟物。心異也。次明止觀修習者。發心<a 
name="0675c20" id="0675c20"></a>信解既明。必須修行定慧。即是三乘行人之根本也。<a 
name="0675c21" id="0675c21"></a>次明破諸法遍者。若不破見思兩輪所執妄想。破妄<a 
name="0675c22" id="0675c22"></a>想不遍。則止觀有滯也。次明須知通塞者。隨所破法<a 
name="0675c23" id="0675c23"></a>從淺至深。皆有道滅之通。苦集之塞。若迷此理。即不<a 
name="0675c24" id="0675c24"></a>知得失。是字非字去取失宜也。次明道品調適者。三<a 
name="0676a01" id="0676a01"></a>十七品是三乘入道之正要。能引進眾行到三脫門<a 
name="0676a02" id="0676a02"></a>入涅槃也。次明對治助道法者。即是四禪．四無量心．<a 
name="0676a03" id="0676a03"></a>四無色定．九想．八念．十想．八背捨．八勝處．十一切處．<a 
name="0676a04" id="0676a04"></a>九次第定．事中六度等。諸對治法。助開三脫門也。次<a 
name="0676a05" id="0676a05"></a>明須知位次者。三乘入道。從乾慧地乃至佛地。若能<a 
name="0676a06" id="0676a06"></a>分別不謬。即不生叨濫。破增上慢心也。次明安忍強<a 
name="0676a07" id="0676a07"></a>&#x8F2D;兩賊者。未入外凡之位。內外八風壞三乘行人出<a 
name="0676a08" id="0676a08"></a>世善根。若能安忍。則不為所壞。入乾慧地。因發&#x7157;頂<a 
name="0676a09" id="0676a09"></a>入性地也。次明順道法愛不生者。三乘之人若入性<a 
name="0676a10" id="0676a10"></a>地。發善有漏五陰所有善法功德智慧。順道若生法<a 
name="0676a11" id="0676a11"></a>愛。即便頂墮。不得<span class="corr" title='[遮&gt;進]'>進</span>入忍法成世第一法發真無漏<a 
name="0676a12" id="0676a12"></a>也。若能不生法愛。即不頂墮。得入忍法位。成世第一<a 
name="0676a13" id="0676a13"></a>法。發真無漏。即是三乘之人同見第一義諦。斷界內<a 
name="0676a14" id="0676a14"></a>見思煩惱。出三界火宅。是為此乘從三界出。至有餘<a 
name="0676a15" id="0676a15"></a>涅槃住。盡智．無生智運入。無餘不明故。以十法成三<a 
name="0676a16" id="0676a16"></a>乘者。其義顯也。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>三明料簡者。問曰。自有眾生值佛。隨<a 
name="0676a17" id="0676a17"></a>聞一乘法即得成道。或有隨修一法門即入道。何必<a 
name="0676a18" id="0676a18"></a>悉具此十法成乘也。答曰。皆以往昔<span class="corr" title='[巳&gt;已]'>已</span>修引十法成<a 
name="0676a19" id="0676a19"></a>根性也。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>問曰。法華經出三界火宅。諸子門外方索三<a 
name="0676a20" id="0676a20"></a>車。今何得十法成乘從界內而出。答曰。法華的據盡<a 
name="0676a21" id="0676a21"></a>無生三乘。有為無漏功德是究竟三乘。能運入無餘<a 
name="0676a22" id="0676a22"></a>涅槃。乃是真乘。既無無餘涅槃之可入。又不能運至<a 
name="0676a23" id="0676a23"></a>常住涅槃。則三乘之義不成。故索三車不得也。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>今通<a 
name="0676a24" id="0676a24"></a>明乘義有六種不同。一者理乘。二者教乘。三者行乘。<a 
name="0676b01" id="0676b01"></a>四者相似乘。五者分證真實乘。六者究竟乘。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>一理乘<a 
name="0676b02" id="0676b02"></a>者。三乘行人悉有四諦．十二因緣．六度之一。三乘根<a 
name="0676b03" id="0676b03"></a>性不同也。二教乘者。即是佛開三乘之教。三乘諸子<a 
name="0676b04" id="0676b04"></a>以佛教門出三界苦。亦名名字乘也。三觀行乘者。即<a 
name="0676b05" id="0676b05"></a>是三乘之人修五停心觀。別相總相念處。乾慧地之<a 
name="0676b06" id="0676b06"></a>觀行也。故勝鬘經云。三乘之初業。不愚於法。意在此<a 
name="0676b07" id="0676b07"></a>也。四相似乘者。即是四善根人所得善有漏五陰也。<a 
name="0676b08" id="0676b08"></a>五分證真實乘者。即是學智。從發苦忍真明無漏。乃<a 
name="0676b09" id="0676b09"></a>至非想第九無礙金剛三昧也。六究竟乘者。即是無<a 
name="0676b10" id="0676b10"></a>學智阿羅漢．辟支佛．佛所得非想第九解脫道盡智．<a 
name="0676b11" id="0676b11"></a>無生智。佛如實智能運入無餘涅槃也。此即三藏教<a 
name="0676b12" id="0676b12"></a>析法觀．通教體法觀。十法成乘意在此也。但別教三<a 
name="0676b13" id="0676b13"></a>觀十法成乘。明六種乘義。意不同分別<span class="note">(云云)</span>。</p><a 
name="0676b14" id="0676b14"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>問曰。佛法無量。何故的取此十法成三乘耶。答曰。佛<a 
name="0676b15" id="0676b15"></a>法雖復無量。必須取其正要。如諸小乘經論．大乘經<a 
name="0676b16" id="0676b16"></a>論所明成乘義者。教門悉有此十意分明也。但隨緣<a 
name="0676b17" id="0676b17"></a>散說。不聚一處。今探經論。撰十意以成乘義者。為欲<a 
name="0676b18" id="0676b18"></a>令一家義學坐禪之徒。知佛法小乘經論所明．大乘<a 
name="0676b19" id="0676b19"></a>經論所明入道正意。異外人各說一究竟道。亦異此<a 
name="0676b20" id="0676b20"></a>土莊老之說也。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>諸先生若言莊老即是佛一無異者。<a 
name="0676b21" id="0676b21"></a>又末代朝有法師禪師。或同諸外道作此解者。可將<a 
name="0676b22" id="0676b22"></a>此十法比並。若莊老明空具有此十意分明名義成<a 
name="0676b23" id="0676b23"></a>就者。可許是同。若無此十法。或名義似同而斫覈橫<a 
name="0676b24" id="0676b24"></a>&#x7AEA;不通。事理滯礙。名字有闕。作義不成。豈得同也。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>今<a 
name="0676c01" id="0676c01"></a>毗曇．成實雖是佛法小乘之論。明空入道。撿其論文<a 
name="0676c02" id="0676c02"></a>即十意宛然。名義無闕。通申佛法。小乘人入道意轉<a 
name="0676c03" id="0676c03"></a>分明。況復大乘經論者乎。外人經書既無此名義故。<a 
name="0676c04" id="0676c04"></a>不可皆言同佛法也。</p><a 
name="0676c05" id="0676c05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第二明一心三智但是一佛乘者。即為二意。一明一<a 
name="0676c06" id="0676c06"></a>心三智即大乘。二明十法成大乘。一明一心三智即<a 
name="0676c07" id="0676c07"></a>大乘者。若觀因緣三諦。初心即得一心三智。開佛知<a 
name="0676c08" id="0676c08"></a>見。見佛性。見佛性者即大乘也。此則無復開三乘之<a 
name="0676c09" id="0676c09"></a>別。故此經觀眾生品。舍利弗問天女言。汝於三乘為<a 
name="0676c10" id="0676c10"></a>何志求。天女答曰。我為化三乘。如人入瞻蔔林。唯&#x9F45;<a 
name="0676c11" id="0676c11"></a>瞻蔔。不&#x9F45;餘。得入此室者。唯聞大乘功德之香。不樂<a 
name="0676c12" id="0676c12"></a>聞聲聞．辟支佛功德香也。當知一心三智即是圓教。<a 
name="0676c13" id="0676c13"></a>般若波羅蜜即是大乘。故大品會宗品云。般若波羅<a 
name="0676c14" id="0676c14"></a>蜜即是摩訶衍。摩訶衍即是般若波羅蜜。般若波羅<a 
name="0676c15" id="0676c15"></a>蜜．摩訶衍無二無別。今明一心三觀會成大乘者。大<a 
name="0676c16" id="0676c16"></a>名不可思議。乘以能運為義。一心三觀境智並是不<a 
name="0676c17" id="0676c17"></a>思議法。能運菩薩至今道場。名大乘也。故涅槃經云。<a 
name="0676c18" id="0676c18"></a>一乘者名為佛性。佛性者亦一．非一．非一非非一。云<a 
name="0676c19" id="0676c19"></a>何名為亦一。謂一切眾生悉是一乘故。云何名非一。<a 
name="0676c20" id="0676c20"></a>非是數法故。云何非一非非一。數非數不決定故。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>一<a 
name="0676c21" id="0676c21"></a>切眾生即一乘者。一念無明具十法界。眾生悉有正<a 
name="0676c22" id="0676c22"></a>因佛性。正因佛性即是理。即本有之一乘也。數法者。<a 
name="0676c23" id="0676c23"></a>即是緣因佛性。緣因佛性即是福德莊嚴。福德莊嚴<a 
name="0676c24" id="0676c24"></a>有為有漏。是聲聞法分。為數法說三乘也。非一非非<a 
name="0677a01" id="0677a01"></a>一。數非數不決定者。即了因佛性。了因佛性即是智<a 
name="0677a02" id="0677a02"></a>慧莊嚴。智慧莊嚴能從一地至於一地。不得緣因亦<a 
name="0677a03" id="0677a03"></a>不得正因。心無取著。數非數不決定也。數不決定者。<a 
name="0677a04" id="0677a04"></a>緣因性不可得也。非數不決定者。正因性不可得也。<a 
name="0677a05" id="0677a05"></a>若所觀不決定不可得。能了亦不決定不可得。以十<a 
name="0677a06" id="0677a06"></a>二因緣不可說故。三種佛性大乘皆不可說也。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>此三<a 
name="0677a07" id="0677a07"></a>佛性成三德大乘。不縱不橫。如世伊字。故涅槃經云。<a 
name="0677a08" id="0677a08"></a>佛性者即是一乘。法華經云。佛自住大乘。如其所得<a 
name="0677a09" id="0677a09"></a>法。定慧力莊嚴。以此度眾生。此亦三德成乘義也。故<a 
name="0677a10" id="0677a10"></a>真諦三藏說理乘．得乘．隨乘之三種乘。問曰。此經但<a 
name="0677a11" id="0677a11"></a>明不思議解脫。云何乃引涅槃經佛性一乘義耶。答<a 
