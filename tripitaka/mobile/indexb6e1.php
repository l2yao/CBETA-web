name="0683a15" id="0683a15"></a>不斷癡愛起諸明脫。以五逆相而得解脫之法門也。<a 
name="0683a16" id="0683a16"></a>菩薩住是解脫。能以須彌內芥子。種種示現。三正用<a 
name="0683a17" id="0683a17"></a>三觀釋毗摩羅詰栗致淨無垢稱義者。淨名居士因<a 
name="0683a18" id="0683a18"></a>緣所生之心。三諦之理。性常皎然。目之為淨。不斷三<a 
name="0683a19" id="0683a19"></a>諦惑障。能起一心三觀三智之智。明解脫。智明解脫。<a 
name="0683a20" id="0683a20"></a>雖處三惑之內。不為三惑所染。故稱無垢。一心三智<a 
name="0683a21" id="0683a21"></a>會三諦之理。大用無方。稱機而化。故名為稱。故云淨<a 
name="0683a22" id="0683a22"></a>無垢稱義也。</p><a 
name="0683a23" id="0683a23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第七用三觀釋此經一部義者。略為二意。一釋經玄<a 
name="0683a24" id="0683a24"></a>五義。二正用三觀通釋此經文。一釋玄五義者。初用<a 
name="0683b01" id="0683b01"></a>三觀釋淨名之號。<span class="corr" title='[巳&gt;已]'>已</span>如前說。二次釋經體。第三觀顯<a 
name="0683b02" id="0683b02"></a>不思議正道之理。即是真性之體顯也。三釋經宗者。<a 
name="0683b03" id="0683b03"></a>三觀斷三諦之緣縛。即是四種佛國之因行。行成得<a 
name="0683b04" id="0683b04"></a>四種淨土之果也。四釋經用者。三觀破三諦之惑。即<a 
name="0683b05" id="0683b05"></a>是析伏之義。攝三諦之理善。即攝受義。即經之用也。<a 
name="0683b06" id="0683b06"></a>五明教相者。此經約三觀明教。與餘有同有異。具如<a 
name="0683b07" id="0683b07"></a>前釋。即判教相也。二正約三觀釋此三卷經文者。即<a 
name="0683b08" id="0683b08"></a>為三意。一釋室外。二釋室內。三釋出室。一釋室外四<a 
name="0683b09" id="0683b09"></a>品。三觀成佛國因果之義也。<span class="corr" title='[巳&gt;已]'>已</span>如明宗所說。即是釋<a 
name="0683b10" id="0683b10"></a>佛國品也。次析體二種入空。釋方便品訶諸國王長<a 
name="0683b11" id="0683b11"></a>者。體假入空第二第三觀。釋弟子品訶十大弟子之<a 
name="0683b12" id="0683b12"></a>意正在此也。次第三中道第一義觀。釋菩薩品訶四<a 
name="0683b13" id="0683b13"></a>大菩薩也。次明入室六品。若問疾品。淨名空室以疾<a 
name="0683b14" id="0683b14"></a>而臥。即表中道第一義諦觀。相應種智。依常寂國現<a 
name="0683b15" id="0683b15"></a>空室相也。慰喻有疾菩薩自調伏其心者。即用三觀<a 
name="0683b16" id="0683b16"></a>慰喻有疾菩薩。有疾菩薩亦用三觀以調三諦之疾<a 
name="0683b17" id="0683b17"></a>也。不思議解脫品即是第三觀。種智之果住於正道。<a 
name="0683b18" id="0683b18"></a>雙照二諦。種種示現也。次釋觀眾生品者。即是正用<a 
name="0683b19" id="0683b19"></a>初觀釋也。次釋佛道品者。即是用第二觀釋也。次釋<a 
name="0683b20" id="0683b20"></a>入不二法門品者。即是用第三觀釋也。次釋香積品<a 
name="0683b21" id="0683b21"></a>者。即是還用第三觀雙照二諦。垢淨俱遊釋也。次釋<a 
name="0683b22" id="0683b22"></a>室外。菩薩行品見阿&#x95A6;佛者。即是用三觀通釋佛國<a 
name="0683b23" id="0683b23"></a>因果。同前佛國之意也。次釋法供養．囑累二品。是流<a 
name="0683b24" id="0683b24"></a>通室內室外三觀析伏抑訶之意耳。譬如諸樹林。華<a 
name="0683c01" id="0683c01"></a>葉或生落。如是諸佛剎。成敗亦復然。如依種種樹。有<a 
name="0683c02" id="0683c02"></a>種種果生。如是種種剎。有種種眾生。種種差別故。果<a 
name="0683c03" id="0683c03"></a>實生不同。行業若于故。佛剎種種異。心如工<span class="gaiji">畫<!--gaiji,畫,1[書-曰+(且-二+人)],2&#x26615;,3--></span>師。<span class="gaiji">畫<!--gaiji,畫,1[書-曰+(且-二+人)],2&#x26615;,3--></span><a 
name="0683c04" id="0683c04"></a>種種五陰。一切世間中。無法而不造。如心佛亦爾。如<a 
name="0683c05" id="0683c05"></a>佛眾生然。心佛及眾生。是三無差別。諸佛悉了知。一<a 
name="0683c06" id="0683c06"></a>切從心轉。若能如是解。彼人見真佛。</p><a 
name="0683c07" id="0683c07"></a><a 
name="0683c08" id="0683c08"></a><p><span class="juanname">維摩詰經三觀玄義卷下<span class="note">(終)</span></span></p>
