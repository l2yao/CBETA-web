<a 
name="0874b12" id="0874b12"></a><a 
name="0874b13" id="0874b13"></a><a 
name="0874b14" id="0874b14"></a><a 
name="0874b15" id="0874b15"></a><p><span class="juanname">金剛經闡說卷下</span></p><a 
name="0874b16" id="0874b16"></a><a 
name="0874b17" id="0874b17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>爾時須菩提白佛言<span class="note">(至)</span>阿耨多羅三藐三菩提心者。</p><a 
name="0874b18" id="0874b18"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此段申明前文。而以無有法結云。&#x8462;須菩提敘述<a 
name="0874b19" id="0874b19"></a>前此問答之詞。有請益之意。所以者何以下。佛答<a 
name="0874b20" id="0874b20"></a>也。實無有法發阿耨多羅三藐三菩提心者。者字。<a 
name="0874b21" id="0874b21"></a>自明自性。告人。亦自明自性。何法之有。乃實指菩<a 
name="0874b22" id="0874b22"></a>薩示之。菩薩應除能所心也。</p><a 
name="0874b23" id="0874b23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提於意云何<span class="note">(至)</span>即非一切法是故名一切法。</p><a 
name="0874b24" id="0874b24"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此段佛又現身說法。以無上正等正覺。證實有得<a 
name="0874c01" id="0874c01"></a>阿耨多羅三藐三菩提也。如來。佛自謂也。正義云。<a 
name="0874c02" id="0874c02"></a>梵語釋迦。此云能仁。梵語牟尼。此云寂默。寂默為<a 
name="0874c03" id="0874c03"></a>體。即是如。能仁為用。即是來。先釋迦而後牟尼者。<a 
name="0874c04" id="0874c04"></a>攝用以歸體也。先如而後來者。從體以起用也。總<a 
name="0874c05" id="0874c05"></a>是一個真性。如號則為釋迦牟尼。通稱則為如來。<a 
name="0874c06" id="0874c06"></a>又為佛諸法如義。應前以是義故之義。皆是佛法。<a 
name="0874c07" id="0874c07"></a>即無&#x8657;不滅相也。即非一切法。即無實。不著相也。</p><a 
name="0874c08" id="0874c08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提譬如人身長大<span class="note">(至)</span>如來說名真是菩薩。</p><a 
name="0874c09" id="0874c09"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此段又申覆前文。而以無我法結之。佛復述前譬<a 
name="0874c10" id="0874c10"></a>之人身長大。須菩提既知非大身。是名大身。是悟<a 
name="0874c11" id="0874c11"></a>非大身者。即真心也。即此無四相之心也。故以菩<a 
name="0874c12" id="0874c12"></a>薩亦如是告之焉按前佛言我應滅度眾生云云。<a 
name="0874c13" id="0874c13"></a>此云若作是言。我當滅度無量眾生。即不名菩薩。<a 
name="0874c14" id="0874c14"></a>何也。&#x8462;此為菩薩未到佛地住者言之未到佛地<a 
name="0874c15" id="0874c15"></a>住。而作是言。即不名菩薩。佛欲菩薩先去四相為<a 
name="0874c16" id="0874c16"></a>要。故緊接以佛說一切法無四相。而以通達無我<a 
name="0874c17" id="0874c17"></a>法結之。此無我兼人眾壽者言。一說莫作是言。欲<a 
name="0874c18" id="0874c18"></a>菩薩去能所心也。亦通。</p><a 
name="0874c19" id="0874c19"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提於意云何<span class="note">(至)</span>如是世尊如來有佛眼。</p><a 
name="0874c20" id="0874c20"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>華嚴經云。肉眼。見一切色故。天眼。見一切眾生心<a 
name="0874c21" id="0874c21"></a>故。慧眼見一切眾生諸般境界故。法眼見一切法<a 
name="0874c22" id="0874c22"></a>如實相故。佛眼。見如來十力故。大般若經。所謂清<a 
name="0874c23" id="0874c23"></a>淨五眼是也。</p><a 
name="0874c24" id="0874c24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提於意云何<span class="note">(至)</span>未來心不可得。</p><a 
name="0875a01" id="0875a01"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此段重明不住心。而語益加切矣。王曰。佛世界者。<a 
name="0875a02" id="0875a02"></a>謂凡一大世界。必有一佛設化。故凡大世界。皆謂<a 
name="0875a03" id="0875a03"></a>之佛世界。若干種心。如來悉知。以五眼知之也。六<a 
name="0875a04" id="0875a04"></a>祖曰。爾所國土中。所有眾生。一一眾生。皆有若干<a 
name="0875a05" id="0875a05"></a>差別。心數雖多。總名妄心。識得妄心非心。是名為<a 
name="0875a06" id="0875a06"></a>心。顏云。思念前事為過去。思念今事為現在。思念<a 
name="0875a07" id="0875a07"></a>後事為未來。三念總放下著謂之不可得。王曰。不<a 
name="0875a08" id="0875a08"></a>可得者。謂無也。言此三心。本來無有。乃因事而有<a 
name="0875a09" id="0875a09"></a>耳。圓覺經。所以言六塵緣影為自心相者。謂眾生<a 
name="0875a10" id="0875a10"></a>以六種塵緣之影。為自<span class="corr" title='[已&gt;己]'>己</span>之心相也。過去現在未<a 
name="0875a11" id="0875a11"></a>來讀斷。下心不可得四字。連誦成句。</p><a 
name="0875a12" id="0875a12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提於意云何<span class="note">(至)</span>如來說得福德多。</p><a 
name="0875a13" id="0875a13"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>因緣者。由因而緣。因布施緣及福德也。福德有實<a 
name="0875a14" id="0875a14"></a>者。布施有用之福德。福德無者。即福德性。此多字。<a 
name="0875a15" id="0875a15"></a>與第八分多字異。彼以多為不足道。此則真為多<a 
name="0875a16" id="0875a16"></a>矣。張國維曰。此不是無福德。乃是福德無。無福德。<a 
name="0875a17" id="0875a17"></a>則為斷滅。福德無。乃是湛然清淨。即所謂福德性<a 
name="0875a18" id="0875a18"></a>也。疏鈔云。若據捨大千珍寶布施。其福極多。若執<a 
name="0875a19" id="0875a19"></a>著希望福德。有得則有盡。故云若福德執實有。如<a 
name="0875a20" id="0875a20"></a>來不說得福德多。此是反釋之義。言以福德無者。<a 
name="0875a21" id="0875a21"></a>希望心也。既無希望。即為無住相施。是名無為福。<a 
name="0875a22" id="0875a22"></a>若依無住無為而施者。故如來說得福德多。訥曰。<a 
name="0875a23" id="0875a23"></a>福有者取相也。福無者。離相也。離相。故稱性等&#x8657;<a 
name="0875a24" id="0875a24"></a>空。其福無量。</p><a 
name="0875b01" id="0875b01"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提於意云何<span class="note">(至)</span>無法可說是名說法。</p><a 
name="0875b02" id="0875b02"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此段亦覆前文。而以無法可說結之。具足。無欠缺<a 
name="0875b03" id="0875b03"></a>也。具足諸相。則不止三十二相。凡種種變現神通<a 
name="0875b04" id="0875b04"></a>之相。皆是。即非者。不執有。是名者。不執無。通經皆<a 
name="0875b05" id="0875b05"></a>然。王曰。說法者。實無有法。謂本來無法。特為眾生<a 
name="0875b06" id="0875b06"></a>除外妄而說耳。眾生既悟。則不用此法耳。故<span class="corr" title='[&#x4F39;&gt;但]'>但</span>&#x8657;<a 
name="0875b07" id="0875b07"></a>名為說法而<span class="corr" title='[巳&gt;已]'>已</span>。</p><a 
name="0875b08" id="0875b08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>爾時慧命須菩提白佛言<span class="note">(至)</span>說非眾生是名眾生。</p><a 
name="0875b09" id="0875b09"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此段佛若輕視。視眾生。可見慈悲心。無時無處不<a 
name="0875b10" id="0875b10"></a>有也。疏鈔云。言慧命者。善現達佛智海。入深法門。<a 
name="0875b11" id="0875b11"></a>悟慧無生。覺本源之命。非去非來。故曰慧命須菩<a 
name="0875b12" id="0875b12"></a>提。佛言彼非眾生者。皆具真一之性。與佛同源。故<a 
name="0875b13" id="0875b13"></a>曰非眾生。言非不眾生者。背真逐妄。自喪<span class="corr" title='[已&gt;己]'>己</span>靈。故<a 
name="0875b14" id="0875b14"></a>曰非不是眾生。爾時慧命須菩提六十二字。秦譯<a 
name="0875b15" id="0875b15"></a>無。惟魏譯有。唐穆宗時。長安僧靈幽。入冥誦經。少<a 
name="0875b16" id="0875b16"></a>此一段。冥王歎曰。貫花之線。何斷而不續乎。增壽<a 
name="0875b17" id="0875b17"></a>十年。令往濠州鐘離寺。求石<span class="gaiji">&#x7891;<!--gaiji,碑,1[石*((白-日+田)/廾)],2&#x7891;,3--></span>全文補入。</p><a 
name="0875b18" id="0875b18"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提白佛言世尊<span class="note">(至)</span>是名阿耨多羅三藐三菩提。</p><a 
name="0875b19" id="0875b19"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此段須菩提以如來說非眾生。可見各有自性。佛<a 
name="0875b20" id="0875b20"></a>之得阿耨多羅三藐三菩提。為得之自性而非得<a 
name="0875b21" id="0875b21"></a>之自外也。是信詞。非疑詞。顏曰。有法可得。是名法<a 
name="0875b22" id="0875b22"></a>縛。無法可得。方名解脫。須菩提以無可得之辭而<a 
name="0875b23" id="0875b23"></a>告世尊。世尊即以如是如是而證據之。佛又云。我<a 
name="0875b24" id="0875b24"></a>於無上正等正覺。乃至無少法可得。&#x8657;名而<span class="corr" title='[巳&gt;已]'>已</span>。王<a 
name="0875c01" id="0875c01"></a>曰。無小法可得。則蕩然空空。是不可此形相求。不<a 
name="0875c02" id="0875c02"></a>可以言說求也。但說名為無上正等正覺而見。</p><a 
name="0875c03" id="0875c03"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>復次須菩提是法平等<span class="note">(至)</span>即非善法是名善法。</p><a 
name="0875c04" id="0875c04"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>言上自諸佛。下至蠢動含靈。其真性一同。故云平<a 
name="0875c05" id="0875c05"></a>等。無有高下。即此名為無上正等正覺也。但眾生<a 
name="0875c06" id="0875c06"></a>雖同具此無上正等正覺之性。因迷於四相妄緣<a 
name="0875c07" id="0875c07"></a>而失之耳。若能以無四相。而修一切善法。即得無<a 
name="0875c08" id="0875c08"></a>上正等正覺也。佛又呼須菩提。而謂所言善法者<a 
name="0875c09" id="0875c09"></a>即。非善法謂。本來無此善法乃。假此以開悟眾生<a 
name="0875c10" id="0875c10"></a>耳。故但&#x8657;名為善法而<span class="corr" title='[巳&gt;已]'>已</span>。此段是佛至此明示學<a 
name="0875c11" id="0875c11"></a>者受持之方。最喫緊處。全在以脩兩字著力。</p><a 
name="0875c12" id="0875c12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提若三千大千世界中<span class="note">(至)</span>算數譬喻所不能及。</p><a 
name="0875c13" id="0875c13"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>五祖曰。自性若迷。福何可救。六祖曰。乘船永世求<a 
name="0875c14" id="0875c14"></a>珠。不知身是七寶。二佛之言。皆為世人不脩身脩<a 
name="0875c15" id="0875c15"></a>性。徒施寶以求福道。</p><a 
name="0875c16" id="0875c16"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提於意云何<span class="note">(至)</span>即非凡夫是名凡夫。</p><a 
name="0875c17" id="0875c17"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此段重戒人不當存有我私見也。呼汝等者。為提<a 
name="0875c18" id="0875c18"></a>醒會下諸大眾也。僧訥曰。如來雖設法施。廣度眾<a 
name="0875c19" id="0875c19"></a>生。而不作是念。故誡云汝等勿謂也。莫作是念。重<a 
name="0875c20" id="0875c20"></a>誡也。度無度相。能所一空。又曰。如來既無我人眾<a 
name="0875c21" id="0875c21"></a>生等相。何有時稱我。須知假名稱我。對所度眾生。<a 
name="0875c22" id="0875c22"></a>隨世說我。顏云。當人自性自度。迷來悟度。邪來正<a 
name="0875c23" id="0875c23"></a>度。從上諸佛所言。但有指出路頭。須是自行自履。<a 
name="0875c24" id="0875c24"></a>豈由他人。所以道實無有眾生如來度者。若有可<a 
name="0876a01" id="0876a01"></a>度。是如來有四相。如來乃見性人也。所以無我。凡<a 
name="0876a02" id="0876a02"></a>夫未見性人也。所以我相未忘。佛又恐人落分別<a 
name="0876a03" id="0876a03"></a>界。故曰。即非凡夫。所以見如來凡夫。本同一性。不<a 
name="0876a04" id="0876a04"></a>容分別。</p><a 
name="0876a05" id="0876a05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提於意云何<span class="note">(至)</span>是人行邪道不能見如來。</p><a 
name="0876a06" id="0876a06"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此段佛云云。上見就如來說。又云不應以具足色<a 
name="0876a07" id="0876a07"></a>身。具足諸相見。此觀就學者說。轉輪聖王。即四天<a 
name="0876a08" id="0876a08"></a>王。正五九月照南閻浮提二六十月。照西瞿耶尼。<a 
name="0876a09" id="0876a09"></a>三七十一月照。北<span class="gaiji">&#x9B31;<!--gaiji,鬱,1[鬱-山+止],2&#x9B31;,3--></span>單越。四八十二月。照東弗婆<a 
name="0876a10" id="0876a10"></a>提。如輪之轉管四天下。察人間善惡者。以業報福<a 
name="0876a11" id="0876a11"></a>德。亦具三十二相。六祖曰。轉輪聖王。雖有三十二<a 
name="0876a12" id="0876a12"></a>相。豈得同如來。世尊引此言者。以遣執相之病。須<a 
name="0876a13" id="0876a13"></a>菩提是大阿羅漢。所悟甚深。得方便門。不生迷路。<a 
name="0876a14" id="0876a14"></a>以冀世尊除遣細惑。令後世眾生。所見不謬也。此<a 
name="0876a15" id="0876a15"></a>段是佛與須菩提。恐會下大眾未能了悟非相之<a 
name="0876a16" id="0876a16"></a>說。故再細辨。前論。而結以偈言。以釋眾惑也。不然<a 
name="0876a17" id="0876a17"></a>須菩提前<span class="corr" title='[巳&gt;已]'>已</span>知不應。以三十二相見如來。此何以<a 
name="0876a18" id="0876a18"></a>又言色相如是也。疏鈔曰。如來法身者。非色非聲。<a 
name="0876a19" id="0876a19"></a>無相無形。不可以心思。不不可以識識。在凡不少。<a 
name="0876a20" id="0876a20"></a>至聖不增。看時不見。悟則全彰。</p><a 
name="0876a21" id="0876a21"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提汝若作是念<span class="note">(至)</span>於法不說斷滅相。</p><a 
name="0876a22" id="0876a22"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此段戒人不可存斷滅見也。王曰。諸法斷滅者。謂<a 
name="0876a23" id="0876a23"></a>一切法。皆斷之滅之而不用也。相謂凡法之相也。<a 
name="0876a24" id="0876a24"></a>法固不可以泥。然亦豈可以斷滅之哉譬如渡水。<a 
name="0876b01" id="0876b01"></a>既渡之後。固不須舟楫。未渡之前。豈可無舟楫耶。<a 
name="0876b02" id="0876b02"></a>所以發求。無上正等正覺真性之心。必須依佛法<a 
name="0876b03" id="0876b03"></a>脩行。不可遂斷滅佛法。而謂不用法也。顏曰此一<a 
name="0876b04" id="0876b04"></a>卷經。雖然說無之一字。佛又恐人執著此無。一面<a 
name="0876b05" id="0876b05"></a>沉空滯寂。棄有著無。反成斷滅相。故此一分專戒<a 
name="0876b06" id="0876b06"></a>人不可斷滅。未見惟人。如何便說一切皆無。所以<a 
name="0876b07" id="0876b07"></a>佛告須菩提。汝莫作是念。如來不以具足相故。得<a 
name="0876b08" id="0876b08"></a>阿耨多羅三藐三菩提。法若果作是念。發心即是<a 
name="0876b09" id="0876b09"></a>諸法斷滅相。何以故。凡發無上正等正覺。心不可<a 
name="0876b10" id="0876b10"></a>說斷滅相。又云。昔張秀才拙。參西堂禪師。問山河<a 
name="0876b11" id="0876b11"></a>大地三世諸佛。是有是無。師答云有。張云錯。師云<a 
name="0876b12" id="0876b12"></a>爾曾參見什麼人來。張云參見徑山禪師來。某甲<a 
name="0876b13" id="0876b13"></a>問徑山皆言無。師云。俟爾得似徑山時。一切皆無<a 
name="0876b14" id="0876b14"></a>即得大凡。</p><a 
name="0876b15" id="0876b15"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提若菩薩以滿恒河沙等<span class="note">(至)</span>是故說不受福德。</p><a 
name="0876b16" id="0876b16"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>知一切法二句。即宏毅以任重而道遠也。不受福<a 
name="0876b17" id="0876b17"></a>德。即施德不望報意。此段承上文。言既不斷滅。而<a 
name="0876b18" id="0876b18"></a>於一切法。皆當成於堅忍。以無我念而不應貪著<a 
name="0876b19" id="0876b19"></a>福德也。六祖曰。通達一切法。無能所心。是名為忍。<a 
name="0876b20" id="0876b20"></a>此人所得福德。勝前七寶之福。王曰若為作福德<a 
name="0876b21" id="0876b21"></a>而度眾生。則是貪著其福德。而欲亭受也。前以較<a 
name="0876b22" id="0876b22"></a>量福德屬眾生說。此以較量布施福德屬菩薩說。<a 
name="0876b23" id="0876b23"></a>亦以見是法平等也。</p><a 
name="0876b24" id="0876b24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提若有人言<span class="note">(至)</span>亦無所去故名如來。</p><a 
name="0876c01" id="0876c01"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此段云云。疏鈔云。如來者。來而不來。去而不去。住<a 
name="0876c02" id="0876c02"></a>而不住。非動非靜。上合諸佛。下等&#x7FA3;生。一性平等。<a 
name="0876c03" id="0876c03"></a>故號如來。至此段明如來真性法身。</p><a 
name="0876c04" id="0876c04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提若善男子善女人<span class="note">(至)</span>但凡夫之人貪著其事。</p><a 
name="0876c05" id="0876c05"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>此段又重戒貪著也。世界者微塵之合也。微塵者。<a 
name="0876c06" id="0876c06"></a>世界之散也。舉世界微塵。而萬事萬物皆然。顧欲<a 
name="0876c07" id="0876c07"></a>合而不散。則貪著矣。六祖曰。一合相者。如眼見色<a 
name="0876c08" id="0876c08"></a>愛色。即與色合。耳聞聲愛聲。即與聲合。至於六塵。<a 
name="0876c09" id="0876c09"></a>若散即是真世界。合即凡夫。散即非凡夫。凡夫之<a 
name="0876c10" id="0876c10"></a>人。於一切法。皆合相。若菩薩於一切法。皆不合而<a 
name="0876c11" id="0876c11"></a>散。何以故。合即繫縛。起生滅。散即解脫。亦不生不<a 
name="0876c12" id="0876c12"></a>滅。若有繫縛生滅者。即是凡夫所以經云<span class="corr" title='[&#x4F39;&gt;但]'>但</span>凡夫<a 
name="0876c13" id="0876c13"></a>之人。貪著其事。</p><a 
name="0876c14" id="0876c14"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提若人言佛說我見<span class="note">(至)</span>即非法相是名法相。</p><a 
name="0876c15" id="0876c15"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>黃蘗師曰。前說四相。此說四見。相粗而見精。因有<a 
name="0876c16" id="0876c16"></a>四見。故形四相也。又云。佛以見性為見。人以見相<a 
name="0876c17" id="0876c17"></a>為見。如是即指無四見言。顏曰。如是二字。說來若<a 
name="0876c18" id="0876c18"></a>發無上正等正覺心者。於一切法。應當如此知。如<a 
name="0876c19" id="0876c19"></a>此見。如此信解。不必外求法相。然初入道時。不假<a 
name="0876c20" id="0876c20"></a>法相。故無入頭處。既見性了。亦當遠離。不要執著。<a 
name="0876c21" id="0876c21"></a>所謂得魚忘&#x5374;筌。到岸不須船之說。此段言人若<a 
name="0876c22" id="0876c22"></a>悟了。并此四見之名。亦當無有。故於一切法應如<a 
name="0876c23" id="0876c23"></a>是知見信解也。</p><a 
name="0876c24" id="0876c24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須菩提若有人以滿無量<span class="note">(至)</span>應作如是觀。</p><a 
name="0877a01" id="0877a01"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>凡佛所言四句偈等者。謂不必專於偈。凡可以演<a 
name="0877a02" id="0877a02"></a>說者皆是。況其為言之要者乎。顏曰如如不動。湛<a 
name="0877a03" id="0877a03"></a>然太&#x8657;。何以故。&#x8462;世間一切有為之法。如夢寐之<a 
name="0877a04" id="0877a04"></a>非真。如燈幻之眩惑。如水泡之暫時。如人影之易<a 
name="0877a05" id="0877a05"></a>滅。如朝露之易消。如閃電之&#x5010;忽。應作如是觀者。<a 
name="0877a06" id="0877a06"></a>應立如此見性之法。陳曰佛所謂一切法者。真實<a 
name="0877a07" id="0877a07"></a>無相法也。故一切賢聖。皆以無為法。曰有為法。則<a 
name="0877a08" id="0877a08"></a>夢幻泡影露電之如。不其妄乎。惟了真空無相者。<a 
name="0877a09" id="0877a09"></a>能作是觀。以悟亦如之妄。則必離亦如以證如如<a 
name="0877a10" id="0877a10"></a>不動之理。昔優婆離尊者。語阿難曰諸有為法。並<a 
name="0877a11" id="0877a11"></a>是無常。想夫觀六如而得是句。張國維云。佛法有<a 
name="0877a12" id="0877a12"></a>空假中三觀。末段仍歸於無為。而以偈語收結。至<a 
name="0877a13" id="0877a13"></a>此則無聲無臭至矣。</p><a 
name="0877a14" id="0877a14"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛說是經<span class="corr" title='[巳&gt;已]'>已</span>長老須菩提<span class="note">(至)</span>皆大歡喜信受奉行。</p><a 
name="0877a15" id="0877a15"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>優婆塞華言覲事男。優婆夷。華言覲事女。覲事者。<a 
name="0877a16" id="0877a16"></a>謂在家全受五戒。恒以親供養三寶。而為其事也。</p><a 
name="0877a17" id="0877a17"></a><a 
name="0877a18" id="0877a18"></a><p><span class="juanname">金剛經闡說卷下<span class="note">(終)</span></span></p><a 
name="0877a19" id="0877a19"></a><a 
name="0877a20" id="0877a20"></a><a 
name="0877a21" id="0877a21"></a><a 
name="0877a22" id="0877a22"></a><a 
name="0877a23" id="0877a23"></a><a 
name="0877a24" id="0877a24"></a><a 
name="0877b01" id="0877b01"></a><a 
name="0877b02" id="0877b02"></a><div class="xu"><p style="margin-left: 2em"><span class="headname">No. 508-C<a 
name="0877b03" id="0877b03"></a> 復題金剛經闡說後序</span></p><a 
name="0877b04" id="0877b04"></a><a 
name="0877b05" id="0877b05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>陳子滋甫。予至契友也。常語予曰。昔先祖存吾公。知<a 
name="0877b06" id="0877b06"></a>常州府事。公餘之暇。著作甚富。必有可觀。惜與先生。<a 
name="0877b07" id="0877b07"></a>南北睽違。不能共閱手澤為憾。予曰。令先祖之著作<a 
name="0877b08" id="0877b08"></a>也。袖歸自八千里外。珍藏至五十年餘。其間有關名<a 
name="0877b09" id="0877b09"></a>教。可傳後人者。自當亟付剞劂。以公於世。方不失為<a 
name="0877b10" id="0877b10"></a>繼述之盛事。奚止為觀閱鑒賞計哉。洛甫應之曰。唯<a 
name="0877b11" id="0877b11"></a>唯迨至同治壬戌。彩屬擾亂。洛甫家被困。閱兩三月。<a 
name="0877b12" id="0877b12"></a>一旦圍解。轉危為安。人言伊家有巨人手。自室中出。<a 
name="0877b13" id="0877b13"></a>大如車輪。&#x7FA3;懼而散。後洛甫遍覽室中。並無他物。惟<a 
name="0877b14" id="0877b14"></a>架上有祖遺一書箱耳。啟閱皆在署。手犒散帙。飄颻<a 
name="0877b15" id="0877b15"></a>纂成者。惟金剛經闡說一編。方悟此手。其印金剛手<a 
name="0877b16" id="0877b16"></a>歟。是編亦洛甫平日所虔誦也。不期<span class="corr" title='[巳&gt;已]'>已</span>被&#x8827;魚竊去<a 
name="0877b17" id="0877b17"></a>數十字矣。於是滋甫感予前言。膳寫就質於予。屬予<a 
name="0877b18" id="0877b18"></a>補之。予曰。前言戲之耳。今竟有是事耶。但是事固吾<a 
name="0877b19" id="0877b19"></a>黨所不語。故予不敢敘之於前。而特書之於後者。乃<a 
name="0877b20" id="0877b20"></a>不自揣固陋也。今熟讀其文。細玩其說。竊取其意以<a 
name="0877b21" id="0877b21"></a>補之。方知存吾先生之用心苦而遺澤長。故其闡是<a 
name="0877b22" id="0877b22"></a>經也。不過緣經以聞義。因義以著說。而旨意了徹。人<a 
name="0877b23" id="0877b23"></a>易覺悟。大有儒宗之風。其不同於高談元妙也。明矣。<a 
name="0877b24" id="0877b24"></a>予故樂倡而共襄之。庶不員闡說之婆心。亦足徵善<a 
name="0877c01" id="0877c01"></a>人之有後也。故謂之功德不可思議也可。即謂之果<a 
name="0877c02" id="0877c02"></a>報不可思議也。亦無不可。</p><a 
name="0877c03" id="0877c03"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>同治丁卯夏月臺陽洪壽椿載撰</p></div>
