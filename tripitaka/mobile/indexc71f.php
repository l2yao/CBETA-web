<a 
name="0945c16" id="0945c16"></a><a 
name="0945c17" id="0945c17"></a><a 
name="0945c18" id="0945c18"></a><a 
name="0945c19" id="0945c19"></a><p><span class="juanname">勝鬘經疏義私鈔卷之三</span></p><a 
name="0945c20" id="0945c20"></a><a 
name="0945c21" id="0945c21"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】從爾時世尊於勝鬘以下章中第二如來讚成<a 
name="0945c22" id="0945c22"></a>就中亦有二第一讚成第二從是故以下結勸就<a 
name="0945c23" id="0945c23"></a>第一讚成又開為二第一直述。</p><a 
name="0945c24" id="0945c24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>爾時世尊於勝鬘所說攝受正法大精進力起隨喜<a 
name="0946a01" id="0946a01"></a>心。</p><a 
name="0946a02" id="0946a02"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】攝受正法謂八地以上萬行中之一行。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>第二<a 
name="0946a03" id="0946a03"></a>別述就中有三第一舉力士譬明勝人天乘。</p><a 
name="0946a04" id="0946a04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>如是勝鬘如汝所說攝受正法大精進力如大力士<a 
name="0946a05" id="0946a05"></a>少觸身分生大苦痛。</p><a 
name="0946a06" id="0946a06"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【鈔】如大力士等者如無力人為大力士少觸身分<a 
name="0946a07" id="0946a07"></a>生大苦痛無名云上來雖承旨而說若不得如來<a 
name="0946a08" id="0946a08"></a>美述其事者則未來世眾生信敬情薄。</p><a 
name="0946a09" id="0946a09"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>如是勝鬘少攝受正法令魔苦惱我不見餘一善法<a 
name="0946a10" id="0946a10"></a>令魔憂苦如少攝受正法。</p><a 
name="0946a11" id="0946a11"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】第二舉牛王譬明過二乘。</p><a 
name="0946a12" id="0946a12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如牛王形色無比勝一切牛如是大乘少攝受正<a 
name="0946a13" id="0946a13"></a>法勝於一切二乘善根以廣大故。</p><a 
name="0946a14" id="0946a14"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】第三舉須彌山王譬明勝七地以還大乘。</p><a 
name="0946a15" id="0946a15"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又如須彌山王端嚴殊特勝於眾山如是大乘捨身<a 
name="0946a16" id="0946a16"></a>命財以攝取心攝受正法勝不捨身命財初住大乘<a 
name="0946a17" id="0946a17"></a>一切善根何況二乘以廣大故。</p><a 
name="0946a18" id="0946a18"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】三中即有開合皆可見以攝取心者謂萬行之<a 
name="0946a19" id="0946a19"></a>心七地以還亦能捨身命但形於八地以上一念<a 
name="0946a20" id="0946a20"></a>備修故云不捨。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>就第二結勸亦有四一廣勸。</p><a 
name="0946a21" id="0946a21"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>是故勝鬘當以攝受正法開示眾生教化眾生建立<a 
name="0946a22" id="0946a22"></a>眾生。</p><a 
name="0946a23" id="0946a23"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】開示眾生謂人天乘即對上力士比教化眾生<a 
name="0946a24" id="0946a24"></a>謂二乘即對上牛王比建立眾生謂七地以還即<a 
name="0946b01" id="0946b01"></a>對上須彌山比此皆廣勸。</p><a 
name="0946b02" id="0946b02"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【鈔】開示眾生謂人天乘等無名云初名開示中名<a 
name="0946b03" id="0946b03"></a>教化後名建立也既以三譬述成其義復加三勸<a 
name="0946b04" id="0946b04"></a>令廣其道也今疏意亦同之前舉三喻述於勝鬘<a 
name="0946b05" id="0946b05"></a>攝受正法勝五乘人故今結勸亦對五乘人勸結<a 
name="0946b06" id="0946b06"></a>也開示眾生對上大力士比者開對上人天乘未<a 
name="0946b07" id="0946b07"></a>開教化對二乘自度建立對上菩薩未如八地建<a 
name="0946b08" id="0946b08"></a>立真應是故少攝受正法<span class="corr" title='[巳&gt;已]'>已</span>勝於五乘人必須開<a 
name="0946b09" id="0946b09"></a>示等也。</p><a 
name="0946b10" id="0946b10"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】二廣結。</p><a 
name="0946b11" id="0946b11"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>如是勝鬘攝受正法如是大利如是大福如是大果。</p><a 
name="0946b12" id="0946b12"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】如是大利結人天乘如是大福者結二乘如是<a 
name="0946b13" id="0946b13"></a>大果者結七地以還大乘亦對三譬為結此名廣<a 
name="0946b14" id="0946b14"></a>結。</p><a 
name="0946b15" id="0946b15"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【鈔】廣結中皆言結者結謂結示也人天開示即得<a 
name="0946b16" id="0946b16"></a>大利二乘教化即得大福七地建立即得大果是<a 
name="0946b17" id="0946b17"></a>故皆由攝受正法言廣略者廣舉五乘略言無量<a 
name="0946b18" id="0946b18"></a>無邊等。</p><a 
name="0946b19" id="0946b19"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】三略勸。</p><a 
name="0946b20" id="0946b20"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>勝鬘我於阿僧祇阿僧祇劫說攝受正法功德義利<a 
name="0946b21" id="0946b21"></a>不得邊際。</p><a 
name="0946b22" id="0946b22"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】四略結。</p><a 
name="0946b23" id="0946b23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>是故勝鬘攝受正法有無量無邊功德。</p><a 
name="0946b24" id="0946b24"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】從勝鬘我於以下略勸從是故以下略結可見<a 
name="0946c01" id="0946c01"></a>也。</p><a 
name="0946c02" id="0946c02"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【鈔】○第五一乘章無名云此經弘旨名義奇拔皆<a 
name="0946c03" id="0946c03"></a>顯於此章初立名超常韻則不可以言求義超眾<a 
name="0946c04" id="0946c04"></a>倫則不可以意取必要當豁其物習之懷廢我以<a 
name="0946c05" id="0946c05"></a>存道者然後可尋之於因緣耳今釋此章先義門<a 
name="0946c06" id="0946c06"></a>分別次釋經文義門分九。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○一明章意及略分文。</p><a 
name="0946c07" id="0946c07"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】從佛告勝鬘汝今更說以下明他分行中第二<a 
name="0946c08" id="0946c08"></a>一乘章明總&#x53CE;能生攝受所生五乘皆入一乘就<a 
name="0946c09" id="0946c09"></a>中初開為二第一正明皆入一乘第二明一體三<a 
name="0946c10" id="0946c10"></a>寶一乘是一體三寶之因一體三寶是一乘之果<a 
name="0946c11" id="0946c11"></a>欲以果一體為況因一故第二舉一體三寶明果<a 
name="0946c12" id="0946c12"></a>既是一則因一明也。</p><a 
name="0946c13" id="0946c13"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【鈔】二明對昔辨今。</p><a 
name="0946c14" id="0946c14"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】且夫昔日梯橙三寶及五乘之別同是方便之<a 
name="0946c15" id="0946c15"></a>說非是實說今則既會五乘入於一乘同為常住<a 
name="0946c16" id="0946c16"></a>一果之因故亦明昔日梯橙三寶非是究竟唯今<a 
name="0946c17" id="0946c17"></a>日常住一體為歸依之極也。</p><a 
name="0946c18" id="0946c18"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【鈔】五乘之別同是方便等者此章明八地菩薩收<a 
name="0946c19" id="0946c19"></a>前出生五乘以歸一乘出生是施權收入是入實<a 
name="0946c20" id="0946c20"></a>即與釋迦一化先三後一不殊故釋此章文全用<a 
name="0946c21" id="0946c21"></a>一代化儀以釋於中即有褒貶抑揚大意不殊淨<a 
name="0946c22" id="0946c22"></a>名大集若爾與法華何異答此今會者言當得當<a 
name="0946c23" id="0946c23"></a>覺也華言現覺現得此經談其始終而現會也。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○<a 
name="0946c24" id="0946c24"></a>三釋名義。</p><a 
name="0947a01" id="0947a01"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】一然是無二之稱乘是運出為義釋一義義家<a 
name="0947a02" id="0947a02"></a>種種不同一云一則本無但欲破三故故云一也<a 
name="0947a03" id="0947a03"></a>二云一是果一義乘是因名言因中善品雖復種<a 
name="0947a04" id="0947a04"></a>種終必歸於一果故以果義一名因謂之一也三<a 
name="0947a05" id="0947a05"></a>云一與乘皆是因名因中善品雖復種種不同而<a 
name="0947a06" id="0947a06"></a>其流義一故故云一第一解好則好矣但疑十方<a 
name="0947a07" id="0947a07"></a>淨土無三乘之別亦應不說一乘之道而十方淨<a 
name="0947a08" id="0947a08"></a>土皆說一乘若爾那得言但欲破三故一也但第<a 
name="0947a09" id="0947a09"></a>二與第三釋皆可為用然今義家多就因一為釋。</p><a 
name="0947a10" id="0947a10"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【鈔】三釋名義中第二師以終名始第三師一自在<a 
name="0947a11" id="0947a11"></a>因如四瀆之水雖末至海而趣而趣流義一言萬<a 
name="0947a12" id="0947a12"></a>善趣法之義是一也。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○四辨大小。</p><a 
name="0947a13" id="0947a13"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】仍辨大小者不求自度濟物為先等流佛果稱<a 
name="0947a14" id="0947a14"></a>為大乘化物為患但求自度臧彼無實名曰小乘。</p><a 
name="0947a15" id="0947a15"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【鈔】與物同趣佛果故曰等流化城權設名臧彼無<a 
name="0947a16" id="0947a16"></a>實。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○五辨一大同異。</p><a 
name="0947a17" id="0947a17"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】大乘一乘大意雖復同所以少異大乘猶是三<a 
name="0947a18" id="0947a18"></a>中之別名一乘則無三二之稱波若維摩雖名大<a 
