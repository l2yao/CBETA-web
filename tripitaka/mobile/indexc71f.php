name="0951b14" id="0951b14"></a>羅漢辟支佛不成就一切功德言得涅槃者是佛方<a 
name="0951b15" id="0951b15"></a>便。</p><a 
name="0951b16" id="0951b16"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】次言無量。</p><a 
name="0951b17" id="0951b17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>唯有如來得般涅槃成就無量功德故阿羅漢辟支<a 
name="0951b18" id="0951b18"></a>佛成就有量功德言得涅槃者是佛方便。</p><a 
name="0951b19" id="0951b19"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】次言不思議。</p><a 
name="0951b20" id="0951b20"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>唯有如來得般涅槃成就不可思議功德故阿羅漢<a 
name="0951b21" id="0951b21"></a>辟支佛成就思議功德言得涅槃者是佛方便。</p><a 
name="0951b22" id="0951b22"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】次言清淨。</p><a 
name="0951b23" id="0951b23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>唯有如來得般涅槃一切所應斷過皆悉斷滅成就<a 
name="0951b24" id="0951b24"></a>第一清淨故阿羅漢辟支佛有餘過非第一清淨言<a 
name="0951c01" id="0951c01"></a>得涅槃者是佛方便。</p><a 
name="0951c02" id="0951c02"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>唯有如來得般涅槃為一切眾生之所瞻仰出過阿<a 
name="0951c03" id="0951c03"></a>羅漢辟支佛菩薩境界是故阿羅漢辟支佛去涅槃<a 
name="0951c04" id="0951c04"></a>界遠言阿羅漢辟支佛觀察解脫四智究竟得蘇息<a 
name="0951c05" id="0951c05"></a>處者亦是如來方便有餘不了義說。</p><a 
name="0951c06" id="0951c06"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】&#x8462;是逐便無別所以觀察舉智解脫舉斷四智<a 
name="0951c07" id="0951c07"></a>釋智蘇息釋解脫無為凝然故為蘇息處也。</p><a 
name="0951c08" id="0951c08"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【鈔】&#x8462;是逐便無別所以者去涅槃界遠故不成就<a 
name="0951c09" id="0951c09"></a>一切功德不成就故有量有量故思議思議故非<a 
name="0951c10" id="0951c10"></a>第一清淨是故去涅槃界遠無名云一切者盡有<a 
name="0951c11" id="0951c11"></a>之名無量者體無之稱不思議者有無兼極之謂<a 
name="0951c12" id="0951c12"></a>也清淨者證斷之極號故言唯有如來得般涅槃<a 
name="0951c13" id="0951c13"></a>無名云向指歸依之義故單言聲聞今廣釋生死<a 
name="0951c14" id="0951c14"></a>不盡是以兼說二乘。</p><a 
name="0951c15" id="0951c15"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】從何以故有二種生死以下第三舉二種生死<a 
name="0951c16" id="0951c16"></a>以釋第二奪段二段云佛昔與者既是方便勝鬘<a 
name="0951c17" id="0951c17"></a>今奪則究竟在佛二乘未故所以舉二種生死以<a 
name="0951c18" id="0951c18"></a>釋與是方便奪由未極之意標疑云。</p><a 
name="0951c19" id="0951c19"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>何以故。</p><a 
name="0951c20" id="0951c20"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】何以昔與是方便究竟唯在如來非在二乘耶。</p><a 
name="0951c21" id="0951c21"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰生死有二種一分段死二變易死佛昔但<a 
name="0951c22" id="0951c22"></a>因除分段而言究竟故云方便為與未除變易故<a 
name="0951c23" id="0951c23"></a>究竟唯在如來非在二乘就中有四第一立二種<a 
name="0951c24" id="0951c24"></a>死章門。</p><a 
name="0952a01" id="0952a01"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【鈔】第三舉二死中有四段初立可見。</p><a 
name="0952a02" id="0952a02"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>有二種死何等為二謂分段死不思議變易死。</p><a 
name="0952a03" id="0952a03"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】第二從分段死者以下釋二種死章門。</p><a 
name="0952a04" id="0952a04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>分段死者謂&#x8657;偽眾生不思議變易死者謂阿羅漢<a 
name="0952a05" id="0952a05"></a>辟支佛大力菩薩意生身乃至究竟無上菩提。</p><a 
name="0952a06" id="0952a06"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【鈔】第二釋中經意生身者楞伽經大慧問佛何名<a 
name="0952a07" id="0952a07"></a>意生佛言譬如意去速疾無礙名為意生此即從<a 
name="0952a08" id="0952a08"></a>喻故彼意生譬如意去彼經兩義而釋通名初云<a 
name="0952a09" id="0952a09"></a>如十萬由旬外憶先所見念念相續疾至於彼次<a 
name="0952a10" id="0952a10"></a>云如幻三昧力憶本願故生諸聖中初云憶所次<a 
name="0952a11" id="0952a11"></a>云憶願二義並是意憶生故名為意生經既云憶<a 
name="0952a12" id="0952a12"></a>憶即作意是故亦名作意生故名為意生次第四<a 
name="0952a13" id="0952a13"></a>卷釋三別名初無常品云意生三種一入三昧樂<a 
name="0952a14" id="0952a14"></a>意成身亦名正受即三四五地心寂不動故二覺<a 
name="0952a15" id="0952a15"></a>法自性意成身即八地中並入佛剎故以法為自<a 
name="0952a16" id="0952a16"></a>性三種類俱生無作意成身謂了佛證法成之與<a 
name="0952a17" id="0952a17"></a>生並從果說總此楞伽只在十地用釋今文菩薩<a 
name="0952a18" id="0952a18"></a>一人二乘未斷無明准此可解亦名三意因移謂<a 
name="0952a19" id="0952a19"></a>因移果易故也須知今文三種與楞伽三種不同<a 
name="0952a20" id="0952a20"></a>受變易死其義不異。</p><a 
name="0952a21" id="0952a21"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】三界中亦有聖人而今從多為明故云&#x8657;偽眾<a 
name="0952a22" id="0952a22"></a>生七地以下未受變易但仍本受名故八地以上<a 
name="0952a23" id="0952a23"></a>名阿羅漢辟支佛也究竟無上菩提者金剛心是<a 
name="0952a24" id="0952a24"></a>因中最究竟若辨生死凡有四種一者以有漏業<a 
name="0952b01" id="0952b01"></a>為因四住地煩惱為緣所感之果身色妙麤有別<a 
name="0952b02" id="0952b02"></a>壽命長短有期名為分段生死二者以無漏業為<a 
name="0952b03" id="0952b03"></a>因無明住地煩惱為緣所感之果身無形色命無<a 
name="0952b04" id="0952b04"></a>期限但以明代闇念念新易者名為變易生死。</p><a 
name="0952b05" id="0952b05"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【鈔】言三界中亦有聖人等者三果四向並未出界<a 
name="0952b06" id="0952b06"></a>故三界果報相望為麤妙人人壽命不同長短不<a 
name="0952b07" id="0952b07"></a>等變易果報無三界之形色但心有明晦而於彼<a 
name="0952b08" id="0952b08"></a>土得聞一乘智慧增長以明代闇將新易故也無<a 
name="0952b09" id="0952b09"></a>名釋此二死文云受生三界名為&#x8657;偽眾生行報<a 
name="0952b10" id="0952b10"></a>有定期期盡則終名分段死也彼三聖者則不然<a 
name="0952b11" id="0952b11"></a>矣滅除諸漏捨離報身藉眾妙之行以成其體體<a 
name="0952b12" id="0952b12"></a>由行成安得無終但潛往冥來莫測其際猶心之<a 
name="0952b13" id="0952b13"></a>代謝故名意生身也隨其脩短非不善有終故名<a 
name="0952b14" id="0952b14"></a>變易死也死而不滅豈可思議。</p><a 
name="0952b15" id="0952b15"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】三者以有漏勝善為因三界餘習為緣所感之<a 
name="0952b16" id="0952b16"></a>果猶有形色壽命有限似同分段念念生滅亦同<a 
name="0952b17" id="0952b17"></a>變易名為二國中間生死四者初流來時在三界<a 
name="0952b18" id="0952b18"></a>外無無漏業但以闇惑相傳而受生死名為初流<a 
name="0952b19" id="0952b19"></a>來生死然即六地以還乃至三途雖有念念生滅<a 
name="0952b20" id="0952b20"></a>之義形質有別壽命有期國家有定故皆分段生<a 
name="0952b21" id="0952b21"></a>死八地以上雖有劫數期限但約他為明前念為<a 
name="0952b22" id="0952b22"></a>因後念為果新新改易故皆是為變易生死菩薩<a 
name="0952b23" id="0952b23"></a>雖有形色但非正結所感故不名分段雖復念念<a 
name="0952b24" id="0952b24"></a>生滅猶有形色故不名變易踟&#x8E70;兩楹故名為二<a 
name="0952c01" id="0952c01"></a>國中間生死初流來時無無漏業故不可同變易<a 
name="0952c02" id="0952c02"></a>未入三界取相故不正分段唯以闇識相續念念<a 
name="0952c03" id="0952c03"></a>下墜故別立為一種生死今約分段變易中間三<a 
name="0952c04" id="0952c04"></a>種生死有十二諦約初流來唯有苦集二諦所以<a 
name="0952c05" id="0952c05"></a>大合有十四諦或云初流來時或有值佛修道得<a 
name="0952c06" id="0952c06"></a>佛故亦有滅道二諦所以猶有十六諦而今釋不<a 
name="0952c07" id="0952c07"></a>然初流來時入三界方起取相乃別苦樂求修滅<a 
name="0952c08" id="0952c08"></a>道即無滅道二諦明矣二國中間緣非正使初流<a 
name="0952c09" id="0952c09"></a>來時未明斷結則不足以證佛究竟且若相收攝<a 
name="0952c10" id="0952c10"></a>二國中間屬於分段初流來時屬於變易然即要<a 
name="0952c11" id="0952c11"></a>在分段變易所以勝鬘但就分段變易為釋究竟<a 
name="0952c12" id="0952c12"></a>不究竟之意。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>第三從二種死中以下正釋昔以<a 
name="0952c13" id="0952c13"></a>方便為與之意。</p><a 
name="0952c14" id="0952c14"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>二種死中以分段死故說阿羅漢辟支佛智我生<span class="corr" title='[巳&gt;已]'>已</span><a 
name="0952c15" id="0952c15"></a>盡得有餘果證故說梵行<span class="corr" title='[巳&gt;已]'>已</span>立凡夫人天所不能辦<a 
name="0952c16" id="0952c16"></a>七種學人先所未作&#x8657;偽煩惱斷故說所作<span class="corr" title='[巳&gt;已]'>已</span>辦阿<a 
name="0952c17" id="0952c17"></a>羅漢辟支佛所斷煩惱更不能受後有故說不受後<a 
name="0952c18" id="0952c18"></a>有。</p><a 
name="0952c19" id="0952c19"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】從二種死中以下第三正釋昔與是方便之意<a 
name="0952c20" id="0952c20"></a>釋曰昔日但因除分段而言四智究竟故。</p><a 
name="0952c21" id="0952c21"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【鈔】從昔與是下釋第三意七種學人者謂三果四<a 
name="0952c22" id="0952c22"></a>向&#x8657;偽煩惱謂斷四住惑故法華但離&#x8657;妄名為<a 
name="0952c23" id="0952c23"></a>解脫其實未得一切解脫。</p><a 
name="0952c24" id="0952c24"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】第四從非盡一切以下正釋究竟非在二乘之<a 
name="0953a01" id="0953a01"></a>意。</p><a 
name="0953a02" id="0953a02"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>非盡一切煩惱亦非盡一切受生故說不受後有。</p><a 
name="0953a03" id="0953a03"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】從非盡一切煩惱以下第四正釋究竟非在二<a 
name="0953a04" id="0953a04"></a>乘之意釋曰非謂未盡一切煩惱是究竟故而推<a 
name="0953a05" id="0953a05"></a>尋此中文外文只釋究竟非在二乘昔與是方便<a 
name="0953a06" id="0953a06"></a>究竟唯在如來但以義自去本義從二種死中以<a 
name="0953a07" id="0953a07"></a>下結第一奪先結佛昔與從非盡一切以下結今<a 
name="0953a08" id="0953a08"></a>日勝鬘奪。</p><a 
name="0953a09" id="0953a09"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【鈔】從究竟非在下釋第四意經意云二乘昔日但<a 
name="0953a10" id="0953a10"></a>斷分段死故得不受後有名等非謂盡一切煩惱<a 
name="0953a11" id="0953a11"></a>受生得不受後有也疏文反釋奪云非謂未盡一<a 
name="0953a12" id="0953a12"></a>切煩惱是究竟故涅槃究竟者盡一切煩惱等方<a 
name="0953a13" id="0953a13"></a>是究竟以此撿之究竟不在二乘以二乘未盡一<a 
name="0953a14" id="0953a14"></a>切煩惱等故。</p><a 
name="0953a15" id="0953a15"></a><a 
