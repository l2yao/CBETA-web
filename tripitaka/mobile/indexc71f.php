name="0947a21" id="0947a21"></a>家云亦是因名言菩薩不求自度與物等流即運<a 
name="0947a22" id="0947a22"></a>心廣大故云大也。</p><a 
name="0947a23" id="0947a23"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【鈔】五辨一大同異中先立同異釋中先釋異次為<a 
name="0947a24" id="0947a24"></a>例下釋同大乘下先釋異中波若淨名通有三乘<a 
name="0947b01" id="0947b01"></a>得道故也大品中若得聲聞緣覺菩薩乘皆云當<a 
name="0947b02" id="0947b02"></a>學波若波羅蜜淨名中佛以足指按地乃至一切<a 
name="0947b03" id="0947b03"></a>大眾自見坐寶蓮華當佛現此國土嚴淨之時五<a 
name="0947b04" id="0947b04"></a>百長者子得無生忍八萬四千人發菩提心佛攝<a 
name="0947b05" id="0947b05"></a>神足於是世界還復如故求聲聞乘三萬二千天<a 
name="0947b06" id="0947b06"></a>及人知有為法皆悉無常遠塵離垢得法眼淨八<a 
name="0947b07" id="0947b07"></a>千比丘不受諸法漏盡意解如斯之例悉是兼三<a 
name="0947b08" id="0947b08"></a>之說但大非一如法華經云十方世界中唯有一<a 
name="0947b09" id="0947b09"></a>乘法無二亦無三唯一佛乘及此經會五乘歸一<a 
name="0947b10" id="0947b10"></a>乘即唯一非大若例一以釋大此大是一家之大<a 
name="0947b11" id="0947b11"></a>仍例為二釋先明果一家之大次明因一家之大<a 
name="0947b12" id="0947b12"></a>可見。</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○六辨體。</p><a 
name="0947b13" id="0947b13"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>【疏】若論乘體善有二種一報善二習善報善雖有<a 
name="0947b14" id="0947b14"></a>脫重令輕之能但匿金剛以還不致於佛果故只<a 
name="0947b15" id="0947b15"></a>是緣由不為正體故今但以習善為體又解與善<a 
name="0947b16" id="0947b16"></a>解是乘之本善是乘之末今就末為乘不以本為<a 
