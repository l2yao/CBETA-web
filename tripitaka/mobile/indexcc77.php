name="0905b01" id="0905b01"></a>中，人不知也，便進入山，索求他木果。」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>烏性<a 
name="0905b02" id="0905b02"></a>既貪，不能忍於饑渴，又聞耆婆歎為神藥，亦<a 
name="0905b03" id="0905b03"></a>見耆婆已飲食之，謂必無毒，便取餘梨噉，盡<a 
name="0905b04" id="0905b04"></a>飲餘水，即便下痢，痢如注水，躄地而臥起輒<a 
name="0905b05" id="0905b05"></a>眩倒，不能復動。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>耆婆往語之曰：「王服我藥，<a 
name="0905b06" id="0905b06"></a>病必當愈。然今藥力未行，餘毒未盡，我今<a 
name="0905b07" id="0905b07"></a>往者必當殺我。汝無所知，起欲得我以解身<a 
name="0905b08" id="0905b08"></a>負，固使汝病。病自無苦，慎莫動搖，三日當<a 
name="0905b09" id="0905b09"></a>瘥；若遂起逐我，必死不疑。」便上象而去。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>耆<a 
name="0905b10" id="0905b10"></a>婆則過墟聚，語伍長曰：「此是大國王使，今<a 
name="0905b11" id="0905b11"></a>忽得病，汝等急往舁取歸家，好養護之，厚<a 
name="0905b12" id="0905b12"></a>其床席，給與麋粥，慎莫令死，若令死者，王<a 
name="0905b13" id="0905b13"></a>滅汝國。」語畢便去，遂歸本國。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>伍長承&#x52C5;，迎<a 
name="0905b14" id="0905b14"></a>取養護，三日毒歇下絕。烏便歸見王，叩頭<a 
name="0905b15" id="0905b15"></a>自陳曰：「我實愚癡，違負王教，信耆婆言，飲<a 
name="0905b16" id="0905b16"></a>食其餘果水，為毒所中，下痢三日，始今旦<a 
name="0905b17" id="0905b17"></a>瘥，自知當死。」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>比烏還三日之中，王病已瘥，<a 
name="0905b18" id="0905b18"></a>王自追念，悔遣烏行，見烏來還，且悲且喜<a 
name="0905b19" id="0905b19"></a>曰：「賴卿不即將兒還，當我恚時必當捶殺。<a 
name="0905b20" id="0905b20"></a>我得其恩，命得生活，而反殺之，逆戾罪不<a 
name="0905b21" id="0905b21"></a>細也。」即悔前後所枉殺者，悉更厚葬，復其<a 
name="0905b22" id="0905b22"></a>家門賜與錢財。思見耆婆欲報其恩，即遣使<a 
name="0905b23" id="0905b23"></a>者奉迎耆婆。耆婆雖知王病已瘥，猶懷餘怖，<a 
name="0905b24" id="0905b24"></a>不欲復往。耆婆復詣佛所，接足頂禮，白佛言：<a 
name="0905b25" id="0905b25"></a>「世尊！彼土遣使來喚，可往不？」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛告耆婆：「汝<a 
name="0905b26" id="0905b26"></a>本宿命，已有弘誓，當成功德，何得中止？今<a 
name="0905b27" id="0905b27"></a>應更往。汝已治其外病，我亦復當治其內病。」</p><a 
name="0905b28" id="0905b28"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>耆婆便隨使者去，王見耆娑，而大歡喜，引<a 
name="0905b29" id="0905b29"></a>與同坐，把持其臂曰：「賴蒙仁者之恩，今得<a 
name="0905c01" id="0905c01"></a>更生，當何以報？當分國土以半相與，宮內婇<a 
name="0905c02" id="0905c02"></a>女，庫藏寶物，悉當分半，幸願仁者受之。」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>耆<a 
name="0905c03" id="0905c03"></a>婆曰：「我本為太子，雖是小國，亦有人民珍<a 
name="0905c04" id="0905c04"></a>寶具足，不樂治國故求為醫，當行治病，當<a 
name="0905c05" id="0905c05"></a>用土地婇女寶物為？皆所不用。王前聽我五<a 
name="0905c06" id="0905c06"></a>願，外病得愈，若重復聽我一願，內病可復<a 
name="0905c07" id="0905c07"></a>除愈。」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>王曰：「唯聽仁教，請復聞一願之事。」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>耆<a 
name="0905c08" id="0905c08"></a>婆曰：「願王請佛，從受明法。」便為王說佛之<a 
name="0905c09" id="0905c09"></a>功德巍巍特尊。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>王聞大喜曰：「今欲遣烏臣白<a 
name="0905c10" id="0905c10"></a>象迎佛，可得致不？」耆婆曰：「不用白象也，佛<a 
name="0905c11" id="0905c11"></a>解一切，遙知人心所念，但宿齋戒清淨，供<a 
name="0905c12" id="0905c12"></a>具燒香，遙請向佛作禮，長跪白請，佛必自<a 
name="0905c13" id="0905c13"></a>來。」王如其言。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛明日與千二百五十比丘俱<a 
name="0905c14" id="0905c14"></a>來，飲食已畢，為王說經，王意開解，便發無<a 
name="0905c15" id="0905c15"></a>上正真道心，舉國大小，皆受五戒，各各恭<a 
name="0905c16" id="0905c16"></a>敬，作禮而去。</p><a 
name="0905c17" id="0905c17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又奈女生既奇異，長又聰明，從父學問，博知<a 
name="0905c18" id="0905c18"></a>經道星曆諸術，殊勝於父，加達聲樂，音如<a 
name="0905c19" id="0905c19"></a>梵天。諸迦羅越，及梵志家女，合五百人，皆<a 
name="0905c20" id="0905c20"></a>往從學，以為大師。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>奈女常從五百弟子講受<a 
name="0905c21" id="0905c21"></a>經術，或相與遊戲園池，及作音樂。國人不<a 
name="0905c22" id="0905c22"></a>解其故，便生謗議，呼為婬女，五百弟子皆<a 
name="0905c23" id="0905c23"></a>號婬黨。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>又奈女生，時國中復有須曼女及波<a 
name="0905c24" id="0905c24"></a>曇女，亦同時俱生。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>須曼女者，生於須曼華<a 
name="0905c25" id="0905c25"></a>中，國有迦羅越家，常笮須曼，以為香膏，笮<a 
name="0905c26" id="0905c26"></a>膏石邊，忽作瘤節，大如彈丸，日日長大，至<a 
name="0905c27" id="0905c27"></a>如手拳，石便爆破，見石節之中，耿耿如螢<a 
name="0905c28" id="0905c28"></a>火，光射出墮地。三日而生須曼，又三日成<a 
name="0905c29" id="0905c29"></a>華，華舒中有小女兒，迦羅越取養之，名曰<a 
name="0906a01" id="0906a01"></a>須曼女。長大姝好，才明智慧，亞次奈女。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>時<a 
name="0906a02" id="0906a02"></a>又有梵志家，浴池中自然生青蓮華，華特加<a 
name="0906a03" id="0906a03"></a>大，日日益長，如五斗瓶。華舒見中有女兒，<a 
name="0906a04" id="0906a04"></a>梵志取養之，名曰波曇女。長大又好，才明智<a 
name="0906a05" id="0906a05"></a>慧，如須曼女。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>諸國王聞此二女顏容絕世，<a 
name="0906a06" id="0906a06"></a>交來求娉之。二女曰：「我生不由胞胎，乃出<a 
name="0906a07" id="0906a07"></a>草華之中，是與凡人不同，豈宜當隨世人乃<a 
name="0906a08" id="0906a08"></a>復嫁耶？」聞奈女聰明世無與等，又生與我同<a 
name="0906a09" id="0906a09"></a>體，皆辭父母，往事奈女，求作弟子，明智博<a 
name="0906a10" id="0906a10"></a>達，皆勝五百人。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛時遊維耶離國，奈女便<a 
name="0906a11" id="0906a11"></a>將弟子五百，出城迎佛，頭面作禮，長跪白<a 
name="0906a12" id="0906a12"></a>言：「願佛明日到我園中飯食。」佛默然受之，<a 
name="0906a13" id="0906a13"></a>奈女還歸供具。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛進入城，國王又出宮迎佛，<a 
name="0906a14" id="0906a14"></a>禮畢長跪請佛，願佛明日到宮。佛言：「奈女<a 
name="0906a15" id="0906a15"></a>向已前請，王後之矣。」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>王曰：「我為國王，至心<a 
name="0906a16" id="0906a16"></a>請佛，必望哀許！奈女但是婬女，日日將從<a 
name="0906a17" id="0906a17"></a>五百婬弟子行作不軌，佛何為捨我而應其<a 
name="0906a18" id="0906a18"></a>請？」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛言：「此女非婬，其宿命有大功德，已供養<a 
name="0906a19" id="0906a19"></a>三億佛。昔奈女又常與須曼、波曇，俱為姊妹，<a 
name="0906a20" id="0906a20"></a>奈女最大，須曼次之，波曇最小，生於大姓<a 
name="0906a21" id="0906a21"></a>家，財寶饒富。姊妹相率，共供養五百比丘<a 
name="0906a22" id="0906a22"></a>尼，日日施設飲食，及作衣服，隨所乏無，皆<a 
name="0906a23" id="0906a23"></a>悉供之，盡其壽命。三人常誓言：『願我後世<a 
name="0906a24" id="0906a24"></a>逢佛，得自然化生，不由胞胎，遠離垢穢。』今<a 
name="0906a25" id="0906a25"></a>如本願，生值我時。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>「又昔雖供養比丘尼，然<a 
name="0906a26" id="0906a26"></a>其豪富家兒，言語憍逸，時時或戲比丘尼曰：<a 
name="0906a27" id="0906a27"></a>『諸道人於邑日久，必當欲嫁也。迫有我等供<a 
name="0906a28" id="0906a28"></a>養撿押，不得放恣情意耳。』故今者受此餘殃，<a 
name="0906a29" id="0906a29"></a>雖日讀經行道，而虛被誹謗生。此五百弟子，<a 
name="0906b01" id="0906b01"></a>時亦并力相助供養，同心歡喜，今故會此，<a 
name="0906b02" id="0906b02"></a>果復相隨。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>「耆婆時為貧家作子，見奈女供養，<a 
name="0906b03" id="0906b03"></a>意甚慕樂，而無資財，乃常為比丘尼掃除，<a 
name="0906b04" id="0906b04"></a>潔淨已輒發念言：『令我能掃除天下人身病<a 
name="0906b05" id="0906b05"></a>穢，如是快耶！』奈女矜其貧窮，又加勤力，常<a 
name="0906b06" id="0906b06"></a>呼為子。其比丘尼有疾病，常使耆婆迎醫<a 
name="0906b07" id="0906b07"></a>及合湯藥，曰：『令汝後世與我共獲是福。』耆婆<a 
name="0906b08" id="0906b08"></a>迎醫，所治悉愈，乃誓曰：『願我後世為大醫<a 
name="0906b09" id="0906b09"></a>王，常治一切人身四大之病，所向皆愈。』宿<a 
name="0906b10" id="0906b10"></a>日因緣，今故為奈女作子，皆如其本願。」王<a 
name="0906b11" id="0906b11"></a>聞佛言，乃長跪悔過，&#x5374;期後日。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛明日便<a 
name="0906b12" id="0906b12"></a>與諸比丘，到奈女園，具為說本願功德。三<a 
name="0906b13" id="0906b13"></a>女聞經開解，并五百弟子，同時歡喜，皆得<a 
name="0906b14" id="0906b14"></a>阿羅漢道。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛告阿難：「汝當受持，為四眾說，<a 
name="0906b15" id="0906b15"></a>莫令斷絕。一切眾生慎身口意，勿生憍慢<a 
name="0906b16" id="0906b16"></a>放逸。奈女往昔時，調戲比丘尼故，今被婬<a 
name="0906b17" id="0906b17"></a>謗。汝當修行身口意業，恒發善願，聞者隨<a 
name="0906b18" id="0906b18"></a>喜信樂受持，莫生誹謗，墮於地獄，餘報畜生，<a 
name="0906b19" id="0906b19"></a>經百千劫，後報為人，貧窮下賤，不聞正法，<a 
name="0906b20" id="0906b20"></a>邪見家生，恒值惡王，身不具足。汝當修行，<a 
name="0906b21" id="0906b21"></a>受持讀誦，盡未來際，常使不絕。」</p><a 
name="0906b22" id="0906b22"></a><p><span class="juanname">佛說奈女耆婆經</span></p>
