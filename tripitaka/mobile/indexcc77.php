name="0905a07" id="0905a07"></a>力勢尚微，不堪疾迅，頭眩疲極，便止山間<a 
name="0905a08" id="0905a08"></a>臥息。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>到日過中，王噫氣出，聞醍醐臭，便大<a 
