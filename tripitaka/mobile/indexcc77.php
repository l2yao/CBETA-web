<a 
name="0902b01" id="0902b01"></a><a 
name="0902b02" id="0902b02"></a><p style="margin-left: 2em"><span class="juannum">No. 554 [No. 553]</span></p><a 
name="0902b03" id="0902b03"></a><p><span class="juanname">佛說<span class="corr" title='[柰&gt;奈]'>奈</span>女耆婆經</span></p><a 
name="0902b04" id="0902b04"></a><a 
name="0902b05" id="0902b05"></a><p style="margin-left: 4em"><span class="byline">後漢安世高譯</span></p><a 
name="0902b06" id="0902b06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>佛在世時，維耶離國王苑中，自然生一奈樹，<a 
name="0902b07" id="0902b07"></a>枝葉繁茂，實又加大，既有光色，香美非凡。<a 
name="0902b08" id="0902b08"></a>王實愛此奈，自非宮中尊貴美人，不得噉此<a 
name="0902b09" id="0902b09"></a>奈果。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>其國中有梵志居士，財富無數，一國<a 
name="0902b10" id="0902b10"></a>無雙，又聰明博達，才智超群，王重愛之，用<a 
name="0902b11" id="0902b11"></a>為大臣。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>王請梵志，飯食畢，以一奈賞與之。<a 
name="0902b12" id="0902b12"></a>梵志見奈香美非凡，乃問王曰：「此奈樹下，<a 
name="0902b13" id="0902b13"></a>寧有小栽可得乞不？」王曰：「大多小栽，吾恐<a 
name="0902b14" id="0902b14"></a>妨其大樹，輒除去之。卿若欲得，今當相與。」<a 
name="0902b15" id="0902b15"></a>即以一奈栽與。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>梵志得歸種之，朝夕灌溉，<a 
name="0902b16" id="0902b16"></a>日日長大，枝條茂好，三年生實，光彩大小，<a 
name="0902b17" id="0902b17"></a>如王家奈。梵志大喜，自念：「我家資財無數，<a 
name="0902b18" id="0902b18"></a>不減於王，唯無此奈，以為不如，今已得之，<a 
name="0902b19" id="0902b19"></a>為無減王。」即取食之，而大苦&#x6F81;，了不可食。</p><a 
name="0902b20" id="0902b20"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>梵志更大愁惱，乃退思惟：「當是土無肥潤故<a 
name="0902b21" id="0902b21"></a>耳。」乃捉取百牛之乳，以飲一牛，復取一牛<a 
name="0902b22" id="0902b22"></a>乳，煎為醍醐，以灌奈根。日日灌之，到至明<a 
name="0902b23" id="0902b23"></a>年，實乃甘美，如王家奈；而樹邊忽復生一<a 
name="0902b24" id="0902b24"></a>瘤節，大如手拳，日日增長。梵志心念：「忽有<a 
name="0902b25" id="0902b25"></a>此瘤節，恐妨其實；適欲斫去，復恐傷樹。」連<a 
name="0902b26" id="0902b26"></a>日思惟，遲迴未決，而節中忽生一枝，正指<a 
name="0902b27" id="0902b27"></a>上向，洪直調好，高出樹頭，去地七丈，其杪<a 
name="0902b28" id="0902b28"></a>乃分作諸枝，周圍傍出，形如偃蓋，華葉茂<a 
name="0902b29" id="0902b29"></a>好勝於本樹。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>梵志怪之：「不知枝上當何所有？」<a 
name="0902c01" id="0902c01"></a>乃作棧閣，登而視之。見枝上偃蓋之中，乃<a 
name="0902c02" id="0902c02"></a>有池水，既清且香，又如眾華，彩色鮮明。披<a 
name="0902c03" id="0902c03"></a>視華下，有一女兒，在池水中。梵志抱取，歸<a 
name="0902c04" id="0902c04"></a>長養之，名曰奈女。至年十五，顏色端正，天<a 
name="0902c05" id="0902c05"></a>下無雙，宣聞遠國。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>有七國王，同時俱來，詣<a 
name="0902c06" id="0902c06"></a>梵志所，求娉奈女以為夫人。梵志大恐怖，<a 
name="0902c07" id="0902c07"></a>不知當以與誰，乃於園中，架一高樓，以奈<a 
name="0902c08" id="0902c08"></a>女著上，出謂諸王曰：「此女非我所生，自出<a 
name="0902c09" id="0902c09"></a>於奈樹之上，亦不知是天、龍、鬼神女耶？鬼魅<a 
name="0902c10" id="0902c10"></a>之物？今七王俱來求之，我設與一王，六王<a 
name="0902c11" id="0902c11"></a>當怒，不敢愛惜也。女今在園中樓上，諸王<a 
name="0902c12" id="0902c12"></a>便自共議，有應得者，便自取去，非我所制<a 
name="0902c13" id="0902c13"></a>也。」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>於是七王口共諍之，紛紜未決。至其夕<a 
name="0902c14" id="0902c14"></a>夜，萍沙王從伏竇中入，登樓就之共宿。明<a 
name="0902c15" id="0902c15"></a>晨當去，奈女白曰：「大王幸枉威尊，接近於<a 
name="0902c16" id="0902c16"></a>我，今復相捨而去，若其有子，則是王種，當<a 
name="0902c17" id="0902c17"></a>何所付？」王曰：「若是男兒，當以還我；若是女<a 
name="0902c18" id="0902c18"></a>兒，便以與汝。」王即脫手金鐶之印，以付奈<a 
name="0902c19" id="0902c19"></a>女，以是為信。便出語群臣曰：「我已得奈女，<a 
name="0902c20" id="0902c20"></a>與共一宿，亦無奇異，故如凡人，故不取耳。」<a 
name="0902c21" id="0902c21"></a>萍沙軍中皆稱萬歲，曰：「我王已得奈女。」六<a 
name="0902c22" id="0902c22"></a>王聞之，便各還去。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>奈女後生得男兒，兒生<a 
name="0902c23" id="0902c23"></a>之時，手中抱持針藥囊出。梵志曰：「此國王<a 
name="0902c24" id="0902c24"></a>之子，而執持醫器，必是醫王。」名曰耆婆。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>至<a 
name="0902c25" id="0902c25"></a>年八歲，聰明高才，學問書<span class="gaiji">疏<!--gaiji,疏,1[跳-兆+(梳-木)],2&#x47FD;,3--></span>，越殊倫匹，與<a 
name="0902c26" id="0902c26"></a>比&#x96A3;小兒遊戲，心常輕諸小兒，以不如己。<a 
name="0902c27" id="0902c27"></a>諸小兒共罵之曰：「無父之子，婬女所生，何敢<a 
name="0902c28" id="0902c28"></a>輕我？」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>耆婆愕然，默而不答。便歸問母曰：「我視<a 
name="0902c29" id="0902c29"></a>子曹，皆不如我，而反罵我言：『無父之子。』我<a 
name="0903a01" id="0903a01"></a>父今者，為在何許？」母曰：「汝父者正萍沙王是<a 
name="0903a02" id="0903a02"></a>也。」耆婆曰：「萍沙王乃在羅閱祇國，去此五百<a 
name="0903a03" id="0903a03"></a>里，何緣生我？若如母言，何以為證？」母即出<a 
name="0903a04" id="0903a04"></a>印鐶示之曰：「此則汝父鐶也。」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>耆婆省之，見<a 
name="0903a05" id="0903a05"></a>有萍沙王印文，便奉持此鐶，往到羅閱祇國。<a 
name="0903a06" id="0903a06"></a>徑入宮門，門無訶者，即到王前為王作禮，<a 
name="0903a07" id="0903a07"></a>長跪白王言：「我是王子，奈女所生，今年八<a 
name="0903a08" id="0903a08"></a>歲，始知是大王種類，故持指鐶印信，遠來<a 
name="0903a09" id="0903a09"></a>歸家。」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>王見印文，憶昔日之誓，知是其子，悵<a 
name="0903a10" id="0903a10"></a>然憐之，以為太子。涉歷二年後，阿闍世王生，<a 
name="0903a11" id="0903a11"></a>耆婆因白王曰：「我初生時手持針藥囊，是應<a 
name="0903a12" id="0903a12"></a>當為醫也。王雖以我為太子，非我所樂；王今<a 
name="0903a13" id="0903a13"></a>自有嫡子生矣，應襲尊嗣，我願得行學醫術。」<a 
name="0903a14" id="0903a14"></a>王即聽之。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>王曰：「汝不為太子者，不得空食王<a 
name="0903a15" id="0903a15"></a>祿，應學醫道。」王即命&#x52C5;國中諸上手醫，盡術<a 
name="0903a16" id="0903a16"></a>教之。而耆婆但行嬉戲，未曾受學，諸師責<a 
name="0903a17" id="0903a17"></a>謂之曰：「醫術鄙陋，誠非太子至尊所宜當學，<a 
name="0903a18" id="0903a18"></a>然大王之命，不可違廢。受&#x52C5;以來積有日月，<a 
name="0903a19" id="0903a19"></a>而太子初不受半言之方，王若問我，我當何<a 
name="0903a20" id="0903a20"></a>對？」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>耆婆曰：「我生而有醫證在手，故白大王<a 
name="0903a21" id="0903a21"></a>捐棄榮豪，求學醫術，豈復懈怠須師督促？<a 
name="0903a22" id="0903a22"></a>直以諸師之道，無足學者故耳。」便取本草藥<a 
name="0903a23" id="0903a23"></a>方針脈諸經，具難問師。師窮無以答，皆下<a 
name="0903a24" id="0903a24"></a>為耆婆作禮，長跪叉手曰：「今日密知太子神<a 
name="0903a25" id="0903a25"></a>聖，實非我等所及也。向所問諸事，皆是我師<a 
name="0903a26" id="0903a26"></a>歷世疑義所不能通，願太子具悉說之，開<a 
name="0903a27" id="0903a27"></a>解我等生年之結。」耆婆便為解說其義，諸醫<a 
name="0903a28" id="0903a28"></a>歡喜，皆悉更起，頭面作禮，承受其法。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>於是<a 
name="0903a29" id="0903a29"></a>耆婆便行治病，所治輒愈，國內知名。後欲<a 
name="0903b01" id="0903b01"></a>入宮，於宮門前，逢一小兒擔樵，耆婆望視，<a 
name="0903b02" id="0903b02"></a>悉見此兒五臟腸胃縷悉分明。耆婆心念：<a 
name="0903b03" id="0903b03"></a>「本草經說，有藥王樹，從外照內見人腹臟。<a 
name="0903b04" id="0903b04"></a>此兒樵中，得無有藥王耶？」即往問兒：「賣樵幾<a 
name="0903b05" id="0903b05"></a>錢？」兒曰：「十錢。」便雇兒十錢，兒下樵置地，則<a 
name="0903b06" id="0903b06"></a>更闇冥不復見其腹中。耆婆心更思惟：「不知<a 
name="0903b07" id="0903b07"></a>束中何者為是藥王？」便解兩束，一一取之<a 
name="0903b08" id="0903b08"></a>以著兒腹上，無所照見輒復更取；如是盡<a 
name="0903b09" id="0903b09"></a>兩束樵，最後有一小枝裁長尺餘，試取以照，<a 
name="0903b10" id="0903b10"></a>即復具見腹內，耆婆大喜，知此小枝定是藥<a 
name="0903b11" id="0903b11"></a>王，悉還兒樵。兒即已得錢，樵又如故，歡喜<a 
name="0903b12" id="0903b12"></a>而去。</p><a 
name="0903b13" id="0903b13"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>爾時，國中有迦羅越家女，年十五，臨當嫁日，<a 
name="0903b14" id="0903b14"></a>忽頭痛而死。耆婆聞之，往至其家問女父：「此<a 
name="0903b15" id="0903b15"></a>女常有何病，乃至致死？」父曰：「女小有頭痛疾，<a 
name="0903b16" id="0903b16"></a>日月增甚，今朝發作尤甚於常，以致絕命。」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>耆<a 
name="0903b17" id="0903b17"></a>婆便進，以藥王照視頭中，見有刺蟲，大小相<a 
name="0903b18" id="0903b18"></a>生，乃數百頭，鑽食其腦，腦盡故死。便以金<a 
name="0903b19" id="0903b19"></a>刀，<span class="gaiji">[利-禾+皮]<!--gaiji,,1[利-禾+皮],2&#x34DF;,3--></span>破其頭悉出諸蟲，封著甖中。以三種<a 
name="0903b20" id="0903b20"></a>神膏塗瘡，一種者補蟲所食骨間之傷；一種<a 
name="0903b21" id="0903b21"></a>生腦；一種治外刀瘡。告女父曰：「好令安靜慎<a 
name="0903b22" id="0903b22"></a>莫使驚，七日當愈平復如故，到其日，我當<a 
name="0903b23" id="0903b23"></a>復來。」</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>耆婆適去，女母便啼哭曰：「我子為再<a 
name="0903b24" id="0903b24"></a>死也，豈有<span class="gaiji">[利-禾+皮]<!--gaiji,,1[利-禾+皮],2&#x34DF;,3--></span>破頭醫腦，當復活者？父何忍命<a 
name="0903b25" id="0903b25"></a>他人取子那爾？」父止之曰：「耆婆生而把持針<a 
name="0903b26" id="0903b26"></a>藥，棄國尊位，行作醫師，但為一切人命故<a 
name="0903b27" id="0903b27"></a>耳，此乃天之醫王，豈當妄耶！囑語汝言：『慎<a 
name="0903b28" id="0903b28"></a>莫使驚。』而汝今反啼哭，以驚動之，將令此<a 
name="0903b29" id="0903b29"></a>兒不復得生耶！」母聞父言，止不復哭，供養<a 
name="0903c01" id="0903c01"></a>護之，寂靜七日。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>七日晨明，女便吹氣而寤，<a 
name="0903c02" id="0903c02"></a>如從臥覺曰：「我今者了不復頭痛，身體皆安。<a 
name="0903c03" id="0903c03"></a>誰護我者，使得如是？」父曰：「汝前已死，醫王<a 
name="0903c04" id="0903c04"></a>耆婆，故來護汝，破頭出蟲，以得更生。」便開<a 
name="0903c05" id="0903c05"></a>甖出蟲示之。女見便大驚怖，深自僥倖曰：「耆<a 
name="0903c06" id="0903c06"></a>婆神乃如是，我以何報其恩？」父曰：「耆婆與<a 
name="0903c07" id="0903c07"></a>我期言：『今日當來。』」於是須臾耆婆便來，女<a 
name="0903c08" id="0903c08"></a>大歡喜，出門奉迎，頭面禮足，長跪叉手曰：<a 
name="0903c09" id="0903c09"></a>「願為耆婆作婢，終身供養，以報更生之恩。」</p><a 
