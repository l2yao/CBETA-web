name="0381b24" id="0381b24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>興至持竿駕小舟。乘風泛入五湖秋。櫓聲驚起蘆花<a 
name="0381c01" id="0381c01"></a>鴈。&#x5374;逐流沙過別洲。<span class="note">(谷隱啟)</span></p><a 
name="0381c02" id="0381c02"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>大慈因僧辭。問甚處去。曰江西去。師曰將取老僧<a 
name="0381c03" id="0381c03"></a>去得否。曰更有過於和尚者亦不能將去。師便休。</p><a 
name="0381c04" id="0381c04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>洞山代云得。　法眼別云。和尚若去。某甲提笠子。<a 
name="0381c05" id="0381c05"></a>　汾陽昭代慈云。知汝力微。　淨因成云。者僧可謂擔<a 
name="0381c06" id="0381c06"></a>頭不泊蠅。是則固是。要且祇是孤生獨死漢。　天童<a 
name="0381c07" id="0381c07"></a>覺云。大慈合伴不著。者僧不如獨行。也須是恁麼始<a 
name="0381c08" id="0381c08"></a>得。直饒大慈古佛。也不奈者擔板漢何。且道者漢別<a 
name="0381c09" id="0381c09"></a>有什麼長處。　寶壽方別云。某甲無者閒氣力。</p><a 
name="0381c10" id="0381c10"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>大慈示眾。說得一丈。不如行取一尺。說得一尺。不<a 
name="0381c11" id="0381c11"></a>如行取一寸。</p><a 
name="0381c12" id="0381c12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>洞山价云。說取行不得底。行取說不得底。　雲居膺<a 
name="0381c13" id="0381c13"></a>云。行時無說路。說時無行路。不行不說時。合行甚麼<a 
name="0381c14" id="0381c14"></a>路。　洛浦安云。行說俱到。則本分事無。行說俱不到。<a 
name="0381c15" id="0381c15"></a>則本分事在。　天童覺云。絕是非。沒&#x8E2A;跡。相逢不識<a 
name="0381c16" id="0381c16"></a>面。識面不相逢。諸尊宿各有長處。如今舌頭上無十<a 
name="0381c17" id="0381c17"></a>字關。&#x811A;跟下無五色&#x7DAB;。要行便行。要說便說。若有人<a 
name="0381c18" id="0381c18"></a>問如何是要行便行。向伊道。步。如何是要說便說。向<a 
name="0381c19" id="0381c19"></a>伊道。啊。</p><a 
name="0381c20" id="0381c20"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>大慈因趙州問般若以何為體。師曰般若以何為<a 
name="0381c21" id="0381c21"></a>體。州呵呵大笑而出。師明日見趙州埽地次。&#x5374;問<a 
name="0381c22" id="0381c22"></a>般若以何為體。州放下埽帚撫掌大笑。師便歸方<a 
name="0381c23" id="0381c23"></a>丈。</p><a 
name="0381c24" id="0381c24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>翠峰顯云。前來也笑。後來也笑。笑中有刀。大慈還識<a 
name="0382a01" id="0382a01"></a>麼。直饒識得。也未免喪身失命。　溈山<span class="gaiji">&#x5586;<!--gaiji,哲,1[(土/口)*(土/口)],2&#x5586;,3--></span>云。纔見呵<a 
name="0382a02" id="0382a02"></a>呵大笑。便與一掌。明日問。待伊大笑。又與一掌。大慈<a 
name="0382a03" id="0382a03"></a>若下得者兩掌。可謂生鐵鑄就。風吹不入。宗師家一<a 
name="0382a04" id="0382a04"></a>等搖撼乾坤。直教如旱天霹<span class="gaiji">靂<!--gaiji,靂,1[靂-秝+林],2,3--></span>。萬里無雲轟一聲。教<a 
name="0382a05" id="0382a05"></a>他眼目定動。豈不俊。哉要識二老麼。莫怪從前多意<a 
name="0382a06" id="0382a06"></a>氣。他家曾謁聖明君。　黃龍新云。趙州金&#x936E;不辨。玉<a 
name="0382a07" id="0382a07"></a>石不分。直饒分去。也未夢見大慈在。　黃龍震云。般<a 
name="0382a08" id="0382a08"></a>若非是無體。二老不能知。般若非是無用。二老不能<a 
name="0382a09" id="0382a09"></a>用。若是能知能用。一言可以截斷眾紛。何必再三。<a 
name="0382a10" id="0382a10"></a>　南華昺云。兩個漢祇解掩耳偷鈴。不解移風易俗。或<a 
name="0382a11" id="0382a11"></a>有問南華。般若以何為體。劈脊便打。待伊眼目定動。<a 
name="0382a12" id="0382a12"></a>&#x5374;問般若以何為體。伶俐漢忽然蹋著。必定自知落<a 
name="0382a13" id="0382a13"></a>處。　古南門云。翠峰與賊過梯。古南則不然。前來也<a 
name="0382a14" id="0382a14"></a>笑。後來也笑。明眼衲僧分&#x758E;不下。忽有個漢出來道。<a 
name="0382a15" id="0382a15"></a>古南也是與賊過梯。直向道還我刀來。</p><a 
name="0382a16" id="0382a16"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>以何為體呵呵笑。推到當頭陷虎機。鳥帶香從花裏<a 
name="0382a17" id="0382a17"></a>出。龍含雨向洞中歸。<span class="note">(心聞賁)</span></p><a 
name="0382a18" id="0382a18"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>玉勒金鞍出帝都。杏花冉冉柳扶蘇。一鞭一步春風<a 
name="0382a19" id="0382a19"></a>裏。蹋破狀元歸去圖。<span class="note">(靈巖儲)</span></p><a 
name="0382a20" id="0382a20"></a><p style="margin-left: 3em"><span class="headname">天台平田普岸禪師<span class="note">(百丈海嗣)</span></span></p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>訪茂源。源纔起迎。師便<a 
name="0382a21" id="0382a21"></a>把住曰。開口即失。閉口即喪。去此二途。請師別道。<a 
name="0382a22" id="0382a22"></a>源以手掩鼻。師放開曰。一步較易。兩步較難。源曰<a 
name="0382a23" id="0382a23"></a>著甚死急。師曰。若非和尚。不免諸方檢點。</p><a 
name="0382a24" id="0382a24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>主山高與案山低。幾見雲開又合時。彷彿暮霞堪對<a 
name="0382b01" id="0382b01"></a>處。兩峰相似正凄迷。<span class="note">(絕象鑑)</span></p><a 
name="0382b02" id="0382b02"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>廝撲相逢是對頭。拳來踢去兩相酬。中間手面交加<a 
name="0382b03" id="0382b03"></a>處。鶻眼鷹睛莫可求。<span class="note">(竹屋簡)</span></p><a 
name="0382b04" id="0382b04"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>平田因僧參。師打一拄杖。僧近前把住拄杖。師曰<a 
name="0382b05" id="0382b05"></a>老僧適來造次。僧&#x5374;打師一拄杖。師曰作家作家。<a 
name="0382b06" id="0382b06"></a>僧禮拜。師把住曰。是老僧造次。闍黎造次。僧大笑。<a 
name="0382b07" id="0382b07"></a>師曰。者箇師僧。今日大敗也。</p><a 
name="0382b08" id="0382b08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>鼓山賢云。平田伸縮安閒。自是慣戰老將。者僧前倨<a 
name="0382b09" id="0382b09"></a>後恭。遭他活陷。真個弄巧成拙。雖然。今日討者師僧<a 
name="0382b10" id="0382b10"></a>亦不可得。</p><a 
name="0382b11" id="0382b11"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>祖令初行亦可觀。從它互換太無端。幸然打著其閒<a 
name="0382b12" id="0382b12"></a>漢。艸賊終來識不難。<span class="note">(本覺一)</span></p><a 
name="0382b13" id="0382b13"></a><p style="margin-left: 3em"><span class="headname">廣州安和寺通禪師<span class="note">(百丈海嗣)</span></span></p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>仰山作沙彌時。一日喚<a 
name="0382b14" id="0382b14"></a>寂子。與我將&#x7240;子來。山將到。師曰&#x5374;送舊處著。山<a 
name="0382b15" id="0382b15"></a>從之。師復召寂子。山應諾。師曰&#x7240;那邊是什麼物。<a 
name="0382b16" id="0382b16"></a>曰枕子。師曰者邊是什麼物。曰無物。復召寂子。山<a 
name="0382b17" id="0382b17"></a>應諾。師曰是什麼。山無對。師曰去。</p><a 
name="0382b18" id="0382b18"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>高峰妙云。潦倒安和。用心不臧。仰山命蹇。為魅所著。<a 
name="0382b19" id="0382b19"></a>山僧恁麼道。也是逆風秉炬</p><a 
name="0382b20" id="0382b20"></a><a 
name="0382b21" id="0382b21"></a><p><span class="juanname">宗鑑法林卷十五</span></p>
