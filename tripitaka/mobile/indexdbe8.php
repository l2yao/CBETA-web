name="0474b21" id="0474b21"></a>削去。司馬君實云。永叔不喜佛。舊唐史<a 
name="0474b22" id="0474b22"></a>有涉其事者必去之。因曰。駕性命道德<a 
name="0474b23" id="0474b23"></a>之空言者。韓文也。泯治亂成敗之實效<a 
name="0474b24" id="0474b24"></a>者。新書也○孝宗御製原道論云。朕觀韓<a 
name="0474b25" id="0474b25"></a>愈原道論。徒文繁而理迂耳<span class="note">(云云)</span>。</p><a 
name="0474b26" id="0474b26"></a><p style="margin-left: 3em"><span class="headname">化胡偽經</span></p><a 
name="0474b27" id="0474b27"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>晉成帝。道士王符偽撰老子化胡經。墮地<a 
name="0474b28" id="0474b28"></a>獄受苦。</p><a 
name="0474b29" id="0474b29"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>北魏孝昭。道士姜斌引開天經說。老子西入<a 
name="0474c01" id="0474c01"></a>化胡。以佛為侍者。沙門曇謨最言。佛生昭<a 
name="0474c02" id="0474c02"></a>王時。後三百四十五年。定王時老子始生。太<a 
name="0474c03" id="0474c03"></a>尉蕭綜劾奏。開天偽經。罪當惑眾詔流斌<a 
name="0474c04" id="0474c04"></a>馬邑。</p><a 
name="0474c05" id="0474c05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>唐高宗。詔議老子化胡經。沙門法明曰。既<a 
name="0474c06" id="0474c06"></a>無翻譯朝代。豈非偽造。乃&#x52C5;令搜聚偽本<a 
name="0474c07" id="0474c07"></a>一處禁棄○武后。沙門惠澄。乞依前朝毀<a 
name="0474c08" id="0474c08"></a>化胡經。&#x52C5;八學士議○中宗。&#x52C5;毀化胡經<a 
name="0474c09" id="0474c09"></a>及畫壁變相。違者科罪。</p><a 
name="0474c10" id="0474c10"></a><p style="margin-left: 3em"><span class="headname">事魔邪黨</span></p><a 
name="0474c11" id="0474c11"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>末尼火<span class="corr" title='[祅&gt;祆]'>祆</span><span class="note">(火&#x70DF;反)</span>者。初波斯國有蘇魯支。行<a 
name="0474c12" id="0474c12"></a>火<span class="corr" title='[祅&gt;祆]'>祆</span>教。弟子來化中國。唐正觀五年。其徒穆<a 
name="0474c13" id="0474c13"></a>護何祿詣闕進<span class="corr" title='[祅&gt;祆]'>祆</span>教。&#x52C5;京師建大秦寺○武<a 
name="0474c14" id="0474c14"></a>后延載元年。波斯國拂多誕持二宗經偽<a 
name="0474c15" id="0474c15"></a>教來朝○玄宗開元二十年&#x52C5;末尼本是邪<a 
name="0474c16" id="0474c16"></a>見。妄稱佛教。既為西胡師法。其徒自行。不<a 
name="0474c17" id="0474c17"></a>須科罰○天寶四年&#x52C5;兩京諸郡有波斯<a 
name="0474c18" id="0474c18"></a>寺者。並改名大秦○大歷三年&#x52C5;回紇及<a 
name="0474c19" id="0474c19"></a>荊揚等州。奉末尼各建大雲光明寺。六年<a 
name="0474c20" id="0474c20"></a>回紇。請荊揚洪越等州置摩邪寺。其徒白衣<a 
name="0474c21" id="0474c21"></a>白冠○會昌三年。&#x52C5;天下末尼寺並廢。京城<a 
name="0474c22" id="0474c22"></a>女末尼七十二人皆死。在回紇者流之諸<a 
name="0474c23" id="0474c23"></a>道。五年&#x52C5;。大秦穆護火<span class="corr" title='[祅&gt;祆]'>祆</span>等二千人。並勒還<a 
name="0474c24" id="0474c24"></a>俗○梁貞明六年。陳州末尼反立母乙為天<a 
name="0474c25" id="0474c25"></a>子。朝廷發兵禽斬之。其徒以不茹葷飲酒。<a 
name="0474c26" id="0474c26"></a>夜聚婬穢。畫魔王踞坐佛為洗足。云佛止大<a 
name="0474c27" id="0474c27"></a>乘。我乃上上乘。</p><a 
name="0474c28" id="0474c28"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>白雲萊者。徽宗大觀間。西京寶應寺僧孔清<a 
name="0474c29" id="0474c29"></a>覺居杭之白雲菴。立四果十地造論數篇。<a 
name="0475a01" id="0475a01"></a>教於流俗。亦曰十地萊。覺海愚禪師辨之。<a 
name="0475a02" id="0475a02"></a>有司流恩州。嘉泰二年。白雲菴沈智元自稱<a 
name="0475a03" id="0475a03"></a>道民。進狀乞額。臣寮言。道民者喫菜事魔。<a 
name="0475a04" id="0475a04"></a>所謂姦民者也。既非僧道童行。自植黨與<a 
name="0475a05" id="0475a05"></a>千百為群。挾持<span class="corr" title='[祅&gt;祆]'>祆</span>教聾瞽愚俗。或以修<a 
name="0475a06" id="0475a06"></a>橋砌路斂率民財。創立私菴為逋逃淵藪。<a 
name="0475a07" id="0475a07"></a>乞將智元長流遠地。拆除菴宇以為傳習<a 
name="0475a08" id="0475a08"></a>魔法之戒。奏可。</p><a 
name="0475a09" id="0475a09"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>白蓮菜者。高宗紹興初。吳郡延祥院僧弟子<a 
name="0475a10" id="0475a10"></a>元。依倣天台出圓融四土圖。晨朝禮懺文。<a 
name="0475a11" id="0475a11"></a>偈歌四句。佛念五聲勸男女修淨業。戒護<a 
name="0475a12" id="0475a12"></a>生為尤謹。稱為白蓮導師。有以事魔論<a 
name="0475a13" id="0475a13"></a>於有司者。流之江州。其徒展轉相教。至今<a 
name="0475a14" id="0475a14"></a>為盛。</p><a 
name="0475a15" id="0475a15"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>良渚曰。此三者皆假名佛教以誑愚俗。<a 
name="0475a16" id="0475a16"></a>猶五行之有沴氣也。今摩尼尚扇於三<a 
name="0475a17" id="0475a17"></a>山。而白蓮白雲處處有習之者。大氐不<a 
name="0475a18" id="0475a18"></a>事葷酒。故易於裕足。而不殺物命。故<a 
name="0475a19" id="0475a19"></a>近於為善。愚民無知皆樂趨之。故其黨<a 
name="0475a20" id="0475a20"></a>不勸而自盛。甚至第宅姬妾。為魔女所<a 
name="0475a21" id="0475a21"></a>誘入其眾中。以修懺念佛為名。而實通<a 
name="0475a22" id="0475a22"></a>姦穢。有識士夫宜加禁止。</p><a 
name="0475a23" id="0475a23"></a><p style="margin-left: 3em"><span class="headname">毀法惡報</span></p><a 
name="0475a24" id="0475a24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>魏高貴鄉公。罽賓國王害師子尊者。白乳涌<a 
name="0475a25" id="0475a25"></a>高數尺。王臂尋墮。七日命終。</p><a 
name="0475a26" id="0475a26"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>晉安帝。曇無讖在涼譯經。後西歸。涼王遣<a 
name="0475a27" id="0475a27"></a>人刺於路。王見神人以劍刺之。遂卒。</p><a 
name="0475a28" id="0475a28"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>宋沙門惠琳著黑白論。與佛理相違。後感<a 
name="0475a29" id="0475a29"></a>疾膚肉糜爛竟死。時以為叛教之報○簡靜<a 
name="0475b01" id="0475b01"></a>寺尼用書經絹為兒衣。身瘡白蟲。號叫而<a 
name="0475b02" id="0475b02"></a>死。</p><a 
name="0475b03" id="0475b03"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>梁武帝。沙門智稜值寇還俗。道士孟悉達勸<a 
name="0475b04" id="0475b04"></a>為黃冠。引佛教潤色諸道經。暮年為道士<a 
name="0475b05" id="0475b05"></a>講西昇經。忽失音舌卷。於座委頓而死○望<a 
name="0475b06" id="0475b06"></a>蔡令。殺牛飲啖。臥佛堂上。白癩病死。</p><a 
name="0475b07" id="0475b07"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>宋真宗。慈照聰禪師。郡守笞之。師。曰教他平<a 
name="0475b08" id="0475b08"></a>地起骨堆。守全家死○神宗。王安石子雱資<a 
name="0475b09" id="0475b09"></a>性險惡。既亡荷鐵枷告父。安石請以江寧<a 
name="0475b10" id="0475b10"></a>園廬為僧寺<span class="note">(即半山寺)</span>○徽宗宣和廢佛法。楊<a 
name="0475b11" id="0475b11"></a>戩議廢太平興國寺。瘞佛像於殿基。已而戩<a 
name="0475b12" id="0475b12"></a>病胸腹潰裂而死。○詔天下建神霄宮。太<a 
name="0475b13" id="0475b13"></a>平州將拆承天寺。主議者石儀曹一子。為金<a 
name="0475b14" id="0475b14"></a>甲神納於火曰。石某絕嗣。</p><a 
name="0475b15" id="0475b15"></a><p><span class="juanname">佛祖統紀卷第五十四<span class="note">(終)</span></span></p><a 
name="0475b16" id="0475b16"></a><div class="xu"><p style="margin-left: 3em"><span class="headname">刊板後記</span></p><a 
name="0475b17" id="0475b17"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>咸淳元年乙丑寓東湖月波山。始飭工刊<a 
name="0475b18" id="0475b18"></a>統紀。至六年庚午冬。忽感喘嗽之疾。家<a 
name="0475b19" id="0475b19"></a>林法眷棹船見邀。遂以十二月二十一日<a 
name="0475b20" id="0475b20"></a>歸于福泉之故廬。是時尚有會要志四卷<a 
name="0475b21" id="0475b21"></a>未能刊。於是乘病寫本。俾刊人畢其功。<a 
name="0475b22" id="0475b22"></a>秋七月鋟事既備。擬辨紙印造萬部為最<a 
name="0475b23" id="0475b23"></a>初流通。&#x751E;計之刊板所費將萬券。而印<a 
name="0475b24" id="0475b24"></a>造之本逾二十萬券。非高明識鑑有大財<a 
name="0475b25" id="0475b25"></a>力者。則不能濟。當願　佛祖聖賢冥密<a 
name="0475b26" id="0475b26"></a>勸化當朝居位　王公大人。知有法門。共<a 
name="0475b27" id="0475b27"></a>相激發。或一出己力。或轉化群賢。特輟<a 
name="0475b28" id="0475b28"></a>餘貲建立紙本。使統紀一書布散寰海。是<a 
name="0475b29" id="0475b29"></a>亦助　國行化之大端也。磐臥病日久。恐<a 
name="0475c01" id="0475c01"></a>一旦怛化此志不申。故略述始終。尚祈<a 
name="0475c02" id="0475c02"></a>後賢之克繼勝業者。維　佛維　祖宜當<a 
name="0475c03" id="0475c03"></a>祐之以道。</p><a 
name="0475c04" id="0475c04"></a><p style="text-indent: 0em; margin-left: 2em"><span class="linehead"></span>咸淳辛未端午日　　　　志磐　書</p></div>
