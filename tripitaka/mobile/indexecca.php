name="0914b09" id="0914b09"></a>水灑近坐苾芻。不問房主有飯水。不得棄白<a 
name="0914b10" id="0914b10"></a>衣家內。應棄殘食。不得置於&#x9262;內。若地上無<a 
name="0914b11" id="0914b11"></a>替不應安&#x9262;<span class="gaiji">澗<!--gaiji,澗,1[澗-日+月],2&#x3D4E;,3--></span>邊。不得置&#x9262;危嶮處。不得置<a 
name="0914b12" id="0914b12"></a>&#x9262;峻崖處。不得置&#x9262;。不得立洗&#x9262;<span class="gaiji">澗<!--gaiji,澗,1[澗-日+月],2&#x3D4E;,3--></span>邊。不得<a 
name="0914b13" id="0914b13"></a>洗&#x9262;危嶮處。不得洗&#x9262;峻崖處。不得洗&#x9262;迎<a 
name="0914b14" id="0914b14"></a>暴流。不得以&#x9262;酌水。應當學。人坐己立。不應<a 
name="0914b15" id="0914b15"></a>為說法。人臥己坐不應為說法。人在高座己<a 
name="0914b16" id="0914b16"></a>在下坐。不應為說法。人在前行己在後行。不<a 
name="0914b17" id="0914b17"></a>應為說法。人在道行己在非道行。不應為說<a 
name="0914b18" id="0914b18"></a>法。不應為覆頭人說法。不應為抄衣人說法。<a 
name="0914b19" id="0914b19"></a>不應為收衣拊肩人說法。不應為兩手交項人<a 
