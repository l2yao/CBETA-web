name="0914c02" id="0914c02"></a>三十九應當學。中除有病苾芻無犯。除難緣。<a 
name="0914c03" id="0914c03"></a>不得上樹過人頭。應當學。一百一十二種應<a 
name="0914c04" id="0914c04"></a>當學突吉羅竟。</p><a 
name="0914c05" id="0914c05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>已上四波羅夷篇。十三僧伽婆尸沙篇。三十<a 
