name="0914c19" id="0914c19"></a>緣護持。此有差別義。餘略羯磨儀範文中。或<a 
name="0914c20" id="0914c20"></a>廣毘奈耶中應看。</p><a 
name="0914c21" id="0914c21"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第三若有所犯令修補法者</p><a 
name="0914c22" id="0914c22"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>欲除覆藏罪應須發露。欲除所犯罪。應須懺<a 
name="0914c23" id="0914c23"></a>悔。身語未作意中。所有微細罪業。應須防護。<a 
name="0914c24" id="0914c24"></a>為欲羯磨中不作障難。應須守持為令不復<a 
