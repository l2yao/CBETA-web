name="0914c11" id="0914c11"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第四淨自戒律護持者</p><a 
name="0914c12" id="0914c12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>為淨自戒故作布薩法。又為除滅自他身命<a 
