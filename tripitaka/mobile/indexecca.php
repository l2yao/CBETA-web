name="0914c16" id="0914c16"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第五依安樂住緣護持者</p><a 
name="0914c17" id="0914c17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>衣服飲食住處醫藥。於此四中遠離奢樂極<a 
