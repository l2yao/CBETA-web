name="0914b22" id="0914b22"></a>為戴冠人說法。不應為戴華鬘人說法。不應<a 
name="0914b23" id="0914b23"></a>為纏頭人說法。不應為乘象人說法。不應為<a 
name="0914b24" id="0914b24"></a>乘馬人說法。不應為乘輿人說法。不應為乘<a 
name="0914b25" id="0914b25"></a>坐諸物人說法。不應為著靴人說法。不應為<a 
