name="0914c26" id="0914c26"></a>法。此廣差別義。百一羯磨中應看。若作此受<a 
name="0914c27" id="0914c27"></a>戒護持修補法時。應慎護諸惡。不令損害一<a 
name="0914c28" id="0914c28"></a>切有情。願獲涅槃果。應須護持。</p><a 
name="0914c29" id="0914c29"></a><p><span class="juanname">根本說一切有部苾芻習學略法</span></p><a 
name="0915a01" id="0915a01"></a><div class="w"><div style="margin-left: 1em"><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>恭惟略本之始。&#x5EFC;大元世主今上明君。睿<a 
name="0915a02" id="0915a02"></a>智日新。鴻慈天賦。萬機之暇。釋教是遵。爰<a 
