<a 
name="0912a01" id="0912a01"></a><a 
name="0912a02" id="0912a02"></a><p style="margin-left: 2em"><span class="juannum">No. 1905</span></p><a 
name="0912a03" id="0912a03"></a><p><span class="juanname">根本說一切有部苾芻習學略法</span></p><a 
name="0912a04" id="0912a04"></a><a 
name="0912a05" id="0912a05"></a><p style="margin-left: 4em"><span class="byline">元帝師苾芻拔合思巴集</span></p><a 
name="0912a06" id="0912a06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>敬禮一切智。</p><a 
name="0912a07" id="0912a07"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>教示增上戒學律藏有三種。</p><a 
name="0912a08" id="0912a08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>　第一未得令得儀範　第二已得律儀不犯<a 
name="0912a09" id="0912a09"></a>護持方便　第三若有犯者令修補法</p><a 
name="0912a10" id="0912a10"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第一未得令得儀範者有四種。</p><a 
name="0912a11" id="0912a11"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>能為得律儀作障　能為安住律儀作障　能<a 
name="0912a12" id="0912a12"></a>為增長德業作障　能為端嚴眾作障</p><a 
name="0912a13" id="0912a13"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若俱無此四種違緣。復以歸處形相身體思<a 
name="0912a14" id="0912a14"></a>念儀範。若全有此五種順緣者。方得律儀。此<a 
name="0912a15" id="0912a15"></a>差別義如儀範中應知。</p><a 
name="0912a16" id="0912a16"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第二已得律儀不犯護持方便者。有五種。</p><a 
name="0912a17" id="0912a17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>一依依止師護持　二以對治想護持　三了<a 
name="0912a18" id="0912a18"></a>知應捨相違護持　四淨自戒律護持　五依<a 
name="0912a19" id="0912a19"></a>安樂住緣護持</p><a 
name="0912a20" id="0912a20"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第一依依止師護持者</p><a 
name="0912a21" id="0912a21"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若受近圓已滿足十年。二十一種五德法中<a 
name="0912a22" id="0912a22"></a>具十五法。解律比丘求為依止師。應作不應<a 
name="0912a23" id="0912a23"></a>作。一切事業應問彼師。如師教示隨教所行。<a 
name="0912a24" id="0912a24"></a>自受近圓。滿十年等。三種德業圓滿已來。應<a 
name="0912a25" id="0912a25"></a>依彼師。</p><a 
name="0912a26" id="0912a26"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第二以對治想護持者</p><a 
name="0912a27" id="0912a27"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>於一切時中念知不放。逸具此三種法。除一<a 
name="0912a28" id="0912a28"></a>切相違法。成就一切善法。</p><a 
name="0912a29" id="0912a29"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第三了知應捨相違護持者</p><a 
name="0912b01" id="0912b01"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>總集為五篇二百五十三。應捨法中具清淨<a 
name="0912b02" id="0912b02"></a>戒。或具應修補戒。有心有念。復非初緣不捨<a 
name="0912b03" id="0912b03"></a>學處。不犯學處。第一波羅夷篇者。苾芻於三<a 
name="0912b04" id="0912b04"></a>道中。隨一一道行不淨行者。犯第一波羅夷<a 
name="0912b05" id="0912b05"></a>罪。若盜非屬己身他人物者。犯第二波羅夷<a 
name="0912b06" id="0912b06"></a>罪。除己身外人或人胎斷其命者。犯第三波<a 
name="0912b07" id="0912b07"></a>羅夷罪。妄說過上人法者。犯第四波羅夷罪。<a 
name="0912b08" id="0912b08"></a>此四波羅夷罪。皆能斷絕正戒根本。說第一<a 
name="0912b09" id="0912b09"></a>篇竟。</p><a 
name="0912b10" id="0912b10"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第二十三僧伽婆尸沙篇者。若故泄精者。若<a 
name="0912b11" id="0912b11"></a>染心觸女人身者。若共女人說婬欲&#x9E81;惡名<a 
name="0912b12" id="0912b12"></a>者。若於女人前讚歎自身。可持婬欲法供養<a 
name="0912b13" id="0912b13"></a>者。若為人成夫婦事者。若三不堪處。從他人<a 
name="0912b14" id="0912b14"></a>乞地并材物造房者。若不堪處。建僧伽藍者。<a 
name="0912b15" id="0912b15"></a>若無根謗苾芻犯波羅夷法者。若無根以小<a 
name="0912b16" id="0912b16"></a>因緣。謗說苾芻犯波羅夷法者。若破和合僧<a 
name="0912b17" id="0912b17"></a>者。若助破和合僧者。若因污他家以為僧擯<a 
name="0912b18" id="0912b18"></a>毀謗僧者。若自戒中他比丘。以憐愍心欲令<a 
name="0912b19" id="0912b19"></a>勸時。言說諸僧伽因我一切戒法。莫論說我。<a 
name="0912b20" id="0912b20"></a>而違拒者。此十三法從僧伽應修補。故名為<a 
name="0912b21" id="0912b21"></a>僧伽婆尸沙。第二篇竟。</p><a 
name="0912b22" id="0912b22"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第三波逸底迦篇有二種。</p><a 
name="0912b23" id="0912b23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第一三十泥薩祇波逸底迦者</p><a 
name="0912b24" id="0912b24"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>初篇十種者。若未曾加持衣畜過十宿者。若<a 
name="0912b25" id="0912b25"></a>已加持三衣。過一宿離者。若三衣物不足望<a 
name="0912b26" id="0912b26"></a>他。足過三十宿畜彼物者。若使非親尼浣洗<a 
name="0912b27" id="0912b27"></a>衣者。若從比丘尼取衣者。若有三衣從在家<a 
name="0912b28" id="0912b28"></a>人乞衣者。若自無三衣從他人乞時。過分乞<a 
name="0912b29" id="0912b29"></a>者。若他欲與物未與索者。若他人各各欲與<a 
name="0912c01" id="0912c01"></a>物。未與索者。若已送到不堪作衣物。若過三<a 
name="0912c02" id="0912c02"></a>語索。或過三默索者。初遍十種竟。</p><a 
name="0912c03" id="0912c03"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第二遍十種者</p><a 
name="0912c04" id="0912c04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若蠶綿作新敷具者。若純黑羊毛作新敷具<a 
name="0912c05" id="0912c05"></a>者。若合黑白羊毛作敷具者。若減六年作第<a 
name="0912c06" id="0912c06"></a>二敷具者。若作新坐具不以故者。縱廣佛一<a 
name="0912c07" id="0912c07"></a>張手貼新者。上用敷者。若無持負羊毛人。自<a 
name="0912c08" id="0912c08"></a>持負羊毛。過三由旬者。有持負人自持負過<a 
name="0912c09" id="0912c09"></a>一里者。若使非親比丘尼擘羊毛者。若畜自<a 
name="0912c10" id="0912c10"></a>己珠寶目觸教人觸者。若以資財出納求利<a 
name="0912c11" id="0912c11"></a>者。若買賣者。第二遍十種竟。</p><a 
name="0912c12" id="0912c12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第三遍十種者</p><a 
name="0912c13" id="0912c13"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若應量器不加持畜過十宿者。若自有&#x9262;更<a 
name="0912c14" id="0912c14"></a>求餘者。若使非親織師不與工錢。織作成衣<a 
name="0912c15" id="0912c15"></a>者。若他人為己使人織作衣間。為自利益教<a 
name="0912c16" id="0912c16"></a>長織者。若與比丘衣或&#x9262;已後還奪取者。若<a 
name="0912c17" id="0912c17"></a>夏安居中所得利養安居內取要。或安居中<a 
name="0912c18" id="0912c18"></a>分散。或安居竟自恣後。彼利養不分者。若住<a 
name="0912c19" id="0912c19"></a>阿蘭若苾芻。聚落村舍內留法衣過六夜。後<a 
name="0912c20" id="0912c20"></a>不到留衣處。或自阿蘭若處者。若雨浴衣安<a 
name="0912c21" id="0912c21"></a>居一月前求者。或安居後過半月畜者。若欲<a 
name="0912c22" id="0912c22"></a>與眾僧物。或欲與一僧物。自迴入己者。若應<a 
name="0912c23" id="0912c23"></a>堪取捨四種藥。各各過限畜者。此是第三遍<a 
name="0912c24" id="0912c24"></a>十種三十泥薩祇波逸底竟。</p><a 
name="0912c25" id="0912c25"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第二九遍十種波底逸迦者</p><a 
name="0912c26" id="0912c26"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>初遍十種者。若苾芻故妄語者。若說他苾芻<a 
name="0912c27" id="0912c27"></a>過失者。若兩舌令二苾芻等離間者。若有諍<a 
name="0912c28" id="0912c28"></a>競苾芻和合已後。再發舉諍者。若與在家婦<a 
name="0912c29" id="0912c29"></a>人無男子時說法者。若與未近圓人同聲讀<a 
name="0913a01" id="0913a01"></a>誦教示法者。若知他苾芻有犯墮落法。或僧<a 
name="0913a02" id="0913a02"></a>殘法。隨一一與在家人說者。若向未近圓人。<a 
name="0913a03" id="0913a03"></a>未證聖諦人實說所得上人法者。若眾僧執<a 
name="0913a04" id="0913a04"></a>事苾芻無過失而毀說。言以僧物人事與知<a 
name="0913a05" id="0913a05"></a>識者。若布薩日誦微碎戒時。輕呵而言。何須<a 
name="0913a06" id="0913a06"></a>說如是雜碎戒。令他苾芻聞者。初遍十種<a 
name="0913a07" id="0913a07"></a>竟。</p><a 
name="0913a08" id="0913a08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第二遍十種者</p><a 
name="0913a09" id="0913a09"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若壞種子斷青草木者。若嫌毀輕賤僧伽執<a 
name="0913a10" id="0913a10"></a>事苾芻者。若同學法人教利益語而違惱者。<a 
name="0913a11" id="0913a11"></a>若將僧伽敷具。以放逸心安置露地者。若於<a 
name="0913a12" id="0913a12"></a>僧房內敷草。或葉用已不除掃者。若從僧伽<a 
name="0913a13" id="0913a13"></a>藍遣苾芻出者。若僧住處後來苾芻欺凌先<a 
name="0913a14" id="0913a14"></a>住苾芻者。若僧住處不堅固。重房棚上坐臥<a 
name="0913a15" id="0913a15"></a>脫&#x811A;床者。若為他使用有蟲水等者。若壘寺<a 
name="0913a16" id="0913a16"></a>僧伽藍牆時。除許量外。過量壘者。第二遍十<a 
name="0913a17" id="0913a17"></a>種竟。</p><a 
name="0913a18" id="0913a18"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第三遍十種者</p><a 
name="0913a19" id="0913a19"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若僧伽不曾差。自意往教誡苾芻尼者。若教<a 
name="0913a20" id="0913a20"></a>誡苾芻尼至日暮者。若實無此念而毀說他。<a 
name="0913a21" id="0913a21"></a>為此小飲食教誡苾芻尼者。若苾芻與苾芻<a 
name="0913a22" id="0913a22"></a>尼衣者。若苾芻與苾芻尼縫衣者。若苾芻與<a 
name="0913a23" id="0913a23"></a>苾芻尼作伴道路行者。若苾芻與苾芻尼同<a 
name="0913a24" id="0913a24"></a>乘船者。若苾芻共女人屏處坐者。若苾芻與<a 
name="0913a25" id="0913a25"></a>女人屏處立者。若因苾芻尼讚歎受在家食<a 
name="0913a26" id="0913a26"></a>者。第三遍十種竟。</p><a 
name="0913a27" id="0913a27"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第四遍十種者</p><a 
name="0913a28" id="0913a28"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若一時食中無利養受再食等者。若於外道<a 
name="0913a29" id="0913a29"></a>住處經一宿住受再食等者。若苾芻足食竟。<a 
name="0913b01" id="0913b01"></a>不作餘食法更食者。若乞食苾芻從在家人。<a 
name="0913b02" id="0913b02"></a>受過兩三&#x9262;食者。若苾芻足食竟勸令更食<a 
name="0913b03" id="0913b03"></a>者。若苾芻離眾僧常食處。各無己食。共三苾<a 
name="0913b04" id="0913b04"></a>芻等別食者。若自洲日斜至明相出食。非時<a 
name="0913b05" id="0913b05"></a>食者。若食或自或他。苾芻經畜食者。若食不<a 
name="0913b06" id="0913b06"></a>與不受食者。若從施主索美食食者。第四遍<a 
name="0913b07" id="0913b07"></a>十種竟。</p><a 
name="0913b08" id="0913b08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第五遍十種者</p><a 
name="0913b09" id="0913b09"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若有蟲水等為己身用者。若在家人行不淨<a 
name="0913b10" id="0913b10"></a>行時。同房坐者。若在家人行不淨行時。同房<a 
name="0913b11" id="0913b11"></a>立聽者。若與裸形外道食者。若觀欲戰嚴整<a 
name="0913b12" id="0913b12"></a>軍者。若無因緣軍陣中過二宿住者。若混亂<a 
name="0913b13" id="0913b13"></a>排定軍陣者。若打苾芻者。若以擬手向苾芻<a 
name="0913b14" id="0913b14"></a>者。若知他苾芻有&#x9E81;惡罪覆藏者。第五遍十<a 
name="0913b15" id="0913b15"></a>種竟。</p><a 
name="0913b16" id="0913b16"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第六遍十種者</p><a 
name="0913b17" id="0913b17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若施主欲與苾芻食以冤酬故。遮不令與當<a 
name="0913b18" id="0913b18"></a>日絕食者。若觸火者。若因苾芻作羯磨時。與<a 
name="0913b19" id="0913b19"></a>他欲已而後悔者。若苾芻與未近圓人。近一<a 
name="0913b20" id="0913b20"></a>尋地內。宿過一宿者。若說婬欲不為障礙執<a 
name="0913b21" id="0913b21"></a>事惡見。若僧諫時不捨者。若共眾僧所遣比<a 
name="0913b22" id="0913b22"></a>丘而作伴者。若知是被擯沙彌。而收攝而作<a 
name="0913b23" id="0913b23"></a>眷屬饒益共住者。若受用不染色白衣敷具<a 
name="0913b24" id="0913b24"></a>等者。若屬他寶及以寶好物。自觸教人觸者。<a 
name="0913b25" id="0913b25"></a>若從四月初一日。至六月十五日。除此兩個<a 
name="0913b26" id="0913b26"></a>半月。其餘時中。若未至半月而洗浴者。第六<a 
name="0913b27" id="0913b27"></a>遍十種竟。</p><a 
name="0913b28" id="0913b28"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第七遍十種者</p><a 
name="0913b29" id="0913b29"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若故斷畜生命者。若言苾芻汝非苾芻。令須<a 
name="0913c01" id="0913c01"></a>臾不樂者。若以指擊歷苾芻者。若水中戲者。<a 
name="0913c02" id="0913c02"></a>若獨自共在家女人。一室同宿至明者。若苾<a 
name="0913c03" id="0913c03"></a>芻但一因由恐怖苾芻者。若藏苾芻衣者。若<a 
name="0913c04" id="0913c04"></a>與苾芻衣不迴還復取受者。若苾芻無僧殘<a 
name="0913c05" id="0913c05"></a>罪無因。或以小因謗說犯僧殘罪者。若共<a 
name="0913c06" id="0913c06"></a>在家女人更無男子同道行者。第七遍十種<a 
name="0913c07" id="0913c07"></a>竟。</p><a 
name="0913c08" id="0913c08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第八遍十種者</p><a 
name="0913c09" id="0913c09"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若共盜賊或匿稅商旅同道行者。若知年未<a 
name="0913c10" id="0913c10"></a>滿二十。與授近圓者。若堅實地中掘一抄土<a 
name="0913c11" id="0913c11"></a>者。若受在家人請。住過四月者。若不依僧伽<a 
name="0913c12" id="0913c12"></a>制而反毀謗。告白苾芻言。汝愚癡無所知者。<a 
name="0913c13" id="0913c13"></a>若共有&#x9B2A;諍苾芻欲令&#x9B2A;諍往彼聽其所說<a 
name="0913c14" id="0913c14"></a>者。若從僧伽所作羯磨處。不問一苾芻從坐<a 
name="0913c15" id="0913c15"></a>而起。遠去離聞聲地者。若應敬信僧伽處僧<a 
name="0913c16" id="0913c16"></a>伽執事等處。不敬信不隨順者。若飲諸酒等<a 
name="0913c17" id="0913c17"></a>者。若不問苾芻等。向暮入聚落者。第八遍十<a 
name="0913c18" id="0913c18"></a>種竟。</p><a 
name="0913c19" id="0913c19"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第九遍十種者</p><a 
name="0913c20" id="0913c20"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若苾芻受食家請。午時前去行詣入餘三家<a 
name="0913c21" id="0913c21"></a>等。或向暮時。除僧伽集處。入餘四家等者。若<a 
name="0913c22" id="0913c22"></a>日沒之後。紅相已滅。至明相未出。入灌頂王<a 
name="0913c23" id="0913c23"></a>宮內。或后妃宮內者。若布薩日誦別解脫戒<a 
name="0913c24" id="0913c24"></a>經時。於雜碎戒中。言我今始知如是雜碎戒。<a 
name="0913c25" id="0913c25"></a>是應可學而輕呵者。若用象牙等作鍼筒者。<a 
name="0913c26" id="0913c26"></a>若坐臥足過量床者。若將僧伽敷具。以木綿<a 
name="0913c27" id="0913c27"></a>等霑污者。若作坐具。應長三肘廣二肘六指。<a 
name="0913c28" id="0913c28"></a>過此量作者。若作覆瘡衣。應長六肘廣三肘。<a 
name="0913c29" id="0913c29"></a>過此量作者若作雨浴衣。應長九肘廣三肘<a 
name="0914a01" id="0914a01"></a>一十八指。過此量作者。若同佛衣量作衣。或<a 
name="0914a02" id="0914a02"></a>教他作者。犯波逸底迦。如來衣量者。長中形。<a 
name="0914a03" id="0914a03"></a>人一十五肘廣九肘是也。第九遍十種竟。總<a 
name="0914a04" id="0914a04"></a>九十波逸底迦竟。</p><a 
name="0914a05" id="0914a05"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第四四種波羅底提舍尼者</p><a 
name="0914a06" id="0914a06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若聚落或聚落外。或在道中或近道外。從苾<a 
name="0914a07" id="0914a07"></a>芻尼受食者。若於白衣家內有苾芻尼。趣次<a 
name="0914a08" id="0914a08"></a>指授食不止而食者。若眾僧所制白衣家內。<a 
name="0914a09" id="0914a09"></a>反取食者。若差看守怖難道。&#x5374;不看守。受食<a 
name="0914a10" id="0914a10"></a>坐者。四波羅底提舍尼竟。</p><a 
name="0914a11" id="0914a11"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>第五一百一十二應當學者</p><a 
name="0914a12" id="0914a12"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>齊整著安陀會。不太高不太下。衣角不象鼻。<a 
name="0914a13" id="0914a13"></a>不多羅葉。不穀團形。不蛇頭齊整披法衣。不<a 
name="0914a14" id="0914a14"></a>太高不太下。乞食行時善護身語行。齊整披<a 
name="0914a15" id="0914a15"></a>法衣。行不作聲行不亂視。行當觀一尋地量<a 
name="0914a16" id="0914a16"></a>行。若入聚落不得用衣覆頭行。不得抄衣行。<a 
name="0914a17" id="0914a17"></a>不得收衣附。肩行。不得兩手交項上行。不得<a 
name="0914a18" id="0914a18"></a>兩手交腦後行。不得跳行。不得探&#x811A;行。不得<a 
name="0914a19" id="0914a19"></a>蹲行。不得足指行。不得扠腰行。不得搖身行。<a 
name="0914a20" id="0914a20"></a>不得掉臂行。不得搖頭行。不得磨肩行。不得<a 
name="0914a21" id="0914a21"></a>連手行。若不請不得在白衣家敷具上坐。不<a 
name="0914a22" id="0914a22"></a>善觀察不應坐。不得放身重坐。不得交足坐。<a 
name="0914a23" id="0914a23"></a>不得交腿坐。不重內外踝坐。若床上坐時。不<a 
name="0914a24" id="0914a24"></a>得曲&#x811A;入床下。不得叉&#x811A;坐不得寬&#x811A;坐。應<a 
name="0914a25" id="0914a25"></a>正意受食。不得滿&#x9262;受食。不得菜食齊等受。<a 
name="0914a26" id="0914a26"></a>應依坐次受食。應視&#x9262;受食。行食未至不得<a 
name="0914a27" id="0914a27"></a>預伸&#x9262;為更望重受。不得以食覆菜。不得以<a 
name="0914a28" id="0914a28"></a>菜覆食。若受食時不得安&#x9262;在食上正意而<a 
name="0914a29" id="0914a29"></a>食。不得作極小摶而食。不得作極大摶而食。<a 
name="0914b01" id="0914b01"></a>應作中摶而食。若食未至不得張口待食。不<a 
name="0914b02" id="0914b02"></a>得含食語。不得皺眉而食。不得<span class="gaiji">[口*專]<!--gaiji,,1[口*專],2,3--></span><span class="gaiji">[口*集]<!--gaiji,,1[口*集],2&#x35F1;,3--></span>口食。不<a 
name="0914b03" id="0914b03"></a>得呵氣食。不得吹氣食。不得舒舌食。不得一<a 
name="0914b04" id="0914b04"></a>粒粒取食。若受食時不得叱笑他。若受食時<a 
name="0914b05" id="0914b05"></a>不得換頰嚼食。不得彈舌食。不得齧半食。不<a 
name="0914b06" id="0914b06"></a>得舐手。不得振手。不得刮&#x9262;舌舐。不得振&#x9262;<a 
name="0914b07" id="0914b07"></a>內食食壘作塔形。不應損壞而食。不得輕笑<a 
