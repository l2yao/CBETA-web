name="0508c19" id="0508c19"></a><p style="text-indent: 0em; margin-left: 2em"><span class="linehead"></span>隨好光明功德品。</p><a 
name="0508c20" id="0508c20"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>阿僧祇品明出世間廣大筭數菩薩尚不能知必佛<a 
