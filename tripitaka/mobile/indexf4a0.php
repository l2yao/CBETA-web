name="0508c13" id="0508c13"></a><p style="text-indent: 0em; margin-left: 2em"><span class="linehead"></span>十住品。</p><a 
name="0508c14" id="0508c14"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>四教儀註云華嚴十住前無十信位或以十梵行當<a 
