name="0508b13" id="0508b13"></a><p style="margin-left: 3em"><span class="headname">△華嚴經</span></p><a 
name="0508b14" id="0508b14"></a><p style="text-indent: 0em; margin-left: 2em"><span class="linehead"></span>世主妙嚴品。</p><a 
name="0508b15" id="0508b15"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>或疑世尊成道諸菩薩諸天諸神皆來集會如一人<a 
