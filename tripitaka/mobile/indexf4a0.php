name="0508a19" id="0508a19"></a>之亦無是論非經之理何以故論必宗經違經立論<a 
name="0508a20" id="0508a20"></a>成邪外故。</p><a 
name="0508a21" id="0508a21"></a><a 
name="0508a22" id="0508a22"></a><p style="margin-left: 3em"><span class="headname">△彌陀經</span></p><a 
name="0508a23" id="0508a23"></a><p style="text-indent: 0em; margin-left: 2em"><span class="linehead"></span>彼佛壽命及其人民無量無邊阿僧祇劫故名<a 
name="0508a24" id="0508a24"></a>阿彌陀。</p><a 
name="0508b01" id="0508b01"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>經意蓋云彼佛及其人民所有之壽命悉無量無邊<a 
name="0508b02" id="0508b02"></a>阿僧祇劫也亦可云佛之壽命及其人民之壽命悉<a 
