name="0508c03" id="0508c03"></a><p style="text-indent: 0em; margin-left: 2em"><span class="linehead"></span>華藏世界品。</p><a 
name="0508c04" id="0508c04"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>或疑世界焉得浮處&#x8657;空重重無盡此恐寓言非為<a 
