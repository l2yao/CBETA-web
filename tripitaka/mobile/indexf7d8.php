name="0604a23" id="0604a23"></a><a pin_name="3 分別世品"></a>釋識緣名色　言。遍一期生者。此經部名<a 
name="0604a24" id="0604a24"></a><a pin_name="3 分別世品"></a>色從初生後乃至死有皆名名色。於名色<a 
name="0604a25" id="0604a25"></a><a pin_name="3 分別世品"></a>上更立餘支。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。於大因緣至如是說<a 
name="0604a26" id="0604a26"></a><a pin_name="3 分別世品"></a>故。引教證也。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。如是名色至說為六<a 
name="0604a27" id="0604a27"></a><a pin_name="3 分別世品"></a>處。此釋名色緣六處也。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。次與境合<a 
name="0604a28" id="0604a28"></a><a pin_name="3 分別世品"></a>便有識生。釋六處緣觸也。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。三和故<a 
name="0604a29" id="0604a29"></a><a pin_name="3 分別世品"></a>至樂等三受。釋觸緣受。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。從此三受<a 
name="0604b01" id="0604b01"></a><a pin_name="3 分別世品"></a>至生無色愛。此釋從受生於愛也。三界受<a 
name="0604b02" id="0604b02"></a><a pin_name="3 分別世品"></a>不同故愛三界異。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。從欣受愛起欲等<a 
name="0604b03" id="0604b03"></a><a pin_name="3 分別世品"></a>取。此明愛緣取也　言等者。等戒見．我語<a 
name="0604b04" id="0604b04"></a><a pin_name="3 分別世品"></a>取也。經部宗不同有部。於一境上愛增為<a 
name="0604b05" id="0604b05"></a><a pin_name="3 分別世品"></a>取。即以四取以為取支體也。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。此中<a 
name="0604b06" id="0604b06"></a><a pin_name="3 分別世品"></a>欲者謂五妙欲。已下別釋四取　欲者即色<a 
name="0604b07" id="0604b07"></a><a pin_name="3 分別世品"></a>等五妙欲境。皆能生欲故名欲也。或是所欲<a 
name="0604b08" id="0604b08"></a><a pin_name="3 分別世品"></a>故名欲。於五妙境起貪名為欲取。</p><a 
name="0604b09" id="0604b09"></a><a pin_name="3 分別世品"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。見謂六十二見如梵網經廣說。此釋見取。<a 
name="0604b10" id="0604b10"></a><a pin_name="3 分別世品"></a>亦如婆沙一百九十九．二百廣釋。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。<a 
name="0604b11" id="0604b11"></a><a pin_name="3 分別世品"></a>戒謂遠離惡戒。戒即是內法戒也。遠離不律<a 
name="0604b12" id="0604b12"></a><a pin_name="3 分別世品"></a>儀等惡戒。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。禁謂狗牛等禁者。是外道<a 
name="0604b13" id="0604b13"></a><a pin_name="3 分別世品"></a>持狗．牛等禁。此不能離惡戒故不名戒。</p><a 
name="0604b14" id="0604b14"></a><a pin_name="3 分別世品"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。如諸離繫至無義苦行。已下指人法<a 
name="0604b15" id="0604b15"></a><a pin_name="3 分別世品"></a>也　離繫者。即是露形外道。離衣等繫故<a 
name="0604b16" id="0604b16"></a><a pin_name="3 分別世品"></a>名離繫。此外道受持種種露形拔髮等禁<a 
name="0604b17" id="0604b17"></a><a pin_name="3 分別世品"></a>　婆羅門外道受持手執杖行披烏鹿皮等<a 
name="0604b18" id="0604b18"></a><a pin_name="3 分別世品"></a>　播輸&#x9262;多外道此云牛主。謂事天主。摩醯<a 
name="0604b19" id="0604b19"></a><a pin_name="3 分別世品"></a>首羅天乘牛而行故云牛主。此外道學彼天<a 
name="0604b20" id="0604b20"></a><a pin_name="3 分別世品"></a>法從彼為名故名牛主。此外道持受頂上<a 
name="0604b21" id="0604b21"></a><a pin_name="3 分別世品"></a>持一髻子身體塗灰　般利伐羅句伽外<a 
name="0604b22" id="0604b22"></a><a pin_name="3 分別世品"></a>道此云遍出。此是出家外道。此持執三杖<a 
name="0604b23" id="0604b23"></a><a pin_name="3 分別世品"></a>行擬護淨安衣服．瓶．&#x9262;等并剪鬚髮無<a 
name="0604b24" id="0604b24"></a><a pin_name="3 分別世品"></a>義苦行　等。等取自餘無義五熱灸身等<a 
name="0604b25" id="0604b25"></a><a pin_name="3 分別世品"></a>也。外道受此等法皆名禁也。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。我語謂<a 
name="0604b26" id="0604b26"></a><a pin_name="3 分別世品"></a>內身依之說我故。釋我語取。即此內身實非<a 
name="0604b27" id="0604b27"></a><a pin_name="3 分別世品"></a>我語。依之起我語故名我語。此是有財釋<a 
name="0604b28" id="0604b28"></a><a pin_name="3 分別世品"></a>也。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。有餘師說至名為我語。述異釋<a 
name="0604b29" id="0604b29"></a><a pin_name="3 分別世品"></a>也。此師以我見．我慢名為我語。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。云<a 
name="0604c01" id="0604c01"></a><a pin_name="3 分別世品"></a>何此二至說名我語。釋二名我語所以也。<a 
name="0604c02" id="0604c02"></a><a pin_name="3 分別世品"></a>但有其語無所詮故。引經證可解。</p><a 
name="0604c03" id="0604c03"></a><a pin_name="3 分別世品"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。於前四種至所謂欲貪。正釋取體前四取<a 
name="0604c04" id="0604c04"></a><a pin_name="3 分別世品"></a>是所取。貪是能取。故引經說取是欲貪。此<a 
name="0604c05" id="0604c05"></a><a pin_name="3 分別世品"></a>謂於四取上起貪欲也。非謂唯欲界貪。又<a 
name="0604c06" id="0604c06"></a><a pin_name="3 分別世品"></a>此中意唯取欲貪。不同有部兼同時蘊。</p><a 
name="0604c07" id="0604c07"></a><a pin_name="3 分別世品"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。由取為緣至說名為有。釋取緣有。引<a 
name="0604c08" id="0604c08"></a><a pin_name="3 分別世品"></a>經可解。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。有為緣故至說名為生。釋<a 
name="0604c09" id="0604c09"></a><a pin_name="3 分別世品"></a>有緣生。此說生支通其五蘊亦并六識。</p><a 
name="0604c10" id="0604c10"></a><a pin_name="3 分別世品"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。已生為緣故至廣說如經。釋生緣老<a 
name="0604c11" id="0604c11"></a><a pin_name="3 分別世品"></a>死也。辨十二支廣如經說。已上依經部<a 
name="0604c12" id="0604c12"></a><a pin_name="3 分別世品"></a>釋十二支也。已下依論釋經純大苦蘊<a 
name="0604c13" id="0604c13"></a><a pin_name="3 分別世品"></a>集。</p><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>論。如是純言顯唯有行無我我所。<a 
name="0604c14" id="0604c14"></a><a pin_name="3 分別世品"></a>大苦蘊言顯苦無初無後故名大也。集言<a 
name="0604c15" id="0604c15"></a><a pin_name="3 分別世品"></a>為顯諸苦蘊生。集是生義也。此釋經中云<a 
name="0604c16" id="0604c16"></a><a pin_name="3 分別世品"></a>純大苦蘊集。</p><a 
name="0604c17" id="0604c17"></a><a pin_name="3 分別世品"></a><p><span class="juanname">俱舍論疏卷第九</span></p><a 
name="0604c18" id="0604c18"></a><a pin_name="3 分別世品"></a><div class="w"><div style="margin-left: 1em"><p style="text-indent: 0em; margin-left: 2em"><span class="linehead"></span>霜四日夜半於南屋點了</p><a 
name="0604c19" id="0604c19"></a><a pin_name="3 分別世品"></a><p style="text-indent: 0em; margin-left: 3em"><span class="linehead"></span>此卷有不慮事等字界字緣等也。</p><a 
name="0604c20" id="0604c20"></a><a pin_name="3 分別世品"></a><p style="text-indent: 0em; margin-left: 3em"><span class="linehead"></span>前權少僧都傳燈少法師覺受</p><a 
