name="0303b09" id="0303b09"></a>事發露除愆。方合經旨。滅業之源。以罪雖從心生。<a 
name="0303b10" id="0303b10"></a>必因事而成業。故還依事而遣除。所謂罪從因緣<a 
name="0303b11" id="0303b11"></a>而生。還從因緣而滅。緣生無性。故曰罪性本空。因<a 
name="0303b12" id="0303b12"></a>果不昧。是曰果報不失。倘緣生有性。即觀之亦不<a 
name="0303b13" id="0303b13"></a>可脫。惟無實性。故得還從因緣而滅。豈撥緣生而<a 
name="0303b14" id="0303b14"></a>求性空。非不懺摩而罪自謝。若棄事而談理。實未<a 
name="0303b15" id="0303b15"></a>知理之實相。何能觀察諸法猶如&#x8657;空。設離緣而<a 
name="0303b16" id="0303b16"></a>觀諸法。即迷事不異理。寧悟當體即空。今有狂愚<a 
name="0303b17" id="0303b17"></a>不慚<span class="corr" title='[巳&gt;己]'>己</span>過。每藉口談理。妄云觀心。忽事相而不懺<a 
name="0303b18" id="0303b18"></a>悔。事理既迷。行解何由相應。況理無雙立之能。事<a 
name="0303b19" id="0303b19"></a>有挾理之功。是故懺摩而不觀心。猶能決罪之流。<a 
name="0303b20" id="0303b20"></a>倘談理而不說悔。決難清罪之源。事理雙行。罪滅<a 
name="0303b21" id="0303b21"></a>如日消霜。比丘若違律儀。要依律宗殄滅。必須識<a 
name="0303b22" id="0303b22"></a>於罪名種性。隨有牒懺。戒體還淨。若疑不識。不合<a 
name="0303b23" id="0303b23"></a>加法。罪亦難捐。故智度論云。戒律中雖復微細。懺<a 
name="0303b24" id="0303b24"></a>則清淨。犯十善戒。雖懺。三惡道罪不除。如比丘犯<a 
name="0304a01" id="0304a01"></a>諸性戒等。約此而言。比丘說悔。惟滅違制六罪。性<a 
name="0304a02" id="0304a02"></a>愆由須準上事理懺除。倘託事理。而不對他說悔<a 
name="0304a03" id="0304a03"></a>違制之罪猶在。詎能免彼六墮。三者並行。性遮俱<a 
name="0304a04" id="0304a04"></a>遣。其有智者欲得身心皎潔。即可準經律蕩除。若<a 
name="0304a05" id="0304a05"></a>不依律如法說悔。制罪亦不可滅。故僧祇律云。或<a 
name="0304a06" id="0304a06"></a>有緣無罪而得罪者。如比丘無罪作罪悔過。得越<a 
name="0304a07" id="0304a07"></a>毗尼罪。或有緣有罪得無罪者。如作小房。大房。一<a 
name="0304a08" id="0304a08"></a>切乃至三諫。是也。或有緣有罪至有罪者。僧中說<a 
name="0304a09" id="0304a09"></a>戒時乃至三問。有罪者如法悔。無罪者默然。爾時<a 
name="0304a10" id="0304a10"></a>有罪不如法悔。復不語人。又不作念。我待清淨同<a 
name="0304a11" id="0304a11"></a>意人當如法說悔。默然者。得越毗尼罪。或有緣無<a 
name="0304a12" id="0304a12"></a>罪得無罪者。不作不犯。是也。或有緣重至輕者。比<a 
name="0304a13" id="0304a13"></a>丘犯波逸提。向惡邪邊見。被舉人。悔過。得越毗尼<a 
name="0304a14" id="0304a14"></a>罪。或有緣輕至重者。比丘犯越毗尼罪。向謗經人。<a 
name="0304a15" id="0304a15"></a>被舉人。悔過。得波逸提。或有緣重至重者。比丘犯<a 
name="0304a16" id="0304a16"></a>波逸提。向謗經。被舉人。悔過。得波逸提。或有緣輕<a 
name="0304a17" id="0304a17"></a>至輕者。比丘犯越毗尼罪。向未具人悔過。不名悔<a 
name="0304a18" id="0304a18"></a>過。更得越毗尼罪。</p></div><a 
name="0304a19" id="0304a19"></a><a 
