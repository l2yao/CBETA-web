name="0301a08" id="0301a08"></a><div class="entry" style="text-indent: 0em; margin-left: 0em"><p>盡智</p><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>羅漢入無餘。但有孤調解脫。謂<span class="gaiji">&#x7070;<!--gaiji,灰,1[厂@火],2&#x7070;,3--></span>身故無<a 
