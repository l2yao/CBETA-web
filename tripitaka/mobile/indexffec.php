name="0292a15" id="0292a15"></a><p><span class="juanname">四分戒本如釋卷第十二</span></p><a 
name="0292a16" id="0292a16"></a><a 
name="0292a17" id="0292a17"></a><p style="margin-left: 4em"><span class="byline">明　廣州沙門釋　弘贊在&#x7299;　繹</span></p><a 
name="0292a18" id="0292a18"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○八滅諍法<span class="note">(開三)</span>　初總標　中別列　後結問。</p><a 
name="0292a19" id="0292a19"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>△初總標</p><a 
name="0292a20" id="0292a20"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>諸大德。是七滅諍法。半月半月說。戒經中來。若比丘<a 
name="0292a21" id="0292a21"></a>有諍事起。即應除滅。</p><a 
name="0292a22" id="0292a22"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。七滅諍法者。謂比丘有四諍事起。當以此七<a 
name="0292a23" id="0292a23"></a>法如法。如律。如佛所教。而除滅之。故名七滅諍法<a 
name="0292a24" id="0292a24"></a>也。言四諍者。一言諍。二覓諍。三犯諍。四事諍。云何<a 
name="0292b01" id="0292b01"></a>言諍。謂比丘共比丘諍言。引十八事。法。非法。乃至<a 
name="0292b02" id="0292b02"></a>說。不說。若以如是相共諍言語。遂彼此共&#x9B2C;。是為<a 
name="0292b03" id="0292b03"></a>言諍。云何覓諍。謂比丘與比丘覓罪。以三舉事。破<a 
name="0292b04" id="0292b04"></a>戒。破見。破威儀。見。聞。疑。作如相覓罪。是為覓諍。云<a 
name="0292b05" id="0292b05"></a>何犯諍。謂犯七種罪。波羅夷。僧殘。波逸提。悔過法。<a 
name="0292b06" id="0292b06"></a>偷蘭遮。惡作。惡說。是為犯諍。云何事諍。謂言諍中<a 
name="0292b07" id="0292b07"></a>事作。覓諍中事作。犯諍中事作。是為事諍<span class="note">(身犯者名惡作。<a 
name="0292b08" id="0292b08"></a>口犯者名惡說)</span>。</p><a 
name="0292b09" id="0292b09"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>△中別列</p><a 
name="0292b10" id="0292b10"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○現前滅諍法第一</p><a 
name="0292b11" id="0292b11"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>應與現前毗尼。當與現前毗尼。</p><a 
name="0292b12" id="0292b12"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。佛在舍衛國。迦留陀夷。與六&#x7FA3;在河中浴。浴<a 
name="0292b13" id="0292b13"></a>竟。先上岸。錯著六&#x7FA3;衣而去。六&#x7FA3;上岸不見<span class="corr" title='[巳&gt;己]'>己</span>衣。<a 
name="0292b14" id="0292b14"></a>即謂彼偷衣去。彼不現前。便作滅擯羯磨。佛言。無<a 
name="0292b15" id="0292b15"></a>盜心不犯。不應不看衣而著。不應人不現前而作<a 
name="0292b16" id="0292b16"></a>擯羯磨。依止羯磨。遮不至白衣家羯磨。舉羯磨。滅<a 
name="0292b17" id="0292b17"></a>擯羯磨。若作。羯磨不成。得突吉羅。云何現前。謂法。<a 
name="0292b18" id="0292b18"></a>毗尼。人。僧。界也。云何法現前。所持法滅諍者是。云<a 
name="0292b19" id="0292b19"></a>何毗尼現前。所持毗尼滅諍者是。云何人現前。言<a 
name="0292b20" id="0292b20"></a>義往返者是。云何僧現前。同羯磨和集一處。不來<a 
name="0292b21" id="0292b21"></a>者囑授。在現前得訶。而不訶者是。云何界現前。在<a 
name="0292b22" id="0292b22"></a>界內羯磨作制限者是。若諍事滅。更發起者。墮罪。<a 
name="0292b23" id="0292b23"></a>若後來比丘新受戒者。謂是初諍。而更發起者。及<a 
name="0292b24" id="0292b24"></a>與欲<span class="corr" title='[巳&gt;已]'>已</span>後悔者。皆墮罪。此之七法。尼皆同學<span class="note">(磨得勒伽<a 
name="0292c01" id="0292c01"></a>論。明十種不現前。得作羯磨。一覆&#x9262;。二捨覆&#x9262;。謂俗人罵謗比丘等。僧應與作羯磨。不相往來。若後自見<a 
name="0292c02" id="0292c02"></a>過。行隨順心。求僧乞解。僧應羯磨為解。三學家。四捨學家。如悔過法中釋。五作房。謂以故廢寺地。羯磨與<a 
name="0292c03" id="0292c03"></a>居士。任其更為僧作房舍。六擯惡見沙彌。<span class="corr" title='[巳&gt;已]'>已</span>上皆謂未受大戒人。不得聞羯磨法故。不須現前。其二解。及<a 
name="0292c04" id="0292c04"></a>作房。擯沙彌。令在眼見耳不聞處立。七狂痴。謂無知故。不須現前。八不禮拜。九不共語。十不供養。謂比丘<a 
name="0292c05" id="0292c05"></a>非法觸惱比丘尼。不得面治比丘。但遙作不禮拜等。三種羯磨而<span class="corr" title='[巳&gt;已]'>已</span>)</span>。</p><a 
name="0292c06" id="0292c06"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○憶念滅諍法第二</p><a 
name="0292c07" id="0292c07"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>應與憶念毗尼。當與憶念毗尼。</p><a 
name="0292c08" id="0292c08"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。佛在王舍城。摩羅子不犯重罪。諸比丘皆言<a 
name="0292c09" id="0292c09"></a>其犯。問言。汝憶犯重罪不。彼不憶犯。使答言。我不<a 
name="0292c10" id="0292c10"></a>憶犯如是重罪。長老莫數詰問我。而諸比丘故詰<a 
name="0292c11" id="0292c11"></a>問不止。佛聽僧為作憶念毗尼白四羯磨。彼應至<a 
name="0292c12" id="0292c12"></a>僧中乞言。大德僧聽。我某甲不犯重罪。諸比丘言<a 
name="0292c13" id="0292c13"></a>我犯重罪。波羅夷。僧殘。偷蘭遮。諸比丘問我言。汝<a 
name="0292c14" id="0292c14"></a>憶犯重罪不。我不憶犯重罪。答言。我不憶犯如是<a 
name="0292c15" id="0292c15"></a>重罪。諸長老不須數數難詰問我。而諸比丘故難<a 
name="0292c16" id="0292c16"></a>詰不止。我今不憶念。從僧乞憶念毗尼。願僧與我<a 
name="0292c17" id="0292c17"></a>憶念毗尼。慈愍故。如是三乞<span class="corr" title='[巳&gt;已]'>已</span>。僧應為白四羯磨<a 
name="0292c18" id="0292c18"></a>與<span class="note">(羯磨。準乞應作)</span>。云何憶念毗尼。彼比丘此罪。更不應舉。<a 
name="0292c19" id="0292c19"></a>不應作憶念。若諍事如法滅<span class="corr" title='[巳&gt;已]'>已</span>。後更發起者。墮罪。<a 
name="0292c20" id="0292c20"></a>如上<span class="note">(由諸比丘數數令其憶念不止。故佛聽作憶念毗尼。使他不復更問。薩婆多論云。此是守<a 
name="0292c21" id="0292c21"></a>護毗尼。餘比丘不應出其罪過。不應令憶念)</span>。</p><a 
name="0292c22" id="0292c22"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○不癡滅諍法第三</p><a 
name="0292c23" id="0292c23"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>應與不癡毗尼。當與不癡毗尼。</p><a 
name="0292c24" id="0292c24"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。佛在王舍城。難提比丘癡狂心亂。多犯眾罪。<a 
name="0293a01" id="0293a01"></a>後還得心。諸比丘詰問不止。佛聽僧與作不癡毗<a 
name="0293a02" id="0293a02"></a>尼白四羯磨。彼應至僧中乞言。大德僧聽。我某甲<a 
name="0293a03" id="0293a03"></a>比丘。癩狂心亂時。多犯眾罪。行來出入。不順威儀。<a 
name="0293a04" id="0293a04"></a>後還得心。諸比丘問言。汝憶犯重罪不。我答言。先<a 
name="0293a05" id="0293a05"></a>癲狂心亂時。多犯眾罪。行來出入。不順威儀。非我<a 
name="0293a06" id="0293a06"></a>故作。是癲狂心故。諸長老不須數數難詰問我。而<a 
name="0293a07" id="0293a07"></a>諸比丘故難問不止。我今不癡。從僧乞不癡毗尼。<a 
name="0293a08" id="0293a08"></a>願僧與我不癡毗尼。慈愍故。如是三乞<span class="corr" title='[巳&gt;已]'>已</span>。僧應為<a 
name="0293a09" id="0293a09"></a>白四羯磨與<span class="note">(羯磨。準乞應作)</span>。云何不癡毗尼。彼比丘此罪。<a 
name="0293a10" id="0293a10"></a>更不應舉。不應作憶念者是。若如法諍事滅<span class="corr" title='[巳&gt;已]'>已</span>。更<a 
name="0293a11" id="0293a11"></a>發起者。墮罪。如上<span class="note">(薩婆多論云。此是守護毗尼)</span>。</p><a 
name="0293a12" id="0293a12"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○自言滅諍法第四</p><a 
name="0293a13" id="0293a13"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>應與自言治。當與自言治。</p><a 
name="0293a14" id="0293a14"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。佛在瞻婆城。十五日布薩時。眾僧前後圍遶。<a 
name="0293a15" id="0293a15"></a>有犯罪者。在於眾中坐。初中後夜<span class="corr" title='[巳&gt;已]'>已</span>過。世尊默然<a 
name="0293a16" id="0293a16"></a>不說戒。目連即以天眼見知。便促彼臂牽出門外。<a 
name="0293a17" id="0293a17"></a>佛言。不應如是。令彼伏罪。然後與罪。不應不自伏<a 
name="0293a18" id="0293a18"></a>罪而與罪。云何自言。說罪名。罪種。懺悔者是。云何<a 
name="0293a19" id="0293a19"></a>治。語言。自責汝心。生厭雖者是。若諍事滅<span class="corr" title='[巳&gt;已]'>已</span>。更發<a 
name="0293a20" id="0293a20"></a>起者。墮罪。餘如上說。優波離白佛言。作自言治。一<a 
name="0293a21" id="0293a21"></a>切如法不。佛言。不一切如法。若比丘不犯棄罪。彼<a 
name="0293a22" id="0293a22"></a>不作舉。不作憶念。自言犯棄。諸比丘即與作棄罪<a 
name="0293a23" id="0293a23"></a>治。是為非法自言治。若比丘不犯棄。彼不作舉。不<a 
name="0293a24" id="0293a24"></a>作憶念。自言犯僧殘。諸比丘即與作僧殘罪治。是<a 
name="0293b01" id="0293b01"></a>為非法。乃至自言犯惡說。亦如是<span class="note">(次句以僧殘為首。乃至惡說為<a 
name="0293b02" id="0293b02"></a>首。一一作句。亦如是。復以作舉。作憶念。一一句。亦如上)</span>。若比丘犯棄罪。彼不<a 
name="0293b03" id="0293b03"></a>作舉。不作憶念。自言犯僧殘。諸比丘即與作僧殘<a 
name="0293b04" id="0293b04"></a>治。是為非法。乃至自言犯惡說。亦如是。若比丘犯<a 
name="0293b05" id="0293b05"></a>僧殘。彼不作舉。不作憶念。便自言犯棄。諸比丘即<a 
name="0293b06" id="0293b06"></a>與作棄罪治。是為非法。若比丘犯僧殘。彼不作舉<a 
name="0293b07" id="0293b07"></a>及憶念。便自言犯墮。諸比丘即與作墮罪治。是為<a 
name="0293b08" id="0293b08"></a>非法。乃至自言犯惡說。互為首句。亦如是<span class="note">(作舉。作憶念。互<a 
name="0293b09" id="0293b09"></a>為首句。一一如上)</span>。云何如法自言治。若比丘犯棄罪。彼不<a 
name="0293b10" id="0293b10"></a>作舉。不作憶念。自言犯棄。諸比丘即為作棄罪治。<a 
name="0293b11" id="0293b11"></a>是為如法與自言治。乃至惡說亦如是。若比丘犯<a 
name="0293b12" id="0293b12"></a>棄罪。彼作舉。作憶念。自言犯棄。諸比丘即如其言<a 
name="0293b13" id="0293b13"></a>治。是為如法與自言治。乃至惡說。亦如是。</p><a 
name="0293b14" id="0293b14"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○覓罪滅諍法第五</p><a 
name="0293b15" id="0293b15"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>應與覓罪相。當與覓罪相。</p><a 
name="0293b16" id="0293b16"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。佛在釋氏國。象力比丘喜論議。共外道論。得<a 
name="0293b17" id="0293b17"></a>切問時。前後言語相違。於僧中問時。亦復如是。前<a 
name="0293b18" id="0293b18"></a>後言語相違。在眾中故作妄語。外道譏嫌。諸比丘<a 
name="0293b19" id="0293b19"></a>白佛。佛令僧與彼作罪處所白四羯磨。集僧<span class="corr" title='[巳&gt;已]'>已</span>。為<a 
name="0293b20" id="0293b20"></a>作舉。作舉<span class="corr" title='[巳&gt;已]'>已</span>。為作憶念。作憶念<span class="corr" title='[巳&gt;已]'>已</span>。與罪。白言。大德<a 
name="0293b21" id="0293b21"></a>僧聽。此某甲比丘。好論議。與外道論。得切問時。前<a 
name="0293b22" id="0293b22"></a>後言語相違。設於眾僧中問。亦前後言語相違。眾<a 
name="0293b23" id="0293b23"></a>中故作妄語。若僧時到。僧忍聽。僧今與某甲比丘<a 
name="0293b24" id="0293b24"></a>作罪處所羯磨。汝某甲無利。不善得。汝得切問時。<a 
name="0293c01" id="0293c01"></a>前後言語相違。設眾僧中問時。亦復如是。在眾中<a 
