name="0299a20" id="0299a20"></a>法之用。而證五分之果。當知五分法身。前三從因<a 
name="0299a21" id="0299a21"></a>而顯德。後二就果以彰能。盡智正習俱斷。名解脫<a 
name="0299a22" id="0299a22"></a>身。無生智了了覺照。名為知見。菩薩急於化他。故<a 
name="0299a23" id="0299a23"></a>加施忍進。以修六度。聲聞由於自度。唯修三學。故<a 
name="0299a24" id="0299a24"></a>智論。以六度是眾生法。三學是涅槃法。一切法門<a 
name="0299b01" id="0299b01"></a>由學而趣。三學不修。則涅槃無路。木叉不奉。則定<a 
name="0299b02" id="0299b02"></a>慧絕因。何能超渡四流。而登彼岸者哉。</p><a 
name="0299b03" id="0299b03"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○四結頌迴向<span class="note">(共有十四頌。皆結集者所置。今分六釋)</span>。</p><a 
name="0299b04" id="0299b04"></a><p>　<span class="lg">明人能護戒　　能得三種樂　　名譽及利養<a 
name="0299b05" id="0299b05"></a><br>　死得生天上　　當觀如是處　　有智勤護戒<a 
name="0299b06" id="0299b06"></a><br>　戒淨有智慧　　便得第一道</span></p><a 
name="0299b07" id="0299b07"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。前一頌。是世間因果。後一頌。是出世間因果。<a 
name="0299b08" id="0299b08"></a>舉世間因果。以彰出世間因果也。明人者。謂了達<a 
name="0299b09" id="0299b09"></a>三途苦趣是惡因所感。人天樂境由善業所招。故<a 
name="0299b10" id="0299b10"></a>捨諸惡因。而修眾善。戒為眾善之本。乃明達智者<a 
name="0299b11" id="0299b11"></a>能護。非愚暗不別善惡果報者所可堪持也。三種<a 
name="0299b12" id="0299b12"></a>樂者。名譽。利養。是現在華報之樂。得生天上。是未<a 
name="0299b13" id="0299b13"></a>來果報之樂。名是名稱。譽是讚譽。利養即是四聖<a 
name="0299b14" id="0299b14"></a>種。謂能持戒之人。戒德芬馥。十方普聞。人天稱譽。<a 
name="0299b15" id="0299b15"></a>所共供養也。天者。天然。自然清淨。最勝最樂故。苟<a 
name="0299b16" id="0299b16"></a>非淨潔最勝之因。豈生淨潔最勝之處。淨潔因者。<a 
name="0299b17" id="0299b17"></a>禁戒是也。當觀如是處者。謂應思惟觀察世間三<a 
name="0299b18" id="0299b18"></a>種樂報。悉由護戒而有。即知出世第一道。必從戒<a 
name="0299b19" id="0299b19"></a>淨而得也。有智勤護戒者。天上乃明人得生。出世<a 
name="0299b20" id="0299b20"></a>非智者莫辦。亦非懈惰無戒者所獲。由護淨戒。身<a 
name="0299b21" id="0299b21"></a>心皎潔。發生無漏智慧。由智慧力故。破諸煩惱。而<a 
name="0299b22" id="0299b22"></a>得涅槃第一道也。</p><a 
name="0299b23" id="0299b23"></a><p>　<span class="lg">如過去諸佛　　及以未來者　　現在諸世尊<a 
name="0299b24" id="0299b24"></a><br>　能勝一切憂　　皆共尊敬戒　　此是諸佛法<a 
name="0299c01" id="0299c01"></a><br>　若有自為身　　欲求於佛道　　當尊重正法<a 
name="0299c02" id="0299c02"></a><br>　此是諸佛教</span></p><a 
name="0299c03" id="0299c03"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。前一頌半。舉諸佛共尊戒而成道。以證之。後<a 
name="0299c04" id="0299c04"></a>一頌。示欲求佛當重正法。以勸之。一切煩惱諸惑<a 
name="0299c05" id="0299c05"></a>結縛眾生。故名為憂。三世如來皆奉波羅提木叉。<a 
name="0299c06" id="0299c06"></a>對治諸惑。而得解脫。故名能勝。自為身者。謂專為<a 
name="0299c07" id="0299c07"></a>此身超越死生求於佛道。非希世樂而期佛果。言<a 
name="0299c08" id="0299c08"></a>求佛道者揀非聲聞緣覺之乘。志在求佛尊修禁<a 
name="0299c09" id="0299c09"></a>戒。非為二乘奉持律儀。毗尼幽旨。誠在茲焉。學者<a 
name="0299c10" id="0299c10"></a>幸無錯其宗趣。自陷偏小之坑。狂者速宜反省。以<a 
name="0299c11" id="0299c11"></a>雪譏貶之罪。縱而跡現是小。意本實誘歸大。況乃<a 
name="0299c12" id="0299c12"></a>戒海無涯。量同太&#x8657;。隨物受益。等施無二。惟根器<a 
name="0299c13" id="0299c13"></a>自有大小之殊。法無廣狹之咎。欲脫二死之淵。當<a 
name="0299c14" id="0299c14"></a>早投心於戒地。庶得自為無乖。求佛有旨矣正法<a 
name="0299c15" id="0299c15"></a>者。戒法也。所謂有秉羯磨說戒。則正法未滅世間。<a 
name="0299c16" id="0299c16"></a>以此戒法。是出生死之梯航。涅槃之徑路。不同外<a 
name="0299c17" id="0299c17"></a>道邪教。輪王十善治世之術。故此特名之為正法。</p><a 
name="0299c18" id="0299c18"></a><p>　<span class="lg">七佛為世尊　　滅除諸結使　　說是七戒經<a 
name="0299c19" id="0299c19"></a><br>　諸縛得解脫　　<span class="corr" title='[巳&gt;已]'>已</span>入於涅槃　　諸戲永滅盡<a 
name="0299c20" id="0299c20"></a><br>　尊行大仙說　　聖賢稱譽戒　　弟子之所行<a 
name="0299c21" id="0299c21"></a><br>　入寂滅涅槃</span></p><a 
name="0299c22" id="0299c22"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。前一頌半。謂諸佛弟子奉持戒經。得脫諸縛。<a 
name="0299c23" id="0299c23"></a><span class="corr" title='[巳&gt;已]'>已</span>入涅槃。故舉以證之。後一頌。謂今佛弟子。尊佛<a 
name="0299c24" id="0299c24"></a>所說戒修行。便獲涅槃果證。故示以勸之。結使者。<a 
name="0300a01" id="0300a01"></a>謂諸煩惱結縛驅使行人心神。流轉三界不得解<a 
name="0300a02" id="0300a02"></a>脫。略言則三結十使。廣說則八十八使。乃至五百<a 
name="0300a03" id="0300a03"></a>結使。七佛為世<span class="gaiji">[隳-隋+道]<!--gaiji,,1[隳-隋+道],2,3--></span>師。為欲滅除諸弟子結使故。乃<a 
name="0300a04" id="0300a04"></a>說此戒經。弟子奉行。則諸煩惱結縛。因斯解脫。證<a 
name="0300a05" id="0300a05"></a>無生果。<span class="corr" title='[巳&gt;已]'>已</span>般涅槃。一切有無戲論。永悉滅盡矣。七<a 
name="0300a06" id="0300a06"></a>佛大仙所說之經。是一切聖賢所稱讚之戒。若今<a 
name="0300a07" id="0300a07"></a>佛弟子尊之修行。則無不證入寂滅涅槃者也。涅<a 
name="0300a08" id="0300a08"></a>槃是西音。寂滅是華言。華梵兩彰。故二雙舉也。</p><a 
name="0300a09" id="0300a09"></a><p>　<span class="lg">世尊涅槃時　　興起於大悲　　集諸比丘眾<a 
name="0300a10" id="0300a10"></a><br>　與如是教誡　　莫謂我涅槃　　淨行者無護<a 
name="0300a11" id="0300a11"></a><br>　我今說戒經　　亦善說毗尼　　我雖般涅槃<a 
name="0300a12" id="0300a12"></a><br>　當視如世尊　　此經久住世　　佛法得熾盛<a 
name="0300a13" id="0300a13"></a><br>　如是熾盛故　　得入於涅槃　　若不持此戒<a 
name="0300a14" id="0300a14"></a><br>　如所應布薩　　喻如日沒時　　世界皆闇冥</span></p><a 
name="0300a15" id="0300a15"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。初一頌。序佛垂誨。次兩頌半。明佛示教。後一<a 
name="0300a16" id="0300a16"></a>頌。乃重申誡喻也。如來說法四十九年。化緣<span class="corr" title='[巳&gt;已]'>已</span>畢。<a 
name="0300a17" id="0300a17"></a>垂將涅槃。重以戒法。顧命弟子。慇懃付囑。示誨諄<a 
name="0300a18" id="0300a18"></a>諄。是知此戒。實為佛法之綱領。僧伽之命脈。苦海<a 
name="0300a19" id="0300a19"></a>之津梁。涅槃之關要。苟不頂戴受持。豈但沉溺生<a 
name="0300a20" id="0300a20"></a>津。而且辜於如來付囑之念。凡我佛子。當思慈誨。<a 
name="0300a21" id="0300a21"></a>奉遺法而忘軀。敬木叉而盡壽。使正法而得恒存。<a 
name="0300a22" id="0300a22"></a>熾然常住世間。是為知恩報恩。思德報德者矣。戒<a 
name="0300a23" id="0300a23"></a>經者。二百五十別解脫律儀也。毗尼者。是除滅罪<a 
name="0300a24" id="0300a24"></a>之法也。般涅槃者。華言入寂滅。謂生死永寂滅故。<a 
name="0300b01" id="0300b01"></a>亦云滅度。所謂大患永滅。超度四流者也。當視如<a 
name="0300b02" id="0300b02"></a>世尊者。謂此波羅提木叉經在世。即是如來法身<a 
name="0300b03" id="0300b03"></a>常住不滅。故特令師之。如遺教經云。如來於娑羅<a 
name="0300b04" id="0300b04"></a>雙樹間。將入涅槃。告諸弟子曰。汝等比丘。於我滅<a 
name="0300b05" id="0300b05"></a>後。當尊重珍敬波羅提木叉。加闇遇明。貧人得寶。<a 
name="0300b06" id="0300b06"></a>當知此等。則是汝等大師。若我住世。無異此也。此<a 
name="0300b07" id="0300b07"></a>經久住世等者。謂有如戒修行。即此經住世。此經<a 
name="0300b08" id="0300b08"></a>住世。則正法光明熾盛不滅。由此光故。得見諦理。<a 
name="0300b09" id="0300b09"></a>而趣涅槃。是即教行理果。皆悉熾然。若不持戒布<a 
name="0300b10" id="0300b10"></a>薩除愆。罪垢日增。失智慧明。不&#x89A9;聖道。便墮三途。<a 
name="0300b11" id="0300b11"></a>喻如日沒暗冥。無所見故。即墮坑落塹矣。</p><a 
name="0300b12" id="0300b12"></a><p>　<span class="lg">當護持是戒　　如犛牛愛尾　　和合一處坐<a 
name="0300b13" id="0300b13"></a><br>　如佛之所說</span></p><a 
name="0300b14" id="0300b14"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。此頌誡令護戒。當遵佛制。謂行者以護戒自<a 
name="0300b15" id="0300b15"></a>珍。寧死不犯。如犛牛自惜其尾。不顧身命。和合一<a 
name="0300b16" id="0300b16"></a>處坐者。使離別眾之過。如佛所說者。遣除非法之<a 
name="0300b17" id="0300b17"></a>累。總謂行者。以戒自惜。希求無犯。乃半月半月。共<a 
name="0300b18" id="0300b18"></a>和集一處。如佛所教。而作布薩說戒也。</p><a 
name="0300b19" id="0300b19"></a><p>　<span class="lg">我<span class="corr" title='[巳&gt;已]'>已</span>說戒經　　眾僧布薩竟　　我今說戒經<a 
name="0300b20" id="0300b20"></a><br>　所說諸功德　　施一切眾生　　皆共成佛道</span></p><a 
name="0300b21" id="0300b21"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。前半頌。明自他所作事畢。後一頌。以所作事<a 
name="0300b22" id="0300b22"></a>回施有情。共成佛道。言所作事者。即諸功德也。諸<a 
name="0300b23" id="0300b23"></a>功德者。謂一一戒所生之功德也。施一切眾生者。<a 
name="0300b24" id="0300b24"></a>謂先作布薩說戒。乃惟自利。今施眾生。是為利他。<a 
name="0300c01" id="0300c01"></a>共成佛道者。所謂不令有人獨得滅度。皆以如來<a 
name="0300c02" id="0300c02"></a>滅度而滅度之。是也。亦乃菩薩初發大心。即與一<a 
name="0300c03" id="0300c03"></a>切眾生共期佛果。究竟涅槃之弘誓也。問曰。聲聞<a 
name="0300c04" id="0300c04"></a>之法。何用迴向佛道為。答曰。斯正所謂真是聲聞。<a 
name="0300c05" id="0300c05"></a>以佛道聲。令一切聞。惟有一乘法。無二亦無三。又<a 
name="0300c06" id="0300c06"></a>戒本防非止惡。非聲聞之獨持。比丘體通大小。豈<a 
name="0300c07" id="0300c07"></a>羅漢之自目。菩薩原分在家出家。比丘寧同俗類。<a 
name="0300c08" id="0300c08"></a>若不持此修身。更以何成僧法。故云在家菩薩。持<a 
name="0300c09" id="0300c09"></a>在家戒。出家菩薩。持出家戒。在家菩薩名為近事。<a 
name="0300c10" id="0300c10"></a>出家菩薩名為比丘。是知比丘之戒。非局小心。出<a 
name="0300c11" id="0300c11"></a>家菩薩。曷容不護。三世諸佛同斯法教。不惟今之<a 
name="0300c12" id="0300c12"></a>獨爾。或曰。聲聞持戒。名菩薩破戒。聲聞破戒。名菩<a 
name="0300c13" id="0300c13"></a>薩持戒。此義云何。斯以聲聞自度為急。故止之名<a 
name="0300c14" id="0300c14"></a>持。菩薩利生為本。不廣作眾善名之為犯。此正責<a 
name="0300c15" id="0300c15"></a>其心小自利為過。非謂有大心利他共成佛道者<a 
name="0300c16" id="0300c16"></a>在列。縱曰自利。誠乃兼濟。故一一戒中。不離護他。<a 
name="0300c17" id="0300c17"></a>斯實如來善巧方便。密以珠置衣裏。令其終獲大<a 
name="0300c18" id="0300c18"></a>用。今人不達聖意。於木叉而反生睚<span class="gaiji">眥<!--gaiji,眥,1[目*柴],2&#x25210;,3--></span>。若悟法法<a 
name="0300c19" id="0300c19"></a>無非妙行。即駕白牛而歸寶所矣。</p><a 
