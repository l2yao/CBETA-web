name="0296a20" id="0296a20"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○初總結前相</p><a 
name="0296a21" id="0296a21"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>諸大德。我<span class="corr" title='[巳&gt;已]'>已</span>說戒經序。<span class="corr" title='[巳&gt;已]'>已</span>說四波羅夷法。<span class="corr" title='[巳&gt;已]'>已</span>說十三<a 
name="0296a22" id="0296a22"></a>僧伽婆尸沙法。<span class="corr" title='[巳&gt;已]'>已</span>說二不定法。<span class="corr" title='[巳&gt;已]'>已</span>說三十尼薩耆波<a 
