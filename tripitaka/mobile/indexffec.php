name="0296b05" id="0296b05"></a>師。律師。聲聞。緣覺。菩薩也。戒經中來者。既非餘人<a 
name="0296b06" id="0296b06"></a>所說。復非餘經所出也。</p><a 
name="0296b07" id="0296b07"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○二勸學餘法。</p><a 
name="0296b08" id="0296b08"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>若更有餘佛法。是中皆共和合。應當學。</p><a 
name="0296b09" id="0296b09"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。若更有餘佛法者。謂於諸犍度中如來所制<a 
name="0296b10" id="0296b10"></a>學法。及一切修多羅。阿毗曇。定慧之法。以此法能<a 
name="0296b11" id="0296b11"></a>越苦淪而趣涅槃也。是中者。謂非<span class="corr" title='[&#x4F39;&gt;但]'>但</span>二百五十戒<a 
name="0296b12" id="0296b12"></a>法和合同修。此餘法中咸應和合共學。和合者。謂<a 
name="0296b13" id="0296b13"></a>歡喜無諍。同一師學。不生異見。如水乳合也。應當<a 
name="0296b14" id="0296b14"></a>學者。謂是比丘所應學事。勿生放逸。當勤修習也<a 
name="0296b15" id="0296b15"></a><span class="note">(修多羅。是經藏。屬定學。阿毗曇。是論藏。屬慧學也)</span>。</p><a 
name="0296b16" id="0296b16"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>○三七佛略說戒經。</p><a 
name="0296b17" id="0296b17"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>忍辱第一道。佛說無為最。出家惱他人。不名為沙門。<a 
name="0296b18" id="0296b18"></a>此是毗婆尸如來無所著等正覺。說是戒經。</p><a 
name="0296b19" id="0296b19"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。內心能安。忍外所辱之境。名為忍辱。此有二<a 
name="0296b20" id="0296b20"></a>種。一者生忍。二者法忍。生忍有二。一謂於恭敬。供<a 
name="0296b21" id="0296b21"></a>養。順境中。能忍不著。則不生憍逸。二謂於瞋。罵。打<a 
name="0296b22" id="0296b22"></a>害。逆境中能忍。則不生忿恨怨惱。是為生忍。法忍<a 
name="0296b23" id="0296b23"></a>亦有二。一者非心法。謂寒熱風雨饑渴老病死等。<a 
name="0296b24" id="0296b24"></a>二者心法。謂瞋恚憂愁疑婬欲憍慢諸邪見等。若<a 
name="0296c01" id="0296c01"></a>於此二法能忍不動。是名法忍。復以正慧觀察。生<a 
name="0296c02" id="0296c02"></a>法性空。辱不可得。苦空無我。誰為忍者。辱忍既空。<a 
name="0296c03" id="0296c03"></a>無生現前。便證圓寂。以此忍辱是涅槃圓寂之捷<a 
name="0296c04" id="0296c04"></a>徑。故名第一道。無為者。即是涅槃。湛然常寂。無所<a 
name="0296c05" id="0296c05"></a>造作。超絕有為之境。越度生死之流。更無有法勝<a 
name="0296c06" id="0296c06"></a>於此者。故名為最。沙門者。是寂淨義。舊譯為勤行。<a 
name="0296c07" id="0296c07"></a>謂勤行取涅槃故。或翻為勤息。勤為勤修道業。息<a 
name="0296c08" id="0296c08"></a>謂息惡行慈。故阿含經云。捨離恩愛。出家修道。攝<a 
name="0296c09" id="0296c09"></a>御諸根。不染外欲。慈心一切。無所傷害。遇樂不欣。<a 
name="0296c10" id="0296c10"></a>逢苦不慼。能忍如地。故號沙門。若出家不行忍辱。<a 
name="0296c11" id="0296c11"></a>而反怨報他人。則違涅槃無諍之道。豈稱勤息之<a 
name="0296c12" id="0296c12"></a>行。行若不稱。即非沙門。欲得第一道。當不惱他人。<a 
name="0296c13" id="0296c13"></a>若希無為樂。應具沙門法。忍辱是出世之因。無為<a 
name="0296c14" id="0296c14"></a>是出世之果。惱他人是生死之因。非沙門是生死<a 
name="0296c15" id="0296c15"></a>之果。善惡兩彰。淨穢齊舉。其有智者。能如擇乳之<a 
name="0296c16" id="0296c16"></a>鵝王。簡其善者而取之。則不違沙門之行。行既絕<a 
name="0296c17" id="0296c17"></a>倫。果超有頂矣。若據根本部中。謂毗婆尸佛出世<a 
name="0296c18" id="0296c18"></a>時諸弟子眾。及諸外道邪師。多樂苦行修身。妄招<a 
name="0296c19" id="0296c19"></a>樂果。時佛為破此見故。為諸弟子說斯略教。曰。忍<a 
name="0296c20" id="0296c20"></a>是勤中上。能得涅槃處。出家惱他人。不名為沙門。<a 
name="0296c21" id="0296c21"></a>謂忍是精勤中上。不由自苦其身。得勝涅槃。又諦<a 
name="0296c22" id="0296c22"></a>察法忍。由解了法。終獲涅槃。不由苦身而能證會。<a 
name="0296c23" id="0296c23"></a>復為遮彼邪見外道出家之類。妄說異法。教化他<a 
name="0296c24" id="0296c24"></a>人。無益苦身。令同<span class="corr" title='[巳&gt;己]'>己</span>行。自他俱惱。終無果益。故云<a 
name="0297a01" id="0297a01"></a>出家惱他人。不名為沙門<span class="note">(西國九十六種出家外道。俱稱沙門。惟如來弟<a 
name="0297a02" id="0297a02"></a>子名釋迦沙門)</span>。無所著等正覺者。謂如來證涅槃時。斷盡<a 
name="0297a03" id="0297a03"></a>一切煩惱。令身口意清淨。無染無著。故名無所著。<a 
name="0297a04" id="0297a04"></a>以一切法平等開覺一切眾生。成無上覺。故號等<a 
name="0297a05" id="0297a05"></a>正覺。</p><a 
name="0297a06" id="0297a06"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>譬如明眼人。能避嶮惡道。世有聰明人。能遠離諸惡。<a 
name="0297a07" id="0297a07"></a>此是尸棄如來無所著等正覺。說是戒經。</p><a 
name="0297a08" id="0297a08"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。喻人明目不趣嶮難之徑。終至安隱之地。若<a 
name="0297a09" id="0297a09"></a>世間有聰慧明見智者。即知道與非道。非道乃六<a 
name="0297a10" id="0297a10"></a>趣之嶮途。道是八正之平街。嶮途由不善所感。平<a 
name="0297a11" id="0297a11"></a>街因勝慧所至。故須離諸惡因。務修出要。終抵涅<a 
name="0297a12" id="0297a12"></a>槃安隱常樂之處。</p><a 
name="0297a13" id="0297a13"></a><p style="text-indent: 0em; margin-left: 0em"><span class="linehead"></span>不謗亦不嫉。當奉行於戒。飲食知止足。常樂在空閑。<a 
name="0297a14" id="0297a14"></a>心定樂精進。是名諸佛教。此是毗葉羅如來無所著<a 
name="0297a15" id="0297a15"></a>等正覺。說是戒經。</p><a 
name="0297a16" id="0297a16"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。謗是口過。嫉是意業。欲離此二。當奉行於戒。<a 
name="0297a17" id="0297a17"></a>戒淨則三業頓捐。又何口意之可得。為欲成就淨<a 
name="0297a18" id="0297a18"></a>戒。故令不謗不嫉也。飲食屬口。常樂屬意。知足則<a 
