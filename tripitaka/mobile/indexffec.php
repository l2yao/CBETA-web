name="0296b02" id="0296b02"></a><p style="text-indent: 0em; margin-left: 1em"><span class="linehead"></span>釋曰。此中結意。為欲令知戒聚次第。復顯說者文<a 
name="0296b03" id="0296b03"></a>無差脫。句義圓滿。八聚<span class="corr" title='[巳&gt;已]'>已</span>周。二百五十法竟矣。此<a 
